<h1>Co o&nbsp;Firefoxu říkají média?</h1>

<p>Mnoho recenzentů a&nbsp;komentátorů přechází z&nbsp;Internet Exploreru na
Firefox. Někteří vyzkoušeli Firefox, aby poskytli vyvážené zpravodajství,
a&nbsp;při té příležitosti si prostě Firefox oblíbili. Ikonka Internet
Exploreru se časem ocitla na okraji jejich pracovní plochy nebo rovnou
v&nbsp;koši. Ostatní recenzenti Firefox používají často, ale přejít zatím
nechtějí. A&nbsp;někteří prostě o&nbsp;Firefoxu píší pěkné věci, i&nbsp;když
používají Internet Explorer. Vážíme si všech příznivých recenzí, ale nejvíce
těch, které doporučují používat Firefox.</p>

