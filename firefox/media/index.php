<h3>Jak Mozilla Firefox trumfnul Internet Explorer</h3>

<div class="med-logo" style="width:60px"><img
	src="/img/m-msn.gif" alt="MSN" height="24" width="60" /></div>

<p>Obvykle se o&nbsp;počítačové viry nestarám, avšak útok Scobu minulý týden
mě probral. Scob mne nedostal, ale bylo to dost na to, abych dal do starého
železa Explorer a&nbsp;zkusil mnohem méně zranitelný prohlížeč Firefox.</p>

<p>Problém je, že hackeři nepřestávájí hledat a&nbsp;zneužívat bezpečnostní
chyby v&nbsp;Exploreru. Mnoho z&nbsp;nich využívá technologie ActiveX
v&nbsp;Exploreru, který umožňuje webovým stránkám stáhnout
a&nbsp;nainstalovat software na počítač návštěvníka, často bez uživatelova
vědomí. ActiveX bylo zamýšleno jako pomůcka pro přidání nejnovějších
interaktivních multimédii a&nbsp;dalších funkcí na webové stránky, ale místo
toho se stalo nástrojem pro instalaci vyzvědačského software do důvěřivých
PC. Firefox se vyvaroval ActiveX a&nbsp;dalších dobře znamých cest infekce.
Můžete jej nakonfigurovat, aby automaticky stahoval většinu souborů, když na
ně klepnete, s&nbsp;výjimkou souborů .exe, které jsou spustitelnými
programy. Myslel jsem si, že jde o&nbsp;chybu, než jsem zjistil, že Firefox
mně zachraňuje přede mnou samým, protože soubor .exe může být virem nebo
skrytým instalačním programem.<br />

