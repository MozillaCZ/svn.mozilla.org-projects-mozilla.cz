kód neobsahuje žádný záměrně škodlivý kód nebo spyware.</p>


<h2 id="duvod-11">Jednoduché a srozumitelné rozhraní</h2>

<div class="img-c img-right" style="width:200px">
	<img src="/img/ss-kiss.png"	width="200" height="132"
		alt="Celkový pohled na prohlížeč" />
	<p class="comment">Díky jednoduchému rozhraní a&nbsp;zabudovanému vyhledávání
	budete mít na webu nevídané zážitky</p>
</div>

<p><strong>Zjednodušené uživatelské rozhraní.</strong> Snažili jsme se, aby
používání Firefoxu bylo pro každého snadné a&nbsp;jasné. Prohlížeče jako Mozilla
nebo Opera se více zaměřují na množství vlastností a&nbsp;komplexnost na úkor
celkové použitelnosti. Proto jsme vytvořili prohlížeč, který je co
<strong>nejjednodušší</strong> a&nbsp;také <strong>nejsrozumitelnější</strong>.
Všechna základní nastavení byla pečlivě vybrána tak, abyste
<em>nepotřebovali</em> strávit ani minutu jejich nastavováním. Menu
a&nbsp;dialogy jsou čisté a&nbsp;neobsahují zbytečné volby, které vyžívá jen minimum
lidí. Pokud existují vlastnosti, které Mozilla nebo Opera mají, ale Firefox
ne, máme pro to dobrý důvod. Pokud i&nbsp;přesto něco z toho potřebujete,
využijte množství užitečných rozšíření na serveru
<a href="https://addons.mozilla.org/cs/firefox/">Doplňky Mozilly</a>.</p>
</div>

