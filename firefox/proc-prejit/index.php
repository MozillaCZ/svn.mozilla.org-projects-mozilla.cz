<h1>Proč přejít na Firefox?</h1>

<p>Objevte báječné vlastnosti a&nbsp;funkce Firefoxu, po kterých jste
toužili. Přečtěte si 11&nbsp;důvodů proč nahradit zastaralý prohlížeč zbrusu
novým.</p>

<h2 id="duvod-1">Rychlé prohlížení v panelech</h2>

<div class="img-c img-right" style="width:200px">
	<img src="/img/ss-taby.png" width="200" height="141"
		alt="Vyhledávání na Google s otevřením jedné nalezené stránky na pozadí" />
	<p class="comment">Otevírání panelů na pozadí
	umožňuje nahrávat stránky, aniž byste museli přerušit prohlížení té
	současné</p>
</div>

<p>Prohlížení v&nbsp;panelech změní způsob, jakým používáte Internet.
Dovolí vám nahrávat nové stránky na pozadí a&nbsp;přečíst si je, až budete
chtít. Tím, že vidíte zobrazené stránky v&nbsp;jednom okně, vám prohlížení
v&nbsp;panelech usnadní orientaci ve velkém množství stránek. Procházet mezi panely
je jednoduché, stačí jedno kliknutí na požadovaný panel. Stránky se
v&nbsp;panelech nahrávají na pozadí a&nbsp;vaše prohlížení je proto
rychlejší a&nbsp;přehlednější. Při kliknutí na odkaz stačí stisknout
<kbd>Ctrl</kbd>, nebo použít prostřední tlačítko myši.</p>

<div class="img-c img-left" style="width:200px">
	<img src="/img/ss-zalozky-menu.png" width="200" height="175"
		 alt="Ukázka složky záložek s nabídkou otevření do panelů"  />
	<p class="comment">&quot;Otevírat v&nbsp;panelech&quot; otevře záložky ve
	složce do panelů.</p>
</div>

<p>S&nbsp;prohlížením v&nbsp;panelech je procházení webu rychlejší
a&nbsp;efektivnější. Soustředíte se na čtení stránky, kterou právě
prohlížíte, aniž byste museli přepínat mezi okny. Zatímco čtete, nově
otevřené stránky se nahrávají v&nbsp;panelech na pozadí. Když jste
připraveni je přečíst, jejich nahrávání je dokončeno a&nbsp;nemusíte
čekat.</p>

<p>I&nbsp;když to vypadá jednoduše, prohlížení v&nbsp;panelech je skutečně
revoluční myšlenka. Firefox vám přináší ten nejjednodušší
a&nbsp;nejintuitivnější způsob panelového prohlížení, jaký dnes
můžete&nbsp;nalézt.</p>


<h2 id="duvod-2">Blokování vyskakovacích oken</h2>

<div class="img-c img-right" style="width:200px">
	<img src="/img/ss-blokovane-okno.png" width="89" height="72"
		alt="Ikonka v pravém dolním rohu aplikace upozorňující na blokování vyskakovacího okna" />
	<p class="comment">Ikona upozorňuje, že prohlížeč zablokoval vyskakovací
	okno</p>
</div>

<p>Firefox blokuje zobrazování obtěžujících vyskakovacích <span
lang="en">(pop-up)</span> oken. Práce s&nbsp;webem je pak taková, jaká by
měla být - klidná, pohodlná a&nbsp;bez překážek.</p>

<p>Některé stránky ke svému použití otvírání nových oken potřebují. Když
Firefox zablokuje nějaké okno, zobrazí lištu s&nbsp;nápisem &quot;Aplikace Firefox
zabránila stránce otevřít vyskakovací okno&quot; a&nbsp;ve stavovém řádku ikonu (viz
obrázek). Kliknutím na tuto lištu nebo na ikonu zobrazíte seznam oken, která
Firefox zablokoval při nahrávání aktuální stránky. Zde můžete některým oknům
snadno povolit, aby se zobrazila. Pokud chcete, můžete danému serveru otevírání
vyskakovacích oken povolit natrvalo.</p>


<h2 id="duvod-3">Rychlejší vyhledávání</h2>

<div class="img-c img-right" style="width:150px">
	<img src="/img/ss-zalozky-hledani.png" width="150" height="152"
		alt="Hledání fotografických stránek pomocí slova &quot;foto&quot;" />
	<p class="comment">Rychlé hledání v&nbsp;záložkách v&nbsp;akci</p>
</div>

<p>Většinu času stráveného s&nbsp;prohlížečem něco hledáme. To je také důvod,
proč má Firefox ze všech prohlížečů nejlepší vyhledávání. Stisk
<kbd>Ctrl</kbd>+<kbd>K</kbd> vás přenese přímo do pole hledání. Vložte, co
hledáte a&nbsp;stiskněte <kbd>Enter</kbd>. Zobrazí se výsledky nalezené
vyhledávačem
<a href="http://www.google.com/intl/cs/">Google</a>.
Nemusíte hledat jen na Google &ndash; český Firefox
obsahuje i&nbsp;vyhledávání na portálech
<a href="http://www.seznam.cz/">Seznam</a>
a&nbsp;<a href="http://www.centrum.cz/">Centrum</a>,
vyhledávači <a href="http://jyxo.cz/">Jyxo</a>,
v obchodech <a href="http://www.mall.cz/">Internet Mall</a>
a&nbsp;softwarovém katalogu
<a href="http://www.slunecnice.cz/">Slunečnice.cz</a>.</p>

<p>Pokud vložíte text přímo do adresního řádku, provede se na Googlu hledání
&quot;Zkusím štěstí&quot; a&nbsp;do prohlížeče se vám rovnou načte první
nalezený výsledek. Pokud vkládanému textu předřadíte <em>klíčové slovo</em>,
určíte, pomocí kterého vyhledávače bude hledání provedeno (zkuste zadat
třeba <tt>gg CZilla</tt> pro hledání na Googlu).</p>

<p>Motto &quot;Hledejte rychleji&quot; není ve Firefoxu prázdným heslem.
Všechny části prohlížeče jsou navrženy s&nbsp;ohledem na
vyhledávání. Vemte si takové prohlížení v&nbsp;panelech - v&nbsp;jednom
panelu spusťte vyhledávání, vyhledané výsledky si otevřte v&nbsp;panelech na
pozadí, přepínejte se mezi nimi a&nbsp;porovnejte je. Záložky
a&nbsp;historie obsahují pole Hledat, které vám pomůže rychle najít hledanou
záložku i&nbsp;v&nbsp;dlouhém seznamu, nebo se podívat mnoho dní zpět
v&nbsp;historii. Firefox má také šikovné kontextové menu umožňující hledání
označeného textu.</p>


<h2 id="duvod-4">Soukromí snadno a rychle</h2>

<div class="img-c img-left" style="width:130px">
	<img src="/img/ss-privacy.png" width="60" height="50"
		alt="Ikonka sekce Soukromí z Možností" />
	<p class="comment">Firefox má mocné nastavení pro ochranu soukromí.</p>
</div>

<p>Vaše soukromí je pro nás důležité. Firefox vám dává možnost sledovat
všechny informace, které prohlížeč ukládá během prohlížení stránek - např.
věci, které jste vložili do formulářů, cookies, soubory které jste stáhli,
stránky které jste navštívili (Historie) a&nbsp;tak dále. V&nbsp;Možnostech
v&nbsp;sekci <strong>Soukromí</strong> můžete snadno vymazat kteroukoli
z&nbsp;těchto informací. Jestliže používáte veřejný nebo sdílený počítač,
můžete všechny tyto informace vymazat jediným kliknutím na tlačítko Vymazat
vše.</p>


<h2 id="duvod-5">Pryč s obtěžujícími prvky</h2>

<div class="img-c img-left" style="width:130px">
	<img src="/img/ss-web-features.png"	width="50" height="50"
		alt="Ikonka sekce Obsah z Možností" />
	<p class="comment">Firefox znemožní obtěžující triky na stránkách</p>
</div>

<p>Firefox dokáže předcházet i&nbsp;mnoha dalším obtěžujícím akcím, které
vás provázejí již léta, jako jsou různé triky se stavovým řádkem - např.
animace stavového řádku, snaha skrýt, kam odkazy skutečně odkazují atd.
Firefox je navržen tak, aby vás jeho <em>standardní nastavení</em>
obtěžovalo <em>co nejméně</em>. Změnit tato nastavení můžete v&nbsp;sekci
<strong>Možností</strong> pro <strong>Obsah</strong>.</p>

<p>Firefox přichází se stejně bohatými možnostmi nastavení jako Mozilla Suite.
Dovoluje vám kontrolovat cookies, JavaScript a&nbsp;mnohé další prvky podle
vašich potřeb.</p>


<h2 id="duvod-6">Lepší Záložky a Historie</h2>

<div class="img-c img-right" style="width:188px">
	<img src="/img/ss-spravce-zalozek.png" width="188" height="144"
		alt="Celkový náhled Správce záložek" />
	<p class="comment">Se Správcem záložek bude třídění vašich záložek
	snadné.</p>
</div>

<p>Firefox usnadňuje organizování vašich záložek pomocí Správce záložek
(menu <strong>Záložky, Správce záložek...</strong>). Vytvořte si složky pro
seskupování podobných odkazů a&nbsp;připisujte si k&nbsp;záložkám popisky,
abyste si později vzpoměli, proč jste si záložku vytvořili. Záložky si
můžete seřadit, jak chcete; podle Názvu, Umístění, Data poslední návštěvy
atd. Firefox vám umožňuje přiřadit k&nbsp;záložce klíčové slovo, takže ji
můžete snadno otevřít pouhým napsáním tohoto slova do adresního řádku
prohlížeče, což vám ušetří čas při psaní dlouhých adres.</p>

<p>Do záložek lze uložit také aktuální rozložení panelů. Můžete si tak
jedním kliknutím otevřít několik oblíbených stránek najednou, např.
zpravodajské servery k&nbsp;ranní kávě.</p>

<p>Záložky i&nbsp;Historii si můžete zobrazit v&nbsp;postranní liště
a&nbsp;přistupovat k&nbsp;nim tak rychleji. Stačí stisknout
<kbd>Ctrl</kbd>+<kbd>B</kbd> (pro Záložky) nebo <kbd>Ctrl</kbd>+<kbd>H</kbd>
(pro Historii). Odkazy z&nbsp;nich otevřete pouhým kliknutím.</p>


<h2 id="duvod-7">Přístupný, inteligentní a rychlý</h2>

<div class="img-c img-right" style="width:200px">
	<img src="/img/ss-hledani-ve-strance.png" width="200" height="55"
		alt="Ukázka hledání slova Firefox" />
	<p class="comment">Vyhledávání během psaní v&nbsp;akci</p>
</div>

<p><strong>Vše hned po ruce.</strong> Firefox nabízí nejlepší sadu
klávesových zkratek. Díky nim máte všechny důležité funkce hned po ruce.
<kbd>Alt</kbd>+<kbd>D</kbd> nebo
<kbd>Ctrl</kbd>+<kbd>L</kbd> pro rychlý přístup k&nbsp;adresnímu řádku,
<kbd>Ctrl</kbd>+<kbd>K</kbd> pro pole hledání. <kbd>Ctrl</kbd>+<kbd>T</kbd>
otvírá nový panel a&nbsp;<kbd>Ctrl</kbd>+<kbd>S</kbd> dělá to, co asi čekáte
- uloží aktuální stránku. Chcete-li znát další, máme pro vás
<a href="http://support.mozilla.com/cs/kb/Kl%C3%A1vesov%C3%A9+zkratky">seznam klávesových zkratek</a>.</p>

<p>Jak Záložky, tak Historie jsou pro rychlý přístup dostupné
v&nbsp;postranní liště. Jednoduše stiskněte <kbd>Ctrl</kbd>+<kbd>B</kbd> pro
Záložky a&nbsp;<kbd>Ctrl</kbd>+<kbd>H</kbd> pro Historii. Odkazy v&nbsp;nich
otevřete pouhým kliknutím.</p>

<div class="img-c img-left" style="width:220px">
	<img src="/img/ss-historie-formularu.png" width="220" height="62"
		alt="Po zadání prvého písmena se objevily stejně začínající dříve hledané výrazy" />
	<p class="comment">Firefox vám pomáhá s&nbsp;vyplňováním webových formulářů
	a&nbsp;pamatuje si, co jste hledali.</p>
</div>

<p><strong>Inteligentní</strong>. Firefox si pamatuje, co jste zadali do
formulářů, čímž vám urychlí a&nbsp;zjednoduší opakované vyhledávání nebo
vyplňování položek (např. vaše jméno). Šikovný Správce hesel může
automaticky doplnit uživatelská jména a&nbsp;hesla na stránkách, které
vyžadují přihlášení.</p>

<p><strong>Ještě rychlejší</strong>. Při každodenním prohlížení stránek
zjistíte, že Firefox je rychlejší než jeho soupeři (např. Internet
Explorer). Stránky se vám na obrazovce budou objevovat dříve a&nbsp;jen zřídka
narazíte na nepříjemné zaseknutí aplikace. Ve výsledku vám tak skutečně
<em>připadá</em> vše rychlejší.</p>


<h2 id="duvod-8">Přizpůsobitelný a rozšiřitelný</h2>

<div class="img-c img-right" style="width:200px">
	<img src="/img/ss-uprava-list.png" width="200" height="156"
		alt="Přizpůsobení lišt" />
	<p class="comment">Přizpůsobení lišt ve Firefoxu</p>
</div>

<p><strong>Přizpůsobte si nástrojové lišty.</strong> Nastavte si je tak, jak
chcete. Můžete mít třeba jen jednu úzkou lištu. Nebo můžete mít celou sadu lišt
se všemi dostupnými funkcemi. Nástrojové lišty Firefoxu jsou plně
přizpůsobitelné a&nbsp;můžete je mít přesně tak, jak se vám budou líbit.
<strong>Vyberte Zobrazit, Nástrojové lišty, Vlastní...</strong> a&nbsp;jednoduše
přetáhněte vše tam, kde to chcete mít. Firefox vám dovolí i&nbsp;vytvářet nové lišty.
Další lišty můžete získat instalací rozmanitých rozšíření.</p>

<p><strong>Nejrozšířitelnější.</strong> Uživatelské rozhranní Firefoxu můžou
vývojáři rozšířit pomocí průmyslových standardů
<span class="dnpa">(<abbr title="Extensible Markup Language" lang="en">XML</abbr>,
<abbr title="Cascading Style Sheets" lang="en">CSS</abbr>,
JavaScript a&nbsp;další)</span> i&nbsp;pomocí komplexních modulů z&nbsp;C++. Mezi takovými
rozšířeními najdete vše od relativně jednoduchých nástrojů, až po pokročilé
aplikace jako JavaScript Debugger nebo
<abbr title="Document Object Model" lang="en">DOM</abbr> Inspektor.
Kompletní seznam rozšíření je dostupný na serveru
<a href="https://addons.mozilla.org/cs/firefox/">Doplňky Mozilly</a>.
Rozšíření lze
<a href="http://support.mozilla.com/cs/kb/Upravování+Firefoxu+pomocí+doplňků">snadno instalovat</a>.</p>

<div class="img-c img-left" style="width:200px">
	<img src="/img/ss-spravce-motivu-vzhledu.png" width="200" height="152" alt="Správce motivů vzhledu" />
	<p class="comment">Změnit motiv vzhledu je snadné.</p>
</div>

<p><strong>Změna vzhledu.</strong> Uživatelské rozhranní Firefoxu je plně
přizpůsobitelné použitím stejných standardů
<abbr title="Cascading Style Sheets" lang="en">CSS</abbr>,
<abbr title="Extensible Markup Language" lang="en">XML</abbr>
atd. jako u&nbsp;rozšíření. Možnosti změny vzhledu jsou nekonečné. Firefox může
vypadat jako počítač Lcars ze StarTreku nebo dokoce připomínat Microsoft
Internet Explorer. Kompletní seznam motivů vzhledu najdete na serveru
<a href="https://addons.mozilla.org/cs/firefox/browse/type:2/">Doplňky Mozilly</a>.
Všechny motivy lze jednoduše instalovat.</p>

<h2 id="duvod-9">Moderní Správce stahování</h2>

<div class="img-c img-right" style="width:200px">
	<img src="/img/ss-spravce-stahovani.png" width="200" height="150"
		alt="Správce stahování v průběhu stahování souboru" />
	<p class="comment">Stahování do vybraného adresáře, zobrazení ukončení
	stahování v&nbsp;liště.</p>
</div>

<p>Vestavěný <strong>Správce stahování</strong> je moderní
a&nbsp;uživatelsky přívětivý. Zobrazuje seznam všech probíhajících
i&nbsp;dokončených stahování s&nbsp;patřičnými popisky. Uživatel má plnou
kontrolu nad probíhajícími i&nbsp;neaktiními procesy stahování, může znovu
zkusit stáhnout nedokončené, smazat dokončené i&nbsp;nedokončené, otevřít
stažené soubory v&nbsp;systémovém prohlížeči nebo vyčistit celý seznam.
Poklepání (dvojklik) může být použito ke spouštění souborů.</p>

<p>Správce stahování vám <strong>šetří místo v&nbsp;liště úloh</strong> tím,
že má pouze jedno okno pro všechna stahování, a&nbsp;tím, že ukazuje
v&nbsp;seznamu úloh průměrnou staženou část a&nbsp;počet zbývajících
stahování. Po skončení stahování je uživatel upozorněn podobně jako při
přijímání e-mailu: "Stahování dokončeno". Navíc je správce natolik
inteligentní, že vás upozorní, pokud se snažíte ukončít Firefox
v&nbsp;případě, že ještě existují nedokončené procesy stahování.</p>


