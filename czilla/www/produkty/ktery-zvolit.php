</td>
<td style="vertical-align:top">

<p><a href="/produkty/seamonkey/">Seamonkey</a> je vyspělá integrovaná <strong>sada
internetových aplikací</strong> obsahující <strong>webový prohlížeč</strong>,
<strong>e-mailový</strong> a <strong>diskusní klient</strong>, <strong>chatovací
klient</strong> a <strong><abbr title="HyperText Markup Language" lang="en">HTML</abbr>
editor</strong>. Je dostupná i v <strong>češtině</strong>. (<em>Poznámka: Seamonkey není
kombinací Firefoxu a Thunderbirdu, ale jde o naprosto odlišnou aplikaci.</em>)</p>

</td>
</tr>
</table>

<p>Pokud hledáte komplexní řešení na používání Internetu, doporučujeme vám
používat Seamonkey. Velké organizace vyžadující integrovanou sadu aplikací
(např. dřívější uživatelé Netscape Communicatoru) by měly přejít na Seamonkey.
Ostatní mohou zvážit přechod na <a href="#firefox">Firefox</a>
a <a href="#thunderbird">Thunderbird</a>.</p>


<p><strong>Nejdůležitější vlastnosti a funkce Seamonkey:</strong></p>
<ul>
	<li>Prohlížení v panelech</li>
	<li>Blokování obrázků a vyskakovacích oken</li>
	<li>Poštovní a diskusní klient</li>
	<li><abbr title="HyperText Markup Language" lang="en">HTML</abbr> editor</li>
	<li><abbr title="Internet Relay Chat" lang="en">IRC</abbr> klient</li>
	<li>Nástroje pro webové vývojáře</li>
</ul>

<p>Přečtěte si <a href="/produkty/seamonkey/">další informace o Seamonkey</a>.</p>

