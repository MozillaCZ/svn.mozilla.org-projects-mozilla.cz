<h2 id="thunderbird" class="nice tb">Thunderbird</h2>

<table>
<tr>
<td style="vertical-align:top">
	<a href="/img/thunderbird-big.png"><img src="/img/thunderbird.png"
	width="200" height="160" alt="Náhled e-mailového klienta Mozilla Thunderbird"
	style="margin:0 10px 10px 5px"/></a>

</td>
<td style="vertical-align:top">

<p><a href="/produkty/thunderbird/">Thunderbird</a> je <strong>e-mailový a diskusní klient
</strong> nové generace dostupný i v <strong>češtině</strong>. Thunderbird plně podporuje
práci s protokoly <acronym title="Internet Message Access Protocol"
lang="en">IMAP</acronym> a <acronym title="Post Office Protocol"
lang="en">POP</acronym>. Obsahuje filtr nevyžádané pošty (spamu), adresář,
podporu více poštovních účtů a řadu bezpečnostních prvků.</p>

<p>Thunderbird je vhodný uživatele hledající jednoduchého, ale mocného
poštovního klienta. I my vám doporučujeme používat Thunderbird jako vaši <em>hlavní
e-mailovou aplikaci</em>.</p>

</td>
</tr>
</table>


<p><strong>Nejdůležitější vlastnosti a funkce Thunderbirdu:</strong></p>
<ul>
	<li>Rozpoznání nevyžádané pošty (spamu)</li>
	<li>Blokování obrázků a skriptů</li>
	<li>Čtečka zpráv, novinek a blogů pomocí <abbr title="RDF Site Summary"
		lang="en">RSS</abbr> technologie</li>
	<li>S/MIME a další bezpečnostní prvky</li>
	<li>Hravě si poradí i s velmi rozsáhlými
		<acronym title="Internet Message Access Protocol"
		lang="en">IMAP</acronym> složkami</li>
</ul>

<p>Přečtěte si <a href="/produkty/thunderbird/">další informace o Thunderbirdu</a>.</p>

<h2 id="seamonkey" class="nice seamonkey">Seamonkey</h2>

<table>
<tr>
<td style="vertical-align:top">
	<a href="/img/seamonkey-big.png"><img src="/img/seamonkey.png"
	width="200" height="112" alt="Náhled aplikace Seamonkey"
