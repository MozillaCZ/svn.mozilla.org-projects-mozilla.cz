<h2 id="proc-thunderbird" class="nice thunderbird">Proč používat Thunderbird</h2>

<p>Přečtěte si několik důvodů, proč začít používat právě Thunderbird.</p>

<dl>
	<dt>Filtrování nevyžádané pošty</dt>
	<dd>
	   Thunderbird obsahuje anti-spamový filtr na kontrolu nevyžádané pošty. Díky němu se nebudete muset každý den probírat množstvím spamu, která zavítá do vaší poštovní schránky. <br /><a href="http://thunderbird.mozilla.cz/proc-pouzivat/#filtrovani-nevyzadane-posty" title="Více informací o filtrování nevyžádané pošty">Více informací...</a>
	</dd>

	<dt>Bezpečná práce s poštou</dt>
	<dd>
		Poštovní klient Mozilla Thunderbird klade velký důraz na bezpečnost uživatele. Ke své poště můžete přistupovat pomocí šifrovaného spojení skrze <abbr title="Secure Socket Layer" lang="en">SSL</abbr> či <abbr title="Transport Layer Security" lang="en">TLS</abbr>, svou poštu můžete chránit hlavním heslem či pracovat s&nbsp;certifikáty a&nbsp;digitálními podpisy. <br /><a href="http://thunderbird.mozilla.cz/proc-pouzivat/#bezpecna-prace-s-postou" title="Více informací o bezpečné práci s poštou">Více informací...</a>
	</dd>

	<dt>Podpora více účtů a vícenásobných identit</dt>
	<dd>
	 Pokud vlastníte více poštovních schránek, jistě oceníte, že Thunderbird umí snadno pracovat s&nbsp;více poštovními účty. Příjemnou funkcí je též podpora virtuálních identit. <br /><a href="http://thunderbird.mozilla.cz/proc-pouzivat/#podpora-vice-uctu" title="Více informací o podpoře více účtů a virtuálních identit">Více informací...</a>
	</dd>

	<dt>Integrovaná čtečka RSS kanálů</dt>
	<dd>
		Thunderbird v&nbsp;sobě obsahuje integrovanou čtečku <abbr title="Really Simple Syndication" lang="en">RSS</abbr>
kanálů, která vám umožní snadno získat přehled o&nbsp;novinkách na vašich oblíbených webových stránkách. <br /><a href="http://thunderbird.mozilla.cz/proc-pouzivat/#integrovana-ctecka-rss-kanalu" title="Více informací o integrované čtečce RSS">Více informací...</a>
	</dd>

	<dt>Filtry pro třídění zpráv</dt>
	<dd>
		Díky filtrům na třídění zpráv vám Thunderbird umožní filtrovat přijatou poštu do kategorií dle zadaných kritérií, například podle skupiny e-mailových adres, předmětu zprávy, stáří a&nbsp;mnoha dalších. <br /><a href="http://thunderbird.mozilla.cz/proc-pouzivat/#tridici-filtry-zprav" title="Více informací o filtrech pro třídění zpráv">Více informací...</a>
	</dd>

	<dt>K dispozici zdarma a v češtině</dt>
	<dd>
	  Poštovní klient Mozilla Thunderbird je pro každého <strong>dostupný zdarma</strong>. Je k&nbsp;dispozici <strong>v&nbsp;češtině</strong>, takže mu budete vždy rozumět.
	</dd>
</dl>

