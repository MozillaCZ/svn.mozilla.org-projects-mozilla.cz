<p>Každý rok se v&nbsp;této době tradičně bilancuje. Všichni vzpomínáme,
co se událo a&nbsp;co jsme celý rok dělali. Co se událo ve světě
<a href="http://www.mozilla.org/" hreflang="en">Mozilla</a>? Jaký byl rok 2006
pro <a href="/czilla/">CZillu</a>? Dovolte nám malé shrnutí.</p>

<h2 id="leden">Leden</h2>
<p>Hned začátkem roku se staly ve&nbsp;Francii dvě události.
<a href="/zpravicky/francouzske-cetnictvo-prechazi-na-firefox-a-thunderbird/">Francouzské četnictvo přešlo</a>
na Firefox a&nbsp;Thunderbird. Jako důvod uvedlo multiplatformnost
a&nbsp;skvělou podporu webových standardů. Další migrace
<a href="/zpravicky/mistni-parlament-parizske-oblasti-instaluje-firefox-na-1300-pocitacu/">byla oznámena</a>
místním parlamentem Île-de-France.</p>

<p>Podle společnosti
<a href="http://xitimonitor.com/" hreflang="en">XiTi Monitor</a> překročil
podíl Firefoxu v&nbsp;Evropě
<a href="/zpravicky/podil-firefoxu-v-evrope-presahl-20-procent,-v-cr-se-blizi-ke-30-procentum/">hranici 20%</a>
a&nbsp;ČR málem dosáhla až na 30%. Vyšel nový Thunderbird ve&nbsp;verzi
1.5 a&nbsp;uživatelé SeaMonkey dostali novou verzi svého oblíbeného
produktu s&nbsp;označením 1.0. O&nbsp;tom, že byl leden bohatý na vydávání
nových verzí nás přesvědčilo i&nbsp;vydání druhé betaverze prohlížeče
Camino 1.0 pro Mac OS X. </p>

<p>O&nbsp;rostoucí popularitě prohlížeče Firefox svědčilo i&nbsp;jeho
<a href="/zpravicky/firefox-v-televizi/">uvedení</a> v&nbsp;reklamě tehdy ještě
Českého Telecomu. Mimo tento úspěch
<a href="/zpravicky/firefox-sbira-dalsi-oceneni/">se Firefox dostal</a>
do&nbsp;žebříčku deseti nejvlivnějších světových značek a také vyhrál další 4
ocenění odborných časopisů.

</p><h2 id="unor">Únor</h2>
<p>V&nbsp;únoru vyšla aktualizace prohlížeče Firefox 1.5.0.1, která opravovala
stabilitu aplikace. V&nbsp;té&nbsp;době se bojovalo s&nbsp;problémy
<a href="/zpravicky/spotreba-pameti-ve-firefoxu-1.5/">ve&nbsp;spotřeběpaměti</a>.
Camino se dočkalo verze 1.0 a&nbsp;byla vydána první oficiální verze
Songbirdu 0.1. Prohlížeč pro chytré telefony a&nbsp;PDA založený na
jádru Gecko Minimo se dostal na verzi 0.13.</p>

<p>Na konci února <a href="/zpravicky/fosdem-2006/">se podívali
zástupci CZilly</a> do Bruselu, kde se konal již 6. ročník evropského
setkání OpenSource vývojářů FOSDEM 2006 a&nbsp;jemu přidruženému srazu
lokalizátorů Mozilla aplikací.</p>

<p>Univerzita v&nbsp;Berkley
<a href="/zpravicky/firefox-byl-bezpecny-339-dni-v-roce/">uveřejnila výsledky</a>
z&nbsp;roku 2004. Podle těchto výsledků byl Firefox bezpečný 339 dní
v&nbsp;roce. Pro srovnání: Internet Explorer nebyl zabezpečený 358 dní
v&nbsp;roce.</p>

<h2 id="brezen">Březen</h2>
<p>Projekt CZilla
<a href="/zpravicky/czilla-nominovana-do-ankety-czech-open-source-2006/">byl nominován</a>
do ankety CZECH OPEN SOURCE 2006. Do této ankety se dále dostal člen CZilly Adam Přibyl se svým projektem
<a href="http://proc.linux.cz/">Proč používat Linux</a>.</p>

<p>O&nbsp;neustálém vývoji CZilly
<a href="/zpravicky/nove-moznosti-prehledu-lokalizovanych-rozsireni/">nás přesvědčil</a>
Lukáš Petrovický. Obohatil přehled rozšíření o&nbsp;vyhledávání podle
produktu a&nbsp;dokonce podle verze produktu. Tím se stalo používání
přehledu efektivnější a&nbsp;přehlednější.</p>

<p>Právě v&nbsp;březnu bylo jedno datum, které každý musel oslavit. 3.3. server
<a href="http://www.spreadfirefox.com/" hreflang="en">SpreadFirefox</a>
oznámil, že počet stažení instalace Firefoxu
<a href="/zpravicky/firefox-dosahl-150-milionu-stazeni/">dosáhl 150&nbsp;milionů</a>.</p>

<h2 id="duben">Duben</h2>
<p>Ač všichni čekali na 1.dubna nějakou taškařici, žádná se nekonala.
<a href="/zpravicky/relicencovani-mozilly-dokonceno/">Došlo</a> pouze
k&nbsp;dokončení relicencování Mozilla, které trvalo 4,5 roku. Běžných
uživatelů se nikterak nedotklo.</p>

<p>Duben by se dal označit jako měsíc návštěv.
<a href="/zpravicky/tristan-nitot-prezident-mozilla-europe-navstivi-prahu/">Přijel se na nás podívat</a>
<a href="http://www.nitot.com/cv/" hreflang="en" title="Tristan Nitot">Tristan Nitot</a>,
prezident <a href="http://www.mozilla-europe.org/cs/">Mozilla Europe</a>.
<a href="/zpravicky/jsme-na-linuxexpu-2006/">Účastnili jsme</a> se také výstavy
<a href="http://www.linuxexpo.cz/">LinuxExpo 2006</a>, kde Tristan Nitot
<a href="/zpravicky/linuxexpo-2006-skoncilo/">přednesl přednášku</a>
nazvanou Mozilla and the Public Sector. Mezi dalšími přednášejícími se
objevil i&nbsp;David Majda, člen CZilly, se svou přednáškou Vývojová
platforma Mozilla.</p>

<p>Skončilo hlasování v&nbsp;anketě CZECH OPENSOURCE 2006. CZilla se stala
<a href="/zpravicky/czilla-vitezem-ankety-czech-open-source-2006/">absolutním vítězem</a>
díky hlasům veřejnosti. v&nbsp;hlasování odborné poroty jsme byli druzí.</p>

<p>Firefox i&nbsp;Thunderbird se dostali na verze 1.5.0.2. Byla vydána jedna
z&nbsp;posledních aktualizací "jedničkové" verze Firefoxu 1.0.8. SeaMonkey
vyšlo ve verzi 1.0.1, Mozilla Suite ve verzi 1.7.13. Pro nové uživatele
<a href="/zpravicky/uzivatelska-prirucka-k-firefoxu-1-5/">vytvořila</a>
společnost <a href="http://www.foxinus.cz/">FOXINUS Solutions s.r.o.</a>
příručku k&nbsp;prohlížeči Mozilla Firefox 1.5.</p>

<h2 id="kveten">Květen</h2>
<p>Na přelomu dubna a&nbsp;května se
<a href="/zpravicky/oznameni-vitezu-souteze-firefox-flicks/">vyhlašovaly výsledky</a>
další soutěže. Tentokrát šlo o
<a href="http://www.firefoxflicks.com/" hreflang="en">Firefox Flicks</a>,
ve které se soutěžilo o&nbsp;nejlepší reklamní spot na podporu
a&nbsp;propagaci prohlížeče Mozilla Firefox. Soutěže se zúčastnilo více
jak 300 účastníků a&nbsp;její vítězové byli vybráni jako reprezentanti
mediální kampaně.</p>

<p>Květnové
<a href="/zpravicky/podil-firefoxu-v-polovine-evropskych-zemi-presahl-20/">statistiky</a>
společnosti <a href="http://xitimonitor.com/" hreflang="en">XiTi Monitor</a> ukázaly, že
podíl Firefoxu v&nbsp;polovině evropských zemí přesáhl hranici 20&nbsp;%.</p>

<p>V&nbsp;tomto měsíci se objevily nově vzniklé verze produktů Camino
(ve verzi 1.0.1), Sunbird 0.3a2 a&nbsp;Firefox 1.5.0.3. Z důležitých
vývojových milníků jsme se těšili z Bon Echo Alpha 2 a&nbsp;na konci
měsíce z Bon Echo Alpha 3.</p>

<p>Společnost <a href="http://www.foxinus.cz/">FOXINUS Solutions s.r.o.</a>
nás opět
<a href="/zpravicky/uzivatelska-prirucka-k-thunderbirdu-1-5/">překvapila</a>.
a&nbsp;vydala další elektronickou knížku Thunderbird 1.5 - Uživatelská
příručka. Za tuhle práci si zaslouží naše obrovské díky.</p>

<p>Další <a href="/zpravicky/do-ustecke-zoo-pribyla-samicka-pandy-cervene/">událostí</a>,
která se okrajově dotýká CZilly a&nbsp;Mozilly vůbec, je jedno stěhování. Až
z&nbsp;Antverp se do ZOO v&nbsp;Ústí nad Labem přestěhovala samička pandy
červené Geena, která se tak stala přítelkyní tamního samečka Solea.</p>

<h2 id="cerven">Červen</h2>
<p>Vyšel nový Firefox 1.5.0.4 a&nbsp;Thunderbird ve stejné verzi. Verze
Seamonkey opět vzrostla o jednu setinu a&nbsp;usadila se na verzi 1.0.2. Autoři
Songbirdu (hudebního přehrávače založeného na Gecku) vydali novou testovací
verzi označenou jako not-yet-0.2 (v&nbsp;překladu ještě-ne-0.2).</p>

<p>Mozilla
<a href="/zpravicky/mozilla-vyhlasena-nejlepsim-vyvojarskym-projektem-v-parizi/">byla vyhlášena</a>
jako nejlepší vývojářský projekt. Hlavním důvodem bylo ocenění vyvinutého úsilí
vývojářů.</p>

<p><a href="/zpravicky/podil-firefoxu-uz-dosahuje-k-hranici-20-procent/">Zastoupení</a>
Firefoxu se podle serveru <a href="http://www.navrcholu.cz/">Navrcholu</a>
přibližuje k&nbsp;20&nbsp;%. Prohlížeče založené na Gecku získaly celkem
21,65&nbsp;%.</p>

<h2 id="cervenec">Červenec</h2>
<p>V&nbsp;červenci pokračovalo vydávání nových setinových a&nbsp;tisícinových
verzí. Aktualizovali jsme Firefox a&nbsp;Thunderbird na verze 1.5.0.5
a&nbsp;SeaMonkey na verzi 1.0.3. Byl představen Firefox 2.0 Beta 1
a&nbsp;další z&nbsp;rodiny Gecko-prohlížečů K-Meleon dospěl do verze
1.0. Vývojáři oznámili ukončení vývoje rozšíření Mozilla Calendar
a&nbsp;jako náhradu označili rozšíření Lightning a&nbsp;samostatný
program Mozilla Sunbird.</p>

<p>Společnosti <a href="http://www.mcafee.com/cz/">McAfee</a>
a&nbsp;<a href="http://www.sophos.com/" hreflang="en">Sophos</a>
<a href="/zpravicky/nebezpecne-rozsireni-pro-firefox/">uveřejnily informace</a>
o&nbsp;nebezpečném rozšíření, které se šíří využitím chyby
v&nbsp;Internet Exploreru či ručního spuštění uživatelem, infikuje
Firefox a&nbsp;získává různé důvěrné informace. Nevyužívalo tedy žádnou
chybu Firefoxu.</p>

<h2 id="srpen">Srpen</h2>
<p>Server SpreadFirefox informoval o&nbsp;tom, že již
<a href="/zpravicky/firefox-stazeno-jiz-200-milionu-kopii/">200 milionů uživatelů</a>
si stáhlo instalační soubory.</p>

<p>Dalším milníkem bylo vydání poštovního klientu Mozilla Thunderbird 2.0
Alpha&nbsp;1, která vylepšuje práci se štítky, má novou podobu
správce doplňků a&nbsp;lepší upozorňování na novou poštu. Vydány byly Firefox 1.5.0.6,
Seamonkey 1.0.4 a&nbsp;K-Meleon 1.0.1.</p>

<p><a href="http://www.oss-watch.ac.uk/" hreflang="en">OSS Watch</a>
<a href="/zpravicky/vysoke-skoly-v-uk-pouzivaji-produkty-mozilla-org/">publikovali studii</a>,
ve které uvádí, že na 77&nbsp;% ze 114 vysokých školách ve Velké Británii
používá produkty Mozilla.</p>

<p><a href="http://www.microsoft.cz/">Microsoft</a>
<a href="/zpravicky/microsoft-chce-pomoci-vyvojarum-mozilly-s-prechodem-na-windows-vista/">pozval</a>
do svých laboratoří v&nbsp;Redmondu vývojáře Firefoxu a&nbsp;Thunderbirdu. Chce
totiž, aby produkty běželi bez problémů na připravovaném systému
Windows Vista. Pozvání bylo výjimečné v&nbsp;tom, že ještě nikdy Microsoft
nepozval a&nbsp;nenabídl pomoc OpenSource projektu. Většinou se do jeho
laboratoří dostávají pouze komerční projekty.</p>

<h2 id="zari">Září</h2>
<p>Adam Harrsion <a href="/zpravicky/kod-firefoxu-pod-lupou/">provedl kontrolu kódu</a>
Firefoxu 1.5.0.6 s&nbsp;nástrojem Klocwork K7. Došel k&nbsp;závěru, že
kód obsahuje 655 defektů a&nbsp;71 bezpečnostních zranitelností.
V&nbsp;závěru uvádí, že je kód velmi dobře napsaný a&nbsp;on sám
považuje Firefox za kvalitní program. Na jeho práci
<a href="/zpravicky/analyzu-kodu-firefoxu-vetsina-problemu-je-plany-poplach/">zareagoval</a>
vývojář Mozilly Robert O'Callahan s&nbsp;dalšími vývojáři. Výsledky
analýzy zkontrolovali a&nbsp;zjistili, že se jedná o planý poplach. Od
<a href="/zpravicky/bezpecnostni-expertka-window-snyder-pracuje-pro-mozillu/">zaměstnání</a>
bezpečnostní expertky Window Snyder si Mozilla slibuje zvýšení bezpečnosti
všech produktů.</p>

<p><a href="/zpravicky/pripravuje-se-novy-system-pro-hlaseni-padu-aplikaci/">Dozvěděli jsme se</a>,
že Mozilla spolupracuje s Googlem na projektu Airbag. Ve výsledku by
měl vzniknout program, který by nahlásil vývojářům příčiny pádu
aplikace a&nbsp;nahradil tak stávající Talkback. Navíc díky
multiplatformosti a&nbsp;otevřenému zdrojovému kódu ho bude moci využít
kterýkoli projekt.</p>

<p>Na konci září vyšel první kandidát na finální verzi Mozilla Firefox 2.0.</p>

<h2 id="rijen">Říjen</h2>
<p>Kromě další smršti v&nbsp;podobě dalších nových verzí (K-Meleon 1.0.2, Flock
0.7.6, Sunbird 0.3, Songbird 0.2, atd.) vyšla po delší době aktualizace
prohlížeče Netscape. No a&nbsp;konec měsíce byl ve znamení
<a href="/zpravicky/firefox-2-0-je-na-svete/">vydání Mozilly Firefox 2.0</a>
a&nbsp;oslavných párty po celém světě. I&nbsp;my jsme oslavovali.
<a href="/zpravicky/firefox-2-0-party/">CZilla party</a> proběhla 4.11.2006
v&nbsp;pražské restauraci <a href="http://ferdinanda.cz/">Ferdinanda</a>.</p>

<p>Všechny nás překvapila
<a href="/zpravicky/bezpecnostni-chyba-firefoxu-pravdepodobne-plany-poplach/">informace</a>,
která byla vypuštěna na hackerské konferenci
<a href="http://www.toorcon.org/2006/about.html" hreflang="en">ToorCon</a>.
Podle ní bylo totiž možné získání kontroly nad počítačem. Nakonec se ukázalo,
že se mělo jednat o&nbsp;vtip.</p>

<h2 id="listopad">Listopad</h2>
<p>V&nbsp;Olomouci
<a href="/zpravicky/firefox-na-konferenci-openchange-v-olomouci/">proběhla</a>
konference <a href="http://www.openchange.cz/">Openchange</a>. Byla na ní
uvedena i&nbsp;přednáška o&nbsp;Firefoxu, kterou si připravil
<a href="/czilla/lide.html#kdo-je-JB">Jiří Burda</a><a> z&nbsp;CZilly.</a></p>

<p>Fakt, že Firefox využívá stále více lidí
<a href="/zpravicky/bankovnictvi-ceske-sporitelny-oficialne-podporuje-firefox/">si uvědomili</a>
i v&nbsp;<a href="http://www.csas.cz/">České spořitelně</a> a&nbsp;zařadili ho
mezi prohlížeče, které oficiálně podporují v&nbsp;bankovním systému
<a href="http://www.servis24.cz/">Servis24</a>.</p>

<p>Mozilla <a href="/zpravicky/adobe-podpori-mozillu/">získala podporu</a>
od další známé společnosti. Adobe totiž nabídla spolupráci
a&nbsp;věnování VM pro běh ActiveScriptu, což v&nbsp;budoucnu urychlí
implentaci 4. edice ECMAScriptu (JavaScriptu).</p>

<h2 id="prosinec">Prosinec</h2>
<p>Před koncem roku vyšlo několik aktualizací. Byl mezi nimi Firefox 2.0.0.1,
Firefox 1.5.0.9, Thunderbird 1.5.0.9, Seamonkey 1.0.7 a&nbsp;Flock
0.7.9.1. Společnost FOXINUS Solutions s.r.o.
<a href="/zpravicky/vysla-1-betaverze-uzivatelske-prirucky-k-mozilla-firefox-2-0/">vydala</a>
opět další příručku (resp. její 1. betaverzi). Tentokrát si posvítili
na Mozillu Firefox 2.0. A&nbsp;jako perlička na konec roku se objevila
<a href="/zpravicky/xul-v-benatkach/">informace o&nbsp;tajemném projektu</a>
internetové televize
<a href="http://www.theveniceproject.com/" hreflang="en">The Venice Project</a>,
který je postaven na XUL a&nbsp;dalších technologiích.

</p><h2 id="konec">Slovo závěrem</h2>
<p>Tak a&nbsp;dostali jsme se až na konec roku 2006, který byl opravdu ve
světě Mozilly bohatý a&nbsp;úspěšný. Tento přehled obsahuje pouze to
nejdůležitější, co jsme prožili. Chcete-li vidět více událostí,
navštivte náš <a href="/zpravicky/2006/">archiv</a>, kde jsou zprávičky
pro rok 2006 chronologicky seřazeny.</p>

<p>Děkujeme všem uživatelům, přispěvovatelům a&nbsp;fanouškům Mozillích
produktů. Kéž by se nám splnilo přání, ať je tento rok pro CZillu
a&nbsp;všechny lidi okolo ní minimálně stejně tak dobrý, jako byl rok 2006.</p>

