<h2>Pohled do historie</h2>

<p>Před více než deseti lety se objevila první verze
<a href="http://en.wikipedia.org/wiki/Netscape_Navigator" hreflang="en"
 title="Netscape Navigator: Informace o historii">Netscape Navigatoru</a>,
webového prohlížeče, který se díky své použitelnosti i&nbsp;ojedinělým
funkcím stal rychle nejpoužívanějším prohlížečem následujících let. S&nbsp;jeho
raným úspěchem v&nbsp;letech 1994 až 1996 je spojen i&nbsp;rozmach webu jako
média: Netscape zpřístupnil web běžným uživatelům.</p>

<p>Úspěch webu i&nbsp;dominance
<a href="http://en.wikipedia.org/wiki/Netscape_Communications_Corporation"
hreflang="en" title="Netscape: Informace o historii">Netscape</a> v&nbsp;mnoha
webových oblastech
byla tak silná, že softwarový gigant Microsoft začal Internet brát vážně.
V&nbsp;pozici Netscape viděl dokonce své ohrožení: kdo by se staral
o&nbsp;operační systém, když aplikace mohou běžet v&nbsp;internetovém
prohlížeči. Začal jednat: když se nepovedlo Netscape koupit, rozhodl se jej
porazit.</p>

<p>Na základech stařičkého prohlížeče
<a href="http://en.wikipedia.org/wiki/Mosaic_%28web_browser%29" hreflang="en"
 title="Mosaic: Informace o historii">Mosaic</a> začal vznikat Internet
Explorer. První dvě verze byly uživatelům k&nbsp;smíchu, s&nbsp;třetí verzí
ale začala "válka prohlížečů". Vylepšení měla u&nbsp;obou firem přednost
před opravami chyb, uživatelé dostávali nestabilní aplikace
s&nbsp;bezpečnostními chybami. Obětí se staly i&nbsp;webové standardy: obě
společnosti chtěly uživatelům nabídnout něco navíc a&nbsp;tak si rozšiřovaly
specifikace po svém bez ohledu na standardizační organizace.</p>

<p>Při vydání MSIE&nbsp;4.0 na podzim 1997 byl podíl na trhu 72&nbsp;% ku
18&nbsp;% ve prospěch Netscape. Netscape byl ale malou firmou, která měla
příjmy spojené s&nbsp;několika produkty. Microsoft se mohl díky příjmům
z&nbsp;Windows pokusil příjmy Netscape omezit: pokud měl Netscape zisk
z&nbsp;prodeje prohlížeče, rozdával Microsoft svůj prohlížeč zdarma, pokud
měl Netscape zisk z&nbsp;prodeje serverových aplikací, Microsoft vyvinul
vlastní a&nbsp;opět je nabízel uživatelům zdarma. Netscape tak přicházel
o&nbsp;životadárný "vzduch", tolik potřebný pro úspěšný vývoj aplikací.</p>

<p>S&nbsp;vydáním tehdy kvalitního MSIE&nbsp;5.0 v&nbsp;roce 1998 obsadil
Microsoft trůn webového prohlížeče. Integrace Internet Exploreru do Windows
i&nbsp;vlastní neschopnost přizpůsobit prohlížeč novým trendům byly poslední
kapky pro Netscape.</p>

<p>Tehdy nejspíš vznikl nápad přijmout myšlenku Erica Raynmonda
(publikovanou několik měsíců předtím) a&nbsp;
<a href="http://wp.netscape.com/newsref/pr/newsrelease558.html"
hreflang="en" title="Tisková zpráva o uvolnění zdrojového kódu">uvolnit zdrojové kódy jako Open
Source</a>. Důvod byl jednoduchý. Netscape by nejspíš vlastními silami nedokázal
odstranit všechny známé problémy svého prohlížeče, že by mu s&nbsp;tím
komunita dobrovolníků pomohla. Vznikla Mozilla.org.</p>


<h2>Vznik Mozilla.org</h2>

<p><a href="http://wp.netscape.com/newsref/pr/newsrelease577.html"
hreflang="en" title="Tisková zpráva o založení Mozilla.org">Netscape
založil Mozilla.org</a> s&nbsp;cílem vytvořit vše potřebné pro nové
verze prohlížečů nesoucí jméno <a href="http://www.netscape.com"
hreflang="en" title="Netscape: Domovská stránka">Netscape</a>. Karty byly rozdány tak, že
<a href="http://www.mozilla.org" hreflang="en"
title="Mozilla.org: Domovská stránka">Mozilla.org</a> měla
vydávat pouze <a href="/produkty/suite/"
title="Mozilla Suite: Informace o produktu">Mozilla Suite</a> jako ukázku technologií
a&nbsp;Netscape sám bude nabízet výsledky koncovým uživatelům. Situace po
<a href="http://www.mozilla.org/releases/mozilla1.0/"
hreflang="en" title="Mozilla Suite 1.0: Poznámky k verzi">vydání
Mozilla Suite&nbsp;1.0</a> však ukázala, že to byla mylná představa.</p>

<p>Vývoj Mozilly byl od prvopočátku silně podporován vývojáři pocházejících
z&nbsp;Netscape. Jejich počet byl ale postupně snižován. V současné době
tvoří organizaci <a href="http://www.mozilla.org/foundation/"
hreflang="en" title="Mozilla Foundation: Informace o nadaci">Mozilla
Foundation</a> několik stálých zaměstnanců, kteří
projekt koordinují. Veškerá práce je na několika stovkách dobrovolných
vývojářů, kteří se ve svém volném čase podílejí na tvorbě aplikací.</p>


<h2>Mozilla Suite</h2>

<p>Mozilla.org od roku 2002 v&nbsp;pravidelných intervalech vydává balík
webových aplikací Mozilla Suite. Jedná se balík, který obsahuje webový
prohlížeč, poštovního klienta, editor webových stránek a&nbsp;IRC klienta.
Tento produkt lze provozovat na řadě platforem jako například GNU/Linux či
Windows.</p>

<p>Při vývoji celého balíku je
<a href="/podpora/bezpecnost-mozilly-a-odvozenych-prohlizecu.html">kladen
velký důraz na otázku bezpečnosti</a>,
která díky dobře navrhnutém bezpečnostním modelu chrání uživatele před
potencionálně nebezpečným prostředím, kterým Internet bezesporu je.
V&nbsp;prohlížeči samotném je pak velký důraz kladen na podporu obecně
uznávaných standardů z&nbsp;dílny <a href="http://www.w3.org/"
hreflang="en" title="W3C: Domovská stránka">W3C</a>.</p>

<p>To ovšem není vše, čím se Mozilla.org od začátku zabývá. Možná se to na
první pohled nezdá, ale Mozilla je též vývojovou platformou. Ano, Mozilla
poskytuje řadu technologií, na jejímž základě lze stavět vlastní
multiplatformní aplikace. A&nbsp;nejen to. Poměrně snadno lze upravit ty
stávající a&nbsp;pomocí <a href="/doplnky/rozsireni/">rozšíření</a> jim
přidat nové funkce. Díky tomu vznikla
řada dalších zajímavých projektů a&nbsp;rozšíření.</p>


<h2>Přichází Firefox a&nbsp;Thunderbird</h2>

<p>Mozilla Suite si postupem času získala řadu příznivců i&nbsp;odpůrců. Na
jednu stranu se jednalo o&nbsp;komplexní produkt, který působil dosti
robustně, nabízel řadu funkcí, ale to bylo právě to, co část uživatelů
nechtěla. Část z&nbsp;nich balíku vytýkala příliš velké hardwarové nároky
a&nbsp;málo konfigurovatelné grafické rozhraní. To si uvědomila i&nbsp;malá
skupina vývojářů, kteří začali vyvíjet prohlížeč pojmenovaný Phoenix.</p>

<p>Malý, multiplatformní prohlížeč webových stránek založený na nové
architektuře byl z&nbsp;prvopočátku na okraji zájmu jak vývojářů, tak
uživatelů. Prohlížeč byl postupem času dvakrát přejmenován: nejprve na
Mozilla Firebird, a&nbsp;později, z&nbsp;důvodu kolize jména s&nbsp;jiným
projektem, na <a href="/produkty/firefox/"
title="Mozilla Firefox: Stránka o produktu">Mozilla Firefox</a>.</p>

<p>Firefox, samostatný prohlížeč s&nbsp;menšími požadavky na hardware, je
uživatelský přívětivější a&nbsp;v&nbsp;základní konfiguraci nabízí jen to, co
chce většina uživatelů. Na podobném principu krátce nato vznikl samostatný
e-mailový klient <a href="/produkty/thunderbird/"
title="Mozilla Thunderbird: Stránka o produktu">Mozilla Thunderbird</a>.
Oba tyto produkty si za dobu své
existence získaly řadu příznivců a&nbsp;jsou to dnes hlavní produkty Mozilla
Foundation.</p>


<h2>CZilla</h2>

<p><a href="/czilla/" title="Projekt CZilla: Informace o projektu">Projekt CZilla</a>
začal vznikat v&nbsp;srpnu roku 2001. Tehdejšímu
zakladateli, Janu Urbánkovi, se nelíbila situace na českém Internetu, která
byla dosti nepřátelská vůči alternativním prohlížečům. U&nbsp;nás
neexistoval žádný informační zdroj, kde by mohl uživatel čerpat informace
o&nbsp;Mozille. V&nbsp;relativně krátkém čase tedy vznikly stránky
CZilla.org. Po nějakém čase se projekt rozrostl a&nbsp;dostal se do podoby,
jakou má dnes. Tu naleznete na adrese <a href="http://www.czilla.cz">CZilla.cz</a></p>

<p>Základní činností CZilly je samozřejmě lokalizace produktů Mozilla.org do
češtiny. Jedná se o&nbsp;již zmíněné Mozilla Suite, Firefox
a&nbsp;Thunderbird. Lokalizace ale není vše. Pokud uživatel začne používat
některý z&nbsp;těchto programů, má jistě řadu otázek. Na CZille naleznete
<a href="http://forum.czilla.cz">fórum na podporu uživatelů</a>,
kde na otázky uživatelů pokouší odpovídat řada
zkušených uživatelů. Uživatelé na webu naleznou řadu
zajímavých materiálů, návodů a&nbsp;článků.</p>

<p>V&nbsp;neposlední řadě se CZilla snaží o&nbsp;<a
href="http://developer.mozilla.org/cs/docs/Webov%C3%A9_standardy">propagaci webových
standardů</a>. Bohužel stále se vyskytuje řada webů, které jsou špatně vytvořeny
a&nbsp;obsahují nestandardní či chybný kód, který způsobuje jeho špatné
zobrazeni v&nbsp;řadě moderních prohlížečů. Je naší snahou webmastery těchto
webů na chyby upozorňovat a&nbsp;společně s&nbsp;nimi hledat vhodná
řešení.</p>


<h2>Budoucnost Mozilly</h2>

<p>S&nbsp;blížícími se finálními verzemi Mozilla Firefoxu
a&nbsp;Thunderbirdu lze očekávat dlouhodobě vzrůstající zájem uživatelů
o&nbsp;tyto produkty. Mozilla Suite si i&nbsp;nadále bude udržovat stabilní
oblibu u&nbsp;uživatelů a&nbsp;je očekáván vzrůstající zájem zejména firemní
sféry. Na konferenci Mozilla Developer Day, která proběhla letos
v&nbsp;srpnu, byly prezentovány plány na Mozilla&nbsp;2.0, která by dle plánů
měla být dostupná přibližně za rok. Oproti nynějšímu balíku jsou očekávány
výraznější změny.</p>

<p>Pro české uživatele je připravována lokalizace nápovědy k&nbsp;Mozilla
Suite. Tato nápověda by měla být k&nbsp;dispozici v&nbsp;rámci Mozilla
Suite&nbsp;1.8, která je plánována na začátek jara příštího roku. Na
webových stránkách CZilly lze do budoucna očekávat nové články
a&nbsp;návody, které budou určeny jak pro koncové uživatele, tak uživatele
z&nbsp;řad firemní sféry. Celkově lze očekávat slibnou budoucnost Mozilly
jak u&nbsp;nás, tak v&nbsp;zahraničí.</p>

