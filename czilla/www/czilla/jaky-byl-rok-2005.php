<p>Starý kalendář už skončil v&nbsp;koši, na stěně visí ten s&nbsp;letopočtem
2006, a&nbsp;nás něco nutí bilancovat. Pojďte se tedy s&nbsp;námi podívat,
co přinesl rok 2005 do světa Mozilly.</p>

<h2 id="leden">Leden</h2>

<p>Martin Hassman <a href="/czilla/jaky-byl-rok-2004.html">bilancuje rok 2004</a>
a&nbsp;Pavel Cvrček vydává novou verzi  <a
 href="http://mozbackup.jasnapaka.com/">MozBackupu</a>, nástroje na zálohování
profilů Mozilly. Firefox vyhrál ocenění pro
<a hreflang="en" href="http://arstechnica.com/columns/linux/linux-20050102.ars">nejlepší
desktopovou aplikaci roku</a>, v PCWorldu jej zase označili za
<a hreflang="en" href="http://www.pcworld.com/reviews/article/1,aid,118794,pg,2,00.asp">nejlepší
prohlížeč</a>. Navzdory tomu, <a
 hreflang="en"
 href="http://news.com.com/Gates+taking+a+seat+in+your+den/2008-1041_3-5514121.html?tag=nefd.lede">Bill
 Gates se Firefoxu nebojí</a>.</p>

<p><a href="/czilla/tiskove-zpravy/adoptujte-pandu.html">Sbírka na pandy červené</a> se
dočkala nečekaného úspěchu a&nbsp;díky přispěvatelům bylo možné adoptovat ne jednu,
ale hned dvě pandy. Plni&nbsp;radosti vydáváme návod
<a href="/czilla/jak-pomoci/preklad-rozsireni.html">Překládáme rozšíření</a>, který ještě
v&nbsp;lednu následovaly návody na
<a href="/podpora/firefox/prohlizeni-v-panelech.html">prohlížení v&nbsp;panelech</a>,
<a href="/podpora/firefox/instalace-motivu-vzhledu.html">instalaci motivů
vzhledu</a> ve Firefoxu a&nbsp;nakonec návod na
<a href="http://www.czilla.cz/doplnky/vyhledavani/vytvoreni.html">vytvoření
vyhledávacího modulu</a>.</p>

<p>Ani v&nbsp;zahraničí nelenili, vyšlo <a
 hreflang="en"
 href="http://www.glazman.org/weblog/dotclear/index.php?2005/01/05/787-nvu-070">Nvu
  0.7</a>, <a hreflang="en" href="http://www.mozilla.org/releases/mozilla1.8a6/">Mozilla Suite
  1.8a6</a>, Firefox získal v&nbsp;USA podíl <a
  hreflang="en"
  href="http://www.techweb.com/wire/software/57702548">přes 5&nbsp;% uživatelů</a>
  a&nbsp;naši <a hreflang="sk" href="http://www.mozilla.sk/">slovenští kolegové</a>
  vydali lokalizační balíček pro Firefox 1.0. V&nbsp;závěru měsíce spatřila světlo
světa lokalizace Nvu 0.7, nový <a href="/doplnky/rozsireni/">přehled
lokalizovaných rozšíření</a> a&nbsp;dozvěděli jsme se, že
<a hreflang="en" href="http://weblogs.mozillazine.org/ben/archives/007434.html">Firefox 1.1
vyjde až v&nbsp;červnu</a>.</p>

<h2 id="unor">Únor</h2>

<p>Bezpečnostní problémy se nevyhnuly ani Mozille, ale byly
<a
 hreflang="en" href="http://www.mozilla.org/projects/security/known-vulnerabilities.html">včas
opraveny</a>. Vyšlo <a
 hreflang="en" href="http://www.glazman.org/weblog/dotclear/index.php?2005/02/02/852-nvu-080">Nvu
 0.8</a> (včetně <a href="http://ftp.czilla.cz/other/nvu/0.8/">lokalizace</a>),
 objevila se první verze
 <a hreflang="en" href="http://www.mozillazine.org/talkback.html?article=6002">podpory pro
XForms</a> pro Mozillu a&nbsp;také <a hreflang="en"
 href="http://www.mozilla.org/projects/calendar/sunbird.html">Sunbird</a> 0.2,
samostatný kalendář postavený na technologiích Mozilly.</p>

<p>Firefox získal v&nbsp;ČR ocenění
<a href="http://www.softnov.cz/index.php?ID=20413">Produkt roku 2004</a>,
sbírka na pandy skončila <a href="/czilla/adopce.html">s&nbsp;výtěžkem 38 tisíc
Kč</a>. Uvedli jsme spoustu nových lokalizovaných rozšíření a&nbsp;naše řady
rozšířili <a href="http://www.petrovicky.net/">Lukáš Petrovický</a> a&nbsp;<a
 href="http://www.majda.cz/">David Majda</a>.</p>

<p>Firefox se začal prosazovat i&nbsp;v&nbsp;komerčním světě - Yahoo! vydává verzi
<a hreflang="en" href="http://toolbar.yahoo.com/firefox">nástrojové lišty pro Firefox</a>,
Ask Jeeves koketuje s&nbsp;myšlenkou
<a hreflang="en" href="http://blog.ask.com/2005/02/mozillas_on_fir.html">upravit si Firefox
k&nbsp;obrazu svému</a> a&nbsp;Copernic Desktop Search již  <a hreflang="en"
 href="http://www.copernic.com/en/company/press/press-releases/press_70.html">podporuje
kromě Firefoxu i&nbsp;Thunderbird</a>. Tato situace rozhýbala i&nbsp;Microsoft,
který překvapivě oznámil, že <a hreflang="en"
 href="http://www.microsoft.com/presspass/press/2005/feb05/02-15RSA05KeynotePR.asp">první
 betaverze nového Microsoft Internet Exploreru se objeví v&nbsp;létě</a>.</p>

<p><a hreflang="en"
 href="http://www.websidestory.com/services-solutions/datainsights/spotlight.html">Podíl
 Firefoxu neustále roste</a> - aby také ne, vždyť už si jej
 <a hreflang="en" href="http://www.mozilla.org/press/mozilla-2005-02-16.html">stáhlo 25
 milionů lidí</a>! <a hreflang="en"
  href="http://weblogs.mozillazine.org/asa/archives/007630.html">Mozilla
 Foundation se rozrůstá</a> a&nbsp;Firefox dostává od vývojářů <a hreflang="en"
  href="http://www.mozillazine.org/talkback.html?article=6133">přepracované okno
  s&nbsp;nastavením</a>.</p>

<h2 id="brezen">Březen</h2>

<p>Dosáhli jsme počtu <a
 href="/archiv-2005.html#news:82507e9a32655eaa18062fa7515357ff">50
 lokalizovaných rozšíření</a> a&nbsp;<a
 href="/archiv-2005.html#news:310ea3eafdff123790320cf2369995f8">přepracovali
 jejich přehled</a>. Mozilla <a hreflang="en"
 href="http://news.zdnet.co.uk/0,39020330,39189475,00.htm">spolupracuje
 s&nbsp;Google</a> a&nbsp;také <a hreflang="cn" href="http://www.mozilla.org.cn/">otevírá zastoupení
 v&nbsp;Číně</a>.</p>

 <p>Vychází <a hreflang="en" href="http://browser.netscape.com/nsb/">betaverze Netscape 8</a>,
 <a hreflang="en" href="http://www.google.com/press/pressrel/desktopsearch_10.html">Google
 Desktop Search podporuje Firefox</a> a&nbsp;Mozilla Foundation oznamuje <a hreflang="en"
 href="http://www.mozilla.org/seamonkey-transition.html">ukončení vývoje Mozilla
 Suite</a>, za což sklízí od komunity bouři nevole - i&nbsp;v &nbsp;CZille jsme
 o&nbsp;tom vášnivě debatovali :) Život však jde dál a&nbsp;objevuje se <a hreflang="en"
  href="http://www.glazman.org/weblog/dotclear/index.php?2005/03/11/926-nvu-090-released">Nvu
 0.9</a>. V&nbsp;USA mají <a
  href="/archiv-2005.html#news:b6f3768bc75cb0dd76de01759d047841">knihy
 o&nbsp;Firefoxu a&nbsp;Thunderbirdu</a>, <a hreflang="en"
 href="http://www.mozilla.org/projects/security/known-vulnerabilities.html#firefox1.0.2">Firefox
 1.0.2</a> opravuje další bezpečnostní chyby.</p>

<p>Program PC Translator nyní <a
 href="/archiv-2005.html#news:3a97537390d37369840016b38c13346c">lze integrovat
do Firefoxu</a>, vývojáří představili
nového <a hreflang="en" href="http://weblogs.mozillazine.org/ben/archives/007792.html">správce
rozšíření pro budoucí verzi Firefoxu</a>, <a hreflang="en"
 href="http://weblogs.mozillazine.org/ben/archives/007834.html">Google
zaměstnává vývojáře Mozilly</a> a&nbsp;končí měsíc zprávou, že jeho <a hreflang="en"
 href="http://www.google.com/googleblog/2005/03/enhanced-searching-with-firefox.html">vyhledávání
 bude ve Firefoxu rychlejší</a>.</p>

 <h2 id="duben">Duben</h2>

<p>Na <a hreflang="en" href="http://www.mozillazine.org/talkback.html?article=6334">apríla
jsme si užili</a> legraci, ale vývoj Mozilly se kvůli tomu nezastavil.
Došlo k&nbsp;<a hreflang="en" href="http://www.mozillazine.org/talkback.html?article=6321">vylepšení
správy vyskakovacích oken</a>, prezidentka Mozilla Foundation se
<a hreflang="en" href="http://www.time.com/time/2005/time100/">objevila mezi 100
nejvlivnějšími lidmi současnosti</a> a&nbsp;objevily se upřesněné
<a hreflang="en" href="http://article.gmane.org/gmane.comp.mozilla.devel.seamonkey/5182">plány
vývoje Firefoxu a&nbsp;Thunderbirdu 1.1</a>.</p>

<p>Mozilla <a hreflang="en" href="http://www.steelgryphon.com/blog/?p=45">najala dalšího
vyvojáře</a>, <a hreflang="en"
 href="http://www.mozilla.org/products/firefox/releases/1.0.3.html">Firefox
1.0.3</a> opravuje známé bezpečnostní chyby a&nbsp;ve vývojové verzi Firefoxu se
objevila <a hreflang="en"
 href="http://weblogs.mozillazine.org/chase/archives/2005/04/svg_here_we_com.html">podpora
 SVG</a> a&nbsp;<a hreflang="en" href="http://www.mozillazine.org/talkback.html?article=6461">značky
 &lt;canvas&gt;</a>, která umožňuje skriptům kreslit do stránek. Vývojáři také
 oznámili, že v&nbsp;budoucnu bude <a hreflang="en"
 href="http://weblogs.mozillazine.org/roc/archives/2005/04/glimpse_of_the.html">
 k&nbsp;vykreslování v&nbsp;prohlížeči použita 3D akcelerace</a>. V&nbsp;Evropě
se Mozille podařilo dosánout <a hreflang="en"
 href="http://www.adtech.info/NewsPresse/news_detail.php?lang_id=en&amp;node=4&amp;current=190">10&nbsp;%
uživatelů</a> a&nbsp;Firefox ke konci měsíce dosáhl počtu <a hreflang="en"
 href="http://www.spreadfirefox.com/fifty/">50 milionů stažení</a>. Dokonce už
 i&nbsp;<a href="http://www.lupa.cz/clanky/tomas-koska-s-windows-je-vic-zabavy/">Microsoft
 uznává Firefox za konkurenci</a>!</p>

<p>A&nbsp;co se mezitím dělo <q>doma</q>? Rozhodně jsme nezaháleli &ndash;
připravili jsme <a href="http://developer.mozilla.org/cs/docs/Opravy_chyb_webmaster%C5%AF">průvodce nejčastějšími
chybami webmaterů</a>, vydali mnoho nových lokalizovaných rozšíření a&nbsp;připravili
<a href="/archiv-2005.html#news:1c1099ee47420c830ab5bbd8dd9a706f">klubová
třička</a> pro naše přispěvatele a&nbsp;fanoušky :) Pavel Cvrček udělal
<a href="http://www.abclinuxu.cz/clanky/show/82998">rozhovor pro AbcLinuxu</a>.</p>

<p>Událostí měsíce pro nás nepochybně byla výstava <a
 href="http://www.linuxexpo.cz/">LinuxExpo</a>, kde jsme <a
 href="/archiv-2005.html#news:8bfa5d06356618e1854cad69bd94ad8d">několik dní
prezentovali produkty Mozilla</a>. Nebylo téměř chvíle, kdy by u&nbsp;našeho stánku
nepostávali zvědaví návštěvníci a&nbsp;nepokládali nám všetečné otázky, na které
jsme s&nbsp;radostí odpovídali. S úspěchem se také setkala přednáška Davida Majdy
o&nbsp;Mozille jako vývojové platformě.</p>

 <h2 id="kveten">Květen</h2>

<p>Na začátku května jsme spustili sekci pro vývojáře
a&nbsp;rozhovoru se nevyhnul ani <a
 href="http://linuxexpres.cz/index.php?show=001053008001">Martin Hassman</a>.
 Podle jistých měření dosáhl <a hreflang="en"
 href="http://translate.google.com/translate?prev=/language_tools&amp;u=http://www.xitimonitor.com/etudes/equipement6.asp">Firefox
 v&nbsp;Česku téměř 19&nbsp;%</a> a&nbsp;vývojáři oznamují, že <a hreflang="en"
 href="http://weblogs.mozillazine.org/ben/archives/008066.html">nový Firefox
 bude mít vylepšený systém aktualizací</a>. <a
 href="http://ihned.cz/3-22482105-Firefox-000000_d-fd">Lidé opouštějí IE, banky
 modernizují weby</a>. To nám to ale pěkně začalo&hellip;</p>

<p><a href="/doplnky/rozsireni/">Lokalizovaná rozšíření</a> stále přibývají, ale
ve Firefoxu byly <a hreflang="en"
 href="http://www.mozillazine.org/talkback.html?article=6582">objeveny
bezpečnostní chyby</a>, vychází Firefox 1.0.4 aby je opravil. Zjistili jsme, <a hreflang="en"
 href="http://www.informationweek.com/story/showArticle.jhtml?articleID=163100275">co přinese
nová verze Thunderbirdu</a>. <a hreflang="en"
 href="http://news.com.com/IBM+backs+Firefox+in-house/2100-7344_3-5704750.html">IBM
 přechází na Firefox</a> a&nbsp;ani <a hreflang="en"
 href="http://news.bbc.co.uk/1/hi/business/4508897.stm">Bill Gates neodolal</a>
 a&nbsp;Firefox nainstaloval.</p>

<p>Po dlouhých přípravách spouštíme web <a href="http://thunderbird.czilla.cz">Používejte
Thunderbird</a> a&nbsp;v&nbsp;zámoří mezitím <a hreflang="en"
 href="http://gemal.dk/blog/2005/05/19/netscape_80_released/">vychází Nestcape 8</a>.
V&nbsp;závěru měsíce se dozvídáme, že nový <a hreflang="en"
 href="http://blogs.msdn.com/ie/archive/2005/05/26/422103.aspx">Internet
Explorer bude podporovat prohlížení v&nbsp;panelech</a>. Chtělo by se říct, že
už bylo načase :)</p>

<h2 id="cerven">Červen</h2>

<p>Světlo světa spatřila <a hreflang="en"
 href="http://www.mozilla.org/projects/deerpark/releases/alpha1.html">první
 alpha verze nového Firefoxu</a>, nazvaná Deer Park Alpha&nbsp;1.
 <a href="http://www.ceskenoviny.cz/zpravy/index_view.php?id=132346">Gecko
 odletělo do vesmíru</a> a&nbsp;<a hreflang="en"
 href="http://forums.mozillazine.org/viewtopic.php?t=273994">alpha verze se dočkal
i&nbsp;Thunderbird</a>. Firefox byl v &nbsp;Americe <a hreflang="en"
 href="http://www.pcworld.com/reviews/article/0,aid,120763,findid,48080,00.asp">zvolen
 produktem roku 2005</a> a&nbsp;dočkali jsme se i&nbsp;<a hreflang="en"
 href="http://www.mozdev.org/community/books.html">přehledu vydaných knih
 o&nbsp;Firefoxu</a>.</p>

<p>Nová Java bude obsahovat JavaScript od Mozilla.org, který bude <a hreflang="en"
 href="http://weblogs.mozillazine.org/roadmap/archives/008325.html">v budoucnu
podstatně vylepšen</a>. Změnami prošly i&nbsp;<a hreflang="en"
 href="http://gemal.dk/blog/2005/06/14/basics_of_new_filter_actions_like_auto_reply_forward_bounc/">filtry
v&nbsp;Thunderbirdu</a>, Nvu se konečně <a hreflang="en"
 href="http://www.glazman.org/weblog/dotclear/index.php?2005/06/29/1122-nvu-10-is-now-available">dočkalo
verze 1.0</a> a <a hreflang="en" href="http://weekinlife.blogspot.com/2005/06/week-in-soes-life.html">našlo
si cestu i&nbsp;do škol</a>. Podle českých měření má <a
 href="http://www.iinfo.cz/tiskova_zprava/navrcholu_firefox_posiluje_pozici/">Mozilla
 v&nbsp;Čechách již&nbsp;17 %</a>.</p>

<h2 id="cervenec">Červenec</h2>

<p>Ani v&nbsp;létě dění ve světě Mozilly neutichlo. Vznikly plány na <a hreflang="en"
 href="http://www.vlad1.com/~vladimir/blog/archives/2005/07/05/60/">přepsání kódu
záložek</a> ve Firefoxu a&nbsp;Evropským parlamentem <a
 href="http://www.root.cz/zpravicky/ne-softwarovym-patentum/">neprošly softwarové
patenty</a>. Firefox dosáhl <a hreflang="en"
 href="http://www.spreadfirefox.com/?q=node/view/17221">75 milionů stažení</a>
a&nbsp;téměř <a hreflang="en"
 href="http://www.pcworld.idg.com.au/index.php/id;1494315312;fp;2;fpid;1">9&nbsp;%
světového podílu</a> &ndash; pěkně nám roste :)</p>

<p>V&nbsp;Bugzille projektu Mozilla se objevil <a hreflang="en"
 href="http://weblogs.mozillazine.org/gerv/archives/008471.html">třísettisící záznam</a>
a&nbsp;vývojáři vydali <a hreflang="en"
 href="http://www.mozilla.org/projects/deerpark/releases/alpha2.html">Deer Park
 Alpha&nbsp;2</a>, další ukázku nové verze Firefoxu. Současně s&nbsp;ní vychází také
Mozilla Firefox 1.0.5, který opravuje bezpečnostní chyby.</p>

<p>Z plánovaného Firefox 1.1 je najednou Firefox 1.5 a&nbsp;my se dozvídáme, že
elektronické bankovnictví <a
 href="https://ib24.csob.cz/idr/clanek.asp?prmKod=CZ.ACT.IB_CZ.99700&amp;a=0&amp;prmLng=cz&amp;destination=iframe">ČSOB
 bude podporovat Firefox</a>. Vychází <a hreflang="en"
 href="http://www.flexbeta.net/main/articles.php?action=show&amp;id=102">Internet
 Explorer 7 beta&nbsp;1</a>, <a
  href="/archiv-2005.html#news:44295b8aa97242d36b3dc6ec09be51f2">CZilla vydává
  100. lokalizované rozšíření</a> a&nbsp;projekt SeaMonkey, nástupce Mozilla Suite,
<a hreflang="en" href="http://www.mozilla.org/projects/seamonkey/artwork.html">vybírá nové
logo</a>.</p>

<h2 id="srpen">Srpen</h2>

<p><a hreflang="en" href="http://www.mozilla.org/">Mozilla Foundation</a> reorganizuje&nbsp;a
vzniká <a hreflang="en" href="http://www.mozilla.com/">Mozilla Corporation</a>. Firefox byl
<a hreflang="en" href="http://weblogs.mozillazine.org/asa/archives/008701.html">stažen 80
miliony lidí</a> a&nbsp;bylo spočítáno, že <a hreflang="en"
 href="http://wiki.mozilla.org/L10n:Firefox_1.5_Status">lokalizace Firefoxu již
 existují pro 95&nbsp;% populace</a>. Dozvěděli jsme se také, že <a hreflang="en"
 href="http://developer.mozilla.org/devnews/index.php/2005/08/30/9-days-untill-15-beta/">Firefox 1.5
 beta&nbsp;1 vyjde v&nbsp;září</a>.</p>

<p>Na AbcLinuxu vychází <a href="http://www.abclinuxu.cz/clanky/show/96138">seriál
o&nbsp;tvorbě rozšíření</a> a&nbsp;na našem webu se celý měsíc nesl ve znamení
aktualizací  lokalizací rozšíření a&nbsp;vydávání rozšíření nových.</p>

<h2 id="zari">Září</h2>

<p>Léto pomalu končí a&nbsp;s&nbsp;ním i&nbsp;<a hreflang="en"
 href="http://www.mozillazine.org/talkback.html?article=7252">podpora SSL2
 ve Firefoxu</a>. Toto oznámení provází komunitní kampaň, která nabádá
 administrátory, aby na svých serverech tento zastaralý způsob zabezpečení
 nepoužívali. <a hreflang="en"
  href="http://www.mozillazine.org/talkback.html?article=7260">Mozdev slaví
páté narozeniny</a> a&nbsp; kalendář <a hreflang="en"
 href="http://www.babylonsounds.com/2005/09/sunbirdcalendar-progress-report.html">Sunbird
 stále žije</a>, i&nbsp;když se tak na první pohled nezdá.</p>

<p>Vychází <a hreflang="en"
 href="http://www.mozilla.org/products/firefox/releases/1.5beta1.html">Mozilla
Firefox 1.5 beta&nbsp;1</a>, <a hreflang="en"
 href="http://www.mozilla.com/thunderbird/releases/1.5beta1.html">Thunderbird
jakbysmet</a>, a&nbsp;my ve velkém
aktualizujeme lokalizovaná rozšíření, aby je podporovala. Projekt SeaMonkey
vydal první alpha verzi a&nbsp;<a hreflang="en"
 href="http://weblogs.mozillazine.org/seamonkey/archives/2005/09/logo_contest_closing_next_week.html">brzy
vybere nové logo</a>. <a hreflang="en" href="http://developer.mozilla.org/">Vývojářská
dokumentace Mozilly</a> už také vypadá k světu.</p>

<p>Vývojáři <a hreflang="en"
 href="http://marketing.openoffice.org/ooocon2005/schedule/thursday_developer.html#d3">OpenOffice.org
přemýšlí o&nbsp;začlenění Gecka</a> do svého kancelářského balíku, <a hreflang="en"
 href="http://www.mozilla.org/products/firefox/releases/1.0.7.html">Firefox 1.0.7
opravuje další bezpečnostní chyby</a> a&nbsp;mobilní <a hreflang="en"
  href="http://weblogs.mozillazine.org/dougt/archives/008944.html">Minimo se
dočkalo verze 0.009</a>. V závěru měsíce se dozvídáme, že <a hreflang="en"
  href="http://standblog.org/blog/2005/09/26/93114388-mozilla-europe-spreading-mozilla-products-one-language-at-a-time">Firefox
má v&nbsp;Evropě přes 15&nbsp;%</a>.</p>

<h2 id="rijen">Říjen</h2>

<p>V&nbsp;Česku vychází první <a
 href="https://www.cpbooks.cz/Book.asp?ID=1759">kniha o&nbsp;Firefoxu</a> (na kterou
později <a
  href="/podpora/firefox/recenze-kompletni-prirucka.html">vydáváme
recenzi</a>), v&nbsp;zahraničí zase Firefox a&nbsp;Thunderbird 1.5 beta&nbsp;2.
Vylepšení vykreslování, které uvidíme ve Firefoxu 3, se konečně dostalo do
hlavního vývojového stromu a&nbsp;Firefox 1.0.x mezitím <a hreflang="en"
 href="http://mozillazine.org/talkback.html?article=7572">dosáhl 100 milionů
stažení</a> &ndash; netrvalo mu to ani rok! <a href="http://bugzilla.bata.cz/">BugZilla
se dočkala českého překladu</a> a&nbsp;vypadá to, že <a hreflang="en"
 href="http://developer.mozilla.org/devnews/index.php/2005/10/25/15rc1-update/">Firefox
1.5 RC1 bude příští měsíc</a>.</p>

<h2 id="listopad">Listopad</h2>

<p><a hreflang="en"
 href="http://www.numenity.org/blog/2005/10/31/mozilla-site-changes/">Mozilla
 reorganizuje weby</a>, Firefox ve světě získal <a hreflang="en"
  href="http://www.onestat.com/html/aboutus_pressbox40_browser_market_firefox_growing.html">více
než 11&nbsp;% uživatelů</a> a&nbsp;formou soutěže nabádá vývojáře, aby <a hreflang="en"
 href="http://weblogs.mozillazine.org/asa/archives/2005/11/extend_firefox.html">vytvářeli
nová rozšíření</a>. Vychází RC1 i&nbsp;RC2 Firefoxu 1.5, <a hreflang="en"
 href="http://weblogs.mozillazine.org/calendar/2005/11/releasing_is_hard_1.html">Sunbird
vydává první ukázku verze 0.3</a>.</p>

<p><a hreflang="en" href="http://cbeard.typepad.com/mozilla/2005/11/mozilla_product.html">Strategie
vývoje Firefoxu se mění</a> &ndash; vše má jít rychleji. Možná proto, aby byl
<a hreflang="en" href="http://www.usabilitynews.com/news/article2786.asp">Firefox nejpoužitelnější
aplikací</a> i&nbsp;po roce 2005. Verze <a hreflang="en"
 href="http://www.mozilla.org/press/mozilla-2005-11-9.html">1.0 slaví první
narozeniny</a> a&nbsp;my <a href="http://www.linuxsoft.cz/firefox/">oslavujeme Firefox
1.5</a>, který je již za dveřmi. Podobné akce, na kterých se můžeme setkat jak navzájem,
tak s&nbsp;příznivci a&nbsp;uživateli Mozilly, máme opravdu rádi a&nbsp;snažíme se,
abych jich bylo stále dostatek.</p>

<p>Aktualizace lokalizovaných rozšíření už se staly rutinou, <a hreflang="en"
 href="http://weblogs.mozillazine.org/dougt/archives/009315.html">vyšlo Minimo 011</a>,
připravuje se <a hreflang="en"
 href="http://www.songbirdnest.com/roblord/story/what_is_songbird">hudební
přehrávač založený na Mozille</a> a&nbsp;koncem měsíce konečně s&nbsp;plnou parádou <a hreflang="en"
 href="http://www.mozilla.com/firefox/releases/1.5.html">vyšel Firefox 1.5</a>.</p>

<h2 id="prosinec">Prosinec</h2>

<p>Nového Firefoxu bylo za první den od vydání <a hreflang="en"
 href="http://weblogs.mozillazine.org/schrep/archives/2005/11/15.html">staženo
1,5 milionu kopií</a>, <a hreflang="en"
 href="http://www.mozilla.org/projects/seamonkey/artwork.html">SeaMonkey má nové
logo</a>, <a hreflang="en"
 href="http://www.melez.com/mykzilla/2005/12/tabbed-message-browsing-in-thunderbird.html">Thunderbird
připravuje prohlížení v panelech</a> a&nbsp;<a
 href="http://www.iinfo.cz/tiskova_zprava/navrcholu_rust_firefoxu_se_zpomalil/">Firefox
v&nbsp;Čechách již používá více než 15&nbsp;% lidí</a>.</p>

<p>Úspěch Firefoxu pohnul i&nbsp;s&nbsp;horou a&nbsp;tak nový Internet Explorer bude mít
pro RSS <a hreflang="en" href="http://blogs.msdn.com/rssteam/archive/2005/12/14/503778.aspx">ikonku
z&nbsp;Firefoxu</a>, <a
 href="/archiv-2005.html#news:6044142dd6a9bd63f25a48b15c1ea327">Google vydává
rozšíření pro Firefox</a> a&nbsp;<a hreflang="en" href="http://www.theinquirer.net/?article=28380">Dell
předinstalovává Firefox na svá PC</a>.</p>

<p><a hreflang="en" href="http://www.mozilla.org/projects/seamonkey/releases/">SeaMonkey 1.0
Beta</a> se stala realitou, <a hreflang="en"
 href="http://wiki.mozilla.org/Global:1.9_Trunk_1.8_Branch_Plan">plán vývoje
Mozilly byl aktualizován</a>, Thunderbird 1.5 se dočkal RC2 a&nbsp;o&nbsp;pár dní
později začaly bouchat šampusy, létat petardy a&hellip; a&hellip; najednou bylo
prvního ledna.</p>

<h2 id="bilancujeme">Bilancujeme</h2>

<p>A&nbsp;tady se musíme s&nbsp;tímto dílem seriálu o&nbsp;jedné roztomilé
ještěrce rozloučit. V&nbsp;díle 2005 se staly různé věci, dobré i&nbsp;špatné.
Přejme si, aby v&nbsp;příštím špatných ubývalo a&nbsp;aby bylo hodně těch,
o&nbsp;kterých je radost psát.</p>

<p>Doufáme, že jste s&nbsp;ním byli spokojeni alespoň tak, jako my, a&nbsp;budeme
potěšeni, zachováte-li nám svou přízeň i&nbsp;do dalšího pokračování.</p>

<p>A&nbsp;na závěr každé produkce se sluší uvést jména aktérů. Tak tedy:
v&nbsp;hlavních rolích v&nbsp;českém znění členové a&nbsp;spolupracovníci <a
 href="/czilla/prispevatele.php">týmu CZilla</a>, rodina, přátelé a&nbsp;známí, kteří
nás v&nbsp;naší práci podporovali, a&nbsp;samozřejmě uživatelé produktů Mozilla,
bez kterých by to nemělo smysl :) Hodně štěstí do nového roku všem!</p>

