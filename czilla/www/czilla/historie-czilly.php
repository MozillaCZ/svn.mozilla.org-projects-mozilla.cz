<p>Během srpna roku 2001 mě popadl nápad, že bych mohl udělat stránky pro
uživatele Mozilly. Byl jsem uživatelem od verze M6, to jest od roku 1999
a&nbsp;přecházel jsem na
<a href="http://www.mozilla.org/projects/seamonkey/release-notes/m6.html">Mozillu</a>
z&nbsp;Netscapu. Shledal jsem situaci na českém internetu dosti žalostnou a&nbsp;nepřátelskouk
&nbsp;alternativnímu prohlížeči. Existoval jen projekt
<a href="http://www.mozilla.cz/">Mozilla.cz</a> - lokalizace, ale neexistoval ani
samotný zdroj informací ani konference o&nbsp;Mozille. Nápad tedy vypadal logický,
vytvořit zdroj informací a&nbsp;stránky pro komunitu kolem Mozilly.</p>

<p>Sedl jsem k počítači a během 3 týdnů naprogramoval kostru stránek <a
href="http://www.czilla.org/">CZilla.org</a>. Obrázky dvou dráčků na
moje přání namalovala Lenka Fialová. Celé stránky byly zprovozněny v září
roku 2001. Kontaktoval jsem Mozilla.cz s tím, že jsem jim nabízel prezentaci
lokalizací i na CZilla.org a tak se také stalo. O několik měsíců později mně
bylo Mozillou.cz sděleno, že připravují podobný portál s podobným zaměřením
a to v duchu, že zřejmě budu muset CZillu ukončit. Jejich slib však nebyl
realizován. Později jsem přestával dostávat avíza o nových verzích české
Mozilly.</p>

<p>Obliba stránek CZilla.org rostla a začal jsem zjišťovat, že stránky mají
úspěch. CZilla.org žila vlastním životem. Kontaktoval jsem Mozilla.org a
dostal jsem povolení jakéhokoliv přebíraní informací a kódů od Mozilla.org.
Také byly stránky CZilla.org zapsány do komunity Mozilla.org pro českou
republiku.</p>

<p>15. září 2002 jsme na stránkách uvedli českou lokalizaci
Mozilly&nbsp;1.0. Jeden šikovný člověk se nemohl dívat na sliby Mozilla.cz o
vydání české lokalizace Mozilla&nbsp;1.0 a udělal vlastní. Trvalo mu to
jeden měsíc, ale lokalizace, kterou Mozilla.cz pouze slibovala, byla na
světě. Ten člověk se jmenuje Radek Vybíral a získal přezdívku "pirát". Byli
jsme totiž 10.&nbsp;října 2002 nařčeni z ukradení kódu překladu, neboť náš
překlad byl založen na verzi&nbsp;0.9.7. Nazvali nás piráty. Bohužel pánové
od Mozilla.cz do zdroje neuvedli licenci, pod kterou se tento kód
distribuoval a tak byl kód považován za volný. Tímto si CZilla.org velmi
polepšila a začala vykazovat sledovanost kolem 2500 unikátních IP za týden.
Dále bych doporučil projít si <a
href="/czilla/tiskove-zpravy/stav-mozilla-cz.php">článek</a>, který popisuje
zřejmě spekulativní prodej domény Mozilla.cz.</p>

<p>Nyní existuje pouze tým CZilla. Domény CZilla.org
a&nbsp;<a href="http://www.czilla.cz/">CZilla.cz</a>
jsou jedním projektem momentálně sídlícím na
<a href="http://www.czilla.cz/">CZilla.cz</a>. Rozšířila se
základna <a href="/czilla/lide.php">tvůrců a
přispěvovatelů</a>. Nyní se dá bez nadsázky říct, že je stále nejobsáhlejším
zdrojem informací o Mozille na českém internetu.</p>

<p>Pro zajímavost: Název CZilla znamená CzechZilla.</p>

