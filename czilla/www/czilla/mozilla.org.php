vytvořil interní tým lidí nazvaný
Mozilla.org, který se měl starat o propagaci, podporu a vedení dalšího
vývoje balíku internetového software. Zrodil se projekt <strong>Mozilla</strong>.</p>

<h2 id="mozilla-org">Mozilla.org</h2>

<p>Od roku 1998 uplynula řada let a tak je dnes Mozilla.org nezávislá na
Netscape Communications. Je stále skupinou lidí koordinující celý projekt a
je privilegovaná rozhodovat jako virtuální shromáždění pro kód projektu
Mozilla. Je středobodem kontaktu komunity lidí zajímajících se o využívání a
vylepšování zdrojového kódu:</p>

<ul>
	<li>Směřuje technologie a architekturu projektu.</li>

	<li>Sbírá úpravy, pomáhá autorům slaďovat jejich činnost a pravidelně
	zveřejňuje výslednou práci.</li>

	<li>Koordinuje seznamy chyb, udržuje přehled o probíhajících pracích a
	poskytuje plán vývoje kódu a projektů na něm založených.</li>

	<li>Provozuje diskuzní fóra pro výměnu informací mezi přispěvateli.</li>

	<li>Není skupinou klíčových vývojářů, ale spojujícím článkem mezi
	vývojáři, uživateli a firmami. Pomáhá těmto skupinám najít vzájemnou
	shodu.</li>
</ul>

<p>Činnost skupiny Mozilla.org je kontrolována Mozilla Foundation.</p>

<h2 id="mozilla-foundation">Mozilla Foundation</h2>

<p><a href="http://www.mozillafoundation.org/" hreflang="en"
lang="en">Mozilla Foundation</a> je neziskovou organizací založenou v roce
2003 společností America Online pro podporu a vývoj prohlížeče Mozilla a
odvozených produktů. Mozilla Foundation, která sídlí v kalifornském Mountain
View, v současnosti dodává prvotřídní webový prohlížeč Mozilla Firefox,
balík aplikací Mozilla Suite a samostatný e-mailový klient Mozilla
Thunderbird. Všechny zmiňované produkty poskytuje zdarma.</p>

