<p><i>Praha, 2. prosince 2005</i></p>

<p><span lang="en">Mozilla Corporation</span> ve spolupráci s&nbsp;projektem
CZilla dnes zveřejnila českou verzi Mozilla Firefox&nbsp;1.5 pro operační
systémy MS&nbsp;Windows, Linux a&nbsp;Mac OS&nbsp;X. Firefox je rychlý
a&nbsp;bezpečný webový prohlížeč, oblíbený u&nbsp;uživatelů i&nbsp;webových
tvůrců. Za poslední rok se dočkal více než 100&nbsp;milionů stažení
a&nbsp;stal se druhým nejpoužívanějším prohlížečem v&nbsp;České republice
i&nbsp;ve světě.</p>

<h3>Hlavní novinky Firefoxu verze 1.5:</h3>
<ul>
	<li><strong>rychlejší zobrazování webových stránek</strong> díky nové
	verzi jádra Gecko&nbsp;1.8</li>
	<li><strong>účinnější blokování vyskakovacích oken</strong></li>
	<li><strong>rychlá tlačítka Zpět a&nbsp;Vpřed</strong> &ndash; návrat na
	předchozí stránku je nyní okamžitý</li>
	<li>funkce <strong>Vymazání důvěrných dat</strong> nabízí snadnou cestu
	pro rychlé odstranění historie navštívených stránek, formulářů, zadaných
	hesel a&nbsp;cookies.</li>
	<li><strong>přepracovaný systém aktualizací</strong> umožňuje
	automatizované rozdílové aktualizace prohlížeče</li>
	<li><strong>možnost nahlásit chybné stránky</strong>, které ve
	Firefoxu nefungují správně</li>
	<li><strong>zlepšená přístupnost</strong> pro hendikepované uživatele
	zahrnující např. podporu pro čtečky obrazovky</li>
	<li><strong>vylepšená použitelnost celé aplikace</strong> zahrnující
	popisné chybové hlášky, přehlednější dialog nastavení prohlížeče nebo
	možnost měnit pořadí panelů</li>
	<li><strong>pokračující rozšiřování podpory webových technologií</strong>
	&ndash; formát <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr>
	pro vektorovou grafiku,	značka <code>canvas</code>, JavaScript&nbsp;1.6,
	skriptování pomocí <span lang="en">ECMAscript
	for&nbsp;<abbr title="Extensible Markup Language" lang="en">XML</abbr></span>
	(<abbr title="ECMAScript for XML" lang="en">E4X</abbr>)
	a&nbsp;mnoho vylepšení <abbr title="Cascading Style Sheets" lang="en">CSS</abbr></li>
</ul>

