<p><em>Brno, 25. října 2006</em></p>

<p><span lang="en">Mozilla Corporation</span> ve spolupráci s&nbsp;projektem
CZilla dnes zveřejnila českou verzi Mozilla Firefox&nbsp;2.0 pro operační
systémy MS&nbsp;Windows, Linux a&nbsp;Mac OS&nbsp;X. Firefox je rychlý
a&nbsp;bezpečný webový prohlížeč, oblíbený u&nbsp;uživatelů i&nbsp;webových
tvůrců. Dočkal se již stovek milionů stažení a&nbsp;stal se druhým 
nejpoužívanějším prohlížečem v&nbsp;České republice i&nbsp;ve světě.</p>

<h3>Hlavní novinky Firefoxu verze 2.0:</h3>
<ul>
	<li>Vestavěná <strong>ochrana proti podvodným stránkám</strong>.</li>
	<li><strong>Kontrola pravopisu</strong> upozorní na překlepy v&nbsp;polích
		formulářů.</li>
	<li><strong>Obnovení stavu po pádu</strong> umožňuje
		obnovit otevřené panely poté, co byl Firefox ukončen nestandardním způsobem.</li>
	<li><strong>Vylepšené vyhledávání včetně našeptávače</strong>, nový správce
		vyhledávacích modulů.</li>
	<li><strong>Přívětivější uživatelské rozhraní</strong> díky novému motivu
		vzhledu.</li>
	<li><strong>Prohlížení v&nbsp;panelech</strong> umožňuje obnovit uzavřené
		panely, je intuitivnější a&nbsp;snadněji použitelné.</li>
	<li><strong>Inovovaná správa <acronym
		title="Really Simple Syndication">RSS</acronym> kanálů</strong> zobrazuje
		náhled každého zdroje a&nbsp;lze zvolit, kterou aplikací jej odebírat.
		Firefox nyní také umí tyto zdroje lépe rozpoznat.</li>
	<li><strong>JavaScript&nbsp;1.7</strong>, podporující <a
		href="http://developer.mozilla.org/en/docs/New_in_JavaScript_1.7" 
		hreflang="en">nové vlastnosti</a>, které ulehčují tvorbu stránek.</li>
	<li>A&nbsp;další&hellip;</a>
</ul>

<p>Firefox ve verzi 1.0 získal 
<a href="http://www.mozilla.org/press/awards.html" hreflang="en"
	title="Mozilla.org: Firefox Awards">řadu ocenění</a>,
např. <em>Produkt roku&nbsp;2005</em> od časopisů Softwarové noviny a&nbsp;<span
lang="en">PC&nbsp;World</span>. Renomovaný americký magazín <span
lang="en">CNET</span> zařadil Firefox mezi 10&nbsp;nejlepších produktů
desetiletí.</p>

<p>Pro uživatele MS&nbsp;Internet Exploreru jsou připraveny webové stránky
