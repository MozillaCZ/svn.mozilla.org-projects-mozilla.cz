<h2>Mozilla 1.5 CZ - nejen prohlížeč, ale i ochrana před viry a nevyžádanou
poštou</h2>

<p><i>Praha 4. listopadu 2004</i></p>

<p>Projekt <a href="/">CZilla</a> zabývající se lokalizací produktů
mozilla.org s potěšením oznamuje uvolnění nové české verze uceleného
internetového balíku <a href="/produkty/suite/">Mozilla 1.5</a>,
samostatného prohlížeče <a href="/produkty/firefox/">Mozilla Firebird 0.7</a> a
samostatného poštovního klienta <a href="/produkty/thunderbird/">Mozilla
Thunderbird 0.3</a> pro operační systémy Microsoft Windows
(98/ME/NT/2000/XP) a GNU/Linux (x86). Zmíněné produkty naleznete na stránce
<a href="http://www.czilla.cz/stahnout/">http://www.czilla.cz/stahnout/</a>.</p>

<p>Mozilla je plnohodnotný internetový balík obsahující webový prohlížeč,
poštovní klient, editor webových stránek, klienta pro IRC s volitelnou
možností instalace kalendáře či zásuvného modulu pro Macromedia Flash. Další
dvě samostatné aplikace, prohlížeč Mozilla Firebird 0.7 a poštovní klient
Mozilla Thunderbird 0.3 se nejvíce uplatní na méně výkonných počítačích, či
pro uživatele, kteří chtějí např. využívat pouze bezpečný přístup k
elektronické poště.</p>

<p>Nová verze přináší kromě vylepšení rychlosti a stability i mnoho nových
vlastností. Poštovní klient má zahrnutu podporu pro <strong>rozpoznávání
nevyžádané pošty</strong> (anglicky nazývané spam). S vaší pomocí se pak
program může ve velmi krátké době naučit automaticky třídit došlou poštu.
Také riziko nekontrolovatelně se šířících virů je zde naprosto minimální,
protože aplikace nepoužívá žádné nedokumentované vlastnosti operačního
systému, které jsou viry k šíření zneužívány.</p>

<p>Nejpříjemnější novinkou poštovního klienta aplikace Mozilla, či stejně
funkční aplikace Mozilla Thunderbird, je <strong>integrovaná kontrola
pravopisu</strong>, takže si uživatelé před odesláním, např. obchodní
korespondence, mohou nechat zprávu zkontrolovat. Na stránkách projektu <a
href="http://dictionaries.mozdev.org/">http://dictionaries.mozdev.org/</a>
jsou k dispozici i slovníky pro dalších 40 jazyků včetně anglického či
německého. Jako třešnička na dortu je do české verze přidána malá aplikace
Thundertray, která umožňuje spustit a "schovat" aplikaci Mozilla Thunderbird
do systémového panelu.</p>

<p>Začátečníci mohou hledat rady a klást své dotazy na webovém fóru <a
href="http://forum.czilla.cz/">http://forum.czilla.cz/</a>. Dají se zde ale
také nalézt rady pro pokročilé uživatele, či návody pro tvorbou aplikací
založených na kódu Mozilly.</p>

<p>Výsledek počeštění bude samozřejmě také oznámen na oficiálním serveru
mozilla.org, takže lze předpokládat, že další verze všech rozšířených
distribucí Linuxu budou mít zahrnuty výše uvedené lokalizované aplikace.</p>

