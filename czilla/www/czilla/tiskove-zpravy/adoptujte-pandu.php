MS&nbsp;Internet Exploreru klesá po řadu měsíců jak na českém tak i světovém
webu. Dokažte, že jste hrdým uživatelem tohoto programu a přispějte na podporu
chovu <a href="http://www.zoopraha.cz/lexikon.php?i=53">pandy červené</a>.</p>

