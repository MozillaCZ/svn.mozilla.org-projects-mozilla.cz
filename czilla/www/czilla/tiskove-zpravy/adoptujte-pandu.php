<h3>O ZOO Praha</h3>

<p><a href="http://www.zoopraha.cz/">Zoologická zahrada Praha</a>
se snaží o vytváření ekologicky vyváženého a esteticky účinného prostředí pro chov a prezentaci
zvířat ze všech zoogeografických oblastí i pro pěstování rostlin.
Její snahou je i pořádání osvětových přednášek, péče o kladný vztah veřejnosti k přírodě a mezinárodní
chovatelská a pěstitelská spolupráce.</p>

<p>V současné době prochází ZOO převratným obdobím, kdy se jí po ničivých povodních v roce 2002
podařilo nejen zrekonstruovat všechny zničené pavilony a expozice, ale také postavit pavilony nové -
<strong>Afrika zblízka</strong> a <strong>Indonéská džungle</strong>. Zejména díky tomu stoupl počet
návštěvníků v roce 2004 k hranici jednoho milionu.</p>

<h3>O DMS</h3>

<p>"<a href="http://www.darcovskasms.cz/">DMS - Dárcovská SMS</a>" je společným projektem <a href="http://www.donorsforum.cz">Fóra dárců</a> - organizace pro dárce, usilující o podporu filantropie v České republice a <a href="http://www.apms.cz">Asociací provozovatelů mobilních sítí</a>. Cílem projektu DMS je nabídnout nadacím a dalším neziskovým organizacím možnost získávat příspěvky od jednotlivců prostřednictvím mobilního
telefonu formou SMS zpráv.</p>

