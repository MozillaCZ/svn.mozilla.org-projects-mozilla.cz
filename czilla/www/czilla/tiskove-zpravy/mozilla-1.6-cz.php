<h2>Mozilla 1.6 CZ</h2>

<p><i>Praha 28. ledna 2004</i></p>

<p>Projekt <a href="/">CZilla</a> zabývající se lokalizací produktů
mozilla.org oznamuje uvolnění lokalizace Mozilla 1.6 pro operační systémy
Microsoft Windows a GNU/Linux.</p>

<p>Mozilla je zdarma dostupný balík internetových aplikací. Obsahuje webový
prohlížeč, emailový klient, editor webových stránek, klienta IRC a
volitelnou možnost instalace kalendáře či zásuvného modulu pro Macromedia
Flash. Mozilla klade velký důraz na bezpečnost a vyznačuje se řadou
nadstandardních funkcí. Na stránce <a
href="/produkty/suite/">http://www.czilla.cz/produkty/suite/</a>
o nich naleznete bližší informace.</p>

<p>Nová verze Mozilly přináší kromě zlepšení rychlosti a stability i řadu
nových vlastností. Např. emailový klient nyní umožňuje odstraňovat poštu z
POP3 serveru po zvoleném počtu dní či přidávat signaturu nad citovaný text.
Novinkou je i přidaná podpora elektronických vizitek vCard a NTLM
autentizace na všech platformách. Na stránce
<a href="http://www.mozilla.org/releases/mozilla1.6/README.html#new">http://www.mozilla.org/releases/mozilla1.6/README.html#new</a>
naleznete podrobnější přehled všech novinek.</p>

