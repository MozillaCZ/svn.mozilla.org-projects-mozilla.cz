<h2>Mozilla.org zvyšuje bezpečnost svých produktů</h2>

<p><i>Praha 10. srpna 2004</i></p>

<p>Projekt CZilla oznamuje uvolnění lokalizovaných aktualizací produktů
Mozilla.org - balíku internetových aplikací <em>Mozilla&nbsp;1.7.2&nbsp;CZ</em>, webového
prohlížeče <em>Mozilla Firefox&nbsp;0.9.3&nbsp;CZ</em> a poštovního klienta Mozilla
Thunderbird&nbsp;0.7.3&nbsp;CZ. Dostupné jsou verze pro operační systémy MS&nbsp;Windows
a&nbsp;GNU/Linux.</p>

<p>Aktualizace, odstraňující několik potenciálních bezpečnostních chyb
objevených na konci července, dále zvyšují bezpečnost uživatelů produktů
<a href="http://www.mozilla.org/">Mozilla.org</a>. Uživatelům je doporučen brzký
přechod na aktualizované verze.</p>

<p>Jelikož bezpečnost a ochrana soukromí uživatelů je pro Mozilla Foundation,
americkou neziskovou organizaci zastřešující vývoj produktů Mozilla.org,
důležitým cílem dnes i v&nbsp;budoucnu, oznámila 2.&nbsp;srpna nový <strong>Security Bug
Bounty Program</strong>. V&nbsp;jejím rámci odmění 500&nbsp;dolary každého uživatele, který
nahlásí případnou kritickou bezpečnostní chybu ve volně dostupném kódu
jejích aplikací. Program, který by měl vést k objevení a opravě
bezpečnostních chyb před jejich zneužitím, je dotován americkou společností
<em>Linspire,&nbsp;Inc.</em> a prvním vesmírným turistou <em>Markem Shuttleworthem</em>.</p>

Mozilla Security Bug Bounty Program:<br />
<a href="http://www.mozilla.org/security/bug-bounty.html">http://www.mozilla.org/security/bug-bounty.html</a>

