<h2>Mozilla Firefox 1.0 CZ &ndash; Nejlepší webový prohlížeč</h2>

<p style="font-variant:small-caps;font-size:14px"><strong>Otevřený software žene vpřed inovaci prohlížení webu</strong></p>

<p><i>Praha, 9. listopadu 2004</i></p>

<p><a href="http://www.mozilla.org/foundation/">Mozilla Foundation</a> ve
spolupráci s&nbsp;<a href="http://www.czilla.cz/">Projektem CZilla</a> dnes
zveřejnila dlouho očekávanou českou verzi Mozilla Firefox&nbsp;1.0 pro
operační systémy MS&nbsp;Windows, GNU/Linux a&nbsp;Mac OS&nbsp;X. Firefox je
rychlý a bezpečný webový prohlížeč, který si oblíbili uživatelé
i&nbsp;weboví tvůrci. Již během svého vývoje se stal nejdiskutovanějším
prohlížečem na Internetu, zároveň je považován za nejlepší současný webový
prohlížeč.</p>

<h3>Výběr z vlastností Firefoxu:</h3>

<ul>
	<li>Rychlejší prohlížení webu</li>
	<li>Účinné blokování vyskakovacích oken </li>
	<li>Integrované vyhledávání na portálech Seznam.cz, Centrum.cz či Google.com</li>
	<li>Ochrana před spyware, viry a obtěžujícím chováním webových stránek</li>
	<li>Snadná změna vzhledu</li>
	<li>Mnoho volitelných rozšíření </li>
	<li>Správce stahovaných souborů</li>
	<li>Intuitivní ovládání v češtině</li>
	<li>Jednoduchý převod dat z ostatních prohlížečů</li>
	<li>Široká podpora nejnovějších webových standardů</li>
	<li>Pro každého zdarma</li>
</ul>

<p>Kvalita a vyspělost prohlížeče přesvědčila milióny uživatelů k nahrazení
MS&nbsp;Internet Exploreru právě Firefoxem &ndash; jen ukázková verze Firefoxu zaznamenala
během několika týdnů více než 8&nbsp;miliónů stažení. Masivní přechod uživatelů
se odrazil i v podílu prohlížečů na trhu &ndash; podle analytických firem podíl
MS&nbsp;Internet Exploreru klesá po řadu měsíců jak na českém tak i světovém
webu.</p>

<p>Firefox získal velmi příznivé ohlasy recenzentů a odborné veřejnosti.
V srpnu obdržel ocenění "Best In Show" na celosvětové výstavě LinuxWorld. V
září americké deníky Wall Street Journal a USA&nbsp;Today doporučily vyzkoušet
Firefox uživatelům, kterým záleží na bezpečnosti. Živě.cz ve své recenzi
uvádí: "Kdo jej vyzkouší, pravděpodobně již nebude mít důvod ho opustit."
Firefox také vyhrál velký test prohlížečů českého časopisu Computer.</p>

<p>Pro uživatele MS&nbsp;Internet Exploreru jsou připraveny webové stránky
<strong>&quot;Přejděte na Firefox&quot;</strong>
(<a href="http://firefox.czilla.cz/">firefox.czilla.cz</a>),
které jim pomohou se změnou prohlížeče. Uživatelé na stránkách naleznou
důvody proč změnit webový prohlížeč, odpovědi na své nejčastější otázky i
jednoduchého průvodce samotnou výměnou prohlížeče.</p>

<p>Instalační program Firefoxu (pro Windows 5&nbsp;MB, pro Linux a Mac OS&nbsp;X 8&nbsp;MB) je pro uživatele i firmy <strong>zdarma</strong> ke stažení na adrese:</p>
