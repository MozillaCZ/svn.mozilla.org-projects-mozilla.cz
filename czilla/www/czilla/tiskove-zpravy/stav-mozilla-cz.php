<h2>Historie</h2>

<p>K&nbsp;zaregistrování domény 
<a href="http://www.mozilla.cz/">mozilla.cz</a>
došlo během posledních několika let. Učinili tak dva lidé, kteří se chtěli
věnovat českému překladu Mozilly. Započali tak práci na kvalitním českém
překladu aplikací Mozilla. Na stránkách se, již nějaký čas před oficiálním
vydáním Mozilly&nbsp;1.0, objevilo upozornění, že k&nbsp;vydání české verze
Mozilly, resp. jejího počešťovacího balíku, dojde během dvou týdnů po vydání
finální verze Mozilly&nbsp;1.0 z&nbsp;Mozilla.org. K&nbsp;vypuštění anglické
Mozilly&nbsp;1.0 došlo <i>5.&nbsp;června 2002</i>. Spousta uživatelů se
tehdy v&nbsp;naději obracela na stránky mozilla.cz s&nbsp;očekáváním
počeštění. Bohužel, zřejmě díky zaneprázdnění autorů, měsíce ubíhaly, obsah
stránek nevěštil nic nového a&nbsp;autory se kontaktovat nedařilo.</p>

<p>Tak se stalo, že <i>15.&nbsp;září 2002</i> vyšlo na webu CZilla.org
oznámení o&nbsp;počeštění Mozilly. Překlad byl založen na posledním veřejně
dostupném beta překladu z&nbsp;mozilla.cz, který se vztahoval na
Mozillu&nbsp;0.9.7 a&nbsp;zbytek byl dokončen dle nejlepšího vědomí
a&nbsp;svědomí autora
(<a href="/czilla/lide.php#kdo-je-RV">Radek Vybíral</a>).
Vydání překladu mělo za následek, že nedošlo ke splnění podmínek (veřejně
neznámých), které měli dohodnuty původní překladatelé (majitelé domény
mozilla.cz), se svými sponzory a&nbsp;ke sporům ohledně možnosti použít
jejich překlad jako základ pro pokračování v&nbsp;práci na překladu.</p>

<p>Proto bylo <i>10.&nbsp;října 2002</i> vyvěšeno na mozilla.cz 
<a href="/clanky/tz-stav-mozilla-cz-extra.html">oznámení</a>,
že vývoj je ze strany vlastníků mozilla.cz ukončen a&nbsp;mozilla.cz byla
nabídnuta k&nbsp;&quot;přenechání&quot; připadným zájemců.</p>

<p>Zájemců se ozvalo několi a&nbsp;<i>24.&nbsp;listopadu 2002</i> jim bylo
oznámeno, že proběhne výběrové řízení. Rozpoutala se e-mailová diskuse mezi
účastníky konkurzu o&nbsp;nejvhodnější formě převzetí domény
a&nbsp;o&nbsp;jejím budoucím obsahu. Z&nbsp;diskuse mezi novými zájemci
vzešlo rozhodnutí převést doméhu na jednu ze zvolených osob, popř.
organizaci
<a href="http://www.linux.cz/czlug/"><abbr title="Czech Linux Users' Group" lang="en">CZLUG</abbr></a>.
Tento návrh byl předložen stávajícím majitelům. Ti se rozhodli
a&nbsp;<i>16.&nbsp;prosince 2002</i> oznámili, že doména bude pronajata (!)
jednotlivci, který se předchozích debat neúčastnil a&nbsp;pravděpodobně
navrhl řešení pro původní majitele nejzajímavější.</p>

<p>Další vystoupení budoucího vlastníka však nepřesvědčilo aktivní účastníky
prosazující co nejrychlejší napravení obsahu mozilla.cz
o&nbsp;životaschopnosti utajovaného řešení. Ještě téhož dne bylo proto
rozhodnuto o&nbsp;vytvoření alternativy pod názvem &quot;Renesance serveru
CZilla.org&quot; &ndash; <a href="http://www.czilla.cz/">czilla.cz</a>.</p>

<h2>Součastnost</h2>

<p>Obavy o&nbsp;osud mozilla.cz, byly zřejmě oprávněné, protože
k&nbsp;dnešnímu dni (<i>12.&nbsp;února 2003</i>) je obsah stránek mozilla.cz
stále stejný jako 10.&nbsp;10.&nbsp;2002. Poslední zprávy od vlastníků ze
6.&nbsp;2.&nbsp;2003 hovoří o&nbsp;tom, že doména má být v&nbsp;brzku
převedena (nikoli pronajata?) a&nbsp;její obsah by se měl změnit. Jakým
směrem, to ovšem nikdo z&nbsp;nás neví. Společně však doufáme, že se připojí
k&nbsp;rostoucí aktivitě CZilla, případně vytvoří důstojné konkurenční
prostředí.</p>

