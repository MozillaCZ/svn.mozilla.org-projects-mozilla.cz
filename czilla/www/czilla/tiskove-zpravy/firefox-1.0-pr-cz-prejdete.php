<h2>Mozilla Firefox 1.0 PR CZ, nový web &quot;Přejděte na Firefox&quot;</h2>

<p><i>Praha 4. října 2004</i></p>

<p>Projekt <a href="/">CZilla</a> dnes zveřejnil českou verzi Mozilla
Firefox&nbsp;1.0 Preview Release pro operační systémy MS&nbsp;Windows
a&nbsp;GNU/Linux. Firefox je rychlý a&nbsp;bezpečný webový prohlížeč, který
si oblíbili nejen weboví tvůrci, ale i&nbsp;milióny uživatelů, které
neobtěžují nevyžádaná vyskakovací okna ani&nbsp;spyware.</p>

<p>Nová verze přibližuje Firefox k&nbsp;velmi očekávané
verzi&nbsp;1.0 a&nbsp;přináší řadu vylepšení a&nbsp;novinek:</p>

<ul>
	<li>vylepšené blokování vyskakovacích oken</li>
	<li>posílené zabezpečení</li>
	<li>aktuální záložky</li>
	<li>snadná instalace zásuvných modulů (pluginů)</li>
	<li>lepší vyhledávání na webových stránkách</li>
</ul>

<p>Kvalita a&nbsp;vyspělost posledních verzí prohlížeče Firefox vedla ke
spuštění webového serveru <strong>&quot;Přejděte na Firefox&quot;</strong>
(<a href="http://firefox.czilla.cz/">firefox.czilla.cz</a>) určeného
uživatelům MS&nbsp;Internet Exploreru. Kromě přehledu důvodů proč změnit
webový prohlížeč, naleznou uživatelé na webu odpovědi na své nejčastější
otázky i&nbsp;jednoduchého průvodce samotnou výměnou prohlížeče.</p>

<p>Firefox získává stále příznivější ohlasy recenzentů. V&nbsp;srpnu obdržel
ocenění &quot;Best In Show&quot; na celosvětové výstavě LinuxWorld.
V&nbsp;zářijovém čísle kultovního magazínu Wired byl Firefox oficiálně
označen za &quot;zavedený&quot;, zatímco Internet Explorer za
&quot;vyhaslý&quot;. V&nbsp;září americké deníky Wall Street Journal
a&nbsp;USA Today doporučily vyzkoušet Firefox uživatelům, kterým záleží na
bezpečnosti. Živě.cz ve své recenzi uvádí: &quot;Kdo jej vyzkouší,
pravděpodobně již nebude mít důvod ho opustit.&quot; Firefox také vyhrál
velký test prohlížečů českého časopisu Computer.</p>

