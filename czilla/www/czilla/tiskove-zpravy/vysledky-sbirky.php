na číslo <strong>8&nbsp;77&nbsp;77</strong> SMS zprávu ve tvaru <strong>DMS ZOO PANDA</strong>. Při odeslání
<a href="http://www.darcovskasms.cz/index.php?page_id=zoo">dárcovské SMS</a> bude k tarifní ceně za vaši SMS
připočtena částka 30&nbsp;Kč + DPH. Celkový stav došlých DMS, či seznam dalších zvířat, která můžete sponzorovat pomocí DMS, najdete na stránkách
<a href="http://www.zoopraha.cz/sms_seznam.php">pražské ZOO</a>.</p>

