<h2>Uživatelé prohlížeče Firefox adoptovali pandy červené</h2>

<p style="font-variant:small-caps;font-size:14px"><strong>Komunita Mozilly podporuje ohrožené druhy zvířat</strong></p>

<p><i>Praha, 8. února 2005</i></p>

<p>Projekt CZilla ve spolupráci s pražskou ZOO <a href="http://www.czilla.cz/czilla/tiskove-zpravy/adoptujte-pandu.php">vyhlásil</a> před Vánoci akci
<strong>Adoptujte si svého Firefoxe</strong>. Cílem akce bylo vybrat <a href="http://www.zoopraha.cz/adopce_zvirata.php">dostatečné množství</a>
peněz a adoptovat jednu ze dvou <a href="http://www.zoopraha.cz/lexikon.php?i=53">pand v pražské ZOO</a>. Uživatelé moderního webového prohlížeče
Firefox, a nejen oni, mohli v průběhu sbírky přispívat pomocí dárcovských SMS a přímých převodů na účet ZOO. Konečné výsledky sbírky předčily všechna
naše očekávání. Celkem bylo zasláno <strong>831 dárcovských SMS</strong> (1 DMS = 27 Kč) a celková vybraná suma je <strong>38 000 Kč</strong>.</p>

<p>Z vybrané částky je zřejmé, že sbírka byla úspěšná, a CZilla se spolu s ostatními přispívajícími stala oficiálním adoptivním rodičem
jak samečka Minga, tak i samičky Mary Carmen. Projekt CZilla by tímto také rád poděkoval všem lidem, kteří v této sbírce přispěli, jelikož bez jejich podpory by se
celá tato akce nemohla uskutečnit.</p>

