<h2>Pošťák Mozilla Thunderbird 1.0 CZ</h2>

<p style="font-variant:small-caps;font-size:14px"><strong>Otevřený software umožňuje větší kontrolu nad vaší poštou</strong></p>

<p><i>Praha, 8. prosince 2004</i></p>

<p><a href="http://www.mozillafoundation.org/">Mozilla Foundation</a> ve
spolupráci s&nbsp;<a href="http://www.czilla.cz/">Projektem CZilla</a> dnes
zveřejnila českou verzi Mozilla Thunderbird&nbsp;1.0 pro operační systémy
MS&nbsp;Windows, GNU/Linux a&nbsp;Mac OS&nbsp;X. Poštovní klient
<a href="/produkty/thunderbird/">Thunderbird</a>
se zaměřuje na funkce a&nbsp;nastavení, které pomáhají zastavit nevyžádanou
poštu a&nbsp;zabraňují šíření virů &ndash; největších problémů, kterým dnes
uživatelé elektronické pošty čelí. Uživatelé si Thunderbird oblíbili pro
jeho stabilitu, komfort, důvěryhodnost a&nbsp;inovativní funkce. Mozilla
Thunderbird následuje velmi úspěšnou verzi prohlížeče
<a href="http://firefox.czilla.cz/">Mozilla Firefox</a>&nbsp;1.0,
kterou si od listopadového vydání stáhlo již více než 9&nbsp;milionů
uživatelů.</p>

<p>&quot;Thunderbird&nbsp;1.0 představuje pro Mozilla Foundation další
milník úspěšného produktu, vytvořeného v&nbsp;rámci komunity stovkami
dobrovolníků a&nbsp;vývojářů po celém světě,&quot; poznamenala Mitchell
Baker, prezidentka Mozilla Foundation. &quot;Thunderbird je dokonalý
společník Firefoxu a&nbsp;znamená další úspěch vývojového modelu open
source.&quot;</p>

<h3>Výběr z vlastností Thunderbirdu:</h3>

<ul>
	<li>Nástroje pro správu pošty - globální doručená pošta, třídící filtry, náhledy, označování zpráv.</li>
	<li>Účinná kontrola nevyžádané pošty - adaptivní filtry pro blokování nevyžádané pošty.</li>
	<li>Integrovaná čtečka RSS kanálů - pro snadnější přehled zpráv a novinek</li>
	<li>Ochrana před nebezpečným obsahem - blokování zákeřných skriptů a obrázků ze vzdálených serverů.</li>
	<li>Řada volitelných rozšíření a motivů - pro lepší přizpůsobení vzhledu a funkcí.</li>
	<li>Složky uloženého hledání - pro snadnější hledávání ve vaší poště.</li>
	<li>Podpora zabezpečeného spojení - pro připojení pomocí šifrovaných protokolů.</li>
	<li>Jednoduchý převod dat z ostatních poštovních programů.</li>
	<li>Intuitivní ovládání v češtině.</li>
	<li>Pro každého zdarma.</li>
</ul>

