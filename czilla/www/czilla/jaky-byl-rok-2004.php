<p><strong>A další rok je v nenávratnu</strong>. Co se v tom minulém roce
vlastně odehrálo? Byl významný pro <a href="/czilla/">CZillu</a>,
<a href="http://www.mozilla.org/"
	hreflang="en">Mozilla.org</a> nebo celý web? Dovolte
nostalgickou vzpomínku, jaký ten rok pro Mozillu vlastně byl.</p>

<h2 id="leden">Leden</h2>

<p>Mozilla do nového roku vstoupila
<a href="http://www.mozillazine.org/talkback.html?article=4152"
	hreflang="en">rozhovorem Mozilla Foundation</a>
v rádiu. (Slyšeli jste tu americkou výslovnost
<cite>Mozilla browser</cite>?) Mozilla se
<a href="http://nerv.cable.nu/mozillascary3/"
	hreflang="en">mihla i na filmovém plátně</a>
a proč by nemihla, když se nám
<a href="http://weblogs.mozillazine.org/bz/archives/004668.html"
	hreflang="en">zrychluje a zrychluje</a>. Vyšla
<a href="http://www.mozilla.org/releases/mozilla1.6/"
	hreflang="en">Mozilla 1.6</a> a
<a href="/archiv.html#news:b6f9b55325818c8221ab32ac8a9d0bd6">relicencování</a>
(přidání dalších licencí pro vývojáře) se dokončuje.</p>

<p>Komerční banka ve 21. století zřejmě <a
href="/archiv.html#news:7b7414f6c6ea1dad7a18708d448b0c96">nemá ráda všechny
své zákazníky</a>, MozillaZine spustil wiki
<a href="http://kb.mozillazine.org/"
	hreflang="en" lang="en">Knowledge base</a>
obsahující informace pro uživatele, prohlížeč MyIE2 (dnes již Maxthon) se
<a href="http://www.mozillazine.org/talkback.html?article=4133"
	hreflang="en">naučil používat Gecko</a> a web www.czilla.cz se mohl
<a href="/archiv.html#news:a4dfa652863dc7337dff025eb5f2444f">chlubit
PageRankem 8</a>. Jó tehdy se to ještě nosilo.</p>


<h2 id="unor">Únor</h2>

<p>Mozille se narodil <a
href="/archiv.html#news:4a022172010ce8bbbd24da970589722e">nevlastní
bratříček <strong>Nvu</strong></a> (<acronym title="What You See Is What You Get"
lang="en">WYSIWYG</acronym> editor <abbr title="Hypertext Markup Language"
lang="en">HTML</abbr>). Má přes 6&nbsp;mega a táta
<a href="http://www.glazman.org/"
	hreflang="en">D. Glazman</a>
se už těší, až novorozenci bude 1.0. Vyšel
<a href="http://www.mozilla.com/thunderbird/releases/0.5-release-notes.html"
	hreflang="en">Thunderbird 0.5</a>
spolu s verzí 0.8 <a href="/archiv.html#news:019691c9ca06efb105bcaa07631a4fcc">prohlížeče</a>,
který byl z Mozilla Firebird přejmenován na <a
href="http://firefox.czilla.cz/">Mozilla Firefox</a>. Dle pesimistů mu nový
název nevydrží déle než dvě verze.</p>

<p>Lukáš Růžička přeložil <a href="/produkty/firefox/firefox-vs-ie.html">13
důvodů proč místo Internet Exploreru používat Mozilla Firefox</a>. Že by na
té malé chlupaté
<a href="http://www.mozilla.org/projects/firefox/firefox-name-faq.html"
	hreflang="en">pandě</a>
přeci jen něco bylo?</p>

<p>Vyvedlo by vás z míry, když by se vaše přítelkyně vznášela nad postelí a
volala: <cite>"There is no Dana, there is only Zůůůl!"</cite>? V Netscapu
měli rádi <a href="http://www.ghostbusters.com/"
	hreflang="en">Ghostbusters</a>, a proto
<cite><a href="http://www.mozilla.org/keymaster/gatekeeper/there.is.only.xul"
	hreflang="en">there is no data, there.is.only.xul</a></cite>,
aneb pamatujte <a href="/archiv.html#news:7743aa88869d477101247da20d6f0234">jak správně
vyslovovat <acronym title="XML-based User Interface Language">
XUL</acronym></a>. Byla <a
href="/archiv.html#news:7c5d305227fa631a18b7cb0682cdc850">založena
nezisková organizace Mozilla Europe</a>, vyšla
<a href="http://www.mozilla.org/releases/mozilla1.7a/"
	hreflang="en">Mozilla 1.7 alfa</a> a
<a href="http://www.mozillazine.org/talkback.html?article=4353"
	hreflang="en">Minimo 0.1</a>
(Mozilla pro PDA). Jo únor, to byl plodný měsíc.</p>


<h2 id="brezen">Březen</h2>

<p>Březen, za kamna vlezem a nakreslíme tam <a
href="http://www.biographics.cz/articles/art_00011/">nové ikonky pro
Mozilla Kalendář</a> nebo <a
href="http://enigmail.spi.cz/konfigurace.html">nakonfigurujeme
Enigmail</a>. Byl <a href="/archiv.html#news:453d7ae471e68b4e53efdaaa3dfb5bf2">zrušen
patent společnosti Eolas</a> a Asa Dotzler objevil
<a href="http://weblogs.mozillazine.org/asa/archives/005006.html"
	hreflang="en">logo Firefoxu na hvězdné obloze</a>,
jaké to záhadné poselství pozemšťanům?</p>

<p>Mozilla slaví šest let od
<a href="http://wp.netscape.com/newsref/pr/newsrelease591.html"
	hreflang="en">otevření zdrojového kódu</a>,
vzhled Firefoxu si můžete
<a href="/archiv.html#news:cce71c571014dce0896909568e4418ac">sladit s Office 2003</a>,
vyšla
<a href="http://www.mozilla.org/releases/mozilla1.7b/"
	hreflang="en">Mozilla 1.7 beta</a>
a <a href="/archiv.html#news:9cfc75d9e24a5fee7633d09ea0a11733">Nvu 0.2</a>.</p>


<h2 id="duben">Duben</h2>

<p>V obchodě
<a href="http://www.mozillastore.com/"
	hreflang="en">Mozilla Store</a>
se objevila trička s tématem Mozilla Firefox a plyšový Firefox. Připojili jsme se k
<a href="http://www.root.cz/clanek/2140">demonstraci proti softwarovým
patentům</a>. <a href="/archiv.html#news:5e31ea00d123cc9282065e674b189718">Knihy o
Mozille</a> se nám rychle množí a podobně i <a
href="/archiv.html#news:f23e018ce6e2fc332a61ba032c95a2e9">čeští <acronym
title="XML-based User Interface Language">XUL</acronym> vývojáři</a> (ale
já je chytám).</p>

<p>Šušká se o
<a href="http://www.hicksdesign.co.uk/journal/431/coming_soon"
 hreflang="en">změně loga Thunderbirdu</a>... a šuškání
<a href="http://www.mozillazine.org/talkback.html?article=4645"
	hreflang="en">je potvrzeno</a>. Gecko na českém Internetu
<a href="/czilla/tiskove-zpravy/gecko-stats-cz-2004-03.html">používá 4,34&nbsp;% uživatelů</a>.
S Pavlem a AHA si s úsměvem říkáme, mít do konce roku 10&nbsp;% by byla frajeřina.
<a href="/archiv.html#news:590f5346d8143efa7cb43f23dd9cfc6a">Mozilla je zadarmo</a>,
šušká se o
<a href="http://www.mozillazine.org/talkback.html?article=4663"
	hreflang="en">správci aktualizací Firefoxu</a>.
Jó v dubnu za kamny, tam se pořád něco šušká.</p>


<h2 id="kveten">Květen</h2>

<p><cite>"Dobrý den, já jsem Mozilla."</cite> &ndash; <cite>"Opera, těší
mě."</cite> &ndash; <a
href="/archiv.html#news:ca6ef597c8f3f11fde0934f9611e4cc9">Mozilla a Opera
byly společně na půdě W3C</a>. Vzejde z toho nová spolupráce? Vyšla stabilní
<a href="http://www.mozillazine.org/talkback.html?article=4731"
	hreflang="en">Mozilla 1.4.2</a>,
<a href="http://www.mozilla.com/thunderbird/releases/0.6-release-notes.html"
	hreflang="en">Thunderbird 0.6</a>
a <a href="http://www.mozilla.org/releases/mozilla1.8a1/"
	hreflang="en">Mozilla 1.8 alfa 1</a>. Byl zprovozněn veřejný
<a href="http://groups.google.com/groups?as_umsgid=c57ant$74s1@ripley.netscape.com"
	hreflang="en">Talkback server</a>. (<cite>Spadne-li ti Mozilla, nestěžuj si na Billa</cite> a
odešli <a href="/podpora/talkback.html">hlášení o pádu</a>.)</p>

<p>Nových verzí je již na jednoho člověka příliš, je třeba <strong>rozdělit
lokalizace</strong> mezi více lidí. O Mozilla Suite se začíná starat <a
href="/czilla/lide.html#kdo-je-PC">Pavel Cvrček</a>, o Firefox a
Thunderbird <a href="/czilla/lide.html#kdo-je-PF">Pavel Franc</a>. <a
href="/czilla/lide.html#kdo-je-RV">Radkovi Vybíralovi</a> patří náš obdiv,
že se dokázal o lokalizace starat tak dlouho sám. Lokalizace Nvu a Sunbirdu zatím
necháváme, až se ozve nějaký dobrovolník.</p>


<h2 id="cerven">Červen</h2>

<p>Vznikla
<a href="http://www.mozillazine.org/talkback.html?article=4816"
	hreflang="en">skupina
<acronym title="Web Hypertext Application Technology Working Group"
	lang="en">WHATWG</acronym></a>
připravující specifikace webových aplikací. Stojí za ní vývojáři Mozilly,
Opery a Safari. Firefox
<a href="/archiv.html#news:d0ec8650f83c3dd104c93924e45e3af0">změní motiv vzhledu</a>,
pesimisté jsou zklamáni &ndash; název zůstává. Vzniká portál
<a href="https://addons.mozilla.org/"
	hreflang="en">Mozilla Update</a>
shromažďující rozšíření a motivy vzhledu. V souvislosti se šířením nového viru <a
href="/archiv.html#news:bf789a92f60bf2a4a1bcdae7a94c6b1c">ČTK zmiňuje
doporučení používat Mozillu</a>.</p>

<p>Vyšlo
<a href="http://www.mozillazine.org/talkback.html?article=4836"
	hreflang="en">Nvu 0.3</a>,
nepřejmenovaný
<a href="http://www.mozillazine.org/talkback.html?article=4843"
	hreflang="en">Firefox 0.9</a>,
<a href="http://www.mozillazine.org/talkback.html?article=4853"
	hreflang="en">Thunderbird 0.7</a>
a <a href="/czilla/tiskove-zpravy/mozilla-1.7-cz.html">Mozilla 1.7</a>.
<a href="/czilla/lide.html#kdo-je-JM">Jan Matoušek</a> se svou prací <a
href="http://developer.mozilla.org/cs/docs/%C3%9Aprava_instalace_Mozilly_pro_pot%C5%99eby_instituce">Úprava instalace Mozilly pro potřebu
instituce</a> se dostal do celorepublikového kola <a
href="http://www.soc.cz/"><acronym title="Středoškolská odborná činnost">
SOČ</acronym></a>.</p>

<h2 id="cervenec">Červenec</h2>

<p>Pluginy se dočkají
<a href="http://www.mozillazine.org/talkback.html?article=4923"
	hreflang="en">nového rozhraní</a>,
podílí se na něm Adobe, Apple, Macromedia, Mozilla Foundation, Opera a Sun
Microsystem. Vývoj nespí
a <a href="https://bugzilla.mozilla.org/"
	hreflang="en">Bugzilla projektu Mozilla.org</a>
konečně
<a href="http://weblogs.mozillazine.org/gerv/archives/005922.html"
	hreflang="en">dosáhla čtvrt millonu hlášení</a>
(škoda, že 50&nbsp;% jsou duplicity). Vyšly bezpečnostní opravy
<a href="http://www.mozillazine.org/talkback.html?article=4960"
	hreflang="en">Mozilla 1.7.1, Firefox 0.9.2, Thundebird 0.7.2</a>
a <a href="http://www.mozilla.org/releases/mozilla1.8a2/"
	hreflang="en">Mozilla 1.8 alfa 2</a>. Na Slunečnici vznikla stránka
<a href="http://www.slunecnice.cz/mozilla/">Mozilla a její svět</a>.
Sílí odezvy o nasazení české Mozilly ve firmách. Testování v úzkém kruhu
přestává být dostatečné:
<a href="/czilla/jak-pomoci/#testovani">nabíráme testery</a>
a zakládáme konferenci k testování lokalizovaných aplikací. Všem testerům
dík!</p>

<p><strong>Podíl Gecka</strong> (renderovacího jádra společného pro Mozillu a Firefox) na
českém Internetu roste asi o procento za měsíc. Začínáme
10&nbsp;% do konce roku vidět reálně.
<a href="/czilla/lide.html#kdo-je-AHA">AHA</a> odvážně slibuje: <cite>"Deset
procent na konci roku bude, i&nbsp;kdybych měl ty MSIE odinstalovat sám."</cite>
S Pavlem se tajně těšíme na Adama běhajícího v převleku Dědy Mráze o
Vánocích od jednoho uživatele k druhému s <abbr title="Compact Disc"
lang="en">CD</abbr> Mozilly. Zrodila se myšlenka
vytvořit propagační web pro migraci uživatelů MSIE na <a
href="/produkty/firefox/">Firefox</a>.</p>

<h2 id="srpen">Srpen</h2>

<p>Nalezením bezpečnostní chyby v Mozille si můžete
i <a href="http://www.mozilla.org/press/mozilla-2004-08-02.html"
	hreflang="en">vydělat peníze</a>. <a
href="http://www.iinfo.cz/tiskova_zprava/navrcholu_rizika_ie_zpusobuji_odliv_uzivatelu/">Gecko
má v <abbr title="Česká republika">ČR</abbr> 7,1&nbsp;%</a>. (To nám to ale
roste, že by to AHA o Vánocích dohánět nemusel? Lidi, mějte rozum a rychle
Mozillu i s Firefoxem odinstalujte, ať nám nezkazíte legraci.)</p>

<p>Vyšel
<a href="/archiv.html#news:0633bbf6853d334475574ae6ae0a80b6">Netscape 7.2</a>,
<a href="http://www.mozilla.org/releases/mozilla1.8a3/"
	hreflang="en">Mozilla 1.8 alfa 3</a>,
<a href="http://www.nvu.com/download.html"
	hreflang="en">Nvu 0.4</a>
a opravné verze
<a href="/czilla/tiskove-zpravy/mozilla-zvysuje-bezpecnost.html">Mozilla 1.7.2, Firefox
0.9.3, Thunderbird 0.7.3</a>. Mozilla.org vydává dokument
<a href="/podpora/bezpecnost-mozilly-a-odvozenych-prohlizecu.html">Bezpečnost
Mozilly a odvozených prohlížečů</a>,
<a href="http://cs.wikipedia.org/wiki/Kniha_Mozilly">Kniha Mozilly</a> je v
češtině a pro modemisty zakládáme stránku
<a href="/produkty/mozilla-na-cd.html"><strong>Mozilla
na <abbr title="Compact Disc Read Only Memory">CD-ROM</abbr></strong></a>. Pamatujte, že
<a href="/podpora/document-all.html">k document.all se prostě nečuchá</a>.</p>

<p>Počet návštěvníků <a href="http://forum.czilla.cz/">CZilla fóra</a>
rapidně roste. Již je nestačíme zvládat sami, a tak zavádíme <a
href="/czilla/lide.html#moderatori">moderátory fóra</a> &ndash; lidi, kteří
fórum pravidelně sledují, odpovídají, mažou duplicitní a zamykají nevhodné
příspěvky. Pomáhá jim řada těch lidí, kteří fórum sledují a trpělivě a
slušně uživatelům pomáhají řešit jejich problémy. Všem takovým děkujeme!</p>


<h2 id="zari">Září</h2>

<p>Konečně jsme našli pomoc pro tvorbu linuxových verzí. Díky
<a href="/czilla/lide.html#kdo-je-OK">Ondrovi Kudlíkovi</a>
tu máme opět plné verze pro <a href="http://www.linux.cz/">Linux</a>. Vyšla
<a href="http://ftp.mozilla.org/pub/mozilla.org/mozilla/releases/mozilla1.7.3/"
	hreflang="en">Mozilla 1.7.3</a>,
<a href="http://www.mozilla.com/thunderbird/releases/0.8-release-notes.html"
	hreflang="en">Thundebird 0.8</a>,
<a href="http://www.mozilla.org/products/firefox/releases/0.10.html"
	hreflang="en">Firefox 1.0 PR</a>
a <a href="http://www.mozilla.org/releases/mozilla1.8a4/"
	hreflang="en">Mozilla 1.8 alfa 4</a>.
Přemýšlíte, <a href="/produkty/ktery-zvolit.html">který z nich zvolit</a>?
Ztracené <a
href="/archiv.html#news:49f2049c89b13e72e570345b5d9a9821">rybičky z
Netscape se našly</a> a server <a href="http://www.mozdev.org/">Mozdev.org</a>
poskytující zázemí pro vývoj rozšíření slaví 4 roky.</p>

<p>K jazykové kvalitě textů na CZille nyní významně přispívají korektoři
<a href="/czilla/lide.html#kdo-je-VO">Vlastimil Ott</a> a
<a href="/czilla/lide.html#kdo-je-ZH">Zdeněk Hájek</a>. (A jen tak mezi námi,
my autoři si pak při pohledu na korektury svých textů občas připadáme jako
jazykoví neumětelové.)</p>

<p>Celý měsíc je ve stádiu příprav webu
<a href="http://firefox.czilla.cz/"><strong>Přejděte na Firefox</strong></a>.
Pod vedením <a href="/czilla/lide.html#kdo-je-AHA">AHA</a> se dolaďují
překlady, vychytávají screenshoty, tvoří design. Lidé nám posílají své
zkušenosti s přechodem na Firefox a vůbec netuší, k čemu je chceme použít.
Časovaná bomba pomalu odtikává.</p>

<h2 id="rijen">Říjen</h2>

<p>Tři, dva, jedna, teď! Vychází
<a href="/czilla/tiskove-zpravy/firefox-1.0-pr-cz-prejdete.html"><strong>český Firefox 1.0 PR</strong></a>
a oficiálně spouštíme web <a href="http://firefox.czilla.cz/">Přejděte na Firefox</a>.
Lidé na něj hromadně odkazují a začínají propagovat Firefox &ndash; necelé dva měsíce
práce sklízí své ovoce. Je to 10 let, co byl
<a href="http://groups.google.com/groups?selm=MARCA.94Oct13005712%40neon.mcom.com">oznámen
Netscape 0.9 beta</a>, vyšlo
<a href="http://www.nvu.com/download.html"
	hreflang="en">Nvu 0.5</a>,
propagátoři Firefoxu se sdružují na portálu
<a href="http://www.spreadfirefox.com/"
	hreflang="en">Spread Firefox</a>
a připravuje se Mozparty2 ku příležitosti vydání Firefoxu 1.0.
<a href="http://www.openforce.at/mozparty2/?party=65">Česká Mozparty2</a> se
koná 20. listopadu.</p>

<p><a href="http://www.mozilla.org/"
	hreflang="en">Mozilla.org</a>
bude lokalizované verze Firefoxu sestavovat sama. Ulehčí tak lokalizačním týmům a
lokalizované verze můžou vycházet ve stejný den s originálními.
Následujíci týdny jsou tak ve znamení přesouvání lokalizace do <abbr
title="Concurrent Versions System" lang="en">CVS</abbr>, ladění
sestavovacích skriptů, odstraňování chyb. Kdo si myslí, že to je sranda,
ten nikdy žádný Firefox nelokalizoval. Díky
<a href="/czilla/lide.html#kdo-je-PF">Pavlu Francovi</a> a mnoha dalším vše
klape a koncem měsíce vychází
<a href="/archiv.html#news:96076cd238334c20325057ed0df3b799">Firefox 1.0 RC1</a>,
který je již úspěšně sestaven na Mozilla.org.</p>

<h2 id="listopad">Listopad</h2>

<p>9. listopadu <a href="/czilla/tiskove-zpravy/mozilla-firefox-1.0-cz.html"><strong>vychází
nejlepší webový prohlížeč</strong></a> Firefox 1.0. Mezi řadou lokalizovaných
verzí je i česká verze, její současné vydání je umožněno sestavováním na Mozilla.org.
Příjemným překvapením je lokalizovaná
<a href="http://www.google.com/firefox?client=firefox-a&amp;rls=org.mozilla:cs-CZ:official">startovací
stránka od Googlu</a>. Web Mozilla Europe má <a
href="http://www.mozilla-europe.org/cs/">českou verzi</a>, <a
href="/czilla/lide.html#kdo-je-JB">Jiří Burda</a> má <a
href="/archiv.html#news:6119e0f806dcdd07d70a0a3a72909055">rozhovor v rádiu
Akropolis</a>, uživatelé mají
<a href="http://www.mozilla.com/thunderbird/releases/0.9-release-notes.html"
	hreflang="en">Thunderbird 0.9</a> a my všichni máme
<a href="/archiv.html#news:61a6ea47a8dea9166bd7e7d474f03293">MozParty2</a>.</p>

<p>Na <strong>MozParty2</strong> se rozvinula debata o Aniččině názoru
"<cite>Firefox už dávno vyrostl z dětských kalhotek</cite>" (viz titulní
stránka <a href="http://firefox.czilla.cz/">Přejděte na Firefox</a> "Ohlasy
z médií" napravo). Řešili jsme, co tedy dnes Firefox vlastně nosí?
Konkrétně zda nosí trenýrky, anebo slipy? <a
href="http://www.anicka.net/anicka.php">Anička</a> náš spor zkomplikovala
tvrzením, že Firefox je panda, a proto určitě nosí tanga. Ženy těmto debatám
prostě nerozumí.</p>

