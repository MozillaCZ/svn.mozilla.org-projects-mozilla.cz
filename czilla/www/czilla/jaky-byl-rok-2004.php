<h2 id="bilancujeme">Bilancujeme</h2>

<p>Byl to rok dobrý, nebo špatný? To posuďte sami. Nemám pocit, že jsme se
v <a href="/czilla/">CZille</a> tak úplně flákali. Přibylo 370 <a
href="/archiv.html">novinek</a>, rozeslali jsme osm <a
href="/czilla/tiskove-zpravy/">tiskových zpráv</a>, vydali bezpočet <a
href="/stahnout/">lokalizací</a> a množství webů řešených v rámci <a
href="/czilla/technicka-evangelizace.html">Technické Evangelizace</a> se
pohybuje již ve stovkách. Celé bychom to nezvládli bez <strong><a
href="/czilla/lide.html">pomoci řady lidí</a></strong>, kterým
<strong>děkujeme</strong> a doufáme, že nám zachovají přízeň i v roce,
který právě začíná.</p>

<p>Snad nám <strong>naši přátelé, partneři a příbuzní</strong> odpustí,
když jsme na ně pro samou práci pro CZillu někdy neměli tolik času, kolik
by si zasloužili. Odměnou nám naopak může být přízeň a spokojenost
uživatelů a fakt, že podíl Gecka konečně významě roste (byť nám to s Pavlem
zkazilo náš vánoční žertík).</p>

<p><strong>Nový rok</strong> je plný
očekávání. Setrvá trend nárůstu Gecka na webu? Dosáhne Gecko 20&nbsp;% do
konce roku 2005? Nebo Microsoft stihne přijít s nečekanou odezvou a vše se
opět změní? Začne se Mozilla ve větším používat jako platforma pro webové a intranetové
aplikace? A kdo bude dělat příští rok Dědu Mráze? Na to vše si budeme muset
počkat.</p>

<p>Nemusíme ale čekat s <strong>rukama za zády</strong>. Třeba se najdou <a
href="/czilla/lide.html">další lidé</a>, kteří <a
href="/czilla/jak-pomoci/">nám pomůžou</a>. Možná se někdo dokonce ujme
osiřelých lokalizací a dočkáme se českého Sunbirdu a Nvu. Doufáme, že se
podaří zrealizovat lokalizaci ChatZilly a Venkmanu. Nu, necháme se překvapit.</p>

