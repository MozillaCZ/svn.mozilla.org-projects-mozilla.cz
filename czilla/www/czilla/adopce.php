<h2 class="nice">A co dále?</h2>

<p>Přestože akce Adoptujte si svého Firefoxe již skončila, můžete svého Firefoxe v pražské ZOO podporovat i nadále. Stačí, zašlete-li mu stejně
jako během sbírky dárcovskou SMS. Celkový stav došlých DMS, či seznam dalších zvířat, která můžete sponzorovat pomocí DMS, najdete
na stránkách <a href="http://www.zoopraha.cz/cs/pomozte-nam/sms/sms-sponzoring">pražské ZOO</a>.</p>

<p>Pandy můžete také samozřejmě v ZOO navštívit. Najdete je ve výběhu hned u hlavního vchodu.</p>

