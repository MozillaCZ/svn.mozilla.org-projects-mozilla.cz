<h2 class="nice">A jak to dopadlo?</h2>

<p>Konečné výsledky sbírky předčily všechna naše očekávání.</p>
<p>V průběhu oněch tří týdnů, kdy sbírka probíhala, přišlo pandě celkem <strong>831 dárcovských SMS</strong>. Je potěšitelné, že panda dostává DMS i nyní
a v seznamu zvířat, která můžete pomocí mobilního telefonu podpořit, předstihla i taková populární zvířata, jako je slon, gorila, či tygr a
<a href="http://www.zoopraha.cz/cs/pomozte-nam/sms/sms-sponzoring">nezadržitelně vede</a>.</p>

<p>Na účtu pražské ZOO se mezitím sešlo pěkných 12 402 Kč. A když jsme k této částce přidali 22 437 Kč za DMS a peníze, co byly vybrány v rámci
<a href="/czilla/fotky/mozparty2.php">podzimní Mozparty 2</a>, dostali jsme celkovou vybranou částku - neuvěřitelných
<strong>38 000 Kč</strong>. Z vybrané částky je zřejmé, že sbírka byla úspěšná, a CZilla se spolu s ostatními přispívajícími stala oficiálním
adoptivním rodičem jak samečka Minga, tak i samičky Mary Carmen.</p>

<p><strong>Projekt CZilla by tímto rád poděkoval všem lidem, kteří přispěli, jelikož bez jejich podpory by se celá tato akce nemohla
uskutečnit.</strong></p>

