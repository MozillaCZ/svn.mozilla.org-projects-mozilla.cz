<h2 id="z-disku">Instalace motivu vzhledu z&nbsp;disku</h2>

<p>Stažený motiv vzhledu můžete nainstalovat tak, že jej přetáhnete do okna
Správce motivů vzhledu, který naleznete v&nbsp;hlavní nabídce pod <span
class="nabidka">Nástroje -&gt; Správce motivů vzhledu</span>. Pro
pohodlnější instalaci je vhodné nainstalovat rozšíření
<a href="http://www.mrtech.com/extensions/"
	title="Local Install: Domovská stránka rozšíření"
	hreflang="en" lang="en">Local Install</a>.</p>

<p>Po nainstalování rozšíření otevřete Mozilla Firefox a&nbsp;v&nbsp;hlavní
nabídce zvolte <span class="nabidka">Nástroje -&gt; Správce motivů
vzhledu</span>. Zobrazí se vám následující dialog:</p>

<div class="img-center"><div class="img-c" style="width:400px">
	<a href="images/ff-motivy-05.png" class="zoom"><img
		src="images/ff-motivy-05n.png"
