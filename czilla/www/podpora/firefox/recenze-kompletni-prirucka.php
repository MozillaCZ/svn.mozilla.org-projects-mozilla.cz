<p>Prohlížeč <a href="/produkty/firefox/" title="Stránka o produktu">Mozilla 
Firefox</a> si u&nbsp;nás za svou existenci získal řadu spokojených uživatelů. 
Získal by si jich pravděpodobně ještě více, kdyby při nárůstu popularity 
existovala kniha v&nbsp;češtině, která by jednoduše a srozumitelně
vysvětlila méně zkušeným uživatelům, proč <a href="/produkty/firefox/" 
title="Stránka o produktu">Firefox</a> používat, jak ho používat a&nbsp;jak 
nejsnáze. Při vší úctě k&nbsp;návodům na webu, kniha je prostě kniha. Můžete 
si ji přečíst ve vlaku, na zastávce, na dovolené... prostě kdekoliv.</p>

<p>Skoro po roce od vydání <a href="/produkty/firefox/" 
title="Stránka o produktu">Firefoxu</a>&nbsp;1.0 se na český trh dostala kniha 
s&nbsp;názvem <a href="http://knihy.cpress.cz/pocitac/Book.asp?ID=1759" 
title="CPBooks.cz: Více informací o knize">Mozilla Firefox: Kompletní 
uživatelská příručka</a>, kterou sepsal Václav Kadlec a&nbsp;vydalo 
nakladatelství <a href="http://www.cpbooks.cz/" 
title="CPBooks a.s: Domovská stránka">CP Books</a>. Jedná se o&nbsp;první 
českou knihu o&nbsp;<a href="/produkty/firefox/" 
title="Stránka o produktu">Firefoxu</a> a&nbsp;jistě řadu z&nbsp;vás zajímá, 
co obsahuje a&nbsp;zda má smysl si ji zakoupit.</p>

<p>Jako každá kniha podobného žánru zprvu uživateli vysvětluje, co 
to <a href="/produkty/firefox/" title="Stránka o produktu">Firefox</a> 
je, jak vznikl a&nbsp;proč je výhodné mu dát přednost před jinými webovými 
prohlížeči. Autor knihy též vysvětluje, kde si můžete instalaci 
<a href="/produkty/firefox/" title="Stránka o produktu">Firefoxu</a> obstarat. 
Zde je možná škoda, že není <a href="/produkty/firefox/" 
title="Stránka o produktu">Firefox</a> přiložen na nějakém doprovodném CD, 
což by uživatele jistě potěšilo.</p>

<p>Kniha, jako je tato, je určena převážně pro méně zkušené uživatele. Takový 
uživatel očekává, že jej kniha seznámí se základními úkony, které lze 
s&nbsp;produktem vykonávat a&nbsp;jednoduše je vysvětlí. V&nbsp;případě 
<a href="/produkty/firefox/" title="Stránka o produktu">Firefoxu</a> člověka 
napadne prohlížení v panelech, instalace rozšíření a&nbsp;motivů vzhledu, 
konfigurace či návod, jak efektivně vyhledávat na Internetu. Prostě vše, 
s&nbsp;čím se dosud nejspíš nesetkal či setkal v&nbsp;jiné formě. Pokud 
patříte do této skupiny uživatelů, pak toto vše v&nbsp;knize naleznete.</p>

<p>Vzhledem k&nbsp;tomu, že většina uživatelů dnes používá webový prohlížeč 
Internet Explorer, poukazuje kniha na řadě míst na odlišnosti, na které mohou 
uživatelé přecházející na <a href="/produkty/firefox/" 
title="Stránka o produktu"> Firefox</a> narazit. Jedná se zejména 
o&nbsp;některé odlišné klávesové zkratky, terminologii či funkce.</p>

<p>Na své si přijde nejenom skupina méně zkušených uživatelů. Kniha 
obsahuje kapitolu věnující se zásuvným modulům (pluginům), naleznete 
v&nbsp;ní pojednání o&nbsp;bezpečnosti na Internetu a&nbsp;zkušenější 
uživatelé zde najdou tipy na možnosti skrytého nastavení 
<a href="/produkty/firefox/" title="Stránka o produktu">Firefoxu</a>.</p>

<p>V závěru knihy je jedna kapitola věnována e-mailovému klientu 
<a href="/produkty/thunderbird/" title="Stránka o produktu">Mozilla 
Thunderbird</a>, který je v&nbsp;základu představen. Kniha je doplněna 
přehledem klávesových zkratek <a href="/produkty/firefox/" 
title="Stránka o produktu">Firefoxu</a> a&nbsp;odkazy na další informační 
zdroje.</p>

