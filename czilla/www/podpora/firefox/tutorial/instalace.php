<p><a href="/produkty/firefox/">Mozilla Firefox</a> je jedním z&nbsp;nejlepších
webových prohlížečů současnosti. Podobně jako jiné prohlížeče je k&nbsp;dispozici
zdarma. Díky unikátnímu vývoji založeném na Open Source dosahuje výsledný
produkt vynikající rychlosti a&nbsp;obsahuje méně chyb než programy vyvíjené
tradičními metodami. Mozilla Firefox obsahuje řadu unikátních funkcí, které
v&nbsp;konečném celku tvoří dobrý produkt. V&nbsp;této sérii článku se vám pokusím
prohlížeč Firefox představit.</p>

<h2 class="nice ff">Instalace a&nbsp;konfigurace</h2>

<p>První věc, kterou potřebujete udělat, je získat instalační program Mozilla
Firefoxu. Na stránce <a href="http://firefox.czilla.cz/stahnout/">stažení 
Firefoxu</a> naleznete zdarma vždy nejnovější lokalizovanou verzi.</p>

<p>Na stránce si doporučuji pečlivě přečíst, kterou verzi byste si měli stáhnout. 
Osobně používám verzi programu pro Windows, a&nbsp;proto se následující díly budou 
točit okolo ní. Pokud si nejste jisti a&nbsp;používáte Windows, stáhněte si 
<strong>českou verzi s&nbsp;instalátorem</strong>.</p>

