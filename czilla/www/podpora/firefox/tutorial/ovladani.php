<h2 class="nice ff">Klávesové zkratky</h2>

<p>Pokud jste fanda klávesnice, můžete Mozilla Firefox 
ovládat klávesovými zkratkami. Nejpoužívanější z&nbsp;nich naleznete 
v&nbsp;následujícím rychlém přehledu:
</p>

<ul>
	<li><kbd>Ctrl</kbd>-<kbd>N</kbd> - Otevře nové okno prohlížeče</li>
	<li><kbd>Ctrl</kbd>-<kbd>T</kbd> - Otevře nový panel v&nbsp;aktuálním 
  okně prohlížeče</li>
	<li><kbd>Ctrl</kbd>-<kbd>O</kbd> - Otevře soubor</li>
	<li><kbd>Ctrl</kbd>-<kbd>W</kbd> - Zavře aktuální panel v&nbsp;prohlížeči</li>
	<li><kbd>Ctrl</kbd>-<kbd>Shift</kbd>-<kbd>W</kbd> - Zavře aktuální okno 
  prohlížeče</li>
	<li><kbd>Ctrl</kbd>-<kbd>R</kbd> - Obnovení obsahu aktuálního okna </li>
</ul>

<p>Toto jsou pouze základní klávesové zkratky, které můžete použít. Samozřejmě
existují i&nbsp;další. Můžete si je zobrazit postupným rozbalováním jednotlivých
nabídek <span class="nabidka">Soubor</span>, <span class="nabidka">Úpravy</span> apod. 
Pokud máte zájem o&nbsp;jejich kompletní výpis, navštivte adresu <a
href="http://firefox.czilla.cz/otazky/klavesove-zkratky/" 
title="Přehled klávesových zkratek Firefoxu">http://firefox.czilla.cz/otazky/klavesove-zkratky/</a>.</p>


<h2 class="nice ff">Blokování vyskakovacích oken</h2>

<p>Svévolně vyskakující okna prohlížeče (pop-up okna) mohou být mnohdy velmi
nepříjemná, pokud se proti nim nebráníte. Zajděte do nabídky
<span class="nabidka">Nástroje->Možnosti...</span> 
a&nbsp;v&nbsp;zobrazeném dialogu zvolte sekci
<span class="nabidka">Obsah</span>. Uvidíte následující okno:</p>

