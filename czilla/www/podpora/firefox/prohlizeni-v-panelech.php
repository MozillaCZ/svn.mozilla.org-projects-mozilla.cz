<h2 class="nice" id="co-je-prohlizeni-v-panelech">Co je to prohlížení v&nbsp;panelech</h2>

<p>Prohlížení v&nbsp;panelech (v&nbsp;originále <span lang="en">tabbed
browsing</span>) je lepší alternativou k&nbsp;prohlížení webových stránek
v&nbsp;oknech. V&nbsp;jednom okně můžete otevřít více webových stránek
a&nbsp;v rámci něj se pomoci panelů (v&nbsp;originále <span
lang="en">tabs</span>) mezi těmito stránkami jednoduše přepínat.</p>

<p>Následující obrázek porovnává panely v&nbsp;Microsoft Windows a&nbsp;Mozilla Firefox.
Jak sami vidíte, panely již dobře znáte.</p>

<div class="img-center"><div class="img-c" style="width:366px">
	<a href="images/ffpvp-panely-windows.png"
