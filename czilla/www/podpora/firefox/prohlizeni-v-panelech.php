<p>Další možností, jak uzavřít panel, je najet myší na křížek v&nbsp;pravé
části lišty s&nbsp;panely a&nbsp;klepnout na něj.</p>

<p>Klávesová zkratka pro uzavření aktivního panelu, tedy panelu, jehož obsah
si právě prohlížíte, je <kbd>Ctrl</kbd>+<kbd>W</kbd>.</p>

<p>Můžete také uzavřít všechny ostatní panely kromě zvoleného. Klepněte
pravým tlačítkem myši na nadpis panelu a&nbsp;v následující nabídce zvolte
příslušnou možnost. Tato funkce se hodí obzvláště ve chvíli, kdy máte
otevřeno několik panelů s&nbsp;výsledky vyhledávání a&nbsp;chcete zavřít
všechny kromě právě jediného, který se vám zamlouvá.</p>


<h2 class="nice" id="vyuziti">Praktické využití</h2>

<p>V této části si na příkladu ukážeme, jak vám může používání panelů výrazně
ušetřit čas a&nbsp;zjednodušit vaši každodenní rutinu.</p>

<h3>Procházení výsledků vyhledávání</h3>

<p>Zadáte do vyhledávače (např.
<a href="http://www.google.com/">Google</a> nebo <a href="http://www.seznam.cz/">
Seznam</a>) klíčová slova a&nbsp;dostanete dlouhý seznam mnoha a&nbsp;mnoha
odkazů. Klepnete na první, přečtete si jej a&nbsp;pokud vás nezaujme, vrátíte se
zpět na výsledky hledání. Klepnete na druhý odkaz a&nbsp;celý proces se opakuje.
Pokud k&nbsp;tomu používáte ještě více oken prohlížeče, vše se prodlužuje
a&nbsp;unavuje vás to.</p>

<p>S prohlížením v&nbsp;panelech je tomu konec. Na stránce s&nbsp;výsledky
hledání pouze klepnete prostředním tlačítkem na všechny výsledky, které vás
zajímají, a&nbsp;Mozilla Firefox otevře panely a&nbsp;začne
načítat jejich obsah. Můžete je postupně procházet a&nbsp;bez jakýchkoliv
dalších kroků je uzavírat nebo se přesouvat k&nbsp;dalším. Ve chvíli, kdy
jste našli, co jste hledali, můžete dvěma klepnutími uzavřít všechny ostatní
panely a&nbsp;pročíst si blíže to, oč máte skutečně zájem.</p>


<h3>Pročítání zpravodajských serverů</h3>

<p>Při čtení zpravodajských serverů se prohlížení v&nbsp;panelech také hodí:
zajímavé články si klepnutím prostředním tlačítkem myši na titulek otevřete
do nových panelů a&nbsp;Mozilla Firefox je pro vás začne načítat.
Pak už stačí jen zavřít titulní stránku a&nbsp;můžete se nerušeně věnovat
pročítání článků.</p>


<h3>Nakupování na Internetu</h3>

<p>Vybíráte si novou pračku ve svém oblíbeném internetovém obchodě
a&nbsp;nyní se rozhodujete v&nbsp;rámci jeho nabídky. Váháte mezi třemi
výrobky. Jaké výhody vám tady prohlížení v&nbsp;panelech přinese?</p>

<p>Pokud si všechny tři výrobky otevřete v&nbsp;panelech, můžete se mezi
nimi přepínat pouze pomocí myši a&nbsp;ihned tak porovnávat konkrétní
vlastnosti daných výrobků bez zbytečných okolků při přepínání oken.</p>

<p>Po chvíli cviku již panely používáte zcela intuitivně a&nbsp;téměř si
nedokážete představit, jak to bez nich vůbec mohlo jít.</p>

