<h3 id="pohyb-mezi-panely">Jak se mohu mezi panely pohybovat</h3>

<p>Pokud máte otevřeno více panelů, můžete se mezi nimi velice intuitivně
přepínat. Stačí najet myší do oblasti, ve které se zobrazují nadpisy
panelů, klepnout levým tlačítkem na nadpis požadovaného panelu
a&nbsp;panel se stane aktivním.</p>

<p>Klávesová zkratka pro přepnutí na následující panel je
<kbd>Ctrl</kbd>+<kbd>PageDown</kbd>, <kbd>Ctrl</kbd>+<kbd>Tab</kbd> . Pro
přechod na předchozí panel použijte zkratku <kbd>Ctrl</kbd>+<kbd>PageUp</kbd>
 či <kbd>Ctrl</kbd>+<kbd>Shift</kbd>+<kbd>Tab</kbd>.</p>

<p>Na panely můžete přecházet i přímo. Klávesovou zkratkou <kbd>CTRL</kbd>+<kbd>1</kbd> se
přemístíte na první panel zleva, zkratkou <kbd>CTRL</kbd>+<kbd>2</kbd> na druhý panel atd.</p>

<h3>Obnovení panelů</h3>

<p>Obnovení stránky v&nbsp;panelu je možné provést buď stiskem klávesy
<kbd>F5</kbd>, klepnutím na tlačítko <span class="nabidka">Obnovit</span> nebo
klepnutím pravým tlačítkem na nadpis panelu a zvolením <span class="nabidka">
Obnovit panel</span>. Po klepnutí pravým tlačítkem na nadpis panelu a&nbsp;zvolení
položky <span class="nabidka">Obnovit všechny panely</span> můžete provést aktualizaci
všech otevřených panelů. Funkci můžete použít ve chvíli, kdy máte například
v&nbsp;různých panelech otevřeno více vláken svého oblíbeného diskusního fóra.</p>


<h3 id="zavreni-panelu">Uzavření panelu</h3>

<p>Panel, který již nebudete využívat, můžete samozřejmě zavřít. To
provedete opět velmi jednoduše &ndash; najeďte na nadpis zvoleného panelu
a&nbsp;stiskněte na něm prostřední tlačítko myši. Panel ihned zmizí. Pokud
již nemáte žádný další otevřený panel, zmizí zároveň i&nbsp;celá oblast, ve
které se nadpisy panelů zobrazují.</p>

<p>Pro tuto akci existuje i&nbsp;alternativa s&nbsp;pomocí pravého tlačítka.
Klepněte na nadpis zvoleného panelu pravým tlačítkem a&nbsp;zvolte
příslušnou možnost.</p>

