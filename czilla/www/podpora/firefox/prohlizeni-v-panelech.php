	src="images/ffpvp-panely-windows-m.png"
	title="Panely již dobře znáte, například z&nbsp;operačního systému Microsoft Windows"
	alt="Panely ve Windows a Firefoxu"
	height="148" width="366" /></a>
</div></div>

<p>Prohlížení v&nbsp;panelech přijde obzvláště vhod, pokud jste zrovna
navštívili svůj oblíbený zpravodajský server a&nbsp;zaujalo vás několik
článků, které si chcete přečíst. Jednoduše klepnete prostředním tlačítkem
myši na všechny články, jež si chcete přečíst, a&nbsp;ty se vám
<a href="#otevreni-panelu">otevřou v&nbsp;nových panelech</a>. Všechny články
máte již načtené v&nbsp;rámci jednoho okna a&nbsp;hlavní stránku tak můžete
zavřít a&nbsp;již se k&nbsp;ní nevracet. Až si vybraný článek přečtete,
můžete <a href="#zavreni-panelu">panel jednoduše zavřít</a>, nebo se pomocí
myši či kláves <a href="#pohyb-mezi-panely">přesunout na další článek</a>. Tím
šetříte čas a&nbsp;tlačítko <span class="nabidka">Zpět</span> téměř přestanete
používat!</p>

<p>Prohlížení v&nbsp;panelech je jednou z&nbsp;uživatelsky zajímavých funkcí
prohlížečů nové generace a&nbsp;není tedy divu, že mnoho uživatelů opouští
Internet Explorer právě kvůli ní &ndash; vždyť šetří i&nbsp;váš čas!</p>

<h2 class="nice" id="ovladani-panelu">Jak prohlížení v&nbsp;panelech funguje</h2>

<p>Ke všem důležitým operacím s&nbsp;panely vám stačí jediný stisk tlačítka
myši nebo stisk určité kombinace kláves.</p>


<h3 id="otevreni-panelu">Jak panel otevřu</h3>

<p>Představte si, že jste na stránce, která pojednává o&nbsp;štěněti labradora,
a&nbsp;narazíte na odkaz na fotografii štěněte. Chcete si ji prohlédnout, a&nbsp;tak
na odkaz klepnete <strong>prostředním tlačítkem (kolečkem)</strong> myši.
Otevře se nový panel a&nbsp;v&nbsp;něm se vybraný obrázek zobrazí.</p>

<p>Ano, správně, pro otevření nového panelu se ve Firefoxu užívá
prostřední tlačítko (kolečko). Pokud budete při klepání na odkazy používat levé
tlačítko, Mozilla Firefox otevře stránku v&nbsp;aktuálním panelu.</p>

<p>Máte-li myš bez prostředního tlačítka či kolečka, nezoufejte &ndash;
odkaz v&nbsp;novém panelu lze také otevřít, když na něj klepnete pravým
tlačítkem myši a&nbsp;vyberete v&nbsp;nabídce možnost <span class="nabidka">Otevřít
odkaz v novém panelu</span>.</p>

<div class="img-center"><div class="img-c" style="width:398px">
	<a href="images/ffpvp-novy-panel.png"
	class="zoom"><img
	src="images/ffpvp-novy-panel-m.png"
	alt="Otevření odkazu v&nbsp;novém panelu za pomoci pravého tlačítka myši"
	height="200" width="398" /></a>
</div></div>

<p>Odkaz můžete v&nbsp;novém panelu otevřít také pomocí klávesnice. Pomocí
tabelátoru označte požadovaný odkaz (mačkejte klávesu <kbd>Tab</kbd> tak dlouho, dokud se požadovaný odkaz neorámuje) a&nbsp;stiskněte <kbd>Alt</kbd>+<kbd>Enter</kbd>.</p>

<p>Pokud chcete otevřít nový prázdný panel, stačí použít klávesovou zkratku
<kbd>Ctrl</kbd>+<kbd>T</kbd>.</p>


<h3>Kde panely najdu</h3>

<p>Každý panel, který otevřete, se zobrazí pod <em>navigační lištou</em>
&ndash; (řádkem, do kterého zadáváte webovou adresu). Pokud otevřete více panelů
současně, budou se skládat vedle sebe zleva doprava.</p>

<p>Právě prohlížený panel bude oproti ostatním graficky zvýrazněný, např.
trochu vysunutý do popředí.</p>

