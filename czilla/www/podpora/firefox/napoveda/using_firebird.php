<h2 id="improving_speed_and_efficiency">Rychlá a efektivní práce s programem</h2>

<h3 id="managing_different_file_types">Správa zásuvných modulů</h3>
<p>Firefox umí pracovat s řadou typů souborů. Pro řadu z nich, jako například video či hudbu, potřebuje
  Firefox zásuvný modul nebo &quot;pomocné&quot; aplikace, které umí s těmito soubory pracovat. Pokud si Firefox
  neumí poradit s načítaným souborem, nabídne jej ke stažení.</p>

<p><strong>Zásuvné moduly</strong><br/>
	Zásuvné moduly jsou pomocné programy, které rozšiřují funkčnost Firefox a umožňují spouštět přidružené 
	soubory jako například videa, animace či hry přímo v okně prohlížeče. Jako zásuvné moduly mohou pracovat 
  například Java, Macromedia Flash či RealNetworks RealPlayer..</p>

<p>Zásuvné moduly lze též doinstalovat po instalaci Firefox. Které zásuvné moduly jsou nainstalovány zjistíte
  zvolbou <span class="win menuPath">Nástroje &gt;                             Možnosti</span><span class="unix menuPath">Úpravy &gt;                             Předvolby</span> v hlavní nabídce a v zobrazeném dialogu pod 
  <em>Stahování</em> klepněte na tlačítko <em>Zásuvné moduly</em>. Zde můžete povolit či zakázat funkčnost 
  nainstalovaných modulů.</p>

<p>Seznam základních zásuvných modulů, které jsou dostupné pro Firefox, naleznete na 
  <a href="http://www.czilla.cz/doplnky/zasuvne-moduly/" target="_blank">následující stránce</a>.</p>

<p><strong>Pomocné aplikace</strong><br/>
	Pokud není možné soubor otevřít přímo v Firefox, máte možnost spustit pomocnou aplikaci vně Firefox.
	Například takto můžete spustit přehrávání MP3 souboru v programu jako je třeba Winamp.</p>

<p>Nastavením v sekci <em>Stahování</em> dialogu <span class="win">Možnosti</span><span                             class="unix">Předvolby</span> můžete Firefox sdělit, které pomocné
  aplikace má používat.
  V sekci <em>Typy souborů</em> můžete vidět typy souborů a externí aplikace, které jejich zavolání spustí. Můžete
  zde změnit akci pro daný typ souboru či odstranit akci úplně. Pokud Firefox nezná pomocnou aplikaci, můžete
  si ji při stahování souboru zvolit.</p>

<p>Dialog ke stažení má následující volby:</p>

<ul>
  <li><strong>Otevřít pomocí</strong>: Firefox otevře soubor s pomocí výchozího programu dle nastavení
	  operačního systému. Můžete též zvolit jiný program volbou <em>Jiná...</em>.</li>
  <li><strong>Uložit na disk</strong>: Firefox umožní vybrat cílový adresář, kam bude soubor uložen. Pokud
	  jste v sekci <em>Stahování</em> zvolili volbu <em>Uložit všechny soubory do složky</em>, nebudete již dotazováni a
	  soubor se automaticky do této složky uloží.</li>
  <li><strong>Provádět od teď automaticky s podobnými soubory</strong>: Pokud zvolíte tuto volbu, Firefox
	  automaticky zajistí, že stejná akce se u daného souboru provede i při příštím stažení.</li>
</ul>

<p><strong>Tip</strong>: Tato nastavení můžete vymazat volbou <em>Při ukládání souboru se vždy zeptat</em> v sekci
	<em>Stahování</em>.</p>

<h3 id="making_firebird_your_default_browser">Nastavení Firefox jako výchozí prohlížeč</h3>
<p><strong>Poznámka</strong>: Tato sekce pojednává o nastavení, které je možné pouze v operačních systémech Windows a Linux.</p>

<p>Firefox je kvalitní prohlížeč webových stránek. Pro snadnější otevírání webových stránek jej můžete učinit výchozím webovým prohlížečem v systému.</p>

<p>Pokud tomu tak chcete, zvolte v hlavní nabídce <span class="win menuPath">Nástroje &gt;                             Možnosti</span><span class="unix menuPath">Úpravy &gt;                             Předvolby</span>. Zde naleznete tlačítko <em>Nastavit výchozí prohlížeč</em>. S jeho pomocí se Firefox stane výchozím webovým prohlížečem.</p>

<h3 id="changing_cache_settings">Změna vyrovnávací diskové paměti</h3>
<p>Firefox ukládá kopie často navštěvovaných stránek do paměti. Díky tomu není nutné, aby  Firefox
  stahoval stránky ze sítě pokaždé, když si ji chcete prohlédnout.</p>

<p>Pro nastavení velikosti vyrovnávací paměti postupujte následovně (pouze Windows či Unix):</p>
<ol>
  <li>Zvolte v hlavní nabídce <span class="win menuPath">Nástroje &gt;                             Možnosti</span><span class="unix menuPath">Úpravy &gt;                             Předvolby</span>.</li>
  <li>Pod kategorií <em>Soukromí</em> zvolte <em>Disková vyrovnávací paměť</em> čímž rozbalíte nabídku.</li>
  <li>Zadejte číslo v poli <em>Použít nejvíce</em>, které udává maximální velikost vyrovnávací paměti. Rozumná velikost je mezi 50 až 70 MB. Pokud chcete tuto paměť vymazat, klepněte na tlačítko <em>Vymazat</em>.</li>
</ol>

<p><strong>Důležité</strong>: 
	Větší vyrovnávací disková paměť umožní rychleji zobrazovat webové stránky, ale je při tom použito více diskového prostoru.</p>

<p>Pokud ukončíte Firefox, paměť bude uchována. Tato operace může zabírat mnoho času. Pokud trvá příliš dlouho, zmenšete v nastavení maximální velikost vyrovnávací diskové paměti.</p>

<p>Pro obnovení stránky:</p>
<ul>
  <li>Klepněte na tlačítko <em>Obnovit</em> v <em>Navigační liště</em> Firefox'.
	    Prohlížeč zkontroluje, zda-li se na Internetu nenachází aktuálnější verze stránky.</li>
</ul>

