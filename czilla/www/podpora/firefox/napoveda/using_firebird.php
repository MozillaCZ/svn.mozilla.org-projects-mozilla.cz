<h2 id="navigating_web_pages">Prohlížení webových stránek</h2>

<h3 id="viewing_your_home_page">Prohlížení domovské stránky</h3>
<p>Po startu programu Firefox se vám automaticky načte vaše domovská stránka. Jako výchozí je zvolena domovská stránka Firefoxu.</p>

<p><strong>Tipy</strong>:</p>
<ul>
  <li>Pro zobrazení více webového obsahu na obrazovce můžete použít režim Celé obrazovky, který zestruční všechny lišty Firefoxu do jedné malé. Pro aktivaci režimu Celá obrazovka zvolte v hlavní nabídce
    <span class="menuPath">Zobrazit &gt; Celá obrazovka</span>. Můžete též použít 
			klávesovou zkratku <kbd>F11</kbd>.</li>
  <li>Pro rychlý přechod na domovskou stránku stiskněte klávesovou zkratku <span class="noMac"><kbd class="noMac">Alt</kbd><kbd                             class="mac">Opt</kbd></span>
    <span class="mac"><kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd></span>+<kbd>Home</kbd></li>
</ul>

<h3><strong id="moving_to_another_page">Přesun na další stránku</strong></h3>
<p>Na novou stránku se můžete přesunout tak, že napíšete její URL  ? její webovou adresu ? do pole <em>Umístění</em> v horní části okna prohlížeče. URL stránky ve většině případů začíná zkratkou &quot;http://&quot; následovanou několika slovy oddělenými tečkou, či zpětným lomítkem, určující přesnou adresu stránky. Například &quot;http://www.seznam.cz&quot;.</p>

<ol>
  <li>Klepněte dvojitě na pole <em>Umístění</em>, čímž vyberete stávající adresu.</li>
  <li>Napište URL stánky, kterou si přejete navštívit. Tato nová URL přepíše tu původní.</li>
  <li>Stiskněte <kbd class="noMac">Enter</kbd><kbd                             class="mac">Return</kbd>.</li>
</ol>

<p><strong>Tip:</strong> Rychlý výběr URL v poli Umístění provedete stiskem klávesové 
    zkratky <kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd>+<kbd>L</kbd>.</p>

<p><img src="../images/help-urlbar.png" width="320" height="30" alt=""/></p>

<p><strong>Neznáte URL</strong>? Zkuste zadat hlavní slovo (jako například &quot;gifts&quot; či &quot;flowers&quot;) do adresního řádku a stiskněte klávesu Enter. Tím zahájíte vyhledávání předmětu, který jste zadali. Výsledkem vám bude URL, které jste pravděpodobně hledali.</p>

<h3 id="clicking_a_link">Klepnutí na odkaz</h3>
<p>Většina stránek obsahuje odkazy, na které můžete klepnout kurzorem myši a přesunout se tak na další stránku.</p>

<ol>
  <li>Pohybujte ukazatelem myši dokud nezmění svůj tvar na ukazující prst. To nastane vždy, pokud je tento ukazatel nad odkazem ? většinou jsou odkazy od ostatního textu odlišeny podtržením a jinou barvou. Není to však pravidlo. Navíc stejně tak mohou být odkazem i různá tlačítka nebo obrázky.</li>
  <li>Klepněte na odkaz. Během hledání stránky na síti a jejího stahování, se ve spodní části okna ve stavovém řádku objevují zprávy informující vás o postupu nahrávání stránky.</li>
</ol>

<h3 id="retracing_your_steps">Vrácení kroků</h3>
<p>Existuje několik způsobů, jak se dostat na stránky navštívené už v minulosti:</p>

<ul>
  <li>Pro pohyb vpřed a vzad o jednu stránku klepněte na tlačítka <em>Vpřed</em> nebo <em>Zpět</em>.</li>
  <li>Pro pohyb vpřed a vzad o více stránek klepněte a podržte malý trojúhelník u tlačítek <em>Zpět</em> a <em>Vpřed</em>. Otevře se vám seznam stránek, které jste navštívili. Pro přesun na danou stránku ji stačí z tohoto seznamu
vybrat.<br/>
    <br/>
    <img src="../images/help-firefox-toolbar.png" width="237" height="50" alt=""/>
    <br/>
  </li>
<li>Pokud klepnete a podržíte šipku v pravé části pole <em>Umístění</em>, otevře se vám seznam URL stránek, které jste do tohoto pole zadali. Pro přesun na danou stránku ji stačí vybrat z tohoto seznamu.<br/>
  <br/>
  <img src="../images/help-urlbar.png" width="320" height="30" alt=""/><br/>
  </li>
  <li>Pokud byste se rádi přesunuli na stránku navštívenou v průběhu aktuálního sezení, otevřete nabídku <em>Přejít</em> a vyberte si ze seznamu ve spodní části této nabídky.</li>
  <li>Pro stránky navštívené během některého z minulých sezení otevřete nabídku <em>Přejít</em> a vyberte položku <em>Historie</em>. Po straně prohlížeče se zobrazí postranní lišta Historie. Tato lišta obsahuje seznam složek. Dvojklepnutím na složku zobrazíte podsložky nebo záložky webových stránek. Dvojklepnutím na záložku zobrazíte danou webovou stránku.</li>
</ul>

<h3 id="stopping_and_reloading">Stopnutí a znovu načtení stránky</h3>
<p>Pokud se stránka načítá příliš pomalu nebo jste se rozhodli jinak a nechcete si stránku prohlížet, klepněte na tlačítko
	  <em>Zastavit</em>.</p>

<p>K obnovení současné stránky nebo k natažení aktualizované verze, klepněte na tlačítko <em>Obnovit</em>
	   nebo stiskněte klávesovou zkratku <span class="noMac"><kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd></span>
  <span class="mac"><kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd></span>+<kbd>Shift</kbd>+<kbd>R</kbd>.</p>

<h3 id="tabbed_browsing">Prohlížení v panelech</h3>
<p>Pokud chcete navštívit více webových stránek současně, můžete pro snadnější a rychlejší prohlížení webových stránek použít prohlížení v panelech.</p>

<p>Prohlížení v panelech vám dovolí otevřít více panelů v rámci jednoho okna prohlížeče Firefox. Na každém panelu můžete zobrazit jinou webovou stránku. Nebudete tedy muset mít otevřeno více oken prohlížeče. Tím výrazně ušetříte místo vaší ploše. Můžete snadno otvírat, zavírat či znovu načítat webové stránky bez nutnosti přepínat na jiné okno.</p>

<p>Pro více informací nahlédněte do kapitoly <a href="tabbed_browsing.html">Prohlížení v panelech</a>.</p>

<h3 id="using_the_sidebar">Používáme postranní lišty</h3>
<p>Postranní lišta je místo na levé straně okna, kde si můžete nechat zobrazit své záložky, historii či stahování. Funkčnost této lišty lze též rozšířit instalací rozšíření.</p>

<p>Pro zobrazení položky v postranní liště zvolte v hlavní nabídce <span class="menuPath">Zobrazit &gt;
  Postranní lišta</span>. Odsud můžete do postranní lišty umístit to, co chcete.</p>

<h2 id="searching">Vyhledávání</h2>

<h3 id="searching_the_web">Vyhledávání webových stránek</h3>

<p>Vyhledávání webových stránek je ve Firefox snadné a to zejména díky poli <em>Vyhledávání</em>. Snadným
  zadáním jednoho či více slov můžete nalézt hledanou webovou stránku:<br/>
<img src="../images/help-searchbar.png" alt="" width="136" height="29"/></p>

<p>Například chcete vyhledat informace o "Mozilla":</p>
<ol>
  <li>Klepněte do pole <em>Vyhledávání</em></li>
	  <li>Zadejte klíčové slovo <kbd>Mozilla</kbd>.</li>
	  <li>Stiskem klávesy <kbd class="noMac">Enter</kbd><kbd                             class="mac">Return</kbd> zahájíte vyhledávání.</li>
	</ol>
	
	<p>Výsledky hledání se vám zobrazí v okně prohlížeče Firefox.</p>
	  

<h3 id="searching_on_selected_words_in_a_web_page">Vyhledání zvolených slov z webové stránky</h3>

<p>Firefox umožňuje, aby jste zahájili vyhledávání zvolených slov, které jsou umístěny v textu na webové stránce.
	Postup je následující:</p>

<ol>
  <li>Označte hledaná slova v textu webové stránky.</li>
  <li>Klepněte pravým tlačítkem myši a za nabídky zvolte "Hledat na webu [hledaná slova]". (V Mac OS zobrazíte místní nabídku stisknutím klávesy <kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd> a klepnutím na tlačítko myši.)</li>
</ol>

<p>Firefox otevře nové okno a použitím aktuálně zvoleného vyhledávacího modulu vyhledá zadaná slova.</p>

<h3 id="searching_within_a_page">Hledání uvnitř webové stránky</h3>

<p>Chcete-li najít text, který je obsažen na stránce načtené v okně Firefox, postupujte následovně:</p>
<ol>
  <li>Zvolte v hlavní nabídce <span class="menuPath">Úpravy &gt; Hledat na této stránce</span>. Pokud stránka obsahuje rámy, musíte nejdříve klepnout do rámu, který chcete prohledávat. Otevře se vám nová lišta blízko stavové lišty v dolní části prohlížeče.</li>
  <li>Napište hledaný text.</li>
  <li>Zatrhněte některé z následujících možností (není nutné):
  <ul>
    <li><strong>Find Next</strong>: choose this option to find items in the 
      page that are below the current cursor position.</li>
    <li><strong>Find Previous</strong>: choose this option to find items 
      that are above the current cursor position.</li>
    <li><strong>Highlight</strong>: choose this option to highlight occurrences 
      of your search term in the current page.</li>
  </ul>
  </li>
  <li>Vyhledávání automaticky začne tehdy, když něco zadáte do textového pole.</li>
</ol>

<p>Chcete-li zopakovat hledání stejného textu:</p>

<ul>
  <li>V nabídce zvolte <span class="menuPath">Úpravy &gt; Hledat znovu</span>.</li>
</ul>

<h2 id="copying_saving_and_printng_pages">Kopírování, uložení a vytištění stránky</h2>

<h3 id="copying_part_of_a_page">Kopírování části stránky</h3>

<p>Chcete-li kopírovat z okna prohlížeče nějaký text:</p>
<ol>
  <li>Vyberte text, který chcete kopírovat.</li>
  <li>Otevřete nabídku <span class="menuPath">Úpravy</span> a vyberte položku <span class="menuPath">Kopírovat</span>.</li>
</ol>

<p>Nyní můžete text vložit do jiného programu.</p>

<p>Ze stránky také můžete zkopírovat adresu odkazu (URL) nebo umístění obrázku:</p>

<ol>
  <li>Najeďte kurzorem myši nad odkaz nebo obrázek.</li>
  <li>Klepněte pravým tlačítkem myši. Otevře se vám plovoucí nabídka.  (V Mac OS stiskněte <kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd>
	    a současně klepněte tlačítkem myši.)</li>
  <li>Vyberte <em>Zkopírovat umístění odkazu</em> nebo <em>Zkopírovat umístění obrázku</em> pokud je obrázek zároveň i odkazem, máte na výběr obě možnosti.</li>
</ol>

<p>Nyní můžete odkaz vložit do jiného programu nebo do pole Umístění Firefox.</p>

<h3 id="saving_all_or_part_of_a_page">Ukládání celé stránky či její části</h3>

<p>Ukládání je nedílnou součásti Firefox:</p>

<ol>
  <li>Otevřete nabídku <span class="menuPath">Soubor</span> a zvolte <span class="menuPath">Uložit stránku jako</span>. Zobrazí se dialog pro uložení stránky na disk.</li>
  <li>Zvolte místo, kam chcete stránku uložit.</li>
  <li>Zvolte formát uložení stránky:
  <ul>
    <li><strong>Webová stránka, kompletní</strong>: Uloží celou webovou stránky i s obrázky. Tyto obrázky (společně se všemi nutnými soubory pro správné zobrazení) Firefox uloží do nového adresáře. Odkazy v HTML stránce jsou následně upraveny tak, aby ukazovali na nově vytvořený adresář.</li>
    <li><strong>Webová stránky, pouze HTML</strong>: Uloží stránku bez obrázků.
		  Tato volba zachová HTML v původní podobě.</li>
    <li><strong>Textové soubory</strong>: Uloží stránku do textového souboru. Tato volba odstraní ze stránky HTML kód a umožní vám zobrazit obsah webové stránky v libovolném textovém editoru.</li>
  </ul>
  </li>
  <li>Zadejte jméno stránky a klepněte na tlačítko <em>Uložit</em>.</li>
</ol>

<p>Ukládání zvoleného rámce:</p>

<ol>
  <li>Umístěte kurzor myši do zvoleného rámce.</li>
  <li>Klepnutím pravým tlačítkem myši (v Mac OS stiskněte <kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd> a klepněte tlačítkem myši) se zobrazí místní nabídka.</li>
  <li>Zvolte <span class="menuPath">Tento rám</span> a následně <span class="menuPath">Uložit rám jako</span> z podnabídky. Zobrazí se vám dialog pro uložení stránky na disk.</li>
  <li>Zvolte místo, kam chcete stránku uložit.</li>
	<li>Zvolte formát stránky jaký chcete uložit.</li>
	<li>Zadejte pojmenování stránky a klepněte na tlačítko <em>Uložit</em>.</li></ol>

<p>Uložením stránky na disk získáte možnost její prohlížení v momentě, když nebudete připojeni k Internetu.</p>

<p>Ukládání obrázku umístěného na stránce:</p>
<ol>
  <li>Umístěte kurzor myši nad zvolený obrázek.</li>
  <li>Klepnutím pravým tlačítkem myši (v Mac OS stiskněte <kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd> a klepněte tlačítkem myši) se zobrazí místní nabídka.</li>
  <li>Zvolte <span class="menuPath">Uložit obrázek jako</span>. Zobrazí se vám dialog pro uložení obrázku na disk.</li>
  <li>Zvolte místo, kam chcete obrázek uložit.</li>
  <li>Zadejte pojmenování obrázku a klepněte na tlačítko <em>Uložit</em>.</li>
</ol>

<p>Ukládání stránky bez jejího zobrazení (tato volba je užitečná tehdy, když chcete získat neformátovanou stránku bez jejího zobrazení):</p>

<ol>
  <li>Umístěte kurzor myši nad zvolený odkaz.</li>
  <li><span class="noMac">Klepnutím pravým tlačítkem nad odkazem</span> (<span class="mac"> zvolte <kbd class="noMac">Ctrl</kbd><kbd                             class="mac">Cmd</kbd> a klepněte tlačítkemmyši</span>) se zobrazí místní nabídka.</li>
  <li>Zvolte <span class="menuPath">Uložit odkaz na disk</span>. Zobrazí se vám dialog pro uložení stránky na disk.</li>
  <li>Zvolte místo, kam chcete stránku uložit.</li>
  <li>Zadejte pojmenování stránky a klepněte na tlačítko <em>Uložit</em>.</li>
</ol>

<p><strong>Důležité</strong>: Pokud klepnete na odkaz, může se též stát, že cílový soubor bude automaticky stažen na disk. Odkazy na tyto soubory obyčejně začínají &quot;ftp&quot; nebo končí příponou &quot;mp3&quot;, &quot;mpeg&quot; apod. Tyto soubory automaticky spustí pomocnou aplikaci, která tyto soubory podporuje.</p>

<p class="win"><strong>Tip</strong>: Pro uložení obrázku na pozadí ve Windows klepněte pravým tlačítkem nad obrázkem a zvolte 
  <em>Nastavit jako tapetu</em> z kontextové nabídky.</p>

<h3 id="printing_a_page">Vytištění stránky</h3>

<p>Chcete-li vytisknout stránku načtenou v okně prohlížeče:</p>

<ul>
  <li>Otevřete nabídku <span class="menuPath">Soubor</span> a vyberte položku <span class="menuPath">Tisk</span>.</li>
</ul>

<p>Vytištění označeného textu:</p>
<ul>
  <li>Na aktuální stránce zvolte požadovaný text.</li>
  <li>V hlavní nabídce zvolte <span class="menuPath">Soubor</span> a vyberte položku <span class="menuPath">Tisk</span>. Zobrazí se vám dialog pro tisk.</li>
  <li>V <em>Oblast tisku</em> klepněte na <em>Výběr</em>.</li>
</ul>

<p>Umístění na stránce je ovlivněno velikostí tištěné stránky a autorem webové stránky, 
    nikoliv velikostí obrazovky. Text je zalomen a grafika přesunuta tak, aby se vše vešlo na papír.</p>

