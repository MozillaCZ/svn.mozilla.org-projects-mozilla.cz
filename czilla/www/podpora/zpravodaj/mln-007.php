<h3 id="ml.23">Podpora autentizace NTLM v Mozille na všech platformách</h3>
<p>Ověřovací protokol NTLM od Microsoftu, populární na windowsovských sítích, je nyní Mozillou podporován na všech platformách. Předtím byl dostupný pouze uživatelům Mozilly ve Windows a vyžadoval SSPI kód obsažený ve Windows. Nyní bylo upuštěno od systému na bázi SSPI a bylo vytvořeno meziplatformní prostředí.</p>
<p>Chyba <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=224653">#224653</a>.</p>

