<h3 id="ml.41">Projekt týdne: Adblock</h3>
<p>Adblock je modul na blokování obsahu pro Mozillu a Mozilla Firebird. Je robustnější a přesnější než zabudovaný <tt>správce obrázků</tt>. Po instalaci filtruje prvky ze zdrojové adresy. Stačí kliknout pravým tlačítkem myši a vybrat Adblock - done. Filtry pracují buď se zástupnými znaky (*) anebo regulárními výrazy. Panel se seznamem okamžitě ukazuje co je a co není blokováno. Zachyceny mohou být skripty, pozadí i Flash. Plugin-media umožňuje filtrovat otevřené panely jediným kliknutím. Speciální klávesová zkratka rychle schová jakýkoli prvek na stránce. Pro vypnutí modulu stačí stisknout libovolnou klávesu.</p>
<p>Prozkoumejte <a href="http://www.mozdev.org/featured/index.html">tento a další projekty týdne</a>.</p>

