<p>Minulý týden byla vydána nová denní verze Mozilla Thunderbirdu (vyvíjená e-mailová aplikace) s mnoha vylepšeními. Verze z 19. 11. 2003 obsahuje přepracované upozornění o příchozí poště, vylepšený stavový řádek, nastavení pro označení zprávy jako přečtené po zobrazení během určitého počtu sekund a několik dalších vylepšení. Nyní je navíc možné ve Windows nastavit Thunderbird jako svou výchozí poštovní aplikaci. Více informací a odkazy na stažení lze nalézt v příspěvku Scotta MacGregora <a href="http://forums.mozillazine.org/viewtopic.php?t=36044">ve fóru na MozillaZine</a>.</p>
