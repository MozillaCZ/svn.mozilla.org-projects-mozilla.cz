<h3 id="ml.24">Mnoho organizací zaměstnává přispěvovatele Mozilly</h3>
<p>Řadu let společnost <a href="http://www.netscape.com/">Netscape</a> zaměstnávala pro práci na prohlížeči několik lidí. Poté, co v červnu AOL (vlastník Netscape) zastavila příspěvky do projektu, se mnoho lidí domnívalo, že veškerou práci nyní vykonávají dobrovolníci. Ale tak tomu není a řada společností posílila své týmy o vývojáře Mozilly nebo přispěvovatele projektu zaměstnala poprvé.</p>
<p>Mozilla Foundation nyní tvoří David Baron, Asa Dotzler, Brendan Eich, Ben Goodger, Chris Hofmann, Scott MacGregor, Myk Melez, Daniel Nunes a Johnny Stenback a Mitchell Baker, který zde pracuje na částečný úvazek.</p>
<p>Krátce po propuštění se IBM rychle ujala několika předchozích zaměstnanců Netscape včetně Darina Fishera, Briana Rynera a Dorona Rosenberga pro posílení své skupiny pracující na Mozille. Sun a Red Hat nadále zaměstnávají přispěvovatele Mozilly. Společnost Lindows.com dotuje Daniela Glazmana za vývoj <a href="http://nvu.com/">Nvu</a>, nové aplikace pro publikování na webu založené na Editoru Mozilly.</p>
<p>Mezitím se spojili bývalí zaměstnanci Netscape (Kevin McCluskey, Rod Spears, Chris Karnaze a Peter Lubczynski) a vytvořili <a href="http://www.mozillaconsulting.com/">Mozilla Consulting</a>. Nedávno byla také vytvořena <a href="http://www.mozdevgroup.com/">Mozdev Group</a>, která nabízí vývoj programů Mozilly a technickou podporu malým a středním podnikům.</p>
<p>Mnoho lidí předpovídalo, že Mozilla v post-Netscapovském světě nepřežije. Během pouhých několika měsíců, kdy AOL odstoupil od projektu, je ještě brzy tvrdit, jaké budou dlouhodobé důsledky. Nicméně již ted je jasné, že ohromující počet společností vidí Mozillu jako perspektivní technologii. A to je pozitivní signál.</p>

<p><b>[<a href="http://www.mozillazine.org/talkback.html?article=3976">Celý článek</a>]</b></p>

<h2 id="ml.3" class="nice">Mozilla pod povrchem</h2>
