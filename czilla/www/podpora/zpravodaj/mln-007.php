<h3 id="ml.11">Rozšíření Preferential</h3>
<p><i>(vyvinuli Stephen Bounds, Biju G. C., Mike Cartwright a další)</i></p>

<p><a href="http://preferential.mozdev.org/">Preferential</a> umožňuje nastavit všechny předvolby Mozilly (nyní jich je už kolem 650), Mozilla Firebird a Mozilla Thunderbird. V přehledném hiearchickém stromu ukazuje a stručně popisuje nastavení dostupná po vložení <tt>about:config</tt> do adresního řádku včetně seznamu všech přípustných hodnot každého nastavení.</p>

<h3 id="ml.12">Tip zpravodaje Mozilla Links: Informujte přátele o dobré webové stránce</h3>
<p><i>(přispěl <a href="http://www.mozillatips.com/">mozillatips.com</a>)</i></p>
<p>Oznámení adresy dobré webové stránky vašim přátelům je snadné. Mozillu můžete použít k odeslání odkazu nebo dokonce přiložit celou webovou stránku.</p>

<p>Na dané stránce vyberte <tt>Poslat odkaz</tt> z nabídky <tt>Soubor</tt>. Mozilla otevře vaši výchozí aplikaci pro odesílání pošty a vytoří novou zprávu s odkazem. Stačí jen připsat adresu vašeho přítele a odeslat zprávu.</p>

<p>Máte-li nainstalovaný Mozilla Mail, vyberte <tt>Odeslat stránku</tt> z nabídky <tt>Soubor</tt>. Dostanete stejnou zprávu s odkazem a webovou stránkou přiloženou jako <abbr title="Hypertext Markup Language">HTML</abbr> soubor do vašeho e-mailu.</p>
<p><b>[<a href="http://www.mozillatips.com/modules.php?op=modload&amp;name=News&amp;file=article&amp;sid=93&amp;mode=thread&amp;order=0&amp;thold=0">Celý tip</a>]</b></p>

<h3 id="ml.13">Supertip zpravodaje Mozilla Links: Poznejte Mozillu</h3>
<p>Příkaz <tt>about:</tt> odhalí zajímavé údaje o vaší Mozille. Někdy pomůže najít požadované informace, jindy slouží pro pobavení (<tt>about:mozilla</tt> nebo <tt>about:kitchensink</tt>). V historii Mozilly byl kitchensink (kuchyňský dřez) ASCII-artem (kresbou pomocí textových znaků), nyní je dostupný na <a href="http://www.mozilla.org/docs/web-developer/samples/kitchensink.xml">mozilla.org</a></p>
<p>Pro vyzkoušení některého z následujících příkazů jej napište do adresního řádku a stiskněte <tt>Enter</tt>.</p>
<ul>
	<li><tt>about:</tt> - Stránka o Mozille</li>
	<li><tt>about:blank</tt> -	Prázdná stránka</li>
	<li><tt>about:buildconfig</tt> - Nastavení pro sestavení verze</li>
	<li><tt>about:cache</tt> -	Informace o vyrovnávací paměti</li>
	<li><tt>about:cache?device=memory</tt> -	Seznam položek ve vyrovnávající paměti</li>
	<li><tt>about:cache?device=disk</tt> -	Seznam položek v paměti disku</li>
