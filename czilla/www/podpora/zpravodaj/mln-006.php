<h3 id="ml.13">Supertip zpravodaje Mozilla Links: Rychlejší zobrazování webových stránek</h3>
<p>(přispěvek od MozillaTips.com, připravil Percy Cabello)</p>

<p>Když Mozilla začíná stahovat webovou stránku, po určité době ji zkouší zobrazit (vykreslit). Pokud po přijetí více dat Mozilla zjistí, že je nutné nové vykreslení pro umístění nových prvků jako obrázky nebo části formuláře, musí zrušit to, co bylo vykonáno a vykreslit celou stránku znovu.</p>

<p>Při vytáčeném připojení je vhodné nechat Mozillu čekat trochu déle před "konečným" vykreslením.</p>

<p>Jak?</p>
<ul>
	<li>Napište about:config v adresním řádku (Location bar) pro otevření úprav předvoleb.</li>
	<li>Najděte nglayout.initialpaint.delay , klikněte pravým tlačítkem a vyberte Úpravy (Modify).</li>
	<li>Napište nglayout.initialpaint.delay pro název a 60 pro hodnotu předvolby.</li>
	<li>Nemůžete-li najít přílsušné nastavení, jednoduše jej přidejte pravým kliknutím na jakékoliv nastavení. Vyberte Nový/Celé číslo (New/Integer). Napiště nglayout.initialpaint.delay pro název a 60 pro hodnotu předvolby.</li>
</ul>

<p>Tak lze v Mozille předejít několika vykreslováním stejné stránky a zobrazovat ji rychleji.</p>
<p><a href="http://www.mozillatips.com/modules.php?op=modload&amp;name=News&amp;file=article&amp;sid=45&amp;mode=thread&amp;order=0&amp;thold=0">http://www.mozillatips.com/modules.php?op=modload&amp;name=..</a></p>

