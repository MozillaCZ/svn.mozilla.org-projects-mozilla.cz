<h2 id="ml.3" class="nice">Mozilla pod povrchem</h2>
<p>(přispěl Alex Bishop)</p>
<p>Tento týden byly provedeny významné změny těm, kterým jsou povoleny změny souborů v Mozilla CVS (Concurrent Versions System), kde je hlavní kopie kódu pro všechny vyvíjené produkty mozilla.org. Sekce Mozilla Firebird v tomto systému byla otevřena všem s právem pro zápis do Seamonkey CVS (Seamonkey je kódové označení pro sadu aplikací Mozilla).</p>
<p>Do pondělí 10. listopadu bylo povolení pro zásah do sekce s prohlížečem omezeno na malou skupinu vývojářů jádra Mozilla Firebirdu. Nyní může změny v adresářích mozilla/browser, mozilla/toolkit a mozilla/chrome provádět každý s právy pro zápis do Seamonkey CVS za předpokladu, že jejich záplaty nejdříve zkontroluje jeden ze správců Firebirdu. Současnými správci jsou Ben Goodger, Blake Ross, Brian Ryner, Pierre Chanial a David Hyatt.</p>
<dl>
	<dt><a href="http://www.mozilla.org/projects/firebird/review.html">http://www.mozilla.org/projects/firebird/review.html</a></dt>
	<dd>Nové podmínky zásahů do Mozilla Firebirdu.</dd>
</dl>

<p>Jiné typy změn byly provedeny u ekvivalentu FB - Mozilla Thunderbirdu. Vývojář Scott MacGregor oznámil nové podmínky pro ty, kdo sestavují Thunderbird: <a href="http://forums.mozillazine.org/viewtopic.php?t=34269">http://forums.mozillazine.org/viewtopic.php?t=34269</a></p>

<p>Vylepšení se dočkala také Mozilla.</p>
<p>Nyní mohou být stránky přidávány do bílé nebo černé listiny ve správci cookies (chyba 222553) a v posledních verzích je možné přehrát zvuk, pokud správce stahování dokončí úlohu (chyba 16498). Toho docílíte přidáním nové položky browser.download.finished_sound_url a výběrem zvukového souboru.</p>
<p>Vývojáře zaujme nové uživatelské rozhraní s podporou pro přidání nových typů ověření do Mozilly prostřednictvím zásuvných modulů (chyba 180049). Navíc bylo přepsáno náhled před tiskem tak, aby mohl být využit jakoukoli XUL aplikací využívající novou sadu nástrojů (tu používá Firebird a Thunderbird ale ne Mozilla).
</p>
<p>Konečně byl vyřešen problém se strojem pro sestavování verzí, jenž od 28. října nesestavoval žádné denní verze Mozilly (chyba 219936).</p>

