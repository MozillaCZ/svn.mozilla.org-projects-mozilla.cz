Osobní zkušenost z propagace Mozilly na Taiwanu
------------------------------------------------------
(Bob Chao)

Jsem Bob Chao, taiwanský student. V současné době se účastním překladu
článků o Mozille a propagace Mozilly u čínských uživatelů. Chtěl bych se 
zde s vámi podělit o své zkušenosti s propagací Mozilly.

Propagace

Od vydání Mozilly 0.9 propaguji Mozillu na katedře naší školy. Protože 
šlo o projekt jedné osoby, byl způsob poněkud pasivní: s každou novou 
verzí jsem překládal dokument "Co je nového" a odesílal ho na školní 
BBS, uváděl novinky o Mozille na své osobní stránce a navíc doporučoval 
Mozillu na vysokoškolských seminářích. Mými posluchači byli studenti se 
zájmem o počítače, takže jsem svou kampaň zaměřil na "technologie"
a "standardy".
Ačkoliv někteří byli ochotni přejít na Mozillu, většina raději používala 
již nainstalovaný Internet Explorer. Kromě toho velká část taiwanských 
studentů používá BBS, a tak prodejci software vydávali velmi populární 
prohlížeč na bázi IE s integrovanou podporou protokolu telnet. Kvůli 
těmto okolnostem měla moje snaha malý úspěch.

Moje kampaň začala být úspěšná až díky Phoenixu (dřívější název 
Firefoxu). Piap, vedoucí čínského lokalizačního projektu, začal 
lokalizovat Phoenix od verze 0.2. Od verze 0.5 jsem Phoenix podporoval 
spolu s Mozillou. Na rozdíl od předešlé snahy jsem se zaměřil na "malou 
velikost" a "rychlost". Tyto vlastnosti přitahují uživatele, kteří rádi 
zkoušejí nové věci. Díky různým setkáním s uživateli přibýval počet 
nových nadšenců. 12. března již 13 % studentů na naší katedře používalo 
Mozillu nebo Firefox. Nejatraktivnějšími vlastnostmi byly rychlost, 
jednoduchost a mnoho různých rozšíření.

Zajímavým faktem je, že na Taiwanu má většina stoupenců Mozilly svůj
vlastní Blog. Jejich příspěvky o zkušenostech s Mozillou jsou pro
uživatele mnohem přitažlivější než obecné propagační články.

Překlad

Po vydání Firefoxu 0.7 jsem začal překládat slavný článek "Proč přejít", 
abych podpořil tento fantastický program. Jiný zapálený fanoušek byl už 
krok přede mnou, takže jsem mu pomohl s úpravami
a inzeroval výsledek na mém blogu, fóru <a href="mailto:Mozilla@Taiwan">Mozilla@Taiwan</a>, atd. Po návrzích 
členů komunity jsem přeložil produktovou stránku Firefoxu, plán vývoje, 
vývojářskou stránku, FAQ a další dokumenty. Od komunity jsem obdržel 
hodně návrhů a podpory, takže to šlo docela hladce. Na návrh Daniela 
Wanga přidalo fórum <a href="mailto:Mozilla@Taiwan">Mozilla@Taiwan
</a> novou diskusní skupinu pro 
dokumentaci a překlad, což pomohlo sjednotit termíny
a centralizovat samostatné dokumenty. Po dokumentech pro koncové 
uživatele jsem se zaměřil na dokumentaci k webovým standardům.
V čínských oblastech je nutné vylepšit pojetí "standardů". Věřím, že pro 
vyřešení stížností uživatelů typu "Tyto stránky nefungují
v Mozille" je zapotřebí "pořádné školení webových vývojářů".

Dokončený překlad zařadíme na fórum <a href="mailto:Mozilla@Taiwan">Mozilla@Taiwan
</a> nebo Wiki ke 
komentářům, úpravám a následnému zveřejnění. Wiki je podobné Bugzille, 
ale tak velkou databázi zatím nepotřebujeme.

Naše překlady nejsou rychlé. Mělo by se zapojit více lidí. Ovládáte-li
tradiční čínštinu a angličtinu, přidejte se k nám!

