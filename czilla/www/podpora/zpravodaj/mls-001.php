Supertip zpravodaje Mozilla Links: Zobrazování webové adresy
s neobvyklými znaky

Pokud prohlížíte stránky v jiném než anglickém jazyce, setkáte se
s webovými adresami s neobvyklými znaky. Mozilla standardně tyto znaky
"escapuje" a zobrazí je ve tvaru jako např. %e3%82%b3%e3. To ztěžuje
prohlížení, protože nevidíte, co přesně adresní řádek obsahuje. Tuto
vlastnost můžete vypnout:

- Zadejte "about:config" do adresního řádku
- Do filtrovacího pole vložte "network.standard-url.escape-utf8"
- Dvojitým klepnutím na předvolbu nastavte její hodnotu na "false"

