Založení Mozilla Europe
------------------------------
(Tristan Nitot, Mozilla Europe)

Založení sdružení Mozilla Europe je přirozeným pokračováním snah 
Evropské komunity Mozilly. Evropská komunita vznikla v roce 2000 během 
prvního Dne evropských vývojářů Mozilly, který uspořádali Axel Hecht 
(člen výboru Mozilla Europe) a SESA. Vývojářský den se držel tradice 
podobných akcí pořádaných společností Netscape v USA. Pro technickou 
stránku věci, jako např. webový prostor a poštovní konference na 
organizaci setkání v Evropě a také na archivování prezentací, byl 
vytvořen projekt eu.mozdev.org. Kvůli lokalizaci začal tyto snahy 
podporovat i Robert Kaiser z Rakouska.

Na Mozdev byl založen projekt MozFR na překlad dokumentace Mozilly 1.0 
do francouzštiny. Některým z nás se zdálo, že potřebujeme jít dále, ale 
nikdo z nás neměl čas ani možnosti na vedení takového projektu. V roce 
2003 se společnost AOL rozhodla přestat podporovat vývoj Mozilly, a ten 
tak přešel pod nově vzniklou organizaci Mozilla Foundation. Propuštěni 
byly i evropští zaměstnanci fy. Netscape pracující na Mozille a protože 
Mozilla Foundation byla schopná převzít správu pouze některých projektů, 
bylo jasné, že je zde potřeba také evropská účast. Založení Mozilla 
Europe na počátku roku 2004 byl další logický krok, který
řídili Tristan Nitot a Peter Van der Beken.

Kontaktovali jsme Mozilla Foundation abychom věděli, jak můžeme
spolupracovat. Ve spolupráci s právníky jsme připravili legální 
neziskovou organizaci a po dlouhém přemýšlení a administrativě bylo 
téměř vše hotovo. Prvním projektem nové komunity byl web 
<a href="http://www.mozilla-europe.org/">http://www.mozilla-europe.org/</a> Stránky jsou zatím dostupné ve
4 jazycích (angličtině, francouzštině, němčině a španělštině). Brzy se 
snad také objeví další jazykové verze stránek. Očekáváme zlepšení ve
2 oblastech, které jsou pro úspěch Mozilly v Evropě velmi důležité: 
lokalizace (koordinovaná pomoc s tou neuvěřitelnou prací, kterou 
odvádějí lokální komunity) a zavedení Mozilly do firemní sféry.

Členské poplatky, dary a peníze pro Mozilla Europe budou mimo jiné 
použity na nákup serveru, hostování a připojení. Pro tyto účely jsme 
zřídili webovou stránku, na které můžete podpořit evropskou iniciativu 
Mozilly: <a href="http://www.mozilla-europe.org/en/contribute/">http://www.mozilla-europe.org/en/contribute/</a>

