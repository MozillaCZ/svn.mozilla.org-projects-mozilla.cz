mozilla.party.jp 5.0
--------------------

Mozilla-gumi, jedna z největších uživatelských skupin v Japonsku,
organizuje 18. dubna každoroční konferenci. Téma pro tento rok je Open
Source Desktop včetně OpenOffice.org a KNOPPIXu. Organizátor pozval také 
předního výzkumníka hnutí open source, Dr. Chihira Suematsu, zástupce 
profesora na Kyotské univerzitě.

Diskutovanými záležitostmi jsou: výzva pro open source projekty
k upoutání většího množství uživatelů, jednotlivé/společné problémy
v open source projektech a podpora aktivnější výměny mezi open source 
komunitami.

Hlavní konference se skládá z:

* konference Mozilla Foundation: video (nebo aktuální přednáška hosta)
člena Mozilla Foundation

* hlavní konference: diskuse s Mozilla-gumi, japonskou skupinou 
uživatelů OpenOffice.org a KNOPPIX Japan.

* konference Dr. Suematsu: dopady hnutí open source na komerční podnikání.

* minikonference: vývoj v XUL a jiné aktivity japonské komunity jako 
např. mozillaZine.jp.

Organizátor chystá také soutěže ve tvorbě XUL aplikací a Mozilla-gumi
bannerů. Konference bude vysílána přes Internet. Brzy budou oznámeny 
další podrobnosti.

Více informací o mozilla.party.jp 5.0:
<a href="http://www.mozilla.gr.jp/party5.0/index_e.html">http://www.mozilla.gr.jp/party5.0/index_e.html</a>

