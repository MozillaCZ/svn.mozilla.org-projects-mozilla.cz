Vydání Mozilly 1.7 <br>
------------------
<br>
Nová stabilní verze Mozilly vyšla 17. června.
Obsahuje novou sadu ikon pro asociované soubory ve Windows,
nástroj pro oznamování chyb a podporu pro upload
na FTP. Poštovní klient přináší vylepšený
synchronizační nástroj Palm Sync, podporu více
identit na stejném e-mailovém účtu, při psaní
mailu můžete nyní pro prohledávání v
seznamu adresátů (To/Cc/Bcc) použít šipky nahoru a
dolů. Chatzilla podporuje změnu velikosti písma pomocí
klávesových zkratek (&#8222;Ctrl + +&#8220; a &#8222;Ctrl + -&#8220;) a
příkaz "/ignore". Mozilla 1.7 je o 7 %
rychlejší při spuštění než 1.6, natahuje stránky
o 9 % rychleji a je o 5 % menší. <br>
