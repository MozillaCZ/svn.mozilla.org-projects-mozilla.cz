 face="moz-fixed">),
webových stránkách patřících
Microsoftu, vychvalující Firefox nad Internet
Explorerem, znamená skutečně &#8222;na celém
webu&#8220;.<br>
<br>
Novinkou této verze je Správce
rozšíření, jednoduché rozhraní pro
správu aktuálně nainstalovaných rozšíření.
Každé z nich můžete konfigurovat (klepněte na zaškrtávací
pole Možnosti), navštívit jeho domovskou stránku, či
zjistit dostupnost aktualizací na nových stránkách
Mozilla Update, nebo si na tomtéž místě vyhledat nová
rozšíření.<br>
<br>
