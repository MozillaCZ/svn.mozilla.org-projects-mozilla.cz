velice podobný, můžete si v něm vybrat a prohlédnout
nainstalované motivy vzhledu, přepínat mezi nimi. Pro
stažení nových motivů pak navštívit stránky
Mozilla Update (</font></font><font style="font-size: 9pt;" size="2"><font
