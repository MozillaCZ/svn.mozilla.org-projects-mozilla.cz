Tip: Spusťte Firefox v safe módu
<br>
(přispěl Percy Cabello) <br>
<br>
Při restartu Firefoxu pro
dokončení instalace rozšíření si někteří
z vás možná všimli okna se zprávou &#8222;Dokončuji
instalaci rozšíření&#8220;, které nikdy nezmizí.
<br>
<br>
Je pravděpodobné, že to je způsobeno chybou v
instalaci rozšíření (podobně jako když instalujete
stejné rozšíření nebo motiv vzhledu dvakrát
po sobě). Zatímco se tento problém řeší
(</font></font><a
 href="https://bugzilla.mozilla.org/show_bug.cgi?id=246687"><font
 style="font-size: 9pt;" size="2"><font face="moz-fixed">https://bugzilla.mozilla.org/show_bug.cgi?id=246687</font></font></a><font
 style="font-size: 9pt;" size="2"><font face="moz-fixed">)
můžete tento a další obdobné problémy spojené
se špatným chováním rozšíření
obejít spuštěním Firefoxu v safe módu
(bezpečném režimu). V tomto režimu se spustí Firefox
se všemi rozšířeními vypnutými. <br>
<br>
Jak
toho dosáhnout: ve Windows klikněte na tlačítko
"Start" a vyberte "Programy", "Mozilla
Firefox" a klikněte na "Mozilla Firefox (Safe Mode)".
<br>
<br>
Bezpečný režim můžete také spustit z
příkazového řádku. Přejděte do složky, kam
byl Firefox nainstalován a spusťte: <br>
<br>
                 
firefox.exe -safe-mode <br>
<br>
Jakmile se Firefox spustí,
můžete si zobrazit Správce rozšíření (nebo
Správce motivů vzhledu) a odinstalovat to rozšíření
(nebo motiv vzhledu), které podezíráte, že
způsobuje problém. Obvykle jím bývá to
posledně instalované. <br>
