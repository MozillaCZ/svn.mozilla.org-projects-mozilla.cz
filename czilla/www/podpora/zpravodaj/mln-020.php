nebude zřejmé, který z nich je &#8222;lepší&#8220;.
Ale zdá se, že na tom příliš nezáleží,
neboť Firefox 0.9 se již, podle kritiky na celém webu,
úspěšně osvědčil. Článek na serveru Slate
(</font></font><font style="font-size: 9pt;" size="2"><font
