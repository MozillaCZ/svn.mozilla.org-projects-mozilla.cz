Thunderbird 0.7 <br>
--------------------- <br>
Dne 16. června byl
vydán Thunderbird 0.7 obsahující menší
instalátor pro Windows, nového Správce rozšíření
a Správce motivů vzhledu, nástroj pro oznamování
chyb (Talkback), nové uživatelské rozhraní pro
