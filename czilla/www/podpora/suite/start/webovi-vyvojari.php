   <h1>Pro webové vývojáře</h1>
   <div id="mainContent">
   
     <p>
      Mozilla je zdaleka nejlepší platforma pro vývojáře. Umožňuje postavit web na
      <a href="http://www.bath.ac.uk/~py8ieh/internet/eviltests/">standardních prvcích</a>.
      Nástroje, které byly vytvořeny nad Mozillou ulehčují vývoj a odstraňování chyb
      ve stránkách, ať už v komplexních, nebo dynamicky generovaných.
     </p>

     <div class="addon">     
      <span class="addonTitle">Venkman - JavaScript Debugger</span>
      Venkman je debugovací prostředí pro prohlížeče postavené na Mozille.
      Umožňuje ladění kódu JavaScriptu použitého ve stránkách, stejně jako
      jste zvyklí ladit C++ pod svým systémem. Umožňuje používat breakpointy,
      prohlížet zásobník, proměnné a objekty a umožňuje spouštění libovolného
      JavaScriptového kódu.<br />
      <a href="http://www.mozilla.org/projects/venkman">Domovská stránka</a> |
      <a href="http://www.hacksrus.com/~ginda/venkman/">Instalovat</a> |
      <a href="http://www.mozilla.org/projects/venkman/venkman-walkthrough.html">Tutorial</a>
     </div>

     <div class="addon">     
      <span class="addonTitle">DOM Inspector</span>
      DOM Inspector je unikátní nástroj, který může být použit k prohlížení
      a editování živého DOM jakéhokoli webového dokumentu. Dovoluje vám
      vidět stránku, přesně tak jak na ni nahlíží prohlížeč.
      Hierarchii DOM můžete procházet použitím dvou oken, která umožňují
      mnoho variací náhledů do dokumentu a jeho uzlů.<br />
      <a href="http://www.mozilla.org/projects/inspector">Domovská stránka</a> |
      <a href="http://grayrest.com/moz/evangelism/tutorials/dominspectortutorial.shtml">Tutorial</a>
     </div>

     <div class="addon">     
      <span class="addonTitle">JavaScript Console</span>
      Naše konzole pro JavaScript dává detailní přehled o jakýchkoli
      chybách nalezených ve skriptech na stránkách společně s hypertextovými odkazy
      na zdrojové kódy.
      Můžete také spustit libovolný vepsaný JavaScript kód, kontrolovat
      varování a zprávy <tt>&lt;xsl:&gt;</tt>.
     </div>

     <p>
      Předchozí tři nástroje jsou dostupné z menu
      <code>Nástroje | Web Development</code>
      pokud jste zvolili plnou instalaci Mozilly.
     </p>

     <div class="addon">     
      <span class="addonTitle">Standardní webové postranní lišty</span>
      Postranní lišty obsahují odkazy na standardy
      pro téměř všechny webové technologie, které si
      dovedete představit - CSS až XSLT, včetně HTML 4.01, XHTML,
      a DOM 2.
      Tyto malé lišty přinášejí specifikace na dosah ruky, umožňujíce
      zkontrolovat syntaxi a status jakéhokoli elementu ve vteřině.<br />
      <a href="http://zvon.org/Output/bar_list.html">Domovská stránka</a>
     </div>

     <div class="addon">     
      <span class="addonTitle">Generátor zdrojového kódu</span>
      Chtěli jste někdy vidět zdrojové kódy stránky po vykonání
      skriptů? Vyzkoušejte generátor zdrojového kódu, který generuje
      HTML zdrojový kód z nahrané DOM stránky.<br />
      <a href="http://mozilla-evangelism.bclary.com/sidebars/">Domovská stránka</a>
     </div>

     <p>A zde je
     <a href="http://www.mozilla.org/catalog/web-developer/">dlouhý seznam</a>
     dokumentace pro vývojáře pracující s Mozillou a W3C technologiemi.</p>
