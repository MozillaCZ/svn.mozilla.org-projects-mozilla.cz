	<div>Byla vydána nová verze webového prohlížeče <a href="http://flock.com/" title="Flock 2.0" hreflang="en">Flock</a>, který je zaměřen na sociální sítě jako je <a href="http://www.youtube.com/" title="YouTube" hreflang="en">YouTube</a>, <a href="http://gmail.com" hreflang="en">Gmail</a>, <a href="http://www.flickr.com/" title="Flickr">Flickr</a> a podobně. Flock 2.0 je postaven na <a href="http://www.czilla.cz/produkty/firefox/" title="Firefox 3.0.3">Firefoxu 3.0.3</a>, čímž zdědil <a href="http://www.mozilla-europe.org/cs/firefox/3.0.3/releasenotes/" title="Poznámky k vydání">spoustu novinek</a>. Mezi další zlepšení patří integrace služby <a href="http://www.myspace.com/" title="MySpace" hreflang="en">MySpace</a>, <a href="http://revver.com/" title="Revver" hreflang="en">Revver</a> a detekce <a href="http://en.wikipedia.org/wiki/Media_RSS" hreflang="en" title="Wikipedia" hreflang="en">Media RSS</a>. Dostupný je pro Windows, Linux a Mac OS X. Flock prozatím není dostupný v češtině, můžete si však počkat na <a href="http://flock.com/versions" title="verze Flocku">slovenskou</a> verzi, které by měla vyjít o něco později.</div>
