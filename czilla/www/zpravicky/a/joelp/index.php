	<h3><a href="/zpravicky/mozilla-firefox-3-5/">Mozilla Firefox 3.5</a></h3>

	<div>Po <a href="/zpravicky/mozilla-firefox-3-0/" title="CZilla: Mozilla Firefox 3.0">více než roce</a> vývoje vydává <a href="http://www.mozilla.com/" hreflang="en" title="Mozilla Corporation">Mozilla Corporation</a> finální verzi prohlížeče <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Mozilla Firefox 3.5</a>. Hlavními novinkami je dramatické urychlení zpracování JavaScriptu a vykreslování stránek, lepší <a href="http://dotnetperls.com/chrome-memory" hreflang="en" title="Dot Net Perls: Chrome and Firefox 3.5 Memory Usage">správa paměti</a> a podpora režimu anonymního prohlížení. Uživatelům nová verze přináší vyšší stabilitu a bezpečnost &ndash; například striktnější kontrolou certifikátů či rozšířením možností vymazání důvěrných dat. Vývojáře pak potěší podpora značek &lt;video&gt; a &lt;audio&gt; z <abbr title="HyperText Markup Language" lang="en">HTML</abbr>5, <a href="http://www.mozilla.com/firefox/geolocation">prohlížení se znalostí polohy</a>, offline application storage a <a href="https://developer.mozilla.org/En/Firefox_3.5_for_developers" hreflang="en" title="Firefox 3.5 for developers">mnoho dalších novinek</a> vykreslovacího jádra Gecko 1.9.1.
<br />Firefox 3.5, který je nazýván nejrychlejším Firefoxem všech dob, je k dispozici ve více než 70 jazycích ve verzích pro Windows, Linux i Mac. Stahovat můžete ze serveru <a href="/produkty/firefox/stahnout.html" title="Stáhnout Firefox">CZilla.cz</a>.</div>
