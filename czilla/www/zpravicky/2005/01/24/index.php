	<h3><a href="/zpravicky/ben-goodger-prechazi-z-mozilla-foundation-do-googlu/">Ben Goodger přechází z Mozilla Foundation do Googlu</a></h3>

	<div><a href="http://www.bengoodger.com/" hreflang="en">Ben Goodger</a>, vedoucí vývojář <a href="/produkty/firefox/">Firefoxu</a>, dnes na <a href="http://weblogs.mozillazine.org/ben/" title="Inside Firefox - The Inside Track on Firefox Development" hreflang="en">svém weblogu</a> oznámil <a href="http://weblogs.mozillazine.org/ben/archives/007366.html" hreflang="en" title="Changes">velmi překvapující zprávu o změně svého zaměstnavatele</a> - místo <a href="http://www.mozilla.org/" hreflang="en">Mozilla Foundation</a> nyní pracuje pro <a href="http://www.google.com/" hreflang="en">Google</a>. Podle svých slov se tam bude nadále plně věnovat své dosavadní práci, tedy vývoji Firefoxu, a z vnějšího pohledu se téměř nic nezmění. O tom, co tímto krokem Google sleduje, a co to přinese do budoucna, se dá nyní jen spekulovat.</div>
