	<h3><a href="/zpravicky/podpora-noveho-standardu-pro-usb-flash-disky/">Podpora nového standardu pro USB flash disky</a></h3>

	<div><a href="http://www.mozilla.org/" hreflang="en" title="The Mozilla Foundation">Mozilla.org</a> oznámila <a href="http://www.linuxelectrons.com/article.php/20050109153139434" title="LinuxElectrons&#8482; - Mozilla Firefox and Thunderbird to Support New Open Standard Platform for USB Drives" hreflang="en">podporu nového standardu U3</a>, který řeší a vylepšuje správu dat a aplikací na USB flash discích. V budoucnu by měla podpora <a title="U3" hreflang="en" href="http://www.u3.com/">U3</a> zjednodušit spouštění <a href="/produkty/firefox/">Firefoxu</a> a <a href="/produkty/thunderbird/">Thunderbirdu</a> z těchto zařízení a rozšířit jejich možnosti. Oba produkty lze na USB discích používat už dnes, díky projektům <a href="http://portablefirefox.mozdev.org/" title="mozdev.org - PortableFirefox" hreflang="en">PortableFirefox</a> a <a href="http://portablethunderbird.mozdev.org/" title="mozdev.org - PortableThunderbird" hreflang="en">PortableThunderbird</a>.</div>
