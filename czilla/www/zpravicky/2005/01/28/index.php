<div class="news">
	<h3><a href="/zpravicky/podpora-xforms-1.0-v-mozille/">Podpora XForms 1.0 v Mozille</a></h3>

	<div>Mezi <a href="http://www.mozilla.org/newlayout/faq.html#Which open standards" hreflang="en">řadu webových standardů</a>, které <a href="http://www.mozilla.org/" title="The Mozilla Foundation" hreflang="en">Mozilla</a> podporuje, brzy přibude další: <a href="http://www.w3.org/TR/2003/REC-xforms-20031014/" hreflang="en">XForms 1.0</a>. Podle <a href="http://weblogs.mozillazine.org/doron/" hreflang="en" title="doron's blaahg">Dorona Rosenberga</a> se <a href="http://weblogs.mozillazine.org/doron/archives/007390.html" hreflang="en" title="XForms XPIs Coming Soon"> jeho implementace pomalu ale jistě chýlí k závěru</a>. Zajímavé je, že byl při ní použit nový <a href="http://www.croczilla.com/xtf" hreflang="en" title="eXtensible Tag Framework for Mozilla">XTF framework</a>, umožňující přidat do Mozilly schopnost zpracování nových značkovacích jazyků. Framework je velmi flexibilní a umí podporu nového jazyka doinstalovat jako obyčejné rozšíření - není tedy nutný žádný speciální build ani přidávání do normálních verzí Mozilly, jako doposud.</div>
