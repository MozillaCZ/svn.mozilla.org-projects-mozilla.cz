	<h3><a href="/zpravicky/mozilla-firefox-1.1-zmenen-na-verzi-1.5/">Mozilla Firefox 1.1 změněn na verzi 1.5</a></h3>

	<div><a href="http://www.mozilla.org/foundation/" title="O Mozilla Foundation" hreflang="en">Mozilla Foundation</a> dnes aktualizovala <a href="http://www.mozilla.org/projects/firefox/roadmap.html" title="Mozilla Firefox: Roadmap" hreflang="en">plán vývoje</a> (anglicky roadmap) <a href="/produkty/firefox/" title="Mozilla Firefox: Stránka o produktu">Mozilla Firefoxu</a>. Můžete se v něm například dočíst, že plánovaná verze Mozilla Firefox 1.1 byla změněna na verzi 1.5. Nejedná se tedy o žádné zrušení verze, jak bylo v některých médiích chybně interpretováno, ale o pouhou změnu verze. Připravovaný <a href="/produkty/firefox/" title="Mozilla Firefox: Stránka o produktu">Mozilla Firefox</a> (pod kódovým označením Deer Park) vyjde podle plánu na začátku podzimu tohoto roku jako verze 1.5. V průběhu srpna bude k dispozici beta verze, kterou bude následovat, podobně jako u <a href="/produkty/firefox/" title="Mozilla Firefox: Stránka o produktu">Mozilla Firefoxu</a> 1.0, minimálně dvě <abbr title="Release candidate" lang="en">RC</abbr> verze. Po vydání <a href="/produkty/firefox/" title="Mozilla Firefox: Stránka o produktu">Mozilla Firefoxu</a> 1.5 bude připravována verze 2.0 pod kódovým označením The Ocho (původně označované jako verze 1.5). Již jsou známy některé informace o kterých vám brzy napíšeme.</div>
