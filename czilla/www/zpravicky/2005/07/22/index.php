	<h3><a href="/zpravicky/co-nas-ceka-ve-firefoxu-2.0/">Co nás čeká ve Firefoxu 2.0?</a></h3>

	<div>Práce na Firefoxu 1.5 zdárně pokračují, ale vývojáři již plánují, co nového přidají do verze 2.0. Chystá se především <a href="http://weblogs.mozillazine.org/roc/archives/2005/07/gecko_19.html" title="Well, I'm Back: Gecko 1.9" hreflang="en">vylepšení renderovacího jádra Gecko</a>, kde dojde k přepracování grafického kódu. Nově bude nasazena knihovna <a href="http://cairographics.org/" hreflang="en">Cairo</a>, což umožní využít na všech podporovaných platformách hardwarovou akceleraci, antialiasing a bilineární zoomování obrázků. Vylepšena bude také podpora obrazovek s vysokým rozlišením a nejspíš dojde i na slavný test <a href="http://webstandards.org/act/acid2/" hreflang="en">Acid2</a>. Uživatele budou zajímat především <a href="/archiv-2005.html#news:a60e33d6d6815c6677042ccc0cd9327b" title="Plánuje se přepsání kódu pro práci se záložkami ve Firefoxu" hreflang="en">vylepšení záložek</a>, <a href="http://wiki.mozilla.org/Firefox:2.0_Configurable_Keybindings" hreflang="en">konfigurovatelné klávesové zkratky</a>, nové <a href="http://wiki.mozilla.org/Firefox:Info_Window" hreflang="en">informační okno o stránce</a> nebo <a href="http://wiki.mozilla.org/Firefox:1.5_Multi-locale_Installers" hreflang="en">instalátor, obsahující všechny jazykové verze</a>.</div>
