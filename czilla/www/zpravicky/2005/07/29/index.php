	<h3><a href="/zpravicky/internet-explorer-7.0-beta1/">Internet Explorer 7.0 Beta1</a></h3>

	<div>Společnost <a href="http://www.microsoft.com" title="Microsoft.com: Domovská stránka" hreflang="en">Microsoft</a> dnes uveřejnila dlouho očekávaný <a href="http://www.microsoft.com/windows/IE/ie7/default.mspx" title="Internet Explorer 7.0 Beta1: Poznámky k verzi" hreflang="en">Internet Explorer 7.0 Beta1</a>. Jedná se o první betaverzi nové verze webového prohlížeče, jehož předchozí verze prosluly zejména problémy s bezpečností a laxním přístupem k dodržováním webových standardů. Nová verze s sebou přináší podporu prohlížení v panelech, podporu průhledných <abbr title="Portable Network Graphics " lang="en">PNG</abbr> souborů a snahu o zvýšení bezpečnosti uživatele. Tato verze je k dispozici pouze pro uživatele Windows XP SP2 a je též součástí betaverze operačního systému Windows Vista (dříve Longhorn). Pro starší verze Windows nebude nová verze k dispozici, a proto uživatelům <a href="http://firefox.czilla.cz" title="Přejděte na Firefox">doporučujeme přejít na Firefox</a>, který je dostupný i pro starší verze Windows.<br /><br /> Z prvních ohlasů v diskusích je zřejmé, že v oblasti podpory webových standardů prozatím nedošlo prakticky k žádnému pokroku. Uživatelé si též dost často stěžují na nové nezvyklé ovládání a vysoké paměťové nároky. Beta1 není v současné době veřejně ke stažení a je dostupná pouze pro platící zákazníky služby MSDN. Rozsáhlejší <a href="http://www.flexbeta.net/main/articles.php?action=show&amp;id=102" title="FlexBeta: Inside Internet Explorer 7 Beta 1" hreflang="en">pohled na Internet Explorer Beta1</a> přináší server <a href="http://www.flexbeta.net/" title="FlexBeta: Domovská stránka" hreflang="en">FlexBeta</a>.</div>
