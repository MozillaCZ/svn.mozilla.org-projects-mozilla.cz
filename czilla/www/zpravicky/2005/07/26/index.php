	<h3><a href="/zpravicky/rozsireni-feedview-bude-integrovano-do-firefoxu-1.5/">Rozšíření Feedview bude integrováno do Firefoxu 1.5</a></h3>

	<div>Vývojáři <a href="/produkty/firefox/" title="Mozilla Firefox: Stránka o produktu">Firefoxu</a> oznámili, že do verze 1.5 bude integrováno rozšíření <a href="http://www.epigoon.com/?page=feedview" title="Feedview: Stránka o rozšíření" hreflang="en">Feedview</a>. Toto rozšíření umožňuje lepší formátování <abbr title="Rich Site Summary" lang="en">RSS</abbr> dokumentů a nahrazuje tak nepříliš vzhledné zobrazení, které získáte zobrazením <abbr title="Rich Site Summary" lang="en">RSS</abbr> dokumentu ve <a href="/produkty/firefox/" title="Mozilla Firefox: Stránka o produktu">Firefoxu</a> 1.0.x. Nová fukce by se měla objevit již ve Firefoxu 1.5 Beta a pro fanoušky starého stylu zobrazení bude k dispozici předvolba na přepnutí vzhledu do starého formátu. Pokud si chcete novinky vyzkoušet již nyní, <a href="http://www.epigoon.com/?page=feedview" title="Feedview: Stránka o rozšíření" hreflang="en">nainstalujte si Feedview</a> do Firefoxu 1.0.x.</div>
