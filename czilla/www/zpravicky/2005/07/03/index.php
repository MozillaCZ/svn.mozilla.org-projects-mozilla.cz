	<div>Robert Kaiser včera <a href="http://groups-beta.google.com/group/netscape.public.mozilla.seamonkey/browse_thread/thread/01ecea589762f45d/2fae2b296dcd6806#2fae2b296dcd6806" title="SeaMonkey has landed!" hreflang="en">oznámil</a> (<a href="http://www.mozilla.org/projects/seamonkey/news.html" title="SeaMonkey Project Continues Internet Suite" hreflang="en">oficiální zpráva</a>) produkt <a href="http://www.mozilla.org/projects/seamonkey/" title="SeaMonkey: Domovská stránka" hreflang="en">SeaMonkey</a>. <a href="http://www.mozilla.org/projects/seamonkey/" title="SeaMonkey: Domovská stránka" hreflang="en">SeaMonkey</a> je produkt navazující na balík <a href="/produkty/suite/" title="Mozilla Suite: Stránka o produktu">Mozilla Suite</a>, který již není Mozilla Foundation dále vyvíjen a jsou vydávány pouze bezpečnostní aktualizace pro Mozilla Suite 1.7.x. <a href="http://www.mozilla.org/projects/seamonkey/" title="SeaMonkey: Domovská stránka" hreflang="en">SeaMonkey</a> pokračuje tam, kde <a href="/produkty/suite/" title="Mozilla Suite: Stránka o produktu">Mozilla Suite</a> skončila a jedná se tedy o plnohodnotného nástupce, který není vyvíjen Mozilla Foundation, ale komunitou. První alpha verze bude k dispozici ve stejný čas jako Deer Park Alpha 2 (vývojová verze Firefoxu 1.1) a vydání finální verze 1.0 bude ve stejnou dobu jako vydání Firefoxu 1.1. <a href="/czilla/" title="O CZille">CZilla</a> bude produkt plně lokalizovat a poskytovat k němu podporu v rámci našich stránek a <a href="http://forum.czilla.cz/" title="Fórum na podporu uživatelů aplikací Mozilla.org">fóra na podporu uživatelů</a>.</div>
