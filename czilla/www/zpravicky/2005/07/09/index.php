	<div>Apple v rámci svého <a href="http://www.apple.com/pr/library/2005/jun/06intel.html" title="Apple to Use Intel Microprocessors Beginning in 2006" hreflang="en">přechodu na platformu x86</a> v červnu <a href="http://weblogs.mozillazine.org/josh/archives/2005/06/wwdc_firefox_ca.html" title="Boom Swagger Boom: WWDC, Firefox, Camino, Intel Updates" hreflang="en">portoval Firefox na Intelovskou verzi systému Mac OS X</a>. Příslušné patche nyní zpracovává a začišťuje <a href="http://weblogs.mozillazine.org/josh/" title="Boom Swagger Boom" hreflang="en">Josh Aas</a>, kterému se už <a href="http://weblogs.mozillazine.org/josh/archives/2005/07/firefox_runs_on.html" title="Boom Swagger Boom: Firefox Runs on Intel Macs" hreflang="en">podařilo Firefox na x86 Macu rozběhnout</a> a <a href="http://weblogs.mozillazine.org/josh/archives/2005/07/porting_to_inte.html" title="Boom Swagger Boom: Porting to Intel Macs" hreflang="en">napsal k tomu i pár komentářů</a>. Pokud nějakou šílenou náhodou máte u sebe x86 Mac, nebo si novinku čtete v době, kdy už budou tyto počítače rozšířené, můžete si vyzkoušet <a href="http://weblogs.mozillazine.org/josh/archives/2005/07/intel_mac_build.html" title="Boom Swagger Boom: Firefox Build for x86 Macs and Firefox with Cocoa Widgets" hreflang="en">testovací buildy</a>.</div>
