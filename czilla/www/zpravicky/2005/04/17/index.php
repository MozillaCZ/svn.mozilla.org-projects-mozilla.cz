	<h3><a href="/zpravicky/karetni-hry-hide-searchbar-a-sessionsaver-v-cestine/">Karetní hry, Hide Searchbar a SessionSaver v češtině</a></h3>

	<div>Po delší době opět přinášíme várku <a href="/doplnky/rozsireni/">lokalizovaných rozšíření</a>. První z nich jsou <a href="/doplnky/rozsireni/card-games/">Card Games</a>, soubor karetních her pro Mozillu a Firefox. Další je <a href="/doplnky/rozsireni/hide-searchbar/">Hide Searchbar</a>, které umožňuje pomocí klávesové zkratky <kbd>Ctrl</kbd> + <kbd>Shift</kbd> + <kbd>S</kbd> skrýt či zobrazit vyhledávací lištu. Poslední dnešní rozšíření je <a href="/doplnky/rozsireni/sessionsaver2/">SessionSaver</a>, který umí po ukončení aplikace znovu otevřít panely, které jste si prohlíželi před jejím zavřením. Za lokalizace děkujeme Davidu Pěgřímkovi a přezdívce Georgo10 a na závěr podotýkáme, že tohle je pouze začátek nové série lokalizovaných rozšíření. Těšte se s námi!</div>
