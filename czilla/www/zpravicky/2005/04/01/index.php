<div class="news">
	<h3><a href="/zpravicky/rozsireni-vylepsujici-blokovani-vyskakovacich-oken/">Rozšíření vylepšující blokování vyskakovacích oken</a></h3>

	<div><a href="http://www.mozilla.org/">Mozilla Foundation</a> testuje úpravu blokování vyskakovacích oken, která narozdíl od dřívějška zabraňuje i otvírání těchto oken pomocí pluginů. Tato metoda začala být v poslední době totiž na webu často využívána a standardně nastavené blokování si s ní neumělo poradit. Úprava blokování je dostupná k testování v podobě rozšíření trefně nazvaného <a href="http://mozilla.osuosl.org/pub/mozilla.org/firefox/nightly/experimental/popupsdie/popupsdie.xpi">Popups Must Die!</a> (XPI, 4&nbsp;kB) a podrobnosti o tom, jak funguje, se <a href="http://www.mozillazine.org/talkback.html?article=6321" title="Improved Popup Blocker Available for Testing">dočtete na MozillaZine</a>.</div>
