	<h3><a href="/zpravicky/podpora-svg-ve-firefoxu/">Podpora SVG ve Firefoxu</a></h3>

	<div>Noční sestavení <a href="/produkty/firefox/">Firefoxu</a> nyní <a href="http://weblogs.mozillazine.org/chase/archives/2005/04/svg_here_we_com.html" title="chase's blog - SVG here we come" hreflang="en">obsahují podporu <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr></a>. S formátem <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr> umí Mozilla pracovat už dlouho, ale dosud bylo nutné zkompilovat si speciální build. Pokud vše půjde dobře, bude podpora <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr> standardní součástí Firefoxu 1.1. Mimochodem, se <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr> si <a href="http://weblogs.mozillazine.org/tor/archives/2005/04/svg_on_minimo.html" title="Mozilla SVG Update: SVG on Minimo" hreflang="en">poradí dokonce i aktuální buildy Minima</a>. Detaily <a href="http://www.mozillazine.org/talkback.html?article=6539" title="SVG Included in Mozilla Firefox Nightly Builds" hreflang="en">najdete na MozillaZine</a>, problematice <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr> v Mozille se také <a href="http://weblogs.mozillazine.org/tor/" title="Mozilla SVG Update" hreflang="en">věnuje samostatný blog</a>.</div>
