	<div><a href="http://weblogs.mozillazine.org/hyatt/" title="Surfin' Safari" hreflang="en">Dave Hyatt</a> úspěšně upravil webový prohlížeč Safari a jeho vývojová verze se tak <a href="http://weblogs.mozillazine.org/hyatt/archives/2005_04.html#008042" title="Safari Passes the Acid2 Test (Updated)" hreflang="en">stala prvním prohlížečem, který plně zvládnul Acid 2 test</a>. Jak jsou na tom Mozilla a Opera? Vývojáři Mozilly momentálně nechtějí riskovat zanesení chyb do blížících se verzí 1.1 svých produktů a <a href="http://weblogs.mozillazine.org/roc/archives/2005/04/acid2.html" title="Well, I'm Back: Acid2:" hreflang="en">podívají se na test později</a>. U Opera Software v době vzniku testu dokončovali verzi 8.0 a <a href="http://my.opera.com/Junyor/journal/49" title="Opera 8.0 and the Acid2 test" hreflang="en">konkrétní plán ohledně testu do budoucna zatím nemají</a>, nicméně oceňují, že opravy chyb v testu přispějí k interoperatibilitě na webu.</div>
