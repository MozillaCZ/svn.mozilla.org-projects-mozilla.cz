	<h3><a href="/zpravicky/wxjs-port-wxwindows-do-javascriptu/">wxJS - port wxWindows do JavaScriptu</a></h3>

	<div>Že prý <a href="http://www.mozilla.org/js/" hreflang="en">JavaScript</a> a <a href="http://www.wxwindows.org/" hreflang="en">wxWindows</a> nejdou dohromady? To možná platilo dřív, nyní se objevil zajímavý projekt <a href="http://wxjs.sourceforge.net/" hreflang="en">wxJS</a>, který se inspiroval <a href="http://www.wxpython.org/" hreflang="en">wxPythonem</a>, <a href="http://wxbasic.sourceforge.net/" hreflang="en">wxBasicem</a> a <a href="http://wxperl.sourceforge.net/" hreflang="en">wxPerlem</a> a portuje rozhraní knihovny wxWindows do JavaScriptu. Jako engine JavaScriptu využívá <a href="http://www.mozilla.org/js/spidermonkey/" title="SpiderMonkey (JavaScript-C) Engine" hreflang="en">SpiderMonkey</a> od <a href="http://www.mozilla.org/" hreflang="en">Mozilly</a>. Projekt je zatím ve verzi 0.62, ale zřejmě se aktivně vyvíjí.</div>
