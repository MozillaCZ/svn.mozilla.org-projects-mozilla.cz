	<h3><a href="/zpravicky/novinky-u-konkurence-opera-8.0-ie-7-safari-a-acid2/">Novinky u konkurence - Opera 8.0, IE 7, Safari a Acid2</a></h3>

	<div>U příležitosti <a href="http://www.opera.com/pressreleases/en/2005/04/19/" title="Speed, Security and Simplicity: Opera 8 Web Browser Released Today" hreflang="en">vydání nové Opery 8.0</a> prohlásil šéf <a href="http://www.opera.com/" hreflang="en">Opera Software</a>, Jon S. von Tetzchne, že pokud si ji stáhne za první 4 dny více než milion lidí, <a href="http://www.opera.com/pressreleases/en/2005/04/21/" title="The one million download challenge of Opera 8" hreflang="en">přeplave z Norska do USA s jedinou zastávkou na Islandu</a>. Během prvních 48 hodin se počet stažení vyšplhal na 600 000, takže šance, že bude muset závazek dodržet, není malá. U <a href="http://www.microsoft.com/" hreflang="en">Microsoftu</a> zatím pilně pracují na novém <abbr title="Internet Explorer" lang="en">IE</abbr> 7 a bylo oznámeno, že (konečně!) <a href="http://blogs.msdn.com/ie/archive/2005/04/22/410963.aspx" title="IEBlog : IE7 beta 1 &#8211; A few details&#8230;" hreflang="en">bude podporovat alfa kanál u PNG a opravovat některé chyby v CSS</a>. Takové samozřejmé věci všechny moderní prohlížeče už mnoho let umí, a tak si například vývojáři <a href="http://www.apple.com/macosx/features/safari/" hreflang="en">Safari</a> mohou klást náročnější cíle, jako je například zvládnutí <a href="http://webstandards.org/act/acid2/" hreflang="en">Acid2 testu</a>. Jak píše <a href="http://weblogs.mozillazine.org/hyatt/" title="Surfin' Safari" hreflang="en">Dave Hyatt</a>, až na jeden řádek už se jim test <a href="http://weblogs.mozillazine.org/hyatt/archives/2005_04.html#008011" title="Acid2: Version 1.1 Posted" hreflang="en">vykresluje perfektně</a>. Vývojáři Mozilly si na test zatím <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=acid2" title="Bug 289480 - Mozilla doesn't pass the acid2 (acid 2) test" hreflang="en">založili bug</a>.</div>
