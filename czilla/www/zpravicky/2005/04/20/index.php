	<h3><a href="/zpravicky/dalsi-zadost-o-komentare-ke-specifikaci-web-forms-2.0/">Další žádost o komentáře ke specifikaci Web Forms 2.0</a></h3>

	<div>Pracovní skupina <a href="http://whatwg.org/" hreflang="en"><abbr title="Web Hypertext Application Technology Working Group" lang="en">WHATWG</abbr></a> předložila <a href="http://www.whatwg.org/specs/web-forms/2005-04-11-call-for-comments/" title="Web Forms 2.0 Call for Comments &#8212; 11 April 2005" hreflang="en">čtvrtý návrh specifikace Web Forms 2.0</a> ke komentářům. Původně měly být výzvy ke komentářům jen tři, ale specifikace byla v reakci na připomínky od poslední verze na mnoha místech upravena, a tak byla vydána její další verze. Po zapracování případných připomínek bude pravděpodobně dalším krokem výzva k implementaci této specifikace, aby se ověřila její praktická použitelnost. Bližší informace o specifikaci můžete kromě <a title="Web Forms 2.0 Call for Comments &#8212; 11 April 2005" href="http://www.whatwg.org/specs/web-forms/2005-04-11-call-for-comments/" hreflang="en">specifikace samotné</a> získat z naší <a href="/archiv.html#news:d8b4f991a6da84e1cf95588f5bae7cc4" title="Druhý návrh ke komentářům Web Forms 2.0">dřívější zprávičky</a>.</div>
