	<h3><a href="/zpravicky/testovaci-sestaveni-firefoxu-obsahujici-spatial-navigation/">Testovací sestavení Firefoxu obsahující spatial navigation</a></h3>

	<div>Doug Turner připravil <a href="http://www.meer.net/~dougt/snav/m3/" hreflang="en">testovací sestavení Firefoxu</a>, které podporují tzv. <a href="http://www.mozilla.org/access/keyboard/snav/" title="Spatial Navigation in Mozilla" hreflang="en">spatial navigation</a>. Tato funkce umožňuje snazší navigaci po stránce prostřednictvím klávesnice - mezi odkazy lze přecházet současným stiskem <kbd>Shift</kbd>, <kbd>Alt</kbd> a šipky, určující směr následujícího odkazu. Funkce je určena především pro projekt <a href="http://www.mozilla.org/projects/minimo/" title="Minimo Project" hreflang="en">Minimo</a>, ale je možné, že se objeví i ve standardních verzích <a href="/produkty/firefox/">Firefoxu</a>. Analogickou funkci již nabízí prohlížeč <a href="http://www.opera.com/" hreflang="en">Opera</a>.</div>
