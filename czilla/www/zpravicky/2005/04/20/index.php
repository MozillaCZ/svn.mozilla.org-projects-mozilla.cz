	<h3><a href="/zpravicky/v-nocnich-sestavenich-najdete-podporu-html-canvasu/">V nočních sestaveních najdete podporu HTML canvasu</a></h3>

	<div>Do kódu Mozilly byla <a href="http://www.mozillazine.org/talkback.html?article=6461" title="Mozilla Gains Canvas Element Support" hreflang="en">integrována podpora <abbr title="HyperText Markup Language" lang="en">HTML</abbr> canvasu</a>, což je objekt, umožňující vykreslování 2D grafiky přímo do <abbr title="HyperText Markup Language" lang="en">HTML</abbr> stránky. Přibližuje tím značně možnosti webových aplikací aplikacím desktopovým. S canvasem <a href="http://weblogs.mozillazine.org/hyatt/archives/2004_07.html#005913" title="Introducing the Canvas" hreflang="en">přišla původně firma Apple</a> ve svém <a href="http://www.apple.com/macosx/features/dashboard/" hreflang="en">Dashboardu</a> a je zahrnut ve vznikající specifikaci <a href="http://www.whatwg.org/specs/web-apps/current-work/#canvas">Web Applications 1.0</a>, kterou připravuje <a href="http://www.whatwg.org/" title="Web Hypertext Application Technology Working Group" hreflang="en"><abbr title="Web Hypertext Application Technology Working Group" lang="en">WHATWG</abbr></a>. Detaily <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=288714" title="Bug 288714 - HTML canvas impl" hreflang="en">najdete v bugu</a> a u <a href="http://pavlov.net/blog/" title="Evil Thoughts" hreflang="en">Steva Parmentera</a> se můžete podívat <a href="http://pavlov.net/blog/archives/2005/04/canvas_and_xul.html" title="Evil Thoughts: Canvas and XUL" hreflang="en">na malou ukázku, co canvas dovede</a>.</div>
