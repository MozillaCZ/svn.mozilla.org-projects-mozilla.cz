	<h3><a href="/zpravicky/docasny-mirror-netscape-devedge/">Dočasný mirror Netscape DevEdge</a></h3>

	<div><a href="http://www.mozilla.org" hreflang="en">Mozilla Foundation</a> zprovoznila <a href="http://devedge-temp.mozilla.org/index_en.html" hreflang="en">dočasný mirror Netscape DevEgde</a>, což je rozsáhlý zdroj článků a informací pro webové vývojáře. Provoz webu Netscape DevEdge byl společností AOL ukončen na konci roku 2004, ale Mozilla Foundation se s AOL <a href="/archiv-2005.html#news:dacd1a39821ac5b55497eff94f4a9942" title="Mozilla Foundation získala práva na materiály z Netscape DevEdge">dohodla na právech k obsahu</a> a možnosti hostingu na svých serverech. Aktuální plán je přesunout cenný obsah z DevEdge na připravované vývojářské centrum <a href="http://developer.mozilla.org" title="developer.mozilla.org - Developing on the Mozilla Platform" hreflang="en">developer.mozilla.org</a>, konkrétně do <a href="http://developer.mozilla.org/docs/Main_Page" hreflang="en">wiki</a>, které je ale ještě v plenkách.</div>
