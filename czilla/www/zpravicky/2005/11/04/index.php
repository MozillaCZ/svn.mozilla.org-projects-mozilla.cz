	<h3><a href="/zpravicky/sunbird-0.3a1/">Sunbird 0.3a1</a></h3>

	<div>Na <a href="http://weblogs.mozillazine.org/calendar/" hreflang="en" title="Calendar Weblog">Calendar Weblogu</a> bylo oznámeno <a href="http://weblogs.mozillazine.org/calendar/2005/11/releasing_is_hard_1.html" hreflang="en" title="Calendar Weblog: Releasing is hard">vydání testovací verze Sunbird 0.3a1</a> (<a href="http://www.mozilla.org/projects/calendar/releases/sunbird_0.3alpha1.html" hreflang="en" title="Sunbird 0.3 alpha1 Release Notes">poznámky k vydání</a>). Verze pro Windows, Linux a Mac OS X je možno stáhnout z <a href="http://ftp.mozilla.org/pub/mozilla.org/calendar/sunbird/releases/0.3a1/" hreflang="en" title="Index of /pub/mozilla.org/calendar/sunbird/releases/0.3a1">FTP Mozilly</a>. Největší novinkou verze 0.3a1 je vylepšená vnitřní architektura, která umožňuje ukládat kalendářová data různými způsoby. Již nyní funguje podpora ICS, SQLite a rozepsána je podpora CalDAV.</div>
