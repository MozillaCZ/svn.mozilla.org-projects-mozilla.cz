	<h3><a href="/zpravicky/nova-strategie-platformy-mozilla/">Nová strategie platformy Mozilla</a></h3>

	<div><a href="http://cbeard.typepad.com/mozilla/" hreflang="en" title="cbeard&#039;s mozilla blog">Chris Beard</a> na svém blogu zveřejnil předběžnou <a href="http://cbeard.typepad.com/mozilla/2005/11/mozilla_product.html" hreflang="en" title="cbeard&#039;s mozilla blog: Mozilla Product Strategy Proposal (DRAFT)">představu o dalším vývoji platformy Mozilla a jejích produktů</a>. Nové verze Gecka by měly vycházet každých 12&ndash;15 měsíců, zatímco nové verze produktů budou vydávány v intervalu 6&ndash;9 měsíců. Mimo tento cyklus by měly být vydávány bezpečnostní aktualizace &ndash; cca každých 6&ndash;8 týdnů. Lepší představu asi dá <a href="http://cbeard.typepad.com/.shared/image.html?/photos/uncategorized/releaseroadmapdraftv1_2.png" hreflang="en">diagram vývoje Firefoxu</a>, ze kterého mj. plyne, že Firefox 2.0 bude založen na Gecku 1.8.1 a nikoliv na 1.9, jak bylo plánováno dříve. V širším kontextu se o nové strategii <a href="http://weblogs.mozillazine.org/roadmap/archives/009218.html" hreflang="en" title="Brendan&#039;s Roadmap Updates: New Roadmaps">rozepsal Brendan Eich</a>. Na <a href="http://wiki.mozilla.org/Main_Page" hreflang="en" title="Main Page - wiki.mozilla.org">Mozilla Wiki</a> lze najít <a href="http://www.mozilla.org/roadmap/gecko-1.9-roadmap.html" hreflang="en" title="DRAFT Gecko 1.9 roadmap">draft Gecko 1.9 roadmap</a> a <a href="http://wiki.mozilla.org/Firefox:2.0_Product_Planning" hreflang="en" title="Firefox:2.0 Product Planning - wiki.mozilla.org">přeběžný rozpis nových funkcí pro Firefox 2.0</a>.</div>
