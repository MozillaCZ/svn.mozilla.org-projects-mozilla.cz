	<h3><a href="/zpravicky/extend-firefox-soutez-o-nejlepsi-rozsireni/">Extend Firefox: Soutěž o nejlepší rozšíření</a></h3>

	<div><a href="http://www.mozilla.org/" hreflang="en" title="Mozilla - Home of the Firefox web browser and Thunderbird e-mail client">Mozilla Corporation</a> včera <a href="http://weblogs.mozillazine.org/asa/archives/2005/11/extend_firefox.html" hreflang="en" title="Asa Dotzler - Firefox and more: extend firefox">vyhlásila soutěž Extend Firefox o nejlepší rozšíření</a>. Soutěží se v několika kategoriích a do soutěže mohou autoři přihlásit jak nová rozšíření, tak aktualizované verze již existujících &ndash; až na jednu kategorii budou obě skupiny hodnoceny zvlášť. Ceny jsou lákavé: v kategoriích <em>nejlepší rozšíření</em> a <em>nejlepší využití novinek Firefoxu 1.5</em> bude vítěz odměněn <a href="http://www.alienware.com/product_detail_pages/Aurora_7500/aurora_7500_features.aspx" hreflang="en" title="Alienware: Aurora 7500 Desktop - Learn More!">Alienware Aurora 7500 Firefox Edition PC</a>, v jiných kategoriích lze vyhrát třeba <a href="http://www.apple.com/ipodnano/" hreflang="en" title="Apple - iPod nano">iPod nano</a>. Více informací o soutěži <a href="http://developer.mozilla.org/contests/extendfirefox/" hreflang="en" title="Extend Firefox">najdete na jejím webu</a>. <a href="http://www.squarefree.com/" hreflang="en" title="Jesse Ruderman">Jesse Ruderman</a> má <a href="http://www.squarefree.com/2005/11/03/extension-ideas/" hreflang="en" title="Jesse Ruderman &#187; Extension ideas">několik tipů, jaká rozšíření vytvořit</a>. Přihlásíte-li se do soutěže, budeme rádi, pokud kontaktujete <a href="/czilla/prispevatele.php#kdo-je-DM">Davida Majdu</a> &ndash; rádi se o vás dozvíme, pomůžeme s testováním a na zajímavé nápady upozorníme čtenáře.</div>
