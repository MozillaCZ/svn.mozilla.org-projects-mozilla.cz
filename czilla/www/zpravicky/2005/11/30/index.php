	<h3><a href="/zpravicky/dalsi-ukazky-vyuziti-svg-a-znacky-canvas-ve-firefoxu-1.5/">Další ukázky využití SVG a značky canvas ve Firefoxu 1.5</a></h3>

	<div>Po <a href="/archiv-2005.html#news:4ee0821dd37353bec7b1ed984e4f7cd5" title="Ukázka možností tagu canvas - 3D demo">Raycasteru</a> a <a href="/archiv-2005.html#news:4275fa1402b3700183de4cc2fe985f76" title="Canvascape - zárodek herního 3D enginu ve Firefoxu">Canvascape</a> se objevila další ukázka použítí nové značky <code>&lt;canvas&gt;</code> &ndash; <a href="http://caimansys.com/painter/index.html" hreflang="en" title="Canvas Painter">jednoduché kreslítko Canvas Painter</a>. Photoshop to sice není, ale obrázky se s ním kreslit dají &ndash; bohužel chybí možnost je uložit. Značku <code>&lt;canvas&gt;</code> využívá také rozšíření <a href="http://ted.mielczarek.org/code/mozilla/tabpreview/" title="Ted&#039;s Mozilla page - Tab Preview">Tab Preview</a>, zobrazující po najetí myší nad panel náhled stránky, která je v něm zobrazena. Možnosti <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr> ve spojení s JavaScriptem pro změnu demostruje jednoduchá výuková aplikace <a href="http://isthis4real.com/orbit.xml" hreflang="en" title="Can you put the moon into orbit?">How Did The Moon Get Into Orbit?</a>, ve které si můžete pohrát s gravitační simulací Země a Měsíce. <a href="http://blog.vlad1.com/" hreflang="en" title="Vladimir Vukićević">Vladimir Vukićević</a>, který pracuje na vývoji grafiky v <a href="http://www.mozilla.org/newlayout/" hreflang="en" title="Mozilla Layout Engine">Gecku</a>, je z nových grafických možností nadšený a <a href="http://blog.vlad1.com/archives/2005/11/30/81/" hreflang="en" title="Vladimir Vukićević &#187; Blog Archive &#187;">chce se nyní podívat na možnost implementace 3D <abbr title="Application Programming Interface" lang="en">API</abbr> do <code>&lt;canvas&gt;</code>u</a>. Pokud by se implementace opravdu uskutečnila, posunou se možnosti webových aplikací o další velký kus dopředu.</div>
