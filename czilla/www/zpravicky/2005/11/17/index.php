	<h3><a href="/zpravicky/w3c-opet-aktivni-na-poli-webu/">W3C opět aktivní na poli webu</a></h3>

	<div><a href="http://www.w3.org/" hreflang="en" title="World Wide Web Consortium">Konsorcium <abbr title="World Wide Web Consortium" lang="en">W3C</abbr></a> se po dlouhé době probudilo a založilo dvě nové pracovní skupiny (<a href="http://www.w3.org/2006/webapi/" hreflang="en" title="W3C Web APIs Working Group">Web APIs Working Group</a> a <a href="http://www.w3.org/2006/appformats/" hreflang="en" title="W3C Web Application Formats Working Group">Web Application Formats Working Group</a>), které by měly zdokumentovat a standardizovat <code><abbr title="Extensible Markup Language" lang="en">XML</abbr>HttpRequest</code>, objekt <code>Window</code>, drag &amp; drop a další součásti dnešních webových prohlížečů, na kterých jsou postaveny tzv. <em>rich client applications</em>. Mezi další úkoly patří standardizace <abbr title="Extensible Binding Language" lang="en">XBL</abbr>2 a deklarativního jazyka pro specifikaci uživatelského rozhraní (analogie <abbr title="XML User Interface Language" lang="en">XUL</abbr> či <abbr title="Extensible Application Markup Language" lang="en">XAML</abbr>). Iniciativa <abbr title="World Wide Web Consortium" lang="en">W3C</abbr> přichází rok a půl po založení non-<abbr title="World Wide Web Consortium" lang="en">W3C</abbr> skupiny <a href="http://whatwg.org/" hreflang="en" title="Web Hypertext Application Technology Working Group"><abbr title="Web Hypertext Application Technology Working Group" lang="en">WHATWG</abbr></a>, jejíž cíle jsou obdobné. <abbr title="Web Hypertext Application Technology Working Group" lang="en">WHATWG</abbr> v současnosti finišuje specifikaci <a href="http://www.whatwg.org/specs/web-forms/2005-04-11-call-for-comments/" hreflang="en" title="Web Forms 2.0">Web Forms 2</a>, rozšiřující především možnosti klasických <abbr title="HyperText Markup Language" lang="en">HTML</abbr> formulářů, a připravuje obsáhlejší specifikaci <a href="http://www.whatwg.org/specs/web-apps/current-work/" hreflang="en" title="Web Applications 1.0">Web Applications 1.0</a>. Názory na nynější aktivitu <abbr title="World Wide Web Consortium" lang="en">W3C</abbr> jsou spíše rozporuplné &ndash; viz například <a href="http://glazman.org/weblog/dotclear/index.php?2005/11/17/1384-w3c-catches-up-with-xul-and-xaml-finally" hreflang="en" title="W3C catches up with XUL and XAML, finally - Glazblog">komentář Daniela Glazmana</a>.</div>
