	<div>Neuběhl ani týden a už vám přinášíme další nová rozšíření z dílny našich lokalizátorů. První se jmenuje <a href="/doplnky/rozsireni/image-zoom/">Image Zoom</a> a umožňuje vám provést prakticky libovolnou změnu rozměrů obrázku na webové stránce. <a href="/doplnky/rozsireni/linkification/">Linkification</a> umí v prostém textu vyhledat všechna <abbr title="Uniform Resource Locator" lang="en">URL</abbr> a převést je na běžné odkazy, na které můžete klepnout. <a href="/doplnky/rozsireni/minimeter/">MiniMeter</a> jednoduše zobrazuje statistiky přenesených dat u vašeho poskytovatele internetového připojení a obsahuje také modul pro v Česku oblíbený Karneval. Na závěr přinášíme ještě rozšíření <a href="/doplnky/rozsireni/reminderfox/">ReminderFox</a>, které umožňuje nastavit si do vaší aplikace uložit různé události a nechat se na ně upozornit. Za dnešní lokalizace děkujeme Pavlu Francovi, Karlu Ulmanovi a The Zero a upozorňujeme, že jsou již všechny kompatibilní s netrpělivě očekávaným Firefoxem 1.5.</div>
