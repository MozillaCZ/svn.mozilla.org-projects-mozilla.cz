	<h3><a href="/zpravicky/firefox-25-milionu-stazenych-kopii/">Firefox: 25 milionů stažených kopií</a></h3>

	<div><a href="http://www.mozilla.org/" hreflang="en">Mozilla Foundation</a> oznámila, že <a href="http://www.mozilla.org/press/mozilla-2005-02-16.html" title="MOZILLA FOUNDATION ANNOUNCES 25 MILLION DOWNLOADS OF FIREFOX BROWSER" hreflang="en">počet stažených kopií Firefoxu 1.0 překročil hranici 25 milionů za 99 dní po vydání</a>. <a href="http://weblogs.mozillazine.org/asa/" title="adot's notblog*" hreflang="en">Asa Dotzler</a>, vedoucí testování produktů projektu Mozilla, zveřejnil <a href="http://weblogs.mozillazine.org/asa/archives/007574.html" title="firefox 25,000,000" hreflang="en">grafy o vývoji stahování</a>, podle nichž je každý den staženo dalších 200 tisíc kopií. Dodává, že Firefox je produkt se slibnou budoucností. Tomu napomáhá i fakt, že byl přeložen již do 28 jazyků, takže je dostupný uživatelům z celého světa.</div>
