	<h3><a href="/zpravicky/informace-o-9-opravenych-bezpecnostnich-problemech-v-produktech-mozilla.org/">Informace o 9 opravených bezpečnostních problémech v produktech Mozilla.org</a></h3>

	<div><a href="http://www.mozilla.org/" title="The Mozilla Foundation" hreflang="en">Mozilla.org</a> zveřejnila <a href="http://www.mozilla.org/projects/security/known-vulnerabilities.html" title="Mozilla Foundation Security Advisories" hreflang="en">informace o bezpečnostních problémech</a>, které během posledních měsíců opravila ve svých produktech. <a href="http://secunia.com/advisories/14017/" title="Secunia - Advisories - Firefox / Mozilla / Thunderbird Multiple Vulnerabilities" hreflang="en">Jejich seznam</a> včera přinesla i <a href="http://secunia.com/" title="Secunia - Vulnerability and Virus Information" hreflang="en">Secunia</a> a ohodnotila je jako středně závažné. Všechny uvedené problémy jsou již opraveny ve <a href="/produkty/firefox/">Firefoxu</a> 1.0, <a href="/produkty/thunderbird/">Thunderbirdu</a> 1.0 a <a href="/produkty/suite/">Mozilla Suite</a> 1.7.5, takže většina uživatelů se nemusí obávat možného zneužití. Pokud vás zajímá, proč jsou problémy zveřejněny až po jejich vyřešení a nikoliv už dříve, podívejte se do dokumentu <a href="http://www.mozilla.org/projects/security/security-bugs-policy.html" hreflang="en">Handling Mozilla Security Bugs</a>, kde najdete podrobné vysvětlení, proč je tento přístup podle nás v zájmu uživatelů.</div>
