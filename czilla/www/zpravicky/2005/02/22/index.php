	<h3><a href="/zpravicky/dalsi-informace-o-minimo-na-windows-ce/">Další informace o Minimo na Windows CE</a></h3>

	<div>O tom, že <a href="/archiv-2005.html#news:364d357bd7c0bd02a5cb9ac4ec2142fc" title="Minimo na Windows CE">Doug Turner chystá port Minimo na Windows CE</a>, jsme už psali. Nyní zveřejnil <a href="http://groups.google.com/groups?as_umsgid=42144B49.2000809%40meer.net" title="netscape.public.mozilla.small-devices: Mozilla Windows CE Development" hreflang="en">další detaily o své práci</a> a vyzývá vývojáře, které tato platforma zajímá, aby se k němu přidali. <a href="http://www.mozilla.org/projects/minimo/" hreflang="en">Minimo</a> momentálně ke kompilaci vyžaduje Pocket PC 2003 <abbr title="Software Development Kit">SDK</abbr> a výsledný soubor má 3.8 MB. Můžete <a href="http://www.meer.net/~dougt/minimo_ce/NOTES.txt" hreflang="en">zkusit kompilovat sami</a>, stáhnout si <a href="http://www.meer.net/~dougt/minimo_ce/MinimoCE_0.002.zip">pokusný build</a> (ZIP, 3.8&nbsp;MB) a také sledovat <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=277211" title="Bug 277211 - Support for WinCE OS on PocketPC Platform" hreflang="en">tracking bug</a>.</div>
