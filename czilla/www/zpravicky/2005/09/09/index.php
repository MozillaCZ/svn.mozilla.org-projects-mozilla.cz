	<h3><a href="/zpravicky/gecko-1.9-bude-podporovat-python-pro-vyvoj-v-xul/">Gecko 1.9 bude podporovat Python pro vývoj v XUL</a></h3>

	<div><a href="http://weblogs.mozillazine.org/roadmap/" hreflang="en" title="Brendan's Roadmap Updates">Brendan Eich</a> včera oznámil, že Gecko 1.9 (na kterém bude mj. postaven Firefox 2.0) <a href="http://weblogs.mozillazine.org/roadmap/archives/008865.html" hreflang="en" title="Brendan's Roadmap Updates: Python for XUL scripting">bude podporovat Python jako další jazyk pro vývoj v <abbr title="XML User Interface Language" lang="en">XUL</abbr></a>. Doposud šlo v <abbr title="XML User Interface Language" lang="en">XUL</abbr> používat pouze JavaScript. Hlavním důvodem začlenění podpory <a href="http://www.python.org/" hreflang="en" title="Python Programming Language">Pythonu</a> je nalákání velkého množství vývojářů, kteří v něm pracují. Nelze opomenout ani mnoho nástrojů a knihoven, které jsou v tomto jazyku napsány a půjdou tak z <abbr title="XML User Interface Language" lang="en">XUL</abbr> snadno použít. Hlavním autorem podpory Pythonu, jejíž vývoj v současnosti probíhá na <a href="http://bonsai.mozilla.org/cvsquery.cgi?treeid=default&amp;module=all&amp;branch=DOM_AGNOSTIC_BRANCH&amp;branchtype=match&amp;dir=&amp;file=&amp;filetype=match&amp;who=&amp;whotype=match&amp;sortby=Date&amp;hours=2&amp;date=all&amp;mindate=&amp;maxdate=&amp;cvsroot=%2Fcvsroot" hreflang="en" title="Checkins on branch DOM_AGNOSTIC_BRANCH since the beginning of time">oddělené větvi</a>, je <a href="http://www.oreillynet.com/cs/catalog/view/au/59" hreflang="en">Mike Hammnond</a>.</div>
