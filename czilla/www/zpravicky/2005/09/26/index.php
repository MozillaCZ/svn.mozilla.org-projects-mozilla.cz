	<div><a href="http://standblog.org/blog/2005/09/26/93114388-mozilla-europe-spreading-mozilla-products-one-language-at-a-time" hreflang="en" title="Mozilla Europe: spreading Mozilla products one language at a time - Standblog - Tristan Nitot sur les standards du W3C, les navigateurs et la technologie">Tristan Nitot informuje</a>, že stránky <a href="http://www.mozilla-europe.org/cs/" title="Titulní stránka | Mozilla Europe">Mozilla Europe</a> jsou nyní přeloženy již do 20 jazyků. Také se zmiňuje o <a href="http://www.xitimonitor.com/etudes/equipement11.asp" hreflang="fr" title="XiTi - Etude Xiti Monitor">nejnovějším průzkumu</a> společnosti <a href="http://www.xitimonitor.com/" hreflang="en" title="XiTi Monitor">Xiti Monitor</a>, mapujícím podíl <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefoxu</a> v jednotlivých evropských zemích. Průměrný podíl činí 15,15&nbsp;%, maximum má Finsko (34&nbsp;%) následované Německem (24,1&nbsp;%) a Českou Republikou (23,2 %). Sluší se dodat, že průzkum byl prováděn v neděli, a tedy zahrnuje spíše domácí uživatele. V pracovní den by pravděpodobně podíl Firefoxu vyšel o něco málo nižší vzhledm k tomu, že do firemního prostředí neproniká Firefox tak rychle.</div>
