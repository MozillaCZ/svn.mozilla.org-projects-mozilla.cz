<div class="news">
	<h3><a href="/zpravicky/reorganizace-mozilla-foundation-vznik-mozilla-corporation/">Reorganizace Mozilla Foundation - vznik Mozilla Corporation</a></h3>

	<div><a href="http://www.mozilla.org/about/" title="Mozilla Foundation: O projektu" hreflang="en">Mozilla Foundation</a> dnes oznámila <a href="http://www.mozilla.org/reorganization/" title="Mozilla Foundation Reorganization" hreflang="en">vznik dceřiné společnosti Mozilla Corporation</a>, která bude zajišťovat další rozvoj a propagací produktů jako <a href="/produkty/firefox/" title="Mozilla Firefox: Stránka o produktu">Firefox</a>, <a href="/produkty/thunderbird/" title="Mozilla Thunderbird: Stránka o produktu">Thunderbird</a> a dalších aplikací založených na Mozille. Důvod vzniku nové společnosti <a href="http://weblogs.mozillazine.org/mitchell/archives/2005/08/organizing_the_1.html" title="Organizing the Mozilla Project -- Mozilla Corporation" hreflang="en">vysvětlila ve svém blogu</a> Mitchell Baker, prezidentka nově vzniklé Mozilla Corporation. <a href="http://www.mozilla.org/about/" title="Mozilla Foundation: O projektu" hreflang="en">Mozilla Foundation</a>, jako nezisková organizace, měla poměrně obtížnou situaci při spolupráci a získávání finančních prostředků od komerčních subjektů. Při stálém růstu zájmu o produkty Mozilla začínal být tento stav problematický, a proto se přistoupilo ke vzniku společnosti Mozilla Corporation. <br /><br />Nově vzniklá společnost má 36 zaměstnanců a jejich cílem je další rozvoj produktů <a href="/produkty/firefox/" title="Mozilla Firefox: Stránka o produktu">Firefox</a>, <a href="/produkty/thunderbird/" title="Mozilla Thunderbird: Stránka o produktu">Thunderbird</a> a dalších. Nemění se též cíle projektu jako je například snaha o přinášení inovací v oblasti webu. <a href="http://www.mozilla.org/about/" title="Mozilla Foundation: O projektu" hreflang="en">Mozilla Foundation</a> bude i nadále existovat a bude zajišťovat rozvoj projektu Mozilla.org, podporu komunity a správu ochranných známek. Tato reorganizace nemá žádný vliv na další osud produktů Mozilla, které budou i nadále poskytovány jako open-source aplikace a tudíž zdarma pro každého.</div>
