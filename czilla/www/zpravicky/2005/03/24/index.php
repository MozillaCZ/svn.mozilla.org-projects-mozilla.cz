	<div><a href="http://weblogs.mozillazine.org/mitchell/" title="mitchell's blog" hreflang="en">Mitchell Bakerová</a>, prezidentka <a href="http://www.mozilla.org/" hreflang="en">Mozilla Foundation</a>, absolvovala tento týden dva rozhovory. <a href="http://interviews.slashdot.org/interviews/05/03/22/1442200.shtml" title="Slashdot | Mozilla Foundation Chief Mitchell Baker Replies" hreflang="en">První z nich</a>, <a href="/archiv-2005.html#news:42e8e33fa33de6b53ad2a7070717ff21" title="Mitchell Bakerová bude odpovídat na otázky čtenářů Slashdotu">oznámený již dříve</a>, byl pro <a href="http://slashdot.org/" title="Slashdot: News for nerds, stuff that matters" hreflang="en">Slashdot</a> a Mitchell v něm odpovídala na dotazy čtenářů. Hlavní témata? Podpora <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr>, <a href="/produkty/suite/">Mozilla Suite</a>, jak mohou dobrovolníci pomáhat <a href="http://www.mozilla.org/" hreflang="en">Mozilla.org</a> a zmíněn byl i nejhlučnější obyvatel prostor nadace, pes Sparky :-) Hezké doplnění rozhovoru <a href="http://www.mozillazine.org/talkback.html?article=6278" title="Mitchell Baker Interviewed by Slashdot" hreflang="en">přináší MozillaZine</a>, k dispozici je i <a href="http://images.slashdot.org/articles/05/03/mitchell_baker_interview_cut.mp3">MP3 záznam</a> (8,37&nbsp;MB - 36:27). Druhý rozhovor byl pro americké rádio <a href="http://www.npr.org/" hreflang="en"><abbr title="National Public Radio">NPR</abbr></a> a Mitchell v něm obecně popisovala činnost Mozilla Foundation, mluvila o <a href="/produkty/firefox/">Firefoxu</a> a odpovídala na dotazy posluchačů. K dispozici je <a href="http://www.sciencefriday.com/pages/2005/Mar/hour2_031105.html" hreflang="en">záznam rozhovoru</a> ve formátech RealAudio a <abbr title="Windows Media Audio">WMA</abbr>.</div>
