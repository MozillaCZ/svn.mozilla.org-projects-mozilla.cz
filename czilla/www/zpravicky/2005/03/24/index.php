	<div><a href="http://weblogs.mozillazine.org/ben/" title="Inside Firefox - The Inside Track on Firefox Development" hreflang="en">Bed Goodger</a> představil <a href="http://weblogs.mozillazine.org/ben/archives/007792.html" title="Inside Firefox - The Inside Track on Firefox Development" hreflang="en">změny v novém správci rozšíření ve Firefoxu</a>, na kterém nyní pracuje. Nejdůležitější je asi možnost nainstalovat rozšíření kamkoliv na disk (doteď to bylo možné jen do několika vyhrazených adresářů) a automatická instalace rozšíření pouhým zkopírováním XPI souboru na správné místo a restartem aplikace. Vylepšení by měly být zahrnuty ve Firefoxu 1.1.</div>
