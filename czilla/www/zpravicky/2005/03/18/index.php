	<h3><a href="/zpravicky/pripravuje-se-firefox-a-tunderbird-1.0.2-mozilla-suite-1.7.6-mozilla-1.8-beta-2-se-odklada/">Připravuje se Firefox a Tunderbird 1.0.2, Mozilla Suite 1.7.6; Mozilla 1.8 Beta 2 se odkládá</a></h3>

	<div><a href="http://weblogs.mozillazine.org/asa/" title="adot's notblog*" hreflang="en">Asa Dotzler</a> oznámil <a href="http://weblogs.mozillazine.org/asa/archives/007758.html" title="adot's notblog*: firefox and thunderbird 1.0.2" hreflang="en">aktuální plány Mozilla Foundation</a> ohledně opravných verzí jejích produktů. V nejbližších dnech se chystá vydání <a href="/produkty/firefox/">Firefoxu</a> 1.0.2 a <a href="/produkty/thunderbird/">Thunderbirdu</a> 1.0.2 (plánovaná verze 1.0.1 byla z organizačních důvodů vypuštěna), hned poté by měla následovat <a href="/produkty/suite/">Mozilla Suite</a> 1.7.6. Všechny tyto verze budou jen opravovat nalezené chyby verzí předchozích. Dále došlo k <a href="http://groups.google.com/groups?as_umsgid=d1628e$74l1@ripley.netscape.com" hreflang="en">posunu plánovaného vydání testovací verze Mozilla Suite 1.8 Beta 2</a> o několik týdnů a připravuje se aktualizovaný plán dalšího vývoje všech produktů. Finální verze Mozila Suite již vzhledem ke <a href="/archiv-2005.html#news:a14e40269e22a246e8c87691154f4cca" title="Změny ve vývoji Mozilla Suite">změnám ve vývoji</a> nevyjde pod hlavičkou <a href="http://www.mozilla.org/">Mozilla Foundation</a>, ale o její vydání se <a href="http://wiki.mozilla.org/wiki/SeaMonkey:Home_Page" title="SeaMonkey:Home Page - wiki.mozilla.org" hreflang="en">chce postarat samostatná komunita vývojářů</a> a pravděpodobně se změní i název programu.</div>
