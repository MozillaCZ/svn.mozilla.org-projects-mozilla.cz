	<h3><a href="/zpravicky/zmeny-ve-vyvoji-mozilla-suite/">Změny ve vývoji Mozilla Suite</a></h3>

	<div><a href="http://www.mozilla.org/seamonkey-transition.html" title="Mozilla Application Suite - Transition Plan" hreflang="en">Mozilla Foundation dnes oznámila</a>, že <a href="/produkty/suite/">Mozilla Suite</a> 1.7.x je poslední oficiální vývojovou větví této aplikace - vydání verze 1.8 se neplánuje. Vývoj pod hlavičkou <a href="http://www.mozilla.org/" hreflang="en">Mozilla Foundation</a> se bude soustředit na <a href="/produkty/firefox/">Firefox</a> a <a href="/produkty/thunderbird/">Thunderbird</a>. Větev Mozilla Suite 1.7.x bude nadále podporována a budou pro ni vydávány bezpečnostní aktualizace. <a href="http://web.mit.edu/bzbarsky/Public/SeaMonkey.txt" hreflang="en">Někteří vývojáři v čele s Borisem Zbarskym nyní uvažují</a>, že by si další vývoj Suite vzali na starost sami a Mozilla Foundation by jen poskytovala nezbytné technické zázemí. V této chvíli ještě situace není vyjasněná, nicméně <a href="/">CZilla</a> uvažuje, že v případě vzniku této &quot;komunitní verze&quot; Mozilla Suite ji bude dále podporovat a lokalizovat, takže její uživatelé by o svou oblíbenou aplikaci neměli přijít. O situaci budeme dále průběžně informovat.</div>
