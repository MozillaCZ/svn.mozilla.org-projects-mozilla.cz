	<h3><a href="/zpravicky/jak-je-to-se-zranitelnosti-prohlizecu/">Jak je to se zranitelností prohlížečů?</a></h3>

	<div><a href="http://www.informationweek.com/" title="InformationWeek | Business Innovation Powered By Technology" hreflang="en">InformationWeek</a> nabízí <a href="http://www.informationweek.com/story/showArticle.jhtml?articleID=159906206" title="InformationWeek &gt; Browser Security &gt; IE 'Unsafe' 98% Of 2004, Says ScanIT" hreflang="en">zajímavý pohled na bezpečnost prohlížečů</a>. Informuje, že Internet Explorer byl zranitelný po 98&nbsp;% roku 2004 (tj. po celý rok bez jednoho týdne), zatímco u Mozilly to bylo jen 15&nbsp;%. Zranitelností se zde myslí, že v daný okamžik existovala veřejně známá metoda, jak narušit bezpečnost prohlížeče, i když měl aplikovány všechny tehdy dostupné záplaty. Jednou z příčin pozitivního výsledku pro Mozillu je možná i dřívější vypsání odměny 500&nbsp;USD za každou nalezenou bezpečnostní chybu v produktech <a href="http://www.mozilla.org/" hreflang="en">Mozilla.org</a> - Michael Krax z Německa si tak například <a href="http://www.linuxelectrons.com/article.php/20050328193505478" title="LinuxElectrons&#8482; - Mozilla Pays Out $2500 In Bug Bounties" hreflang="en">vydělal už 2500&nbsp;USD</a>.</div>
