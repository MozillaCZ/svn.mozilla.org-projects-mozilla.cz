	<h3><a href="/zpravicky/microsoft-pry-patent-eolasu-neporusil/">Microsoft prý patent Eolasu neporušil</a></h3>

	<div>Americký federální odvolací soud <a href="http://www.theregister.co.uk/2005/03/03/microsoft_eolas_patent_case/" title="Appeals court hits rewind in Microsoft Eolas case | The Register" hreflang="en">dnes zrušil rozsudek</a>, který nařizoval <a href="http://www.microsoft.com/" title="Microsoft Corporation" hreflang="en">Microsoftu</a> zaplatit 521 milionů dolarů (cca 11,9 mliliardy Kč) za porušení patentových práv firmy <a href="http://www.eolas.com/" title="Eolas Technologies Inc." hreflang="en">Eolas</a>. Ty měl porušit použitím technologie pluginů ve svém Internet Exploreru. Případ byl vrácen soudu nižší instance a bude dále projednán.</div>
