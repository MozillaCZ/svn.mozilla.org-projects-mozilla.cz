	<h3><a href="/zpravicky/mozilla-foundation-a-google/">Mozilla Foundation &amp; Google</a></h3>

	<div>Na <a href="http://news.zdnet.co.uk/" title="Technology News at ZDNet UK" hreflang="en">ZDNet UK</a> najdete <a href="http://news.zdnet.co.uk/0,39020330,39189475,00.htm" title="Google keeping the wolf from Firefox' door - ZDNet UK News" hreflang="en">článek poodhalující detaily okolo spolupráce Mozilla Foundation s Googlem</a>. Pozitivním efektem této spolupráce je zejména přísun financí pro <a href="http://www.mozilla.org/" hreflang="en">Mozilla Foundation</a>, který jí umožňuje zaměstnat další vývojáře. <a href="http://weblogs.mozillazine.org/gerv/" title="Hacking for Christ" hreflang="en">Gervase Markham</a>, který měl na toto téma proslov na <a href="http://www.fosdem.org/2005" title="FOSDEM 2005" hreflang="en"><abbr title="Free and Open Source Software Developers' European Meeting" lang="en">FOSDEM</abbr> 2005</a>, zároveň podotýká, že Mozilla Foundation je velmi ostražitá v tom, aby partnerství bylo ve prospěch uživatelů. Vše ještě <a href="http://weblogs.mozillazine.org/asa/archives/007658.html" title="adot's notblog*: mis-information" hreflang="en">vyjasňuje Asa Dotzler</a>.</div>
