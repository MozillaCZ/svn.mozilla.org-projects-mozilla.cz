	<div>Při tvorbě tak složitého programu, jako je <a href="http://www.mozilla.org/" hreflang="en" title="Mozilla Foundation">Mozilla</a>, se snadno stane, že vývojáři při přidávání nové vlastnosti či opravě chyby nechtěně pokazí nějakou jinou funkci. Takové chyby jsou nepříjemné, komplikují vývoj a přidělávají mnoho práce testerům, kteří je musí odhalovat. Proto nyní <a href="http://weblogs.mozillazine.org/roc/archives/2005/03/visual_regressi.html" title="Well, I'm Back: Visual Regression Tests" hreflang="en">Robert O'Callahan píše nástroj</a>, který by měl toto odhalování automatizovat. Nástroj bude omezen pouze na chyby projevující se při renderování stránek a Robert se při jeho tvorbě setkal se spoustou nečekaných obtíží, které ve svém článku popisuje. Mimochodem, podobný nástroj <a href="http://ln.hixie.ch/?start=1064849326&amp;count=1" title="Hixie's Natural Log: Naked girls, DHCP, GPRS, ART, and high notes" hreflang="en">psal před rokem a půl</a> i <a href="http://ln.hixie.ch/" title="Hixie's Natural Log" hreflang="en">Ian Hickson</a> pro <a href="http://www.opera.com/" title="Opera Web Browser" hreflang="en">Operu</a>.</div>
