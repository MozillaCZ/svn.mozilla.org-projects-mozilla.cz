	<h3><a href="/zpravicky/web-spread-firefox-hacknut/">Web Spread Firefox hacknut</a></h3>

	<div><a href="http://www.mozillazine.org/" hreflang="en" title="mozillaZine - Your Source for Daily Mozilla News and Advocacy">MozillaZine</a> informuje, že <a href="http://www.mozillazine.org/talkback.html?article=7479" hreflang="en" title="Spread Firefox Hacked Again - MozillaZine Talkback">server Spread Firefox byl hacknut</a>. Neznámý útočník zneužil bezpečnostní chybu v nainstalovaném systému <a href="http://twiki.org/" hreflang="en" title="TWiki - Enterprise Collaboration Platform &amp; Wiki">TWiki</a>. Citlivá data uživatelů by neměla být ohrožena, nicméně administrátoři se raději rozhodli server odstavit přibližně do 15. října, než systém prověří a zajistí, aby se podobné příhody neopakovaly. <a href="http://www.spreadfirefox.com/" hreflang="en" title="Spread Firefox">Spread Firefox</a> byl letos hacknut už podruhé &ndash; poprvé to <a href="http://www.mozillazine.org/talkback.html?article=6947" hreflang="en" title="Spread Firefox Hacked - MozillaZine Talkback">bylo v červenci</a>.</div>
