	<h3><a href="/zpravicky/vyvojari-galeonu-prechazeji-k-epiphany/">Vývojáři Galeonu přecházejí k Epiphany</a></h3>

	<div>Vzhledem k nedostatku času na udržování vývoje vlastní aplikace se <a href="http://gnomedesktop.org/node/2450" hreflang="en" title="The future of Galeon | FootNotes">někteří vývojáři prohlížeče Galeon rozhodli, že se raději připojí k vývoji Epiphany</a>. Své síly chtějí nasměrovat především k implementaci některých funkcí Galeonu do Epiphany prostřednictvím systému rozšíření. Připomínáme, že <a href="http://galeon.sourceforge.net/" hreflang="en">Galeon</a> i <a href="http://www.gnome.org/projects/epiphany/" hreflang="en" title="Epiphany: The web browser for the GNOME desktop">Epiphany</a> jsou prohlížeče využívající jádro <a href="http://www.mozilla.org/newlayout/" hreflang="en" title="Mozilla Layout Engine">Gecko</a> a oba jsou určené pro desktopové prostředí <a href="http://www.gnome.org/" hreflang="en" title="GNOME: The Free Software Desktop Project">GNOME</a>.</div>
