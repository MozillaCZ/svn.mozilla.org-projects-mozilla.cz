	<h3><a href="/zpravicky/firefox-cestoval-balonem-oslava-100-milionu-stazeni/">Firefox cestoval balónem - oslava 100 milionů stažení</a></h3>

	<div>Originální způsob, jak oslavit <a href="/archiv-2005.html#news:912b4cb0852f939eba630cd990f6279c" title="Firefox - 100 milionů stažení">100 milionů stažení Firefoxu</a>, vymysleli na <a href="http://oregonstate.edu/" hreflang="en" title="Home page for Oregon State University">Oregon State University</a>. Rozhodli se <a href="http://lug.oregonstate.edu/news/?p=27" hreflang="en" title="Linux Users Group - Oregon State University &#187; Celebrate 100,000,000 Firefox downloads and take back the sky!">vypustit meteorologický balón</a> nazvaný <em>Firefox One</em> a mimo jiné obsahující CD s <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefoxem</a>, propagační plakát a fotoaparát. Balón měl dosáhnout výšky 100&nbsp;000&nbsp;stop (cca 30&nbsp;km) a zde "vyfotit Firefox na hranici vesmíru". Akce se odehrála 22. 10. a můžete si <a href="http://lug.oregonstate.edu/index.php/Projects/Firefox/Firefox_Sky" hreflang="en" title="Linux Users Group - Oregon State University">přečíst o jejím průběhu</a> nebo se podívat na <a href="http://lug.oregonstate.edu/gallery/firefox-one" hreflang="en" title="OSLUG Gallery :: Firefox-One">fotky z vypouštění</a>. Teď zbývá jen počkat na návrat balónu na zem a jeho nalezení &ndash; pak se uvidí, zda fotky vyšly :-)</div>
