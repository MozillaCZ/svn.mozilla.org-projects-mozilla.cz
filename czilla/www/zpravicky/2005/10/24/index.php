	<h3><a href="/zpravicky/firefox-ma-na-homepage-bbc-9,7-procent/">Firefox má na homepage BBC 9,7 %</a></h3>

	<div><a href="http://www.currybet.net/articles/user_agents/index.php" hreflang="en" title="The Software Used To Access The BBC Homepage">Zajímavou statistiku o podílu prohlížečů</a> na <a href="http://www.bbc.co.uk/" hreflang="en" title="BBC - bbc.co.uk homepage - Home of the BBC on the Internet">homepage BBC</a> zveřejnil na svém blogu jeden zaměstnanec této společnosti. Dle očekávání v ní zvítězil Internet Explorer (85,9&nbsp;%), následován <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefoxem</a> (9,7&nbsp;%) a trochu překvapivě Safari (2,6&nbsp;%). Ostatní prohlížeče mají podíl méně než 1 %. Statistika se týká jen hlavní stránky (nikoliv celého webu) a byla vytvořena analýzou týdenního provozu během letošního září.</div>
