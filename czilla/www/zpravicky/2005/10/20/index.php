	<h3><a href="/zpravicky/dalsi-velka-aktualizace-lokalizaci-rozsireni/">Další velká aktualizace lokalizací rozšíření</a></h3>

	<div>Firefox 1.5 se kvapem blíží, autoři svá rozšíření upravují, aby jej podporovala a my samozřejmě nemůžeme zůstat pozadu. Přinášíme vám aktualizaci rozšíření <a href="/doplnky/rozsireni/all-in-one-sidebar/">All-in-one Sidebar</a>, šikovné postranní lišty, a také <a href="/doplnky/rozsireni/firefoxview/">FirefoxView</a>, které umožňuje z <abbr title="Internet Explorer" lang="en">IE</abbr> otevírat stránky ve Firefoxu. <a href="/doplnky/rozsireni/inforss/">Info<abbr title="Really Simple Syndication" lang="en">RSS</abbr></a> je zajímavá <abbr title="Really Simple Syndication" lang="en">RSS</abbr> čtečka, <a href="/doplnky/rozsireni/launchy/">Launchy</a> umožňuje otevírat různé soubory v různých aplikacích. <a href="/doplnky/rozsireni/mailredirect/">MailRedirect</a> umožňuje přeposlat poštu někomu jinému, aniž by to bylo z hlaviček poznat, a <a href="/doplnky/rozsireni/mboximport/">MboxImport</a> umožňuje exportovat/importovat zprávy z Thunderbirdu ve formátu mbox. <a href="/doplnky/rozsireni/measureit/">MeasureIt</a> umí změřit velikost prvků na obrazovce, <a href="/doplnky/rozsireni/mouse-gestures/">Mouse Gestures</a> přidává do Mozilly ovládání gesty myši. <a href="/doplnky/rozsireni/mozimage/">MozImage</a> je přívětivý prohlížeč obrázků, <a href="/doplnky/rozsireni/target-alert/">Target Alert</a> zobrazuje u odkazu na soubor jeho typ, a <a href="/doplnky/rozsireni/text-plain/">text/plain</a> umožňuje otvírat adresy, které nejsou zapsány do odkazů. <a href="/doplnky/rozsireni/virtual-identity/">Virtual Identity</a> umožňuje jednoduše měnit identitu při e-mailové komunikaci a poslední dnešní rozšíření, <a href="/doplnky/rozsireni/windowsupdate/">WindowsUpdate</a>, přidává do nabídky Firefoxu položku Windows Update, po jejímž odklepnutí se tato stránka otevře v <abbr title="Internet Explorer" lang="en">IE</abbr>.</div>
