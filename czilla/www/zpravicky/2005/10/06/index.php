	<div>Nedávno jsme informovali o tom, že <a href="/archiv-2005.html#news:30d71ce09f4dc66235751218ebf342dc" title="Firefox nebude podporovat SSL 2.0">Firefox nebude nadále podporovat <abbr title="Secure Sockets Layer" lang="en">SSL</abbr> 2.0</a>, ale pouze bezpečnější verzi 3.0. Pokud jste webmasteři webů, upgradujte prosím v zájmu kompatibility i bezpečnosti software na serverech, aby se <abbr title="Secure Sockets Layer" lang="en">SSL</abbr> 3.0 dovedl pracovat. A pokud jste uživatelé, pomozte nám najít servery, které <abbr title="Secure Sockets Layer" lang="en">SSL</abbr> 3.0 nepodporují &ndash; návod najdete v <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=307271" hreflang="en" title="Bug 307271 - Eradicate SSL 2.0-only servers from the Internet">bug 307271</a> nebo <a href="http://weblogs.mozillazine.org/gerv/archives/2005/05/quick_ssl_versi.html" hreflang="en" title="Hacking for Christ: Quick SSL Version 2 Server Survey">u Gervase Markhama</a>. Nalezené weby hlaste ve <a href="http://forum.czilla.cz/viewforum.php?f=7" title="Zobrazit fórum - Technická evangelizace (nefungující stránky) :: CZilla fórum">fóru technické evangelizace</a>. Děkujeme!</div>
