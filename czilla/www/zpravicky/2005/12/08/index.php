	<h3><a href="/zpravicky/podil-firefoxu-v-cr-presahl-15-procent/">Podíl Firefoxu v ČR přesáhl 15 %</a></h3>

	<div>Podle <a href="http://www.iinfo.cz/tiskova_zprava/navrcholu_rust_firefoxu_se_zpomalil/" title="Internet Info: NAVRCHOU.cz: Růst Firefoxu se zpomalil">dnes vydané tiskové zprávy</a> společnosti <a href="http://www.iinfo.cz/" title="Internet Info">Internet Info</a>, provozovatele služby <a href="http://navrcholu.cz/" title="NAVRCHOLU.cz: monitoring návštěvnosti">NAVRCHOLU.cz</a>, překročil podíl <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefoxu</a> v ČR hranici 15&nbsp;% a posílil tak o 1,3&nbsp;% oproti <a href="http://www.iinfo.cz/tiskova_zprava/navrcholu_firefox_posiluje_pozici/" title="Internet Info: NAVRCHOLU.cz: Firefox posiluje svou pozici">podílu před půl rokem</a>. Celkový podíl prohlížečů založených na vykreslovacím jádru <a href="http://www.mozilla.org/newlayout/" hreflang="en" title="Mozilla Layout Engine">Gecko</a> pak činí 18,03&nbsp;%. Statistiky dle očekávání vede Intenet Explorer se 78,15&nbsp;%, na třetím místě je Opera (2,89&nbsp;%) a na čtvrtém Konqueror+Safari (0,52&nbsp;%).</div>
