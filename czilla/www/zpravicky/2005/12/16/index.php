	<h3><a href="/zpravicky/nova-rozsireni-pro-firefox-od-google/">Nová rozšíření pro Firefox od Google</a></h3>

	<div><a href="http://www.google.com/" hreflang="en" title="Google">Google</a> vydal dvě nová rozšíření pro <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefox</a>: <a href="http://www.google.com/tools/firefox/safebrowsing/index.html" hreflang="en" title="Google Safe Browsing for Firefox">Google Safe Browsing</a>, které varuje uživatele při návštěvě stránek, které se pokoušejí o phishing, a <a href="http://www.google.com/tools/firefox/webcomments/index.html" hreflang="en" title="Blogger Web Comments for Firefox">Blogger Web Comments</a>, které umožňuje zobrazit komentáře bloggerů z <a href="http://www.blogger.com/" hreflang="en">Blogger.com</a> ke stránce, kterou si právě prohlížíte, a také přidat komentář vlastní. Rozšíření Google Safe Browsing je oficiálně možno stáhnout jen z USA, ale toto omezení lze snadno obejít třeba pomocí služby <a href="http://ieproxy.com/" hreflang="en" title="IEproxy">IEproxy</a>. Obě rozšíření jsou zatím v betaverzi.</div>
