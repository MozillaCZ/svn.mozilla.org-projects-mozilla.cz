	<h3><a href="/zpravicky/uzivatele-firefoxu-mene-klikaji-na-reklamy2/">Uživatelé Firefoxu méně klikají na reklamy</a></h3>

	<div>Po jednom roce přišla německá společnost <a href="http://www.adtech.info/en/index.html" hreflang="en" title="ADTECH &#8211; The World of Marketing Technologies">ADTECH</a> s <a href="http://www.adtech.info/en/pr-05-10.html" hreflang="en" title="ADTECH | Press Release">dalším kolem průzkumu</a> na téma "jak uživatelé jednotlivých prohlížečů klikají na reklamy". Ve <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefoxu</a> na reklamy letos v září klikalo 0,12&nbsp;% uživatelů, zatímco v <abbr title="Internet Explorer" lang="en">IE</abbr>&nbsp;6 celých 0,23&nbsp;% (v <abbr title="Internet Explorer" lang="en">IE</abbr>&nbsp;5.5 a <abbr title="Internet Explorer" lang="en">IE</abbr>&nbsp;5.0 dokonce 0,33&nbsp;% resp. 0,39&nbsp;%). Zajímavé je, že <a href="/archiv-2005.html#news:8bd71b95bfb6469bb74804c12594d413" title="Uživatelé Firefoxu méně klikají na reklamy">oproti loňsku</a> se číslo u Firefoxu téměř nezměnilo, zatímco u <abbr title="Internet Explorer" lang="en">IE</abbr> významně pokleslo. Tisková zpráva ADTECHu také přináší tabulku podílů prohlížečů, kterou vede <abbr title="Internet Explorer" lang="en">IE</abbr> s 83,31&nbsp;%, následován Firefoxem s 12,41&nbsp;%. Všechny údaje byly získány ze vzorku 2000 evropských webů, na kterých ADTECH zobrazuje svou reklamu.</div>
