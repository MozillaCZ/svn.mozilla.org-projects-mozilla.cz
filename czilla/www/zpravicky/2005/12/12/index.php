	<h3><a href="/zpravicky/klasicky-vzhled-nabidek-ve-firefoxu/">&quot;Klasický&quot; vzhled nabídek ve Firefoxu</a></h3>

	<div>Možná jste si všimli, že vzhled nabídek (menu) ve Firefoxu 1.5 ve Windows je uzpůsoben novému vzhledu ve Windows XP. Pokud máte v systému nastaven vzhled klasický, nebo používáte některou starší verzi Windows, nabídky prostě graficky nezapadají. Tento problém řeší rozšíření <a href="http://ilpolipo.free.fr/fx/classicmenus/" hreflang="en" title="Index of /fx/classicmenus">Classic Menus</a>, které upraví vzhled nabídek tak, aby vypadaly stejně jako v ostatních programech s klasickým vzhledem.</div>
