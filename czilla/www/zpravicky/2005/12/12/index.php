	<h3><a href="/zpravicky/mozilla-international-store-spusten-v-betaverzi/">Mozilla International Store spuštěn v betaverzi</a></h3>

	<div><a href="http://www.mozilla.org/foundation/" hreflang="en" title="About the Mozilla Foundation">Mozilla Foundation</a> spustila betaverzi nové služby <a href="http://intlstore.mozilla.org/" hreflang="en" title="Mozilla Store International">Mozilla International Store</a> &ndash; elektronického obchodu se zbožím souvisejícím s vyvíjenými produkty, především <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefoxem</a>. Koupit si v něm můžete <a href="http://intlstore.mozilla.org/index.php?cPath=1" hreflang="en" title="Mozilla Store International">knížky</a>, <a href="http://intlstore.mozilla.org/index.php?cPath=2" hreflang="en" title="Mozilla Store International">něco na sebe</a> a <a href="http://intlstore.mozilla.org/index.php?cPath=3" hreflang="en" title="Mozilla Store International">různé další věci</a> &ndash; třeba <a href="http://intlstore.mozilla.org/product_info.php?products_id=9" hreflang="en" title="Mozilla Store International">stylový batoh na notebook</a>. Nová služba je určena především pro zákazníky z území <abbr title="Evropská unie" lang="cs">EU</abbr> a doplňuje již existující <a href="http://store.mozilla.org/" hreflang="en" title="Mozilla Store">Mozilla Store</a>, který byl zaměřen na USA. Několik poznámek o obchodu si <a href="http://weblogs.mozillazine.org/marcia/archives/009404.html" hreflang="en" title="Marcia&#039;s Tirade of the Day: The Mozilla International Store is open for business!">můžete přečíst u Marcii Knous</a>.</div>
