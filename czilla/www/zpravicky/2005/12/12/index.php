	<h3><a href="/zpravicky/firefox-obdrzel-cenu-magazinu-pc-pro-za-rok-2005/">Firefox obdržel cenu magazínu PC Pro za rok 2005</a></h3>

	<div>Britský počítačový magazín <a href="http://www.pcpro.co.uk/" hreflang="en" title="PC Pro">PC Pro</a> uděloval ceny čtenářů a editorů nejlepším produktům a firmám roku 2005. Cenu editorů <em>Real World award</em> <a href="http://www.pcpro.co.uk/news/81155/pc-pro-awards-2005-the-winners.html" hreflang="en" title="PC Pro: News: PC Pro Awards 2005 - the winners">si odnesla Mozilla Foundation za prohlížeč Firefox</a>. Dle popisu je tato cena (spolu s dalšími cenami editorů) udělována za exceletní design v oblasti hardware nebo software. V Londýně si <a href="http://standblog.org/blog/2005/12/10/93114540-doing-open-source-software-is-sometimes-a-dirty-job" hreflang="en" title="Doing Open Source software is sometimes a dirty job... - Standblog - Tristan Nitot sur les standards du W3C, les navigateurs et la technologie">cenu byl převzít</a> Pascal Chevrel z <a href="http://www.mozilla-europe.org/cs/" title="Titulní stránka | Mozilla Europe">Mozilla Europe</a>.</div>
