	<div>Minulý týden se v kalifornském Mountain View (sídelním městě <a href="http://wiki.mozilla.org/2005Offsite" hreflang="en" title="2005Offsite - wiki.mozilla.org">Mozilla Foundation</a> a <a href="http://www.mozilla.com/" hreflang="en" title="Mozilla Corporation - Home of the Firefox web browser and Thunderbird email client">Corporation</a>) konala konference <a href="http://wiki.mozilla.org/2005Offsite" hreflang="en">Firefox Summit 2005</a>, které se zúčastnili členové Mozilla Corporation, vedení <a href="http://www.mozilla-europe.org/cs/" title="Titulní stránka | Mozilla Europe">Mozilla Europe</a> a mnozí vývojáři a příznivci Firefoxu. Můžete se (<a href="http://wiki.mozilla.org/WebCoverage" hreflang="en" title="WebCoverage - wiki.mozilla.org">mimo jiné</a>) <a href="http://www.flickr.com/photos/beltzner/sets/1537264/" hreflang="en" title="2005 Mozilla Summit - a photoset on Flickr">podívat na fotky</a>, z nichž například plyne, že i v USA <a href="http://www.flickr.com/photos/beltzner/71391959/in/set-1537264/" hreflang="en" title="Firefox 1.5 Release Party on Flickr - Photo Sharing!">měli Firefox 1.5 Party</a> a že do doby konání summitu <a href="http://www.flickr.com/photos/beltzner/71391762/" hreflang="en" title="Look at those numbers! on Flickr - Photo Sharing!">bylo staženo celkem 4,5 milionů kopií</a> nové verze prohlížeče, přičemž ve špičce uživatelé stahovali 127 kopií za sekundu! <a href="http://alex.polvi.net/" hreflang="en" title="Alex Polvi">Alex Polvi</a> nicméně píše, že síťová infrastruktura <a href="http://alex.polvi.net/2005/12/10/firefox-15-release-sysadmin-style/" hreflang="en" title="Alex Polvi &#187; Firefox 1.5 release, sysadmin style">nápor vydržela</a> (narozdíl od loňského vydání Firefoxu 1.0).</div>
