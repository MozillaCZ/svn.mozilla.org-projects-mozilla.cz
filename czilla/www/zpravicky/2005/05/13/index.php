	<h3><a href="/zpravicky/khtml-+-apple-spoluprace-drhne/">KHTML + Apple: Spolupráce drhne</a></h3>

	<div>Před dvěma lety byl internet plný zpráv o tom, že <a href="http://www.apple.com/" hreflang="en">Apple</a> si jako základ svého prohlížeče <a href="http://www.apple.com/safari/" hreflang="en">Safari</a> vybral renderovací jádro <a href="http://en.wikipedia.org/wiki/KHTML" hreflang="en">KHTML</a>. <a href="http://news.com.com/Open-source+divorce+for+Apples+Safari/2100-1032_3-5703819.html?tag=st.num" title="Open-source divorce for Apple's Safari? | CNET News.com" hreflang="en">Nyní se zdá, že hezky nalinkovaná spolupráce poněkud drhne</a>. V kostce jde o to, že Apple si vyvíjí svoji samostatnou verzi KHTML místo toho, aby spolupracoval na vývoji hlavní větve. Obě verze se tak vzdalují a například portování patchů od Apple do hlavní větve KHTML se tak <a href="http://www.kdedevelopers.org/node/view/1001" title="So, when will KHTML merge all the WebCore changes? | www.kdedevelopers.org" hreflang="en">stává téměř nemožným</a>. Někteří zastánci open-source dokonce začínají mít pocit, že Apple si víc bere nežli dává a jeho jednání není etické. <a href="http://weblogs.mozillazine.org/ben/archives/008131.html" hreflang="en">Svůj poněkud pragmatičtější názor na situaci</a> vyjádřil i <a href="http://weblogs.mozillazine.org/ben/" title="Inside Firefox - The Inside Track on Firefox Development" hreflang="en">Ben Goodger</a>.</div>
