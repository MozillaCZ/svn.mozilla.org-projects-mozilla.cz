	<div><a href="http://news.com.com/" title="CNET News.com -- Technology news and business reports" hreflang="en">CNET News.com</a> přinesl zprávu, že <a href="http://news.com.com/IBM+backs+Firefox+in-house/2100-7344_3-5704750.html" title="IBM backs Firefox in-house | CNET News.com" hreflang="en">IBM doporučuje svým cca 300 000 zaměstnancům používat Firefox</a>. Podle článku ve firmě už <a href="/produkty/firefox/">Firefox</a> asi 10&nbsp;% zaměstnanců používá a v současné době probíhá zaškolování pracovníků technické podpory a ověřování kompatibility interních aplikací. Motivem pro nasazení jsou pochopitelně ušetřené náklady - dostupná open-source rozšíření, by měla zefektivnit práci zaměstnanců. Sluší se dodat, že IBM do produktů <a href="http://www.mozilla.org/" hreflang="en">Mozilla.org</a> dlouhodobě investuje a několik jejích zaměstnanců aktivně přispívá do vývoje.</div>
