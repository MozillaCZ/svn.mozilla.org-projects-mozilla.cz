	<h3><a href="/zpravicky/xtech-2005/">XTech 2005</a></h3>

	<div>Téměř všechny vývojářské špičky Mozilly se tento týden sjely do Amsterdamu na <a href="http://www.xtech-conference.org/" hreflang="en">konferenci XTech 2005</a> o <abbr title="Extensible markup Language" lang="en">XML</abbr> a webových technologiích. Mnozí z vývojářů zde měli i přednášky o jednotlivých technologiích, na kterých je Mozilla postavena. <a href="http://www.glazman.org/weblog/dotclear/index.php?2005/05/27/1055-adam-2" hreflang="en">Daniel Glazman nabízí stručné shrnutí</a> a konferenci si velmi pochvaluje, <a href="http://weblogs.mozillazine.org/gerv/" title="Hacking for Christ" hreflang="en">Gervease Markham</a> předkládá svůj <a href="http://weblogs.mozillazine.org/gerv/archives/008211.html" title="Hacking for Christ: XAML and XUL" hreflang="en">názor na rozdíly mezi <abbr title="XML User Interface Language" lang="en">XUL</abbr> a <abbr title="Extensible Application Markup Language" lang="en">XAML</abbr></a>, který si utvořil po prezentacích k těmto jazykům. <a href="http://weblogs.mozillazine.org/roc/" title="Well, I'm Back" hreflang="en">Robert O'Callahan</a> píše mimo jiné <a href="http://weblogs.mozillazine.org/roc/archives/2005/05/xtech.html" title="Well, I'm Back: XTech" hreflang="en">o své prezentaci týkající se <abbr title="Scalable Vector Graphics" lang="en">SVG</abbr> a <code>&lt;canvas&gt;</code></a>. Materiály k většině přednášek budou pravděpodobně brzy dostupné online &ndash; předpokládáme, že vás o nich budeme informovat.</div>
