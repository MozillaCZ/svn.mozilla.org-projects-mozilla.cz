<div class="news">
	<h3><a href="/zpravicky/manual-pro-zacinajici-uzivatele-mozilly/">Manuál pro začínající uživatele Mozilly</a></h3>

	<div>MozillaZine <a href="http://www.mozillazine.org/talkback.html?article=3995" title="Introduction to Mozilla Manual Available in Draft Form">včera oznámil</a> vydání draftu <a href="http://downloads.mozdev.org/mozmanual/mozmanual-draft-4.pdf" title="Mozilla - A Manual for First Time Users">Manuálu pro začínající uživatele Mozilly</a> (PDF - 1.5MB). Manuál čítá 52 stránek a dokonale pokrývá spektrum otázek, na které se obvykle ptají začínající uživatelé, počínaje přehledem klávesových zkratek či "smajlíků" v Pošťákovi a konče třeba nastavováním blokování pop-up oken, spamfiltru či klíčových slov v záložkách. Ačkoliv jsou všechny screenshoty pořizované v OS Windows, zjistíte, že autor nezapoměl ani na uživatele Linuxu či Mac OS X. Jedná se zatím jen o pracovní verzi, takže ještě chybí kapitoly o Chatzille či Editoru. Zbývá jen doufat, že se manuál brzy dočká své finální verze (pak by se totiž dalo uvažovat i o českém překladu ;-). Více infromací naleznete na stránkách projektu <a href="http://mozmanual.mozdev.org/" title="MozManual: An Introduction to Mozilla">MozManual</a>.</div>
