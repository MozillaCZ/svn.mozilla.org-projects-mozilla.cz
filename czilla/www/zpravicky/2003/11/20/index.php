	<h3><a href="/zpravicky/semestralni-prace-o-mozille/">Semestrální práce o Mozille</a></h3>

	<div>Na <a href="http://www.root.cz/">Rootu</a> vyšla zpráva o semestrální práci Davida Majdy <a href="http://www.majda.cz/download.php?filename=mozilla.pdf">Mozilla - platforma pro vývoj aplikací</a> (PDF 1.6MB). Autor nás v ní na začátku seznamuje s historií Netscape a Mozilly, pak stručně popisuje architekturu Mozilly a vysvětluje význam jejích základních kamenů. Je uvedena i jednoduchá ukázková <acronym title="XML-based User Interface Language">XUL</acronym> aplikace. Na jedné straně musím říci, že mě rozsah práce s ohledem na její lákavý název poněkud zklamal (semestrální práce je holt semestrální práce), nicméně je nutné poznamenat, že se v současné době jedná asi o nejlepší dokument na toto téma v češtině (a vážnějšímu zájemci nezbývá než říci, ze se stále bez anglických zdrojů prostě neobejde). Je psán velmi srozumitelnou formou a lze jej jen doporučit všem zájemcům o programování pod Mozillou i těm, kteří by jen rádi trochu nahlédli Mozille pod pokličku.</div>
