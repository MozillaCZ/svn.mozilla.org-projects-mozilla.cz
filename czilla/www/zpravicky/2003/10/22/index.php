	<h3><a href="/zpravicky/google-jako-encyklopedie/">Google jako encyklopedie</a></h3>

	<div>Dle <a href="http://www.sovavsiti.cz/weblog/2003/10/21.html#bp20031021_google" title="Google vševědoucí">Sovy v síti</a> začal vyhledávač <a href="http://www.google.com/">Google</a> sloužit tak trochu i jako encyklopedie. Zkuste v něm zadat např. <a href="http://www.google.com/search?q=define:mozilla">define:Mozilla</a> a dostanete hned několik vysvětlení pojmu Mozilla. Správný uživatel Mozilly si hned přidá ke sbírce svých klíčových slov pro vyhledávání na Googlu (<a href="http://www.google.com/search?q=%s&amp;ie=windows-1250&amp;oe=UTF-8&amp;hl=cs&amp;btnG=Vyhledat+Googlem&amp;lr=" title="Google web">gg</a>, <a href="http://images.google.com/images?q=%s&amp;ie=windows-1250&amp;oe=UTF-8&amp;hl=cs&amp;btnG=Vyhledat+Googlem" title="Google obrázky">gi</a>, <a href="http://groups.google.com/groups?q=%s&amp;ie=windows-1250&amp;oe=UTF-8&amp;hl=cs&amp;btnG=Vyhledat+Googlem" title="Google skupiny">gn</a> a <a href="http://www.google.com/search?hl=cs&amp;ie=windows-1250&amp;oe=UTF-8&amp;cat=gwd%2FTop&amp;q=%s&amp;btnG=Vyhledat+Googlem" title="Google adresář">ga</a>) nové pojmenované např. <a href="http://www.google.com/search?q=define:%s&amp;ie=windows-1250&amp;oe=UTF-8&amp;hl=cs&amp;btnG=Vyhledat+Googlem&amp;lr=" title="Google definice (Encyklopedie)">gd</a>, takže bude mít novou funkci Googlu stále po ruce. Pozn. pro ty, co netuší, jak si klíčové slovo vyrobit: Přidejte si odkaz do záložek (klik pravým myšítkem na odkaz a <tt>Přidat odkaz k záložkám</tt>) a nastavte pro něj klíčové slovo (<tt>Správce záložek</tt> - zobrazit si vlastnosti záložky a napsat do položky <tt>Klíčové slovo</tt> např. gd) - pak můžete místo adresy zadat třeba <a href="http://www.google.com/search?q=define:DNA">gd DNA</a> a číst si o největším objevu minulého století.</div>
