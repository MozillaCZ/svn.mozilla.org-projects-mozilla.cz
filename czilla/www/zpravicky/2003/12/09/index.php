	<h3><a href="/zpravicky/dalsi-zilla-na-mozdev-uz-nebude/">Další Zilla na Mozdev už nebude</a></h3>

	<div>Správci serveru Mozdev <a href="http://mozdev.org/pipermail/project_owners/2003-December/001146.html" title="admin meeting (2003-12-05) notes">prohlásili</a>, že nové projekty již nesmějí ve svých názvech obsahovat "zilla". Projektů založených dříve se omezení netýká. Na toto téma se hned strhla diskuse jak na <a href="http://www.mozillazine.org/talkback.html?article=4060" title="Minutes of the mozdev Admin Meeting of Friday 5th December 2003">MozillaZine</a>, tak v <a href="http://mozdev.org/pipermail/project_owners/2003-December/001120.html" title="use of 'zilla' in new project names">konferenci vývojářů</a> na Mozdev. Důvodem jsou obavy z možných nepříjemností ze strany vlastníka práv na jméno Godzilla, japonské společnosti Toho, která se již jednou snažila zasáhnout proti podobnému názvu - Davezilla. Více článek na <a href="http://zdnet.com.com/2100-1106-949804.html" title="Godzilla vs. the blog thing">ZDNet</a>, který též vysvětluje, proč se Mozilla o své jméno bát nemusí.</div>
