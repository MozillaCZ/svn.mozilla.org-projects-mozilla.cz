	<h3><a href="/zpravicky/the-mozilla-foundation/">The Mozilla Foundation</a></h3>

	<div>Mozilla.org, zastřešující vývoj Mozilly, oznámila vznik neziskové organizace <strong><a href="http://www.mozillafoundation.org/">The Mozilla Foundation</a></strong>. Organizace, která do vínku získala např. domény, ochranné známky a další duševní vlastnictví, má již v okamžiku založení zajišťenou finanční a technickou podporu od <a href="http://www.aol.com/">AOL</a>, vývoj budou podporovat i firmy <a href="http://www.redhat.com/">RedHat</a> a <a href="http://www.sun.com/">Sun</a>. Mozillu čekají světlé zítřky: začne být propagována jako produkt pro koncové uživatele (prvním krokem je např. povedený <a href="http://www.mozilla.org/">redesign webu</a>), očekává se i zvýšený zájem vývojářů a společností doposud váhajících pro určitou závislost Mozilla.org na AOL. <a href="http://daniel.glazman.free.fr/weblog/newarchive/2003_07_13_glazblogarc.html#s105830146977534319">Neoficiální zprávy</a> od zaměstnanců <a href="http://www.netscape.com/">Netscape</a>, že jejich zaměstnavatel - skutečný průkopník webu - končí, jistě mnohé z nás nepotěší, ale vývoje Mozilly se zásadně nedotknou: vývojářů od Netscape je již několik měsíců méně než těch ostatních a bývalí zaměstnanci na vývoji Mozilly i nadále pokračují. Tisková <a href="http://www.mozilla.org/press/mozilla-foundation.html">zpráva</a>.</div>
