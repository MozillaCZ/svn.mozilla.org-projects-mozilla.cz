	<h3><a href="/zpravicky/pomozte-vylepsit-mozillu/">Pomozte vylepšit Mozillu</a></h3>

	<div><a href="http://www.mozilla.org/projects/ml/autocomplete/">'Applying machine learning to autocomplete'</a> je projekt snažící se vylepšit automatické doplňování zadávané adresy. Potřebuje pomoc mnoha dobrovolníků. Stačí mít Mozillu 1.3b, zapnout v ní <a href="http://www.mozilla.org/projects/ml/autocomplete/#how">jedno skryté nastavení</a> a po dvou týdnech odeslat vygenerovaný soubor. Protože tento soubor může obsahovat pro vás citlivé informace, přečtěte si pozorně <a href="http://www.mozilla.org/projects/ml/autocomplete/#how">návod</a>. Více informací naleznete v <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=182366" title="Using machine learning to order autocomplete results">bugzille #182366</a>.</div>
