	<h3><a href="/zpravicky/kuchynsky-drez-v-mozille/">Kuchyňský dřez v Mozille</a></h3>

	<div>Čtete správně :-) <a href="http://www.mozillazine.org/talkback.html?article=2919">mozilllaZine</a> oznámil, že období, kdy byla Mozilla pomlouvána, že je tak nacpaná nesmyslnými a nepotřebnými věcmi, že už jí chybí jen kuchyňský dřez, skončilo. Vývojáři do příští verze přibalí i asciiartový animovaný kuchyňský dřez (bude v <a href="about:kitchensink">about:kitchensink</a>) kompatibilní s w3c standardy! Sešlo se <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=122411">mnoho návrhů</a>, takže pokud máte smysl pro humor, můžete se pokochat prozatím <a href="https://bugzilla.mozilla.org/attachment.cgi?id=114919&amp;action=view">nejnovější verzí</a>. Update: Tak je možné, že dřez nakonec v <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=122411#c120">Mozille nebude</a>. Pokud jej přesto chcete, můžete zkusit pro #122411 hlasovat.</div>
