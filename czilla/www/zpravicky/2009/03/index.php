	<h3><a href="/zpravicky/dalsi-vyvoj-thunderbirdu-seamonkey-a-firefoxu/">Další vývoj Thunderbirdu, SeaMonkey a Firefoxu</a></h3>

	<div>Po několika měsících je zde opět krátký přehled informací o vývoji vybraných produktů založených na Mozille:<ul>
<li>Jako první se na konci února objevil <a href="http://www.mozillamessaging.com/en-US/thunderbird/3.0b2/releasenotes/" hreflang="en" title="Thunderbird 3 Beta 2 Preview Release">Thunderbird 3 Beta 2</a>, mezi jehož hlavní novinky patří možnost archivovat zprávy nebo <a href="http://jasnapaka.bloguje.cz/767925-novy-spravce-aktivit-v-thunderbirdu-3.php" title="JasnaPaka Blog: Nový Správce aktivit v Thunderbirdu 3">Správce aktivit</a>.</li>
<li>Na začátku března následuje <a href="http://www.seamonkey-project.org/releases/seamonkey2.0a3/" hreflang="en">SeaMonkey 2.0 Alpha 3</a> s vylepšeným adresním řádkem, obnovou rozložení oken, či možností úprav nástrojových lišt. Podrobnější přehled novinek připravované SeaMonkey 2.0 naleznete ve <a href="http://www.root.cz/clanky/seamonkey-2-novinky-z-firefox-a-thunderbird/" title="Root.cz: SeaMonkey 2 převezme novinky z Firefoxu a Thunderbirdu">článku Pavla Cvrčka</a> na serveru Root.cz.</li>
<li>V polovině měsíce vyšel také <a href="http://www.mozilla.com/en-US/firefox/3.1b3/releasenotes/" hreflang="en" title="Mozilla Firefox 3.1 Beta 3 Release Notes">Firefox 3.1 Beta 3</a> přinášející dlouho očekávanou možnost konfigurace adresního řádku. Tato verze je také poslední s označením 3.1, neboť <a href="http://www.mozilla.com/" title="Mozilla Corporation: Domovská stránka" hreflang="en">Mozilla Corporation</a> rozhodla o přejmenování na verzi 3.5. Novinkám v přicházejícím Firefoxu 3.5 je věnován <a href="http://www.root.cz/clanky/firefox-3-5-zadna-revoluce-jen-prijemna-evoluce/" title="Root.cz: Firefox 3.5 - žádná revoluce, jen příjemná evoluce">další článek</a> na serveru Root.cz opět od Pavla Cvrčka.</li></ul>
Všechny vývojové verze můžete opět stahovat přímo ze stránek jednotlivých produktů. Firefox i Thunderbird jsou pro hlavní platformy k dispozici i v češtině, pro SeaMonkey je zatím pouze <a href="http://releases.mozilla.org/pub/mozilla.org/seamonkey/releases/2.0a3/langpack/">lokalizační balíček</a>. Opět však upozorňujeme, že jsou to verze určené pro testování a nejsou vhodné pro běžné používání.</div>
