	<h3><a href="/zpravicky/czilla-vitezem-ankety-czech-open-source-2006/">CZilla vítězem ankety Czech Open Source 2006</a></h3>

	<div>Projekt CZilla se stal <a href="http://www.root.cz/clanky/anketa-czech-open-source-2006-vysledky/">absolutním vítězem</a> ankety <a href="http://cos.root.cz/">Czech Open Source 2006</a>, kterou vyhlásil server <a href="">Root.cz</a>. Hlasování veřejnosti nás umístilo na první místo mezi projekty a v hlasování odborné poroty nás předběhl pouze projekt <a href="http://www.openoffice.cz/">OpenOffice.cz</a>, kterému tímto gratulujeme. Vážíme si tohoto ocenění a děkujeme všem, kteří nás svým hlasováním v anketě podpořili &ndash; i nadále budeme dělat vše proto, abychom vaši důvěru nezklamali, a i vy sami v tom <a href="/czilla/jak-pomoci/">můžete pomoci</a>. Vyhlášení výsledků ankety proběhne 12. dubna na výstavě <a href="http://www.linuxexpo.cz/">LinuxExpo 2006</a>, kde nás také <a href="/zpravicky/czilla-na-linuxexpu-2006/">můžete navštívit</a> a pohovořit s námi na témata ze světa Mozilly i mimo něj.</div>
