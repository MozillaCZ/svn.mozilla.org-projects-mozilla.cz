	<div><a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefox</a> se v USA konečně <a href="http://www.betanews.com/article/Report_Firefox_Past_10_Percent_Share/1144184064" hreflang="en" title="BetaNews | Report: Firefox Past 10 Percent Share">přehoupl přes magickou bariéru 10% podílu</a> &ndash; tak alespoň tvrdí Net Applications, společnost zabývající se analýzami webu. Za poslední rok tak Firefox <a href="http://www.czilla.cz/zpravicky/prohlizece-zalozene-na-mozille-maji-v-usa-podil-9-procent/" title="Prohlížeče založené na Mozille mají v USA podíl 9 %">získal více než tři procenta</a>. V Evropě je na tom Firefox již tradičně lépe. Podle zprávy společnosti <a href="http://www.xitimonitor.com/" hreflang="fr">XiTi Monitor</a> v polovině ledna <a href="http://www.czilla.cz/zpravicky/podil-firefoxu-v-evrope-presahl-20-procent,-v-cr-se-blizi-ke-30-procentum/" title="Podíl Firefoxu v Evropě přesáhl 20%, v ČR se blíží ke 30%">překročil podíl Firefoxu v Evropě 20%</a> a v ČR pak dosahuje téměř 30%.</div>
