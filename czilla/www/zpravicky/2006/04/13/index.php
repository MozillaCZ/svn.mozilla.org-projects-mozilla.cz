<div class="news">
	<h3><a href="/zpravicky/experimentalni-sestaveni-firefoxu-proslo-acid2-testem/">Experimentální sestavení Firefoxu prošlo Acid2 testem</a></h3>

	<div>Na blogu <a href="http://diary.e-gandalf.net/about-me/">Zbigniewa Branieckeho</a>, jednoho z vývojářů Mozilla Corporation, vyšla <a href="http://diary.e-gandalf.net/2006/04/12/meet-mr-face/">zprávička</a>, že jedna z vývojových verzí Firefoxu již projde testem <a href="http://www.webstandards.org/action/acid2/" hreflang="en" title="Acid2 Browser Test - The Web Standards Project">Acid2</a>. Jedná se o speciální sestavení s úpravami, které budou později zařazeny do vývojového stromu Firefoxu 3. Ač význam testu je někdy označován za sporný, nerozsypaný <a href="http://flickr.com/photos/dbaron/126886608/" hreflang="en" title="Náhled na ACID2 v onom experimentálním sestavení Firefoxu">smějící se obličej</a> se stal metou nejednoho prohlížeče a úspěšným zvládnutím testu se chlubí Safari, iCab, Konqueror i Opera.</div>
