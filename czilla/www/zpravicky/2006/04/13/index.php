	<h3><a href="/zpravicky/linuxexpo-2006-skoncilo/">LinuxExpo 2006 skončilo</a></h3>

	<div>Včera skončila výstava <a href="http://www.linuxexpo.cz/">LinuxExpo 2006</a>, které jsme se včetně konference
<a href="http://www.oss.cz/opengovernment">Open Government 2006</a> zúčastnili. Celá akce se po všech stránkách velice povedla. Navštívil nás Tristan Nitot, který na konferenci přednesl příspěvek 
<a href="http://www.nitot.com/mozilla/prague-2006/mozilla-prague.html" hreflang="en">Mozilla and the Public Sector</a> a svou návštěvu pozitivně zhodnotil ve <a href="http://standblog.org/blog/2006/04/10/93114744-beers-rain-beautiful-city-and-lots-of-czech-friends" hreflang="en" title="Beer(s), rain, beautiful city and lots of Czech friends">svém blogu</a>. David Majda zaujal svou přednáškou 
<a href="/czilla/prezentace.html#vyvojova-platforma-mozilla">Vývojová platforma Mozilla</a> (slajdy: <a href="http://ftp.czilla.cz/other/propagation/vyvojova-platforma-mozilla-linuxexpo-2006.ppt">PPT</a>, <a href="http://ftp.czilla.cz/other/propagation/vyvojova-platforma-mozilla-linuxexpo-2006.pdf">PDF</a>). Návštěvníci si většinou námi propagované produkty chválili a k zahození nebylo ani osobní setkání lidí, kteří jsou okolo CZilly sdruženi, s vývojovým týmem <a href="http://www.allpeers.com/blog/" hreflang="en">AllPeers</a>. Pro zvídavé jsme připravili <a href="/czilla/fotky/linuxexpo-a-open-government-2006.html">výběr fotografií</a>.
<br/>
<strong>Děkujeme všem, kteří se za CZillu LinuxExpa zúčastnili, pomohli nám, vytvořili přátelskou atmosféru a přispěli k úspěchu celé akce.</strong></div>
