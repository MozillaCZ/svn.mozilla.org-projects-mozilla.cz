	<h3><a href="/zpravicky/rozsireni-selenium-ide-potesi-obzvlaste-testery-webovych-aplikaci-/">Rozšíření Selenium IDE potěší obzvláště testery webových aplikací</a></h3>

	<div><a href="http://www.openqa.org/selenium/" hreflang="en" title="Selenium">Selenium</a> je open source nástroj pro automatické testování webových stránek. Testovací skripty se píší v <abbr title="HyperText Markup Language" lang="en">HTML</abbr> kódu a spouštějí se přímo v prohlížeči.

Pro generování testovacích skriptů je nyní k dispozici rozšíření pro Firefox <a href="http://www.openqa.org/selenium-ide/" hreflang="en" title="Selenium IDE">Selenium <abbr title="Integrated Development Environment" lang="en">IDE</abbr></a>. Skripty se vytváří automaticky během práce s prohlížečem, tzn. vy klikáte a Selenium <abbr title="Integrated Development Environment" lang="en">IDE</abbr> generuje skript. Skripty si můžete ukládat a opakovaně spouštět. To je užitečné hlavně při regresním testování (spouštění existujících testovacích skriptů na nové verze stránek). Podívejte se na krátké <a href="http://wiki.openqa.org/download/attachments/400/Selenium+IDE.swf?version=1" hreflang="en" title="Video o Selenium IDE">video</a>, jak to funguje (k přehrání potřebujete Flash). (Zaslal hor255)</div>
