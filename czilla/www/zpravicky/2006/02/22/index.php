	<div>Společnosti <a href="https://www.openwengo.com/index.php" hreflang="en" title="Wengo">Wengo</a> a <a href="http://disruptive-innovations.com/" hreflang="en" title="Disruptive Innovations">Disruptive Innovations</a> vydaly <a href="https://www.openwengo.com/" hreflang="en" title="OpenWengo">beta verzi rozšíření WengoPhone pro Firefox</a>. S rozšířením WengoPhone můžete volat svým známým přímo z Firefoxu. Na rozdíl od známého programu <a href="http://www.skype.com/" hreflang="en" title="Skype">Skype</a> na telefonování přes internet WengoPhone funguje nad standardizovaným <a href="http://en.wikipedia.org/wiki/Session_Initiation_Protocol" hreflang="en" title="SIP">SIP</a> protokolem.
K dispozici je i <a href="http://dev.openwengo.com/trac/openwengo/trac.cgi/browser/wengophone-ng/trunk" hreflang="en" title="zdrojový kód"> zdrojový kód</a>. Pro první pokusy je po registraci k dispozici 0,2&#8364; zdarma. (Zaslal hor255.)</div>
