	<h3><a href="/zpravicky/bezpecnostni-chyba-firefoxu-pravdepodobne-plany-poplach/">Bezpečnostní chyba Firefoxu: pravděpodobně planý poplach</a></h3>

	<div>Na hackerské konferenci <a href="http://www.toorcon.org/2006/about.html" hreflang="en" title="ToorCon 2006: About TC8">ToorCon</a> bylo během víkendu oznámeno <a href="http://news.com.com/Hackers+claim+zero-day+flaw+in+Firefox/2100-1002_3-6121608.html?tag=nefd.top" hreflang="en" title="Hackers claim zero-day flaw in Firefox | CNET News.com">nalezení údajné bezpečnostní chyby</a> v JavaScriptovém interpreteru, který používají produkty Mozilla.org. Chyba měla umožňovat získání kontroly nad počítačem uživatele. Jak se ale <a href="http://developer.mozilla.org/devnews/index.php/2006/10/02/update-possible-vulnerability-reported-at-toorcon/" hreflang="en" title="Mozilla Developer News &#187; Blog Archive &#187; Update: Possible Vulnerability Reported at Toorcon">později ukázalo</a>, předvedený kus kódu umožňuje pouze shodit prohlížeč a kontrolu nad počítačem se s jeho pomocí hackerům nikdy nepodařilo získat. Přednáška prý měla být pokusem o vtip. Další tvrzení hackerů, že vědí o celkem 30 bezpečnostních problémech, je zřejmě také nepodložené. Dle bezpečnostní expertky Mozilly Window Snyder bude zkoumání celého problému dále pokračovat.</div>
