	<div>Na <a href="http://www.cnet.com/" hreflang="en" title="CNET - Technology product reviews, price comparisons, tech video, and more">CNET News.com</a> vyšel <a href="http://news.com.com/2061-10789_3-6104463.html" hreflang="en" title="Firefox code gets vetted | News.blog | CNET News.com">článek o spolupráci Mozilla Foundation se společností Coverity</a> &ndash; dodavatelem nástroje <a href="http://www.coverity.com/products/prevent.html" hreflang="en" title="Coverity">Coverity Prevent</a>, který umožňuje hledat chyby ve zdrojových kódech v jazyce C++. Mozilla tento nástroj využívá ke hledání problémů jako jsou neplatné ukazatele či "mrtvý" kód a bezpečnostních chyb. Část této aktivity zřejmě probíhá v rámci <a href="http://news.com.com/Homeland+Security+helps+secure+open-source+code/2100-1002_3-6025579.html" hreflang="en" title="Homeland Security helps secure open-source code | CNET News.com">iniciativy amerického ministerstva vnitřní bezpečnosti</a>, které uvolnilo 1,42 milionu dolarů na hledání chyb ve vybraných open source projektech. Více informací najdete v článcích na <a href="http://www.mozillazine.org/talkback.html?article=15701" hreflang="en" title="Mozilla Using Coverity to Scan Mozilla Source Code for Defects - MozillaZine Talkback">MozillaZine</a> a u <a href="http://gemal.dk/blog/2006/05/18/mozilla_and_coverity/" hreflang="en" title="gemal.dk - Mozilla and Coverity">Henrika Gemala</a>.</div>
