	<h3><a href="/zpravicky/podil-firefoxu-uz-dosahuje-k-hranici-20-procent/">Podíl Firefoxu už dosahuje k hranici 20 %</a></h3>

	<div>Společnost <a href="http://www.iinfo.cz/">Internet Info</a>, provozovatel služby <a href="http://navrcholu.cz/">NAVRCHOLU.cz</a>, dnes vydala tiskovou zprávu, podle níž <a href="http://www.iinfo.cz/tiskova_zprava/navrcholu_firefox_kveten_2006/">podíl Firefoxu v ČR už dosahuje k hranici 20 %</a>. Po mírném zpomalení tak <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefox</a> opět nabral na tempu, když si od <a href="/zpravicky/podil-firefoxu-v-cr-presahl-15-procent/" title="CZilla: Podíl Firefoxu v ČR přesáhl 15 %">listopadu 2005</a> polepšil o téměř čtyři procentní body. Celkově získaly prohlížeče založené na jádře <a href="http://www.mozilla.org/newlayout/" title="Gecko - vykreslovací jádro Mozilly">Gecko</a> povzbudivých 21,65 %, zatímco MS Internet Explorer poprvé nedosáhl na hranici 75 % a pokračuje tak v setrvale klesající tendenci.</div>
