	<h3><a href="/zpravicky/firemaster-a-firepassword-ziskavani-a-prace-s-hesly-ve-firefoxu/">FireMaster a FirePassword - získávání a práce s hesly ve Firefoxu</a></h3>

	<div><a href="http://nagmatrix.50webs.com/" hreflang="en" title="nag@matrix.com">Nagareshwar Talekar</a> vydal dva zajímavé programy: <a href="http://nagmatrix.50webs.com/article_firemaster.html" hreflang="en">FireMaster</a> umožňuje zjistit ve Firefoxu hlavní heslo a <a href="http://nagmatrix.50webs.com/article_firepassword.html" hreflang="en">FirePasword</a> je utilitka pro práci s hesly zadanými ve formulářích. Pro prolomení hlavního hesla FireMaster používá útok hrubou silou, slovníkovou metodu a "hybridní metodu". Na jednu stranu mohou tyto programy pomoci zapomnětlivým uživatelům, na druhou stranu můžou být i zneužity k útoku. Doporučujeme tedy uživatelům používat pro své účty <a href="http://www.linuxzone.cz/index.phtml?ids=1&amp;idc=398" title="LINUXZONE - Jak zvolit bezpečné heslo?">bezpečná hesla</a>. Rádi bychom také zdůraznili, že fakt, že heslo jde hrubou silou zjistit, není způsobeno špatným šifrovacím (přesněji hašovacím) algoritmem ve Firefoxu &ndash; pokud jsou zašifrovaná hesla útočníkovi k dispozici a algoritmus veřejný, lze ho popsanými metodami z princpu prolomit vždy.</div>
