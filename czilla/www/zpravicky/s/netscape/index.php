	<h3><a href="/zpravicky/spravna-vyslovnost-xul/">Správná výslovnost XUL</a></h3>

	<div>Pokud se trochu více zabýváte Mozillou, dříve či později (obvykle dříve) narazíte na zkratky <acronym title="XML-based User Interface Language">XUL</acronym> a <acronym title="Extensible Binding Language">XBL</acronym>. Nato dříve či později (obvykle později) zjistíte, co ty zkratky znamenají. Nicméně málokdo ví, jaká je jejich správná výslovnost, proto se často setkáváme s vyslovováním XUL jako [ksul] a XBL jako [ksbl], což není správně. Správná výslovnost XUL je "zool" [zůl] a je <a href="http://www.mozilla.org/xpfe/xulrdf.htm" title="Já si to nevymylel, tvrdí nám to hoši z Mozilla.org">oficiálně definována</a> dle výslovnosti jména jedné z postav filmu <a href="http://www.ghostbusters.com/" title="Krotitelé duchů">Ghostbusters I</a>. Vzpomínáte si, jak Dana otevře chvějící se lednici a démon na ní zařve "Zůůůůůůl"? Pokud ano, víte, jak se vyslovuje XUL ;-) XBL se pro změnu <a href="http://eu.mozdev.org/Brussels2003/talks/brian/slide17.html" title="Ani tohle jsem si nevymyslel">oficiálně</a> vyslovuje "zibble" (což v Ghostbusters už nebylo, snad až někdy ve trojce). Tím ale podobnost se starým hitem nekončí, nalezneme ji totiž v každém dokumentu pracujícím s XUL, neboť jmenný prostor XUL je definovám adresou <a href="http://www.mozilla.org/keymaster/gatekeeper/there.is.only.xul">http://www.mozilla.org/keymaster/ gatekeeper/there.is.only.xul</a>, ve které naleznete nejen jména démonických postav (Strážce klíče a Strážce brány), ale i větu, kterou hlavnímu hrdinovi odpoví démonem ovládaná Dana "Tady není Dana, taky je jen Zůl", asi si dovedete představit, co se vám na dané adrese zobrazí. A pak že v Netscape neměli smysl pro humor ;-)</div>
