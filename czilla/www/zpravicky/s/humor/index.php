	<div>Po několika odkladech zveřejnila <a href="http://www.mozilla.com/" title="Mozilla Corporation: Domovská stránka" hreflang="en">Mozilla Corporation</a> novou verzi serveru s doplňky <a href="https://addons.mozilla.org/">Mozilla Add-ons</a>. Pro české uživatele může být zajímavé, jaký důraz byl kladen na vícejazyčnost: celý web je vedle angličtiny lokalizován do dalších jazyků (česká verze se připravuje) a u nabízených rozšíření je uvedeno, jaké lokalizace obsahuje. Vývojáře pak potěší jednotné přihlašování do veřejné i administrační části webu a nový <a href="http://addons.mozilla.org/en-US/firefox/pages/sandbox" hreflang="en" title="Sandbox Review System">systém kontroly rozšíření</a>, ve kterém pro zajištění lepší stability budou jejich rozšíření zkoumat testeři spolupracující s Mozillou.
