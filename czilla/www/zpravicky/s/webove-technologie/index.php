	<h3><a href="/zpravicky/ukazka-moznosti-tagu-canvas-3d-demo/">Ukázka možností tagu canvas - 3D demo</a></h3>

	<div>Jednou z novinek <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefoxu</a> 1.5 (resp. <a href="http://www.mozilla.org/newlayout/" hreflang="en" title="Mozilla Layout Engine">Gecka</a> 1.8) je podpora značky <tt><a href="http://developer.mozilla.org/en/docs/Category:HTML:Canvas" hreflang="en" title="Category:HTML:Canvas - Devmo">canvas</a></tt>. Moc hezkou ukázku jeho možností připravil Peter Gersmann &ndash; jedná se o <a href="http://developer.mozilla.org/samples/raycaster/RayCaster.html" hreflang="en" title="raycaster">jednoduchou procházku 3D prostředím</a>. Pokud máte Firefox 1.5 Beta 1, určitě si ukázku běžte prohlédnout, budete překvapeni. Můžete si také <a href="http://developer.mozilla.org/en/docs/A_Basic_RayCaster" hreflang="en" title="A Basic RayCaster - Devmo">přečíst dokumentaci</a>. Teď už jen zbývá, aby někdo pod Firefoxem rozchodil klon hry <a href="http://en.wikipedia.org/wiki/Wolfenstein_3D" hreflang="en" title="Wolfenstein 3D - Wikipedia, the free encyclopedia">Wolfenstein 3D</a> :-)</div>
