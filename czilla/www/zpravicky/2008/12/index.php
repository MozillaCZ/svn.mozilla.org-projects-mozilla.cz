	<h3><a href="/zpravicky/komunitni-pruzkumy-mozilly/">Komunitní průzkumy Mozilly</a></h3>

	<div>Smysl projektu Mozilla je od začátku založen na spojení stovek a tisíců lidí do komunity. Také dnes lidé z <a href="http://www.mozilla.com/" title="Mozilla Corporation: Domovská stránka" hreflang="en">Mozilla Corporation</a> vyvíjejí produkty, jako je například <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Mozilla Firefox</a>, s ohledem na přání jejich uživatelů. V tom jim pomáhá řada komunikačních nástrojů a jedním z nich je i projekt <a href="http://surveys.mozilla.org/" title="Mozilla komunitní průzkumy: Domovská stránka">Komunitní průzkumy</a>. Cílem tohoto projektu je formou jednoduchých dotazníků získat zpětnou vazbu od lidí z komunity &ndash; uživatelů či vývojářů. 
<br />Pilotním průzkumem zaměřeným i na české uživatele je dotazník <a href="http://surveys.mozilla.org/?id=16&plocale=cs">Firefox ve vaší zemi</a>, který si bere za cíl odhalit, kdo používá Firefox, proč ho lidé používají a jakým způsobem ho používají. Mozilla je komunita a vy jste její aktivní součástí. Zapojte se do projektu komunitních průzkumů a udělejme spolu Firefox ještě lepším!</div>
