	<h3><a href="/zpravicky/firefox-3-0-5-firefox-2-0-0-19-a-seamonkey-1-1-14/">Firefox 3.0.5, Firefox 2.0.0.19 a SeaMonkey 1.1.14</a></h3>

	<div><a href="http://www.mozilla.com/" title="Mozilla Corporation: Domovská stránka" hreflang="en">Mozilla Corporation</a> oznámila vydání nových verzí produktu Mozilla Firefox. V případě <a href="http://www.mozilla-europe.org/cs/firefox/3.0.5/releasenotes/" title="Poznámky k vydání Firefoxu 3.0.5">Firefoxu 3.0.5</a> se jedná o pravidelnou aktualizaci přinášející opravu několika <a href="http://www.mozilla.org/security/known-vulnerabilities/firefox30.html#firefox3.0.5" hreflang="en" title="Security Advisories for Firefox 3.0">bezpečnostních chyb</a>, pět nových lokalizací a pár drobných vylepšení. 
<br /><a href="http://www.mozilla.com/en-US/firefox/2.0.0.19/releasenotes/" hreflang="en" title="Mozilla Firefox 2.0.0.19 Release Notes">Mozilla Firefox 2.0.0.19</a> je poslední plánovanou aktualizací Firefoxu 2. Tato verze již neobsahuje ochranu proti podvodným stránkám, a uživatelům tedy důrazně doporučujeme přechod na Firefox 3. Aktualizace se uživatelům Firefoxu 2 nabídne buď automaticky, nebo si můžete novou verzi stáhnout ze <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">stránky produktu</a>.
<br />Již tradičně s aktualizací Firefoxu vychází také nová verze balíku aplikací <a href="/produkty/seamonkey/" title="SeaMonkey: Stránka o produktu">SeaMonkey</a>, která přináší opravy <a href="http://www.mozilla.org/security/known-vulnerabilities/seamonkey11.html#seamonkey1.1.14" hreflang="en" title="Security Advisories for SeaMonkey 1.1">stejných chyb</a>. Českou verzi můžete stahovat ze serveru <a href="http://www.czilla.cz/produkty/seamonkey/stahnout.html" title="CZilla: Stáhnout SeaMonkey">CZilla.cz</a>. K dispozici je verze pro Windows a Linux a lokalizační balíček pro ostatní platformy.</div>
