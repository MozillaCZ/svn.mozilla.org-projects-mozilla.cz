	<h3><a href="/zpravicky/nove-vyvojove-verze-firefoxu-thunderbirdu-a-seamonkey/">Nové vývojové verze Firefoxu, Thunderbirdu a SeaMonkey</a></h3>

	<div>Během dvou měsíců od <a href="/zpravicky/aktualni-vyvojove-verze-seamonkey-firefoxu-a-thunderbirdu/" title="CZilla: Aktuální vývojové verze SeaMonkey, Firefoxu a Thunderbirdu">předchozí zprávičky</a> vývojáři nezaháleli, a tak tu máme novou várku vývojových verzí k k otestování:
<ul><li><a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefox</a> ve verzi <a href="http://www.mozilla.com/en-US/firefox/3.1b2/releasenotes/" hreflang="en" title="Mozilla Firefox 3.1 Beta 2 Release Notes">3.1 Beta 2</a> přináší především možnost anonymního prohlížení webu a výrazně <a href="http://met.blog.root.cz/2008/08/23/javascript-ve-firefoxu-31-trhne-vsechny-rekordy/" title="Martin Hassman: JavaScript ve Firefoxu 3.1 trhne všechny rekordy">rychlejší zpracování JavaScriptu</a>.</li>
<li><a href="http://www.mozillamessaging.com/en-US/thunderbird/3.0b1/releasenotes/" hreflang="en" title="Mozilla Messaging: Shredder Beta 1 Release Notes">Thunderbird 3 Beta 1</a> přichází s prohlížením zpráv v panelech, vylepšeným adresářem kontaktů a rychlejší prací se zprávami na IMAP serverech.</li>
<li>Konečně <a href="http://www.seamonkey-project.org/releases/seamonkey2.0a2/" hreflang="en">SeaMonkey 2.0 Alfa 2</a> nově obsahuje základní podporu pro RSS v prohlížeči i v MailNews, již výše zmíněnou rychlejší práci s JavaScriptem a mnoho <a href="http://smtt.blogspot.com/2008/12/alpha-2-roundup.html" hreflang="en" title="SeaMonkey Trunk Tracker: Alpha 2 Roundup">dalších změn</a>.</li></ul>
Stahovat můžete přímo ze stránek jednotlivých produktů. Firefox i Thunderbird jsou pro hlavní platformy v češtině, pro SeaMonkey je k dispozici lokalizační balíček. Opět upozorňujeme, že vývojové verze jsou určeny pouze pro potřeby testování a nejsou rozhodně určeny pro koncové uživatele.</div>
