	<h3><a href="/zpravicky/mozilla-firefox-3-0/">Mozilla Firefox 3.0</a></h3>

	<div><a href="http://www.mozilla.com/" title="Mozilla Corporation: Domovská stránka" hreflang="en">Mozilla Corporation</a> vydala dlouho očekávanou finální verzi prohlížeče <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Mozilla Firefox 3.0</a>. Nejvýraznější změnou na první pohled je <a href="http://blog.mozilla.com/faaborg/2008/05/14/firefox-3-themes/" hreflang="en" title="Alex Faaborg: Firefox 3 Themes">nové téma vzhledu</a> sladěné se vzhledem použitého operačního systému. Významně bylo vylepšeno prohledávání historie navštívených stránek a práce se záložkami, které lze nově prohledávat přímo z <a href="http://jasnapaka.bloguje.cz/625151-firefox-3-0-a-doplnovani-v-adresnim-radku.php" title="JasnaPaka Blog: Firefox 3.0 a doplňování v adresním řádku">adresního řádku</a> prohlížeče. Vylepšení se dočkal také Správce stahování a <a href="http://wiki.mozilla.org/Firefox:Add-ons_Manager_UI_interim_rev2" hreflang="en" title="Mozilla wiki: Add-ons Manager UI interim rev2">Správce doplňků</a>, který nyní umí vyhledávat vhodné doplňky z webu <a href="https://addons.mozilla.org/cs/" title="Mozilla Add-ons">Mozilla Add-ons</a>. Všechny uživatele pak jistě potěší zlepšení rychlosti a paměťové náročnosti aplikace a ještě lepší zabezpečení před podvodnými stránkami. Vývojáři se mohou těšit na <a href="http://developer.mozilla.org/en/docs/Firefox_3_for_developers" hreflang="en" title="MDC: Firefox 3 for developers">podporu nových technologií</a>, jako například <a href="http://developer.mozilla.org/en/docs/New_in_JavaScript_1.8" hreflang="en" title="MDC: New in JavaScript 1.8">JavaScript 1.8</a>, vylepšení podpory <a href="http://developer.mozilla.org/en/docs/CSS_improvements_in_Firefox_3" hreflang="en" title="MDC: CSS improvements in Firefox 3">CSS</a>, <a href="http://developer.mozilla.org/en/docs/SVG_improvements_in_Firefox_3" hreflang="en" title="MDC: SVG improvements in Firefox 3">SVG</a> a canvas, nebo <a href="http://developer.mozilla.org/en/docs/Online_and_offline_events" hreflang="en" title="MDC: Online and offline events">práci offline</a>. Podrobný výčet novinek sepsal Pavel Cvrček pro server <a href="http://www.root.cz/" title="Root.cz - linux, open source a free software">Root.cz</a> do <a href="http://www.root.cz/clanky/novinky-v-chystanem-firefoxu-3-0/" title="Root.cz: Novinky v chystaném Firefoxu 3.0">dvou</a> přehledných <a href="http://www.root.cz/clanky/firefox-3-0-vyssi-bezpecnost-a-pohodli/" title="Root.cz: Firefox 3.0: vyšší bezpečnost a pohodlí">článků</a>. Přečtěte si také <a href="http://www.mozilla-europe.org/cs/firefox/3.0/releasenotes/" title="Poznámky k vydání Mozilla Firefoxu 3.0">poznámky k vydání</a>.
<br />Zapojte se do pokusu o <a href="http://www.spreadfirefox.com/en-US/worldrecord/" hreflang="en" title="Spread Firefox: Download Day 2008">vytvoření světového rekordu</a> v počtu stažení jednoho software za 24 hodin a stáhněte si Firefox 3.0 ještě dnes! K dispozici jsou jako vždy lokalizované verze pro <a href="http://download.mozilla.org/?product=firefox-3.0&amp;os=win&amp;lang=cs">Windows</a>, <a href="http://download.mozilla.org/?product=firefox-3.0&amp;os=linux&amp;lang=cs">Linux</a> i <a href="http://download.mozilla.org/?product=firefox-3.0&amp;os=osx&amp;lang=cs">Mac OS</a> a lokalizační balíček pro ostatní platformy.</div>
