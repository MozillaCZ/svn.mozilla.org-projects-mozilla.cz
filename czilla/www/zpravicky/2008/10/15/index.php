<ul><li>Jako první byl vydán balík <a href="http://www.seamonkey-project.org/releases/seamonkey2.0a1/" hreflang="en">SeaMonkey 2.0 Alfa 1</a>, který po téměř dvou letech vývoje přináší funkce známé z příbuzných produktů, ale i novinky představené v připravovaném Firefoxu 3.1 a Thunderbirdu 3. Současně vývojáři ze <a href="/produkty/seamonkey/" title="SeaMonkey: Stránka o produktu">SeaMonkey</a> odstraňují původní a dnes již zastaralý kód Mozilla Suite, což zrychlí přidávání dalších funkcí v budoucnu. Obsáhlý přehled novinek si můžete přečíst na <a href="http://www.seamonkey-project.org/releases/seamonkey2.0a1/changes#new" hreflang="en" title="What's New in SeaMonkey 2.0 Alpha 1">stránkách produktu</a>, nebo v češtině na <a href="http://jasnapaka.bloguje.cz/728061-vysel-seamonkey-2-0-alfa-1.php" title="JasnaPaka Blog: Vyšel SeaMonkey 2.0 Alfa 1">blogu Pavla Cvrčka</a>.</li>
<li><a href="http://stage.mozillamessaging.com/en-US/" hreflang="en" title="Mozilla Messaging: Domovská stránka">Mozilla Messaging</a> včera zveřejnila <a href="http://www.mozillamessaging.com/en-US/thunderbird/3.0a3/releasenotes/" hreflang="en" title="Mozilla Messaging: Shredder Alpha 3 Release Notes">Thunderbird 3 Alfa 3</a>. Tato verze, původně plánovaná jako Beta 1, přináší integraci s vyhledáváním ve Windows Vista, upravený vzhled, vylepšení výkonu a přes 250 dalších změn.</li>
<li>Do třetice dnes <a href="http://www.mozilla.com/" title="Mozilla Corporation: Domovská stránka" hreflang="en">Mozilla Corporation</a> uvolnila <a href="http://www.mozilla.com/en-US/firefox/3.1b1/releasenotes/" hreflang="en" title="Mozilla Firefox 3.1 Beta 1 Release Notes">Firefox 3.1 Beta 1</a>, který kromě vizuálně přitažlivého přepínání panelů přináší především nové jádro Gecko 1.9.1 s vylepšenou podporou webových standardů.</li></ul>
Všechny vývojové verze můžete stahovat přímo ze stránek jednotlivých produktů. Upozorňujeme však, že slouží především k testovacím účelům a nejsou určeny pro koncové uživatele.</div>
