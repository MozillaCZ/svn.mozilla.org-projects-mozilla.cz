	<h3><a href="/zpravicky/ubuntuzilla/">Ubuntuzilla</a></h3>

	<div>Používáte <a href="http://www.ubuntu.cz/" title="Ubuntu: domovská stránka projektu">Ubuntu</a> a chcete mít vždy nainstalovanou poslední verzi programů <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Mozilla Firefox</a>, <a href="/produkty/seamonkey/" title="SeaMonkey: Stránka o produktu">SeaMonkey</a> nebo <a href="/produkty/thunderbird/" title="Thunderbird: Stránka o produktu">Mozilla Thunderbird</a>? Vyzkoušejte program <a href="http://ubuntuzilla.wiki.sourceforge.net/" hreflang="en" title="Ubuntuzilla: domovská stránka projektu">Ubuntuzilla</a> &ndash; tento script napsaný v pythonu se postará o instalaci a aktualizaci výše jmenovaných programů a přidání ikonky do systémové nabídky. Zároveň umí zálohovat data <a href="http://www.czilla.cz/podpora/soubory-v-profilu.html" title="CZilla: Soubory v profilu aplikací Mozilla">uložená v profilu</a> a integrovat doplňky, které jste si nainstalovali z repozitářů Ubuntu. Programy instalované <a href="http://ubuntuzilla.wiki.sourceforge.net/" hreflang="en" title="Ubuntuzilla: domovská stránka projektu">Ubuntuzillou</a> jsou odděleny od aplikací instalovaných z balíčků a je možné i jejich snadné odstranění. K dispozici je ve <a href="http://sourceforge.net/project/showfiles.php?group_id=202789&package_id=241580" hreflang="en" title="Ubuntuzilla: stažení produktu">formě DEB balíčku</a> pro snadnou instalaci.</div>
