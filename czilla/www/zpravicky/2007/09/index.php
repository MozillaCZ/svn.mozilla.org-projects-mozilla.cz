	<h3><a href="/zpravicky/stranky-podpory-firefoxu-spusteny/">Stránky podpory Firefoxu spuštěny</a></h3>

	<div>Dnes byla na serveru <a href="http://support.mozilla.com/" hreflnag="en">support.mozilla.com</a> spuštěna alfa verze stránek <a href="http://support.mozilla.com/tiki-index.php?page=Firefox%20Support%20Home%20Page" title="Domovská stránka podpory Firefoxu" hreflnag="en">podpory Firefoxu</a>. Cílem projektu SUMO (zkratka ze SUpport.MOzilla.com) je vytvořit centrální a oficiální kanál podpory Firefoxu. Hlavními komponentami serveru by měla být snadno prohledávatelná znalostní databáze (články, návody, referenční příručky, atd.), fórum na podporu uživatelů a živý chat. Alfa verze stránek přináší zatím pouze okolo 60 anglických článků ve znalostní databázi. Spuštění lokalizace stránek, slibovaného fóra a chatu je plánováno v nejbližší budoucnosti.</div>
