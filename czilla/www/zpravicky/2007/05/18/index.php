	<h3><a href="/zpravicky/firefox-ma-nejlepsi-system-aktualizace/">Firefox má nejlepší systém aktualizace</a></h3>

	<div>Percy Cabello na svém zpravodaji <a href="http://mozillalinks.org" hreflang="en">Mozilla Links</a> tvrdí, že <a href="http://mozillalinks.org/wp/2007/05/firefox-update-system-is-the-most-efficient/" hreflang="en" title="Mozilla Links: Firefox update system is the most efficient">Firefox má nejlepší systém aktualizace</a> mezi prohlížeči. Odvolává se přitom na <a href="http://secunia.com/blog/11/" hreflang="en" title="Secunia Blog 28% of all detected applications are insecure">studii</a> společnosti <a href="http://secunia.com/" hreflang="en">Secunia</a>, ve které stojí, že pouze 5,19&nbsp;% uživatelů Firefoxu&nbsp;2 nemá nainstalovanou poslední bezpečností záplatu. Následují uživatelé MSIE&nbsp;7 s 5,4&nbsp;%, MSIE&nbsp;6 s 9,6&nbsp;% a Opery&nbsp;9 s téměř 12&nbsp;% nezabezpečených instalací. 
<br />Jak však Percy Cabello dodává, i tak dobrý výsledek <a href="http://mozillalinks.org/wp/2007/05/improving-firefox-update-rate/" hreflang="en" title="Mozilla Links: Improving Firefox update rate">lze ještě zlepšit</a> propagací novinek <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefoxu 2</a>, nabídnutím aktualizace uživatelům Firefoxu 1.5 či rychlejší přípravou rozšíření kompatibilních s novou řadou prohlížeče.</div>
