	<h3><a href="/zpravicky/seamonkey-1-0-9-a-1-1-2-vyvojari-vyzyvaji-k-upgradu/">SeaMonkey 1.0.9 a 1.1.2: Vývojáři vyzývají k upgradu</a></h3>

	<div><p>Zároveň s aktualizací prohlížeče <a href="/produkty/firefox/" title="Firefox: Stránka o produktu">Firefox</a> vychází také nové verze balíku aplikací <a href="/produkty/seamonkey/" title="SeaMonkey: Stránka o produktu">SeaMonkey</a>, které opravují <a href="http://www.mozilla.org/projects/security/known-vulnerabilities.html#SeaMonkey" hreflang="en" title="Mozilla Foundation Security Advisories">stejné chyby</a>. 
K <a href="http://www.mozilla.org/projects/seamonkey/releases/seamonkey1.0.9/" title="SeaMonkey 1.0.9: Poznámky k vydání" hreflang="en">SeaMonkey 1.0.9</a>  , která je k dispozici <a href="http://www.mozilla.org/projects/seamonkey/releases/1.0.9.html" hreflang="en" title="SeaMonkey 1.0.9">ze stránek projektu</a>, si můžete stáhnout <a href="http://ftp.czilla.cz/mozilla.org/seamonkey/1.0.9/lang/seamonkey-1.0.9.cs-CZ.langpack.xpi">lokalizační balíček</a> pro všechny platformy.
<a href="http://www.mozilla.org/projects/seamonkey/releases/seamonkey1.1.2/" title="SeaMonkey 1.1.2: Poznámky k vydání" hreflang="en">SeaMonkey 1.1.2</a> přináší kromě oprav z Firefoxu 2.0.0.4 i další vylepšení a zvýšení stability. Anglickou verzi můžete stahovat ze <a href="http://www.mozilla.org/projects/seamonkey/releases/" hreflang="en" title="SeaMonkey 1.1.2">stránek projektu</a>, česká verze je připravena také na CZille v sekci <a href="http://www.czilla.cz/produkty/seamonkey/stahnout.html" title="CZilla: Stáhnout SeaMonkey">ke stažení</a> ve verzích pro Windows i Linux. Pro ostatní platformy je samozřejmě k dispozici lokalizační balíček.</p>

<p>SeaMonkey 1.0.9 je poslední opravnou verzí řady 1.0. Vývojáři upozorňují, že pro SeaMonkey 1.0.x již nebudou k dispozici žádné další bezpečnostní aktualizace a <a href="http://www.mozilla.org/projects/seamonkey/news.html#2007-05-30" hreflang="en" title="SeaMonkey 1.1.2 Released - Team Urges Users to Upgrade">důrazně vyzývají uživatele k přechodu na novou řadu SeaMonkey 1.1.x</a>. Stejná výzva se týká stávajících uživatelů původní <a href="/produkty/suite/" title="Mozilla Suite: Stránka o produktu">Mozilla Suite</a> a starých verzí <a href="http://www.czilla.cz/zpravicky/s/netscape/" title="Zprávičky se štítkem Netscape">Netscape</a> 4, 6 a 7. <strong>Tyto programy mohou vzhledem ke svému stáří obsahovat řadu kritických bezpečnostních chyb a jejich používání dnes již není bezpečné.</strong> <a href="/produkty/seamonkey/" title="SeaMonkey: Stránka o produktu">SeaMonkey</a> jakožto nástupce Mozilla Suite poskytuje pro tyto uživatele známé prostředí, všechny oblíbené funkce a přidává k nim <a href="/zpravicky/seamonkey-1-1/" title="CZilla: SeaMonkey 1.1">nové vlastnosti</a> (například kontrolu pravopisu ve formulářových polích nebo vylepšené štítkování e-mailů) a pravidelné bezpečnostní aktualizace.</p>
