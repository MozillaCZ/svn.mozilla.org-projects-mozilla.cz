	<h3><a href="/zpravicky/xm-virtualni-pristroje-v-mozille/">Xm - virtuální přístroje v Mozille</a></h3>

	<div>Včera <a href="http://www.mozillazine.org/talkback.html?article=4256" title="MozillaZine: Xm Virtual Machine Simulator 0.7.1 Released">oznámené</a> vydání <a href="http://www.arxio.de/xm/" title="Arxio: Home of Xm">Xm 0.7.1</a> mě velmi nadchlo. Xm je aplikace postavená na Mozille, která vám umožní pomocí <abbr title="Extensible Markup Language">XML</abbr> a JavaScriptu vytvářet "virtuální přístroje", které můžou reagovat na uživatelův vstup, něco počítat, hýbat se a vytvářet tak pěkné animace atd. - <a href="http://www.arxio.de/xm/xmdescr.html" title="Arxio: Simple logic circuit example">ukázkový příklad se screenshotem</a>. Určitě to ocení hračičkové jako já, případně pedagovové jako názornou pomůcku. Zatím je k dispozici pouze <a href="http://www.arxio.de/xm/xmdownload.html" title="Arxio: Download Xm">verze pro Windows</a>, verze pro Linux se připravuje.</div>
