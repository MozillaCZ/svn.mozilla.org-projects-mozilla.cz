	<h3><a href="/zpravicky/mozilla-1.8-alfa-1/">Mozilla 1.8 alfa 1</a></h3>

	<div>Vyšla <a href="http://www.mozilla.org/releases/#1.8a1">Mozilla 1.8alfa1</a>. Mezi <a href="http://www.mozilla.org/releases/mozilla1.8a1/README.html#new">novinkami</a> najdete základní rozhraní pro FTP upload, vylepšené rozpoznání spamu, podporu LDAP v3 a mnoho dalšího. Stahovat můžete z <a href="http://ftp.mozilla.org/pub/mozilla.org/mozilla/releases/mozilla1.8a1/">Mozilla.org</a> nebo ze <a href="http://www.slunecnice.cz/product/Mozilla/">Slunečnice</a>. Za zmínění stojí <a href="http://www.mozillazine.org/talkback.html?article=4773" title="MozillaZine: New Milestone Schedule Announced">změna vydávacího cyklu</a>, která znamená prodloužení intervalu mezi vydáváním stabilních verzí z 3 mesíců na 20 týdnů. Projeví se to tím, že místo dosavadního vydávání alfa + beta + finalní verze budeme mít alfa1 + alfa2 + beta + finální verzi (tedy o jednu testovací verzi navíc), takže bude vždy víc času na odladění nových fukncí před vydáním stabilní verze.</div>
