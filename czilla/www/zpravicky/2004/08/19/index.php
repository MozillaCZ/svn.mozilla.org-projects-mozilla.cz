<div class="news">
	<h3><a href="/zpravicky/mozilla-japan-otevrena/">Mozilla Japan otevřena</a></h3>

	<div>Další mezinárodní pobočka Mozilla Foundation byla včera otevřena v Japonsku. <a href="http://www.mozilla-japan.org/">Mozilla Japan</a> má cíle podobné své starší evropské sestře <a href="http://www.mozilla-europe.org/">Mozilla Europe</a>: lokalizace produktů Mozilla pro japonský trh, podporu organizací při přechodu na Mozillu a budování japonské komunity. Mozilla Japan je podporována předním japonským dodavatelem Open-Source řešení <a href="http://www.10art-ni.co.jp/english/">Ten Art-ni</a>. Více v <a href="http://www.mozilla.org/press/mozilla-2004-08-18.html" title="Mozilla.org: Mozilla Affiliate in Japan Kicks Off">tiskové zprávě Mozilla Foundation o Mozilla Japan</a>.</div>
