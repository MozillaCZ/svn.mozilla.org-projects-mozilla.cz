	<h3><a href="/zpravicky/mozilla-1.8-alpha-3-2/">Mozilla 1.8 Alpha 3</a></h3>

	<div>Mozilla.org vydala v rámci velmi prodlouženého vývojového cyklu <a href="http://www.mozilla.org/releases/mozilla1.8a3/">Mozillu 1.8 Alpha 3</a>, během několika týdnů se dočkáme i čtvrté alfa verze. Mezi novinkami nalezneme průhledná okna na Windows a <abbr title="The GIMP Toolkit" lang="en">GTK</abbr> 2, <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=248549" title="Bugzilla: bug 248549 - Add undetected document.all support">nedetekovatelnou podporu document.all</a>, možnost vypnout styly stránky, zlepšení importu z mailové klienta Eudora či základní podporu <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=251162" title="Bugzilla: bug 251162 - Implement CSS3 columns">revolučních sloupců z CSS 3</a>. Stahovat můžete z <a href="http://ftp.mozilla.org/pub/mozilla.org/mozilla/releases/mozilla1.8a3/">FTP Mozilla.org</a> či ze <a href="http://www.slunecnice.cz/product/Mozilla/">Slunečnice</a>.</div>
