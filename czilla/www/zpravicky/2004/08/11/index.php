	<h3><a href="/zpravicky/novell-a-ibm-implementuji-xforms-do-mozilly/">Novell a IBM implementují XForms do Mozilly</a></h3>

	<div><a href="http://www.mozilla.org/foundation/" hreflang="en" lang="en">Mozilla Foundation</a> ve spolupráci s <a href="http://www.novell.com/" hreflang="en">Novellem</a> a <a href="http://www.ibm.com/" hreflang="en"><abbr title="International Business Machines" lang="en">IBM</abbr></a> <a href="http://www.mozilla.org/press/mozilla-2004-08-10.html" hreflang="en">oznámila</a> <a href="http://www.mozilla.org/projects/xforms/" hreflang="en" lang="en">XForms Developement Project</a>, jehož výsledkem by měla být implementace <a href="http://www.w3.org/TR/2003/REC-xforms-20031014/" hreflang="en" lang="en">XForms 1.0</a>, doporučení <a href="http://www.w3.org/" hreflang="en"><abbr title="World Wide Web Consortium" lang="en">W3C</abbr></a>, do Mozilly. XForms jsou formulářovým modulem <abbr title="Extensible HyperText Markup Language" lang="en">XHTML</abbr> 2.0, nabízí více vylepšení než formuláře <abbr title="HyperText Markup Language" lang="en">HTML</abbr>, včetně validace dat a integrace s Web services. V současné době žádný z hlavních prohlížečů XForms nepodporuje.</div>
