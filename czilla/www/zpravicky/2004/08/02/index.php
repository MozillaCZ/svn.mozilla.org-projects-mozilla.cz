	<h3><a href="/zpravicky/mozilla-odmeni-nahlaseni-bezpecnostni-chyby/">Mozilla odmění nahlášení bezpečnostní chyby</a></h3>

	<div>Mozilla Foundation dnes <a href="http://www.mozilla.org/press/mozilla-2004-08-02.html" title="Mozilla.org: Mozilla Foundation announces Security Bug Bounty Program">oznámila</a> <a href="http://www.mozilla.org/security/bug-bounty.html">Mozilla Security Bug Bounty Program</a>. V jejím rámci odmění uživatele, kteří naleznou a <a href="http://www.mozilla.org/projects/security/security-bugs-policy.html" title="Mozilla.org: Handling Mozilla Security Bugs">nahlásí bezpečnostní chyby</a> v kódu aplikací Mozilla.org. Pokud chybu <a href="http://www.mozilla.org/about/stafflist.html" title="Mozilla.org Staff Members">tým Mozilla.org</a> posoudí jako kritickou, uživatel dostane 500 amerických dolarů. Iniciativa, která by měla vést k ještě větší kvalitě kódu, je dotována dodavatelem linuxové distribuce <a href="http://www.linspire.com/">Linspire, Inc.</a> a <a href="http://www.markshuttleworth.com/">Markem Shuttleworthem</a>, zakladatelem Thawte a prvním vesmírným turistou.</div>
