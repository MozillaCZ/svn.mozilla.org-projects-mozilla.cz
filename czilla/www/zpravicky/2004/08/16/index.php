	<h3><a href="/zpravicky/kam-se-nam-panely-ztraceji/">Kam se nám panely ztrácejí?</a></h3>

	<div>Mozilla a Firefox používají k prohlížení panely (taby). Nicméně oproti třeba <a href="http://opera.unas.cz/">Opeře</a> si automaticky nepamatují naposledy otevřené panely. Tak se může stát, že prohlížeč omylem zavřete, vypnou vám proud, shodí počítač a rozečtené panely jsou nenávratně ztraceny. Není třeba zoufat! Existuje totiž řada rozšíření, které tento problém vyřeší za nás: <a href="http://extensionroom.mozdev.org/more-info/sessionsaver">Sessionsaver</a>, <a href="http://extensionroom.mozdev.org/more-info/recall">Recall</a> či roztomilý <a href="http://update.mozilla.org/extensions/moreinfo.php?id=58&amp;vid=160&amp;category=">Undoclosetab</a> nebo <a href="http://white.sakura.ne.jp/~piro/xul/_tabextensions.html.en">Tabextensions</a>. Víte o nějakém dalším? Máte s nimi dobré zkušenosti? Podělte se o ně s námi v <a href="http://forum.czilla.cz/viewforum.php?f=12">našem fóru</a>.</div>
