	<h3><a href="/zpravicky/spoustime-firefox-a-thunderbird-z-usb-disku/">Spouštíme Firefox a Thunderbird z USB disku</a></h3>

	<div>Řada z vás tráví část svého času ve škole, na služebních cestách tj. na místech, kde nemáte k dispozici vlastní PC či notebook. Přesto však chcete používat Firefox či Thunderbird, ale tyto programy nejsou nainstalovány. Co s tím? Většina z dnešních PC obsahuje USB porty, které umožňují připojit USB disk (flash disk). Z takového disku se již Firefox či Thunderbird spustit dá. <a href="http://johnhaller.com/jh/" title="Odkaz na domovskou stránku Johna Hallera">John Haller</a> pro tyto účely připravil speciálně upravené verze <a href="http://johnhaller.com/jh/mozilla/portable_firefox/" title="Odkaz na upravenou verzi Mozilla Firefox">Mozilla Firefox 1.0 PR</a> a <a href="http://johnhaller.com/jh/mozilla/portable_thunderbird/" title="Odkaz na upravenou verzi Mozilla Thunderbird">Mozilla Thunderbird 0.8</a>.</div>
