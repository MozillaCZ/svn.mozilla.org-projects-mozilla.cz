	<h3><a href="/zpravicky/druhy-navrh-ke-komentarum-web-forms-2.0/">Druhý návrh ke komentářům Web Forms 2.0</a></h3>

	<div>Pracovní skupina <a href="http://whatwg.org/" hreflang="en"><abbr title="Web Hypertext Application Technology Working Group" lang="en">WHATWG</abbr></a>, o které jsme tu <a href="/archiv.html#news:f1358e5abd9a775e5aacda0f1bf0a80f">už dříve psali</a>, předložila již <a href="http://www.whatwg.org/specs/web-forms/2004-12-10-call-for-comments/">druhý návrh specifikace Web Forms 2.0</a>. Tato specifikace rozšiřuje možnosti formulářů v <a href="http://www.w3.org/TR/html4/">HTML 4</a> o nové typy zadávaných dat (datum, čas, e-mail...), možnosti ověřování platnosti obsahu na straně klienta, schopnost opakování částí formuláře a další užitečné funkce. Podle <a href="http://whatwg.org/news/web-forms-call-for-comments-2" hreflang="en">původního oznámení</a> je specifikace už považována za vyzrálou a stabilní a <abbr title="Web Hypertext Application Technology Working Group" lang="en">WHATWG</abbr> očekává, že do několika měsíců vydá výzvu k její implementaci.</div>
