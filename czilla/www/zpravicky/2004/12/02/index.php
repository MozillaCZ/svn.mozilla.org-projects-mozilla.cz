	<h3><a href="/zpravicky/firefox-v-abc-world-news-tonight/">Firefox v ABC World News Tonight</a></h3>

	<div>Minulý pátek večer se <a href="/produkty/firefox/">Firefox</a> objevil v pořadu <a href="http://abcnews.go.com/WNT/" hreflang="en" lang="en">ABC World News Tonight</a> americké televizní stanice <a href="http://abcnews.go.com/" hreflang="en" lang="en">ABC News</a>. Ve zhruba dvou a půlminutovém příspěvku najdete mimo jiné stručné představení Firefoxu a jeho funkcí, rozhovor se členem vývojového týmu <a href="http://blakeross.com/" hreflang="en">Blakem Rossem</a> a dokonce i pár záběrů z centra <a href="http://www.mozilla.org/" hreflang="en" lang="en">Mozilla Foundation</a>, které mimochodem tvoří jedna velká místnost. Zmíněn je i vývojový model Firefoxu (open-source) a to, že je významnou konkurencí Internet Exploreru. Celá reportáž je dostupná i online ve formátech <a href="http://mirror.randomfoo.net/boingboing/200412/xeni-on-abc-world-news-tonight.wmv">WMV</a> a <a href="http://mirror.randomfoo.net/boingboing/200412/xeni-on-abc-world-news-tonight.rm">Real Media</a>.</div>
