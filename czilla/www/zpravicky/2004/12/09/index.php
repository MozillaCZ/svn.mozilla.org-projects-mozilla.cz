	<h3><a href="/zpravicky/reklama-na-firefox-v-new-york-times-ma-zpozdeni/">Reklama na Firefox v New York Times má zpoždění</a></h3>

	<div>Zdá se, že plánovaná celostránková reklama na <a href="/produkty/firefox/">Firefox</a> v <a href="http://nytimes.com/" hreflang="en">New York Times</a> <a href="http://news.com.com/Firefox's+Times+ad+late+as+10+million+downloads+near/2100-1032_3-5481998.html" hreflang="en">se poněkud zdrží</a> (<a href="http://news.com.com/">CNET News.com</a>). Důvodem je, že ač by všichni přispěvatelé měli být v inzerátu vyjmenováni, cca 1000 z nich zadalo chybná či nesmyslná jména a jsou nyní kontaktováni kvůli vyjasnění. Další zdržení způsobuje zdlouhavé renderování seznamu jmen, čítajícího 10 000 položek. Rob Davis, hlavní organizátor kampaně, doufá, že inzerát by mohl být zveřejněn nejpozději do Vánoc.</div>
