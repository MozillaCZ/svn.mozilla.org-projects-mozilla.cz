	<h3><a href="/zpravicky/uzivatele-firefoxu-mene-klikaji-na-reklamy/">Uživatelé Firefoxu méně klikají na reklamy</a></h3>

	<div>Německá reklamní společnost <a href="http://adtech.de/" hreflang="de">Adtech</a> <a href="http://adtech.de/NewsPresse/news_detail.php?lang_id=de&amp;node=4&amp;current=162" hreflang="de">informovala</a>, že uživatelé <a href="/produkty/firefox/">Firefoxu</a> klikají na reklamních bannerech mnohem méně, než uživatelé <a href="http://www.microsoft.com/windows/ie_intl/cs/default.mspx">Internet Exploreru</a>. Údaje naměřené na 1000 Evropských webech říkají, že pouze 0,11&nbsp;% uživatelů Firefoxu někdy kliknulo na reklamu, oproti 0,5&nbsp;% uživatelů <abbr title="Internet Explorer" lang="en">IE</abbr>. Podle <a href="http://news.com.com/Firefox+users+ignore+online+ads,+report+says/2100-1024_3-5479800.html" hreflang="en">informací na CNET News.com</a> se rozdíly dají vysvětlit zejména integrovaným blokováním vyskakovacích oken ve Firefoxu, ale vliv zřejmě má i složení uživatelů obou prohlížečů.</div>
