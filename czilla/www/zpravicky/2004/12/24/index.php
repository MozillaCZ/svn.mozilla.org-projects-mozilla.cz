	<h3><a href="/zpravicky/integrace-funkci-kalendare-do-thunderbirdu/">Integrace funkcí kalendáře do Thunderbirdu</a></h3>

	<div><a href="http://news.com.com/" hreflang="en">CNET News.com</a> přinesl zajímavou informaci: <a href="http://www.mozilla.org/" hreflang="en">Mozilla.org</a> pod vedením <a href="http://off.net/~shaver/diary/" hreflang="en">Mika Shavera</a> zahajuje <a href="http://news.com.com/Mozillas+Lightning+to+strike+Outlook/2100-7344_3-5501618.html" hreflang="en">práce na projektu Lightning</a>, jehož cílem je integrace funkcí kalendáře z <a href="http://thunderbird.czilla.cz/proc-pouzivat/kalendar/">Mozilla Sunbirdu</a> do <a href="/produkty/thunderbird/">Thunderbirdu</a>. Podle <a href="http://wiki.mozilla.org/index.php/Calendar:Lightning" hreflang="en">Lightning Q &amp; A</a> by to mohlo přilákat zejména uživatele z podnikové oblasti. Thunderbird by tak mohl přetáhnout některé uživatele Microsoft Outlooku, který na tomto poli dominuje. Microsoft se toho nicméně nebojí a ve svém prohlášení tvrdí, že jeho pozici zde Thunderbird nemůže ohrozit.</div>
