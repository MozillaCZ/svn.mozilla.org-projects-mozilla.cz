	<h3><a href="/zpravicky/diskuze-nad-absenci-digitalnich-podpisu-ve-firefoxu/">Diskuze nad absencí digitálních podpisů ve Firefoxu</a></h3>

	<div>Programový manažer Microsoftu <a href="http://blogs.msdn.com/ptorr/" hreflang="en">Pete Torr</a> inicioval diskuzi nad bezpečností <a href="/produkty/firefox/">Firefoxu</a>. Ve <a href="http://blogs.msdn.com/ptorr/archive/2004/12/20/327511.aspx" hreflang="en">svém příspěvku</a> mu vytknul zejména fakt, že samotný program ani rozšíření nejsou digitálně podepsané. Nejnovější Internet Explorer (SP2) naproti tomu ve výchozím nastavení instalaci nepodepsaných součástí nedovolí. Příspěvek rozpoutal bouřlivou <a href="http://it.slashdot.org/article.pl?sid=04/12/21/0038235" hreflang="en">diskuzi na Slashdotu</a> a vyvolal <a href="http://weblogs.asp.net/ptorr/archive/2004/12/21/328377.aspx" hreflang="en">další Torrovu reakci</a>. Otázku, jak moc všudypřítmné digitální podepisování ovlivňuje celkovou bezpečnost <abbr title="Internet Explorer" lang="en">IE</abbr> oproti Firefoxu, už ponecháváme bez komentáře.</div>
