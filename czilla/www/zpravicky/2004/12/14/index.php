	<h3><a href="/zpravicky/daniel-glazman-komentuje-stav-css/">Daniel Glazman komentuje stav CSS</a></h3>

	<div><a href="http://glazman.org/" hreflang="en">Danial Glazman</a> (autor <a href="http://www.slunecnice.cz/product/Nvu/">HTML editoru Nvu</a>, založeného na Mozille) ve svém příspěvku <a href="http://www.glazman.org/weblog/dotclear/index.php?2004/12/14/746-calling-for-a-new-css-revolution" hreflang="en">Calling for a new CSS revolution</a> kritizuje způsob práce <a href="http://www.w3.org/Style/CSS/members.php3" hreflang="en">pracovní skupiny W3C pro CSS</a> a zároveň se omlouvá, protože se jako její bývalý člen za současný stav cítí odpovědný. Vadí mu zejména, kolik energie a času se investovalo do specifikace <a href="http://www.w3.org/TR/CSS21/" hreflang="en">CSS 2.1</a> místo progresivnější <a href="http://www.w3.org/TR/2001/WD-css3-roadmap-20010523/" hreflang="en">CSS 3</a>. Na konci článku pak vyjmenovává dvanáct funkcí, které podle něj tvůrcům webů v CSS nejvíc chybí - a nutno dodat, že se s nimi docela trefil.</div>
