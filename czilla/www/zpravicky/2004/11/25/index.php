	<h3><a href="/zpravicky/pocet-stazeni-firefoxu-1.0-presahl-6-milionu/">Počet stažení Firefoxu 1.0 přesáhl 6 milionů</a></h3>

	<div>Počet stažení Firefoxu 1.0 včera <a href="http://weblogs.mozillazine.org/asa/archives/006976.html" hreflang="en">přesáhl počet 6 milionů</a> - za pouhé dva týdny od jeho vydání. Vzhledem k tomu, že poněkud kulatější hranice 5 milionů byla <a href="http://weblogs.mozillazine.org/asa/archives/006945.html" hreflang="en">prolomena teprve tento víkend</a>, je zřejmé, že zájem uživatelů o tento prohlížeč neutuchá. Více než absolutní čísla je ale podstatné složení stahujících uživatelů - zdá se, že <a href="http://weblogs.mozillazine.org/asa/archives/006953.html" hreflang="en">více než 53&nbsp;% zaujímají uživatelé Internet Exploreru</a>, což indikuje, že na nový Firefox přecházejí noví uživatelé a vysoké počty stažení nejsou pouze upgrady z předchozích verzí.</div>
