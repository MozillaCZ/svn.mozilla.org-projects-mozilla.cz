	<h3><a href="/zpravicky/statistiky-potvrzuji-zvysovani-podilu-prohlizecu-zalozenych-na-mozille/">Statistiky potvrzují zvyšování podílu prohlížečů založených na Mozille</a></h3>

	<div>Podle <a href="http://www.onestat.com/html/aboutus_pressbox34.html" hreflang="en">včera zveřejněných statistik OneStat.com</a> mají prohlížeče založené na Mozille celosvětově mezi uživateli internetu podíl 7,35&nbsp;%, z čehož 4,58&nbsp;% tvoří <a href="/produkty/firefox/">Mozilla Firefox</a>. Za poslední půlrok je to nárůst o více než 5&nbsp;%. Statistiky vede Internet Explorer, jehož podíl ale po dlouhé době klesl pod "magických" 90&nbsp;% - na 88,9&nbsp;%. <a href="http://www.onestat.com/" lang="en" hreflang="en">OneStat</a> komentuje, že výsledky potvrzují trend uživatelů opouštět prohlížeč Microsoftu a nahrazovat ho Mozilla Firefoxem.</div>
