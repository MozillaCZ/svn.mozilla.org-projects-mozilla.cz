	<h3><a href="/zpravicky/firefox-zakladem-pro-pristi-netscape/">Firefox základem pro příští Netscape</a></h3>

	<div><a href="http://moz.sillydog.org/">SillyDog701</a> přinesl informace, že <a href="http://moz.sillydog.org/archives/000465.html" hreflang="en" title="MozInfo701: Netscape Browser Review programme">AOL hledá testery</a> pro připravovanou verzi prohlížeče Netscape, která by měla být postavena na Firefoxu. Z dostupných informací lze usuzovat, že nepůjde jen o prohlížeč <em>přebarvený</em> do barev Netscape, měl by obsahovat vylepšené uživatelské rozhraní a&nbsp;nové funkce. Již před několika měsíci se objevily prvé informace o tom, že AOL připravuje dva nové prohlížeče, přičem jeden z nich by měl být založen na technologiích Mozilla.org. Je zajímavé vidět, že open source projekt Mozilla.org může Netscapu trochu oplatit jeho obrovskou investici. Předpokládá se, že prvá testovací verze bude registrovaným testerům dostupná 30. listopadu.</div>
