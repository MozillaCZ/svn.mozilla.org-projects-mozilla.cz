	<h3><a href="/zpravicky/mozilla-1.7-beta/">Mozilla 1.7 Beta</a></h3>

	<div>Vyšla <a href="http://www.mozilla.org/releases/mozilla1.7b/">Mozilla 1.7b</a>. Přibyla možnost zakázat stránce blokování kontextového menu (kliknutí pravým tlačítkem) a možnost zobrazit hesla ve správci hesel, jsou nové ikonky pro soubory asociované s Mozillou, přepracovány byly dialogy pro práci s cookies, v  pošťáku přibyla podpora IMAP IDLE a zlepšena byla synchronizace s Palmem. Celá aplikace by měla být přibližně o 7&nbsp;% rychlejší. <a href="http://www.mozilla.org/releases/mozilla1.7b/README.html#new">Kompletní novinky</a>, <a href="http://www.mozilla.org/releases/mozilla1.7b/changelog.html">seznam změn</a> a <a href="http://www.mozilla.org/releases/mozilla1.7b/">poznámky k verzi</a>). <br />Prosíme, použijte verze s instalátory, které opět obsahují TalkBack; setkáte-li se s pádem, určitě odešlete záznam o incidentu. Stabilita Mozilly 1.7 je důležitá pro Firefox 1.0, který bude používat její jádro. Mozilla 1.7b je ke stažení z <a href="http://ftp.mozilla.org/pub/mozilla.org/mozilla/releases/mozilla1.7b/">ftp.mozilla.org</a>, verze pro <a href="http://www.slunecnice.cz/product/Mozilla/">Linux, Mac i Windows</a> můžete stáhnout i ze <a href="http://www.slunecnice.cz/">Slunečnice</a>.</div>
