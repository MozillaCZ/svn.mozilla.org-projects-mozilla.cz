	<h3><a href="/zpravicky/mozilla-thunderbird-0.8/">Mozilla Thunderbird 0.8</a></h3>

	<div>Samostatný poštovní klient <a href="/produkty/thunderbird/">Mozilla Thunderbird</a> ve verzi 0.8 přináší uživatelům možnost zkombinovat všechny POP3 účty do jediné globální složky doručené pošty, vylepšuje import veškerých e-mailových dat včetně z běžných poštovních klientů jako Mozilla Suite, Outlook Expressu, Outlooku či Eudory, integruje čtečku RSS, zlepšuje možnosti ochrany soukromí a vylepšuje vyhledávání. Více v <a href="http://www.mozilla.com/thunderbird/releases/0.8-release-notes.html" title="Mozilla.org: Release Notes - Thunderbird 0.8" hreflang="en">poznámkách k vydání</a>, anglické verze pro Windows, Linux a Mac OS&nbsp;X jsou k dispozici na <a href="http://ftp.mozilla.org/pub/mozilla.org/thunderbird/releases/0.8/">FTP Mozilly.org</a> či na stránkách <a href="http://www.slunecnice.cz/product/Mozilla-Thunderbird/">Slunečnice</a>.</div>
