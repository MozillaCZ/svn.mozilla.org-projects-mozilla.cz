	<h3><a href="/zpravicky/bsi-doporucuje-prechod-na-mozillu-nebo-operu/">BSI doporučuje přechod na Mozillu nebo Operu</a></h3>

	<div>Michael Dickopf, tiskový mluvčí německého <a href="http://www.bsi.de/" hreflang="de">Federálního úřadu pro bezpečnost v <abbr title=" Information Technology">IT</abbr></a> (<abbr title="Bundesamt für Sicherheit in der Informationstechnik" lang="de">BSI</abbr>), uvedl pro <a href="http://www.berlinonline.de/berliner-zeitung/archiv/.bin/dump.fcgi/2004/0911/politik/0068/index.html?;mark=internet%20explorer,mozilla,opera" hreflang="de" title="Berliner Zeitung: Warnung vor Internet-Banking">článku Berliner Zeitung</a>, že by uživatelé Internetu měli přejít z Internet Exploreru na Mozillu nebo Operu.  Po několika případech okradení zákazníka internetového bankovnictví díky úspěšnému napadení Internet Exploreru, se přidává i německý <a href="http://www.vzbv.de/go/">Spolkový svaz spotřebitelských centrál</a> (<abbr title="Der Verbraucherzentrale Bundesverband e.V." lang="de">VZBV</abbr>) s doporučením, aby uživatelé Internet Exploreru přemýšleli o bezpečnosti. Více v článku <a href="http://www.theregister.co.uk/2004/09/13/german_ie_jitters/" hreflang="en" lang="en">Germans develop nasty case of IE jitters</a>.</div>
