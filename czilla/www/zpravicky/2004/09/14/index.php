	<h3><a href="/zpravicky/mozilla-firefox-1.0-preview-release/">Mozilla Firefox 1.0 Preview Release</a></h3>

	<div>Webový prohlížeč <a href="/produkty/firefox/">Mozilla Firefox</a> ve verzi 1.0 Preview Release nově nabízí přepracovanou vyhledávací lištu se zvýrazňováním všech nalezených slov, novou službu pro vyhledávání zásuvných modulů (pluginů), RSS čtečku v podobě aktuálních záložek, vylepšené záložky, přidává <a href="/clanky/document-all.html">nedetekovatelnou podporu document.all</a> v režimu zpětné kompatibility a znovu se v této verzi objevuje hlavní heslo. Více v <a href="http://www.mozilla.org/products/firefox/releases/0.10.html" title="Mozilla.org: Release Notes - Firefox Preview Release" hreflang="en">poznámkách k vydání</a> či v článku <a href="http://www.neilturner.me.uk/2004/Jul/13/more_on_new_firefox_features.html" hreflang="en">More on new Firefox features</a>, anglické verze pro Windows, Linux a Mac OS&nbsp;X jsou k dispozici na <a href="http://ftp.mozilla.org/pub/mozilla.org/firefox/releases/0.10/">FTP Mozilly.org</a> či na stránkách <a href="http://www.slunecnice.cz/product/Mozilla-Firefox/">Slunečnice</a>.</div>
