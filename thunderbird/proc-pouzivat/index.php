<div class="main">

<h1>Proč používat Thunderbird</h1>

<p>Thunderbird obsahuje řadu skvělých funkcí, kvůli kterým se vyplatí jej
začít používat. Přečtěte si 12&nbsp;důvodů, proč byste měli nahradit svůj
stávající poštovní klient.</p>


<h2 id="filtrovani-nevyzadane-posty">Filtrování nevyžádané pošty (spamu)</h2>

<div class="img-c img-right" style="width:250px">
	<img src="/img/ss-nevyzdana-posta.png"
		width="250" height="120"
		alt="Upozornění na nevyžádanou poštu" />
	<p class="comment">Zpráva, kterou Thunderbird označil jako nevyžádanou.
	Automatické rozpoznávání nevyžádané pošty vám ušetří čas.</p>
</div>

<p>Hlavním problémem elektronické pošty dneška je velké množství nevyžádané
pošty (spamu), která vám zahlcuje vaši poštovní schránku a&nbsp;okrádá vás
o&nbsp;čas. Nechte Thunderbird, ať sám určí, je-li příchozí zpráva vyžádaná,
či ne, a&nbsp;sami v&nbsp;ušetřeném čase dělejte, co vás baví. Neztrácejte
zbytečně čas tříděním, které za vás klient může udělat sám.</p>

<p><strong>Thunderbird za vás tento problém vyřeší</strong>, a&nbsp;to díky
integrovanému antispamovému filtru. Tento filtr automaticky vytřídí
z&nbsp;vaší každodenní pošty nevyžádané zprávy do složky
<strong>Nevyžádaná</strong>.</p>

<p>Postačí, abyste zpočátku filtru řekli, co je vyžádaná (resp. nevyžádaná)
pošta a&nbsp;on na základě takto získaných znalostí po velmi krátké době
začne automaticky příchozí poštu třídit k&nbsp;vaší plné spokojenosti.</p>

<p>Filtr nevyžádané pošty aktivujete tak, že v&nbsp;hlavní nabídce zvolíte
<strong>Nástroje</strong> a&nbsp;položku <strong>Nastavení nevyžádané
pošty</strong>. Veškeré nastavování antispamového filtru naleznete právě na
tomto místě.</p>


<h2 id="bezpecna-prace-s-postou">Bezpečná práce s&nbsp;poštou</h2>

<div class="img-c img-right" style="width:250px">
	<img src="/img/ss-bezpecna-prace.png"
		width="250" height="38"
		alt="Upozornění na nebezpečný obsah" />
	<p class="comment">Thunderbird umožňuje zamezení načítání obrázků ze
	vzdálených serverů pro zvýšení vašeho soukromí.</p>
</div>

<p>Jednou z&nbsp;velkých předností poštovního klientu Mozilla Thunderbird je
jeho péče o&nbsp;vaše soukromí. Díky tomu jste chráněni od škodlivého
obsahu, který často zavítá do vaší poštovní schránky. Jedná se zejména
o&nbsp;viry či zprávy, jež se vám snaží nějakým způsobem uškodit.</p>

<p>Své soukromí <strong>můžete chránit hlavním heslem</strong>, na něž
budete dotázáni vždy před manipulací s&nbsp;daty, což zamezí zneužití dat
třetí osobou. Pro nastavení hlavního hesla zvolte dialog
<strong>Možnosti</strong> a&nbsp;v&nbsp;něm panel
<strong>Ostatní</strong>.</p>

<p>Thunderbird též umí přistupovat bezpečně k&nbsp;vaší poštovní schránce,
takže veškerá komunikace mezi poštovním serverem a&nbsp;Thunderbirdem může
probíhat zabezpečeně pomocí osvědčených standardů jako
<abbr title="Secure Socket Layer" lang="en">SSL</abbr> či
<abbr title="Transport Layer Security" lang="en">TLS</abbr>.
Po instalaci rozšíření
<a href="/proc-pouzivat/enigmail/">Enigmail</a>
máte možnost šifrovat svou poštu pomocí standardu <strong>OpenPGP</strong>.
Samozřejmostí je též možnost <strong>práce s&nbsp;certifikáty či digitálními
podpisy</strong>.</p>


<h2 id="podpora-vice-uctu">Podpora více účtů</h2>

<div class="img-c img-left" style="width:121px">
	<img src="/img/ss-vice-uctu.png"
		width="121" height="175"
		alt="Složky v několika účtech" />
	<p class="comment">Podpora pro více účtů zpřehlední správu vaší
	pošty.</p>
</div>

<p>Používáte-li více poštovních schránek, jistě vás potěší, že Thunderbird
obsahuje podporu pro více účtů. Můžete tedy přijímat poštu z&nbsp;více
schránek najednou.</p>

<p>Thunderbird vám nabízí svobodu při volbě způsobu organizace vaší pošty.
Může vám vyhovovat, když je pošta z&nbsp;více účtů přijímána do jedné složky
<strong>Doručená pošta</strong>. Můžete chtít, aby byla pošta jednotlivých
účtů od sebe oddělena a&nbsp;každý účet obsahoval svou složku
<strong>Doručená pošta</strong>. S&nbsp;Thunderbirdem můžete realizovat
obojí!</p>

<p>Mezi těmito možnostmi můžete libovolně přepínat. Stačí, když
v&nbsp;dialogu <strong>Nastavení účtu</strong> zvolíte panel
<strong>Nastavení serveru</strong> a&nbsp;klepnete na tlačítko
<strong>Rozšířené</strong>.</p>


<h2 id="podpora-vicenasobnych-identit">Podpora vícenásobných identit</h2>

<div class="img-c img-right" style="width:250px">
	<img src="/img/ss-vicenasobne-ident.png"
		width="250" height="107"
		alt="Dialog nastavení identit" />
	<p class="comment">Dialog pro správu vícenásobných identit.
	Vše snadné a&nbsp;přehledné.</p>
</div>

<p>Možná používáte více poštovních schránek (e-mailových adres). Abyste
nemusel(a) při stahování pošty přistupovat ke všem schránkám, můžete zvolit
možnost přesměrování veškeré pošty do jedné schránky, kterou následně
vybíráte.</p>

<p>Pokud si tímto způsobem necháváte přesměrovat svoji firemní poštu do
soukromé schránky, je nevhodné, aby odpověď na firemní poštu nesla hlavičku
soukromé schránky. Řešením tohoto problému je <strong>podpora vícenásobných
identit</strong>.</p>

<p>Pro nastavení účtu Thunderbirdu klepněte v&nbsp;nastavení na tlačítko
<strong>Spravovat identity</strong> a&nbsp;v&nbsp;zobrazeném dialogu
nastavte další poštovní schránky, které používáte. V&nbsp;případě odpovídání
na firemní poštu pak bude automaticky použita správná identita
a&nbsp;protistrana nepozná, že vaše zpráva nedorazila přímo z&nbsp;vaší
firemní schránky.</p>


<h2 id="integrovana-ctecka-rss-kanalu">Integrovaná čtečka
<abbr title="Really Simple Syndication" lang="en">RSS</abbr>
kanálů</h2>

<div class="img-c img-right" style="width:200px">
	<img src="/img/ss-ctecka-rss.png"
		width="200" height="115"
		alt="Přihlášené RSS kanály" />
	<p class="comment">Přihlášené
	<abbr title="Really Simple Syndication" lang="en">RSS</abbr>
	kanály. Získejte rychlý přehled o&nbsp;tom, co kde píší.</p>
</div>

<p>Dnes již prakticky každý významnější web nabízí
<abbr title="Really Simple Syndication" lang="en">RSS</abbr>
kanál, což je malý soubor, který se do vaší čtečky pravidelně stahuje
a&nbsp;ta na jeho základě zjistí, zda se na sledovaném webu neobjevil nový
článek, příspěvek či nová verze vašeho oblíbeného programu.</p>

<p>Thunderbird v&nbsp;sobě obsahuje integrovanou čtečku
<abbr title="Really Simple Syndication" lang="en">RSS</abbr>
kanálů, která vám umožní snadno získat přehled o&nbsp;novinkách na vašich
oblíbených webových stránkách. Postačí, když v&nbsp;<strong>Nastavení
účtu</strong> klepnete na tlačítko <strong>Přidat účet</strong>
a&nbsp;zvolíte <strong>Účet pro
<abbr title="Really Simple Syndication" lang="en">RSS</abbr>
kanály</strong>. Jak jednoduché a&nbsp;praktické.</p>


<h2 id="tridici-filtry-zprav">Třídicí filtry zpráv</h2>

<div class="img-c img-right" style="width:300px">
	<img src="/img/ss-tridici-filtry.png"
		width="300" height="213"
		alt="Dialog pravidel třídění" />
	<p class="comment">Filtry na třídění pošty jsou skutečně mocnou zbraní.
	Pomocí přehledného dialogu snadno docílíte chování filtru, které
	potřebujete.</p>
</div>

<p>Dostáváte denně velké množství pošty? Musíte se často rozhodovat, kterou
poštu je třeba vyřídit prioritně a&nbsp;kterou stačí vyřídit později? Pak se
vám budou jistě hodit filtry na třídění zpráv!</p>

<p>Díky třídicím filtrům vám <strong>Thunderbird umožní filtrovat přijatou
poštu</strong> do kategorií dle zadaných kritérií, například podle skupiny
e-mailových adres, předmětu zprávy, stáří a&nbsp;mnoha dalších.</p>

<p>Pro vytvoření nového účtu zvolte v&nbsp;nabídce <strong>Nástroje</strong>
volbu <strong>Třídicí filtry zpráv</strong> a&nbsp;v&nbsp;zobrazeném dialogu
klepněte na tlačítko <strong>Nový</strong>. Tvorba vlastního filtru je
jednoduchá. Postačí zvolit jeho jméno, pravidla pro určení zpráv, na které
se má filtr aplikovat, a&nbsp;akci, která se s&nbsp;těmito zprávami má
provést.</p>


<h2 id="pokrocile-vyhledavani">Pokročilé vyhledávání</h2>

<div class="img-c img-right" style="width:240px">
	<img src="/img/ss-pokrocile-vyhledavani.png"
		width="240" height="129"
		alt="Prohledávání zpráv" />
	<p class="comment">Snadné vyhledávání ve zprávách v&nbsp;aktuální
	složce. Postačí jen zvolit kritérium a&nbsp;zadat hledaný text.</p>
</div>

<p>Ani z&nbsp;velkého množství uložené pošty nemusíte mít strach. Thunderbird
obsahuje řadu nástrojů, které vám pomohou se v&nbsp;ní snadno zorientovat.</p>

<p>V&nbsp;rámci aktuálně vybrané složky můžete vyhledávat zprávy pomocí
<strong>Vyhledávací lišty</strong>, která je umístěna nad seznamem
poštovních zpráv. Vyhledávat lze podle řady kritérií. Můžete si vybrat
vyhledávání v&nbsp;předmětech, odesilatelích, tělech zpráv či
v&nbsp;kombinaci předmět a odesílatel.</p>

<div class="img-c img-left" style="width:200px">
	<img src="/img/ss-pohledy.png"
		width="200" height="222"
		alt="Výběr pohledu na zprávy" />
	<p class="comment">Volba pohledů na složku se zprávami. Získejte přehled
	o&nbsp;zprávách ve složce.</p>
</div>

<p>Mocným nástrojem je možnost <strong>uložit výsledky hledání jako
složku</strong>. Jednoduše podle určitého kritéria vyhledáte seznam zpráv
a&nbsp;výsledek tohoto hledání uložíte jako složku. Vytvoří se tak
<strong>virtuální složka</strong>, která bude obsahovat výsledek hledání
a&nbsp;přitom se bude tvářit jako klasická složka.</p>

<p>Nebaví vás třídit poštu? Objevte funkci <strong>Pohled na
zprávy</strong>, která vám umožní pohled na složku se zprávami podle
zvolených pravidel. Chcete zobrazit pouze zprávy za posledních 5&nbsp;dní či
zprávy od známých, které máte uloženy v&nbsp;<strong>Adresáři</strong>?
S&nbsp;touto funkcí to není problém.</p>

<p>Funkci naleznete nad oknem se seznamem zpráv a&nbsp;obsahuje též možnost
vytvořit si vlastní pohled na zprávy.</p>


<h2 id="nastavitelnost-a-rozsiritelnost">Nastavitelnost a&nbsp;rozšiřitelnost</h2>

<div class="img-c img-right" style="width:250px">
	<img src="/img/ss-nastavitelnost.png"
		width="250" height="161"
		alt="Dialog přizpůsobení lišt" />
	<p class="comment">Přizpůsobení lišt v&nbsp;Thunderbirdu. Nastavte si
	uživatelské rozhraní tak, jak chcete a&nbsp;jak vám bude nejlépe
	vyhovovat.</p>
</div>

<p><strong>Nastavte si uživatelské rozhraní</strong> tak, jak sami chcete.
Nelíbí se vám třeba nástrojová lišta? Zvolte si sami funkce, které na ní
chcete mít, velikost ikonek či celou tuto lištu skryjte úplně.
V&nbsp;hlavní nabídce zvolte <strong>Zobrazit</strong>, <strong>Nástrojové
lišty</strong> a&nbsp;<strong>Vlastní</strong>.</p>

<p><strong>Nelíbí se vám rozmístění oken? Změňte si ho!</strong>
V&nbsp;nabídce <strong>Nástroje</strong> zvolte položku
<strong>Možnosti</strong> a&nbsp;v&nbsp;zobrazeném dialogu si zvolte jiné
rozvržení. Co říkáte třeba na třísloupcové rozmístění?</p>

<p><strong>Změna vzhledu</strong>. Nelíbí se vám grafické provedení
Thunderbirdu? Není nic snazšího než si nainstalovat jiný motiv vzhledu
(skin), který změní vzhled programu tak, že jej nejspíš sami nepoznáte.
Můžete tak uzpůsobit vzhled více vašemu operačnímu systému.</p>

<p><strong>Přídavné funkce.</strong> Mozilla Thunderbird je snadno rozšiřitelný
o&nbsp;další funkce. Máte tak možnost si jednoduše doinstalovat ty funkce, které
chcete využívat a&nbsp;které Thunderbird standardně nenabízí. Vyberte si některé
z&nbsp;nabízených rozšíření. Kompletní seznam rozšíření je dostupný na serveru
<a href="https://addons.mozilla.org/cs/thunderbird/">Doplňky Mozilly</a>.
Rozšíření lze
<a href="http://www.czilla.cz/podpora/thunderbird/instalace-rozsireni.html">snadno instalovat</a>.</p>

<h2 id="snadny-prechod">Snadný přechod z&nbsp;jiných poštovních klientů</h2>

<div class="img-c img-right" style="width:250px">
	<img src="/img/ss-import.png"
		width="250" height="203"
		alt="Dialog importu pošty a nastavení" />
	<p class="comment">Dialog, který vás provede importem pošty
	z&nbsp;vašeho stávajícího poštovního klientu. Vše jednoduše
	a&nbsp;přehledně.</p>
</div>

<p>Používáte Outlook Express či Mozilla Mail? Pak vás jistě potěší, že svou
stávající <strong>poštu a&nbsp;kontakty můžete importovat do
Thunderbirdu</strong> a&nbsp;pracovat s&nbsp;ní dále. O&nbsp;nic nepřijdete.
Thunderbird též umožňuje automatický import z&nbsp;Outlooku či Eudory.
Z&nbsp;řady dalších klientů lze
<a href="http://kb.mozillazine.org/Category:Migration_%28mail%29"
	hreflang="en" class="l-en" lang="en">import provést manuálně</a>.</p>

<p>Import z&nbsp;jiného poštovního klientu je skutečně snadný. Při prvním
spuštění programu budete dotázáni, zda si přejete zkopírovat data (poštu,
kontakty, nastavení) z&nbsp;jednoho z&nbsp;nalezených poštovních klientů.
Postačí zvolit, odklepnout a&nbsp;vše ostatní se provede za vás. Import lze
provést i&nbsp;kdykoliv později. Stačí v&nbsp;nabídce
<strong>Nástroje</strong> zvolit položku <strong>Importovat</strong>.</p>

<p>Pokud jste doposud používali jiný poštovní klient, jistě brzy zjistíte,
že používat Thunderbird je minimálně tak snadné, jako váš stávající poštovní
klient.</p>


<h2 id="postaven-na-standardech">Postaven na standardech</h2>

<p><strong>Thunderbird má širokou podporu standardů</strong>. Pro přístup
k&nbsp;poště jsou používány osvědčené protokoly
<abbr title="Post Office Protocol Version 3" lang="en">POP3</abbr>
a&nbsp;<abbr title="Internet Message Access Protocol 4" lang="en">IMAP4</abbr>.
Veškerý přístup k&nbsp;poště můžete zabezpečit pomocí standardů
<abbr title="Secure Socket Layer" lang="en">SSL</abbr>
a&nbsp;<abbr title="Transport Layer Security" lang="en">TLS</abbr>.
Samozřejmostí je podpora pro práci s&nbsp;certifikáty či digitálními
podpisy. Ke svým zprávám můžete připojovat vizitky (tzv.&nbsp;vCards), takže
protistrana bude mít podrobnosti o&nbsp;vaší osobě a&nbsp;snadno si
vás přidat do svých kontaktů.</p>

<p>Mezi další standardy podporované v&nbsp;Thunderbirdu se řadí
<abbr title="Simple Mail Transfer Protocol" lang="en">SMTP</abbr>,
<abbr title="Lightweight Directory Access Protocol" lang="en">LDAP</abbr>,
<abbr title="Secure Multipurpose Internet Mail Extension" lang="en">s/MIME</abbr>
a&nbsp;další. Díky technologii <strong>Gecko</strong> je zajištěno kvalitní
vykreslování
<abbr title="HyperText Markup Language" lang="en">HTML</abbr>
zpráv.</p>


<h2 id="multiplatformni-reseni">Multiplatformní řešení</h2>

<p>Thunderbird je k&nbsp;dispozici pro řadu operačních systémů, namátkově
pro Windows, Linux či třeba Mac&nbsp;OS&nbsp;X. Je jedno, jaký operační
systém používáte. S&nbsp;největší pravděpodobností je pro něj Thunderbird
dostupný.</p>

<p><strong>Multiplatformní řešení vám dává svobodu</strong>. Pokud se někdy
rozhodnete změnit svůj operační systém, pak nebudete mít s&nbsp;poštou žádný
problém. Data, která ukládá Thunderbird do uživatelského profilu, jsou
ukládána ve všech systémech stejným způsobem, takže je při přechodu na jiný
operační systém snadno převedete a&nbsp;můžete Thunderbird používat dál.</p>


<h2 id="uzivatelska-podpora">Uživatelská podpora</h2>

<p>Ač je poštovní klient Thunderbird distribuován zdarma,
<strong>máte na koho se obrátit</strong>. Chcete se na něco zeptat? Nevíte
si s&nbsp;něčím rady? Navštivte
<a href="http://forum.mozilla.cz/viewforum.php?f=4">fórum na podporu uživatelů</a>,
kde vám řada stávajících uživatelů Thunderbirdu ochotně poradí.</p>

